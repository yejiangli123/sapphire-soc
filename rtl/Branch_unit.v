// ╔══════════════════════════════════════════════════════════════════════════╗
// ║  Branch_unit.v — 分支判断单元（纯组合逻辑）                               ║
// ╚══════════════════════════════════════════════════════════════════════════╝
//
// ──────────────────────────────────────────────────────────────────────────
//  1.  模块在 CPU 流水线中的角色与位置
// ──────────────────────────────────────────────────────────────────────────
//
//  本模块位于 **译码阶段（ID, Instruction Decode）** 与 **执行阶段（EX）**
//  之间，属于分支/跳转指令的专用处理路径。在经典的 5 级流水线（IF/ID/EX/MEM/WB）
//  中，它紧接在寄存器文件读取之后：
//
//     IF → ID（译码）→ │ Branch_unit（本模块）│ → EX → MEM → WB
//                       │   - 分支条件判断      │
//                       │   - 无条件跳转检测    │
//
//  具体来说：
//    - 输入端  funct3_in / opcode_in  来自 ID 阶段的指令译码器。
//    - 输入端  source_1 / source_2    来自寄存器文件（rs1、rs2 的读出值）。
//    - 输出端  branch_out / jal_enab  送入流水线控制逻辑：
//        · branch_out = 1  时，配合 EX 阶段计算的 target 地址，
//          控制逻辑将 PC 更新为分支目标并刷新流水线。
//        · jal_enab = 1    时，控制逻辑直接将 PC 更新为
//          JAL/JALR 目标并刷新流水线。
//
//  本模块是 **纯组合逻辑**（无时钟、无复位、无触发器），确保在同一个周期内
//  完成条件判断，为下一拍（EX）提供决策依据，从而将分支延迟压缩到 1 拍。
//
//
// ──────────────────────────────────────────────────────────────────────────
//  2.  设计原理：为什么采用“全并行计算 + MUX 选择”结构
// ──────────────────────────────────────────────────────────────────────────
//
//  2.1  全并行预计算所有 6 种条件
//       -------------------------
//  6 个比较器（beq/bne/blt/bge/bltu/bgeu）在硬件上同时工作，没有任何
//  串行依赖。这样做的好处是：
//    (a) 关键路径只有 1 级比较器 + 1 级 MUX + 1 级 AND 门，延迟约等于
//        一个 32-bit 比较器的延迟（~若干 LUT 级），对时序最友好。
//    (b) 避免了 "先判断 funct3 再选择执行哪个比较" 的串行控制流，
//        在微架构层面消除了条件分支的预测/流水化复杂度。
//    (c) 面积代价很小：32-bit 相等/不等只需异或树，有符号比较只需
//        加高位符号判断，无符号比较是纯减法——全并行仅多花 ~数百个 LUT。
//
//  2.2  funct3 → 条件 MUX
//       -----------------
//  利用 RISC-V 指令编码中 funct3 的精简性和唯一性，用一个 6:1 MUX
//  （实际综合为 LUT 级联）选出当前 B-type 指令所需的条件。没有
//  if-else 优先级问题，综合工具可以将其优化为平衡的 LUT 树。
//
//  2.3  输出 AND 门
//       -----------
//  branch_out = branch_cond && is_branch
//  确保只有当 opcode 是 BRANCH 时才输出有效跳转请求，防止非 B-type
//  指令（如 ALU 操作、Load/Store）因 funct3 碰巧匹配而产生虚假分支。
//
//  2.4  jal_enab 的独立计算
//       -------------------
//  JAL 和 JALR 是无条件跳转，无需判断 source_1/source_2 的值，因此
//  只靠 opcode 就能确定：jal_enab = is_jal || is_jalr。
//  该信号与 branch_out 平行输出，不经过 MUX，延迟最小。
//
//
// ──────────────────────────────────────────────────────────────────────────
//  3.  端口说明
// ──────────────────────────────────────────────────────────────────────────
//
//  ┌──────────────┬──────┬────────────────────────────────────────────────┐
//  │  端口名       │ 方向 │  功能说明                                        │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ funct3_in    │ 输入 │  指令 [14:12] 的 funct3 字段，用于区分                │
//  │              │      │  B-type 的 6 种子类型（BEQ/BNE/BLT/BGE/BLTU/BGEU）  │
//  │              │      │  输入。                                            │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ opcode_in    │ 输入 │  指令 [6:0] 的 opcode 字段，用于区分是              │
//  │              │      │  B-type (1100011)、JAL (1101111) 还是              │
//  │              │      │  JALR (1100111) 指令。通过 Def.v 中的宏与             │
//  │              │      │  `OPCODE_BRANCH / `OPCODE_JAL / `OPCODE_JALR          │
//  │              │      │  比较来识别指令类型。                                │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ source_1     │ 输入 │  操作数 A（rs1 的值），来自寄存器文件读端口 1。         │
//  │ [31:0]       │      │  参与 6 种分支条件的计算。                          │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ source_2     │ 输入 │  操作数 B（rs2 的值），来自寄存器文件读端口 2。         │
//  │ [31:0]       │      │  参与 6 种分支条件的计算。                          │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ branch_out   │ 输出 │  分支条件满足指示。                                │
//  │              │      │  = 1 → 当前指令为 B-type 且分支条件成立，             │
//  │              │      │        流水线控制逻辑应取 target 地址并刷新。          │
//  │              │      │  = 0 → 分支条件不成立或当前指令非 B-type。             │
//  ├──────────────┼──────┼────────────────────────────────────────────────┤
//  │ jal_enab     │ 输出 │  无条件跳转指示。                                   │
//  │              │      │  = 1 → 当前指令为 JAL 或 JALR，                     │
//  │              │      │        流水线控制逻辑应无条件跳转到 target。            │
//  │              │      │  = 0 → 当前指令不是无条件跳转。                       │
//  └──────────────┴──────┴────────────────────────────────────────────────┘
//
//
// ──────────────────────────────────────────────────────────────────────────
//  4.  内部信号详解
// ──────────────────────────────────────────────────────────────────────────
//
//  4.1  6 种分支条件（beq / bne / blt / bge / bltu / bgeu）
//       ─────────────────────────────────────────────────────
//  每条线对应 RISC-V RV32I 规范中的一种 B-type 条件分支：
//
//    beq  — Branch if Equal           (source_1 == source_2)
//          · 实现：32-bit 全等比较，综合为异或树 + 归约 NOR。
//          · 用例：beq rs1, rs2, offset  → 循环计数器判等、switch 边界。
//
//    bne  — Branch if Not Equal       (source_1 != source_2)
//          · 实现：beq 结果的取反。与 beq 共享异或树硬件。
//          · 用例：bne rs1, rs2, offset  → 循环条件 != 0，错误码检查。
//
//    blt  — Branch if Less Than       ($signed(source_1) < $signed(source_2))
//          · 实现：带符号扩展的减法 + 符号标志 (N XOR V) 判断。
//          · 用例：blt rs1, rs2, offset  → 有符号循环计数器 < 上限。
//
//    bge  — Branch if Greater or Equal ($signed(source_1) >= $signed(source_2))
//          · 实现：blt 结果的取反。
//          · 用例：bge rs1, rs2, offset  → 有符号数组下标 >= 0 检查。
//
//    bltu — Branch if Less Than Unsigned (source_1 < source_2)
//          · 实现：无符号减法 + 借位（进位出）判断。
//          · 注意：无 `$signed` 前缀，综合为纯无符号 32-bit 比较。
//          · 用例：bltu rs1, rs2, offset → 地址范围检查（指针不能带符号）。
//
//    bgeu — Branch if Greater or Equal Unsigned (source_1 >= source_2)
//          · 实现：bltu 结果的取反。
//          · 用例：bgeu rs1, rs2, offset → 内存区域上界检查。
//
//  4.2  指令类型识别（is_branch / is_jal / is_jalr）
//       ─────────────────────────────────────────────
//  通过 opcode_in 与 `OPCODE_* 宏（定义于 Def.v）比较来识别指令大类：
//
//    is_branch = (opcode_in == `OPCODE_BRANCH)   // 7'b110_0011
//    is_jal    = (opcode_in == `OPCODE_JAL)      // 7'b110_1111
//    is_jalr   = (opcode_in == `OPCODE_JALR)     // 7'b110_0111
//
//  这三种信号是完全独立的——一个周期内只有一条指令进入 ID 阶段，
//  所以至多一个为 1。它们分别驱动不同的输出路径：
//    - is_branch → 与 branch_cond 进行 AND 得到 branch_out
//    - is_jal / is_jalr → 直接 OR 得到 jal_enab
//
//  4.3  funct3 → 条件选择 MUX（branch_cond）
//       ──────────────────────────────────────
//  该级联 MUX 根据 funct3 字段的值选出对应的预计算条件：
//
//    funct3 编码映射（RISC-V 规范）：
//      `BRANCH_EQ  (3'b000)  →  beq
//      `BRANCH_NE  (3'b001)  →  bne
//      `BRANCH_LT  (3'b100)  →  blt
//      `BRANCH_GE  (3'b101)  →  bge
//      `BRANCH_LTU (3'b110)  →  bltu
//      `BRANCH_GEU (3'b111)  →  bgeu
//      其他值                →  1'b0  (默认不跳转)
//
//  关于默认值 1'b0 的意义：
//    RISC-V B-type 的 funct3 字段只有上述 6 种有效编码（bit[14] = 0
//    对应 EQ/NE，bit[14] = 1 对应 LT/GE 系列，bit[13:12] 区分有无符号）。
//    如果 opcode 是 BRANCH 但 funct3 非法，1'b0 确保不会误跳转；
//    如果 opcode 不是 BRANCH（此时 branch_cond 的值无意义，因为输出
//    会被 is_branch 门控），1'b0 只是一个无关的默认值。
//
//  4.4  输出信号（branch_out / jal_enab）
//       ──────────────────────────────────
//    branch_out = branch_cond && is_branch
//      · 逻辑：只有「是 B-type」且「条件成立」时输出 1。
//      · 用途：送入流水线控制 FSM，决定下一周期 PC 是否为 target。
//
//    jal_enab = is_jal || is_jalr
//      · 逻辑：JAL 或 JALR 的 opcode 置位即输出 1。
//      · 用途：送入流水线控制 FSM，无条件将 PC 更新为 JAL/JALR 目标，
//        同时将当前 PC+4 写入 rd（链接地址）。注意 JALR 的源操作数
//        source_1（rs1）的值在 EX 阶段计算 target 时使用，本模块
//        不需要参与。
//
//
// ──────────────────────────────────────────────────────────────────────────
//  5.  与其他流水线阶段的交互
// ──────────────────────────────────────────────────────────────────────────
//
//  5.1  与 ID 阶段的接口
//       ────────────────
//  ID 阶段完成指令译码后，将 funct3、opcode 和被读出的 rs1/rs2 的值
//  送给本模块。这 4 个信号是 ID→EX 流水线寄存器输入的一部分。
//
//  5.2  与 EX 阶段的接口
//       ────────────────
//  EX 阶段并行计算：
//    (a) 目标地址：PC + 立即数符号扩展（B-type/JAL）
//        或 rs1 + 立即数（JALR）。
//    (b) ALU 运算（如果指令不是分支/跳转）。
//  本模块的输出 branch_out / jal_enab 在 EX 阶段被流水线控制逻辑
//  采样，决定最终 PC 和流水线刷新。
//
//  5.3  流水线刷新机制
//       ──────────────
//  当 branch_out = 1 或 jal_enab = 1 时，控制逻辑在 EX 阶段产生：
//    - PC_mux_sel：选择 target 地址而非 PC+4。
//    - flush_ID：清除 ID 级指令（误取的指令）。
//    - flush_IF（可选）：如果分支在 ID 阶段解决且延迟为 1 拍，
//      清 IF 级。
//  在当前的 2-cycle 分支延迟架构下，IF 和 ID 各需一 NOP 插入。
//
//  5.4  与 CSR / 异常处理的关系
//       ────────────────────────
//  本模块不处理异常。JALR 的目标地址可能在 EX 阶段触发地址未对齐
//  异常，但该异常检测不属于本模块范畴。分支预测（如 BTB）若存在，
//  也位于 IF 阶段与本模块无关——本模块仅在 ID 阶段做确定性的条件
//  判断和指令分类。
//
//
// ──────────────────────────────────────────────────────────────────────────
//  6.  时序与综合注意事项
// ──────────────────────────────────────────────────────────────────────────
//
//  (1) 纯组合逻辑 → 综合后不产生寄存器，关键路径 = 比较器+MUX+AND。
//  (2) source_1 / source_2 来自寄存器文件读取，在 ID 阶段后半拍才稳定；
//      因此本模块的输出应在 ID/EX 流水线寄存器的 setup 窗口内稳定。
//  (3) 所有比较器并行工作，工具会自动 share 公共项（如 beq 和 bne 共用
//      异或树），但不会影响逻辑正确性。
//  (4) 如果未来需要支持 RV64I，只需将 source_1/source_2 宽度扩展为
//      64-bit，比较逻辑的编码思路完全不变。
//
// ╔══════════════════════════════════════════════════════════════════════════╗
// ║  以下为 RTL 代码主体 — 逻辑未做任何修改                                   ║
// ╚══════════════════════════════════════════════════════════════════════════╝

`include "Def.v"

module branch_unit (
    input  wire [2:0]  funct3_in,
    input  wire [6:0]  opcode_in,
    input  wire [31:0] source_1,
    input  wire [31:0] source_2,
    output wire        branch_out,
    output wire        jal_enab
);

    // 6 种分支条件全并行计算
    wire beq  = (source_1 == source_2);
    wire bne  = (source_1 != source_2);
    wire blt  = ($signed(source_1) <  $signed(source_2));
    wire bge  = ($signed(source_1) >= $signed(source_2));
    wire bltu = (source_1 <  source_2);
    wire bgeu = (source_1 >= source_2);

    // 指令类型识别（使用 Def.v 全局宏）
    wire is_branch = (opcode_in == `OPCODE_BRANCH);
    wire is_jal    = (opcode_in == `OPCODE_JAL);
    wire is_jalr   = (opcode_in == `OPCODE_JALR);

    // funct3 → 分支条件 MUX（使用 Def.v 的 BRANCH_* 宏）
    wire branch_cond = (funct3_in == `BRANCH_EQ)  ? beq  :
                       (funct3_in == `BRANCH_NE)  ? bne  :
                       (funct3_in == `BRANCH_LT)  ? blt  :
                       (funct3_in == `BRANCH_GE)  ? bge  :
                       (funct3_in == `BRANCH_LTU) ? bltu :
                       (funct3_in == `BRANCH_GEU) ? bgeu : 1'b0;

    assign branch_out = branch_cond && is_branch;
    assign jal_enab   = is_jal || is_jalr;

endmodule
