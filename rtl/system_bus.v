// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  system_bus.v — SoC 系统总线（地址译码 + 数据多路复用）                       ║
// ║                                                                            ║
// ║  【模块用途】                                                               ║
// ║  本模块是 SoC 的中央总线互连单元，负责将 CPU 的读写请求路由到对应的外设。     ║
// ║  它实现了一个简化的存储器映射 I/O (MMIO) 总线架构，将 CPU 的地址空间划分      ║
// ║  为多个区域，每个区域对应一个不同的外设。模块根据当前地址所在的区域：         ║
// ║    1. 将写使能/读使能信号仅路由到被选中的外设（避免多外设同时响应）           ║
// ║    2. 将选中外设的读数据多路复用后返回给 CPU                                  ║
// ║    3. 向 CPU 报告总线就绪状态                                                ║
// ║                                                                            ║
// ║  【存储器组织与地址映射】                                                     ║
// ║  使用高地址位（部分译码）方案区分外设：                                       ║
// ║    0x00000000 — BRAM (块 RAM，指令/数据存储器)   sel_mem  高4位=0x0         ║
// ║    0x20000000 — GPIO (通用输入输出)              sel_gpio 高8位=0x20        ║
// ║    0x30000000 — UART (串口)                     sel_uart 高8位=0x30        ║
// ║    0x40000000 — Timer (定时器)                  sel_timer 高8位=0x40        ║
// ║    0x50000000 — PLIC (平台级中断控制器)          sel_plic  高8位=0x50        ║
// ║                                                                            ║
// ║  注意：BRAM 使用高 4 位译码（覆盖 0x0000_0000 ~ 0x0FFF_FFFF 共 256MB），    ║
// ║  其余外设使用高 8 位译码（各覆盖 16MB 空间）。这种不对等设计是因为 BRAM       ║
// ║  作为主存储器需要更大的地址空间，而外设只需要少量寄存器映射空间。             ║
// ║                                                                            ║
// ║  【读写时序与协议】                                                           ║
// ║  本总线采用全组合逻辑设计，无流水线阶段：                                     ║
// ║    - 读操作：CPU 在同一个周期内给出地址 (cpu_addr) 和读使能 (cpu_re)，       ║
// ║      总线立即根据地址译码选择对应外设的读数据 (xxx_rdata) 返回给 CPU。       ║
// ║      这是一个组合读路径 —— 地址到数据的延迟仅为多路复用器的传播延迟。        ║
// ║    - 写操作：CPU 在同一个周期内给出地址、写数据 (cpu_wdata) 和写使能         ║
// ║      (cpu_we)，总线将地址和数据广播到所有外设，但写使能仅对选中的外设       ║
// ║      有效（通过 AND 门控）。未选中的外设收不到写使能，不会误写入。           ║
// ║    - 就绪信号：cpu_ready 恒为 1'b1，表示总线始终可以在下一个时钟周期         ║
// ║      接受新的读写请求。这是因为所有外设都承诺在组合逻辑延迟内返回读数据，    ║
// ║      不需要等待周期 (wait state)。                                           ║
// ║                                                                            ║
// ║  【设计选择说明】                                                             ║
// ║                                                                            ║
// ║  1. 组合读 vs 寄存器读                                                       ║
// ║     本设计采用组合读（combinational read），即读数据路径无寄存器，读操作      ║
// ║     在发起请求的同一周期返回数据。这与常见的 AHB/APB 总线的寄存器读           ║
// ║     (registered read，即地址在第 N 周期，数据在第 N+1 或 N+2 周期返回)        ║
// ║     不同。选择组合读的原因：                                                  ║
// ║       a) 外设（BRAM、GPIO、UART、Timer、PLIC）均能在一个周期内返回数据，     ║
// ║          不需要插入等待周期。                                                ║
// ║       b) CPU 核心（如 RISC-V 的单周期或少量流水级实现）期望在同一周期获得     ║
// ║          读数据，组合读简化了 CPU 端的数据通路时序。                          ║
// ║       c) 减少了总线的状态机和延迟，时序收敛更容易。                           ║
// ║     代价：关键路径较长（地址译码 → 外设读数据 → 多路复用器 → CPU），当外设    ║
// ║     数据路径复杂时可能成为频率瓶颈。对于本 SoC 的小规模设计，这个代价可接受。 ║
// ║                                                                            ║
// ║  2. 字节使能 (byte-enable) 设计                                               ║
// ║     本总线没有实现字节使能信号（如 AHB 的 HSIZE + 字节选通），所有传输均为    ║
// ║     32 位全字访问。原因：                                                     ║
// ║       a) 简化总线接口 —— 不需要额外的字节通道选通逻辑。                       ║
// ║       b) CPU 可能只支持 32 位 load/store 指令（lw/sw），无 lb/lh/sb/sh。     ║
// ║       c) 外设寄存器均为 32 位对齐，没有非对齐访问需求。                       ║
// ║     如果需要支持字节/半字访问，需要增加字节使能信号 (mem_be, gpio_be 等)     ║
// ║     和字节移位/合并逻辑。                                                     ║
// ║                                                                            ║
// ║  3. 地址/数据广播 + 使能门控                                                   ║
// ║     地址线 (cpu_addr) 和写数据线 (cpu_wdata) 广播到所有外设 —— 每个外设      ║
// ║     都收到相同的地址和数据。但写使能 (xxx_we) 和读使能 (xxx_re) 仅对选中的   ║
// ║     外设有效（通过 AND 门控：cpu_we && sel_xxx）。这样设计的好处：            ║
// ║       a) 未选中的外设地址/数据端口虽然有信号翻转，但因为没有使能信号，        ║
// ║          内部不会产生任何副作用（无寄存器写入、无状态机触发）。               ║
// ║       b) 扇出管理简单：地址和数据直接连接，无需额外的多路选择器。             ║
// ║       c) 功耗：选中外设的使能信号有效，未选中外设的使能信号为 0，其内部       ║
// ║          组合逻辑可能仍有跳变功耗，但寄存器不会翻转，功耗可控。               ║
// ║     注意：读使能仅在 BRAM (mem_re) 上生成，其他外设 (GPIO/UART/Timer/PLIC)   ║
// ║     没有输出读使能信号 —— 它们的数据始终对外呈现（组合读），无需使能。       ║
// ║     这是一个设计不对称性：写操作需要使能门控来防止误写入，读操作的数据       ║
// ║     始终有效，由多路复用器选择即可。                                          ║
// ║                                                                            ║
// ║  4. cpu_ready 恒为 1                                                         ║
// ║     表示总线永远不会反压 (backpressure) CPU。这是因为：                       ║
// ║       a) 所有外设都承诺组合读（零等待）。                                     ║
// ║       b) 写操作在总线侧也是组合逻辑，下一个时钟沿外设即可采样数据。           ║
// ║       c) 没有需要多个周期完成的 burst 传输或跨时钟域操作。                    ║
// ║     如果将来添加慢速外设（如 SPI Flash），需要让 cpu_ready 根据外设忙闲       ║
// ║     状态来插入等待周期，届时需要修改此信号。                                  ║
// ║                                                                            ║
// ║  5. 部分地址译码                                                              ║
// ║     未使用的地址区域（如 0x1000_0000 ~ 0x1FFF_FFFF 等）在译码时无匹配，     ║
// ║     此时所有 sel_xxx 均为 0，cpu_rdata 为 32'h0，写使能也不触发任何外设。    ║
// ║     这是一种安全的默认行为 —— 访问未映射地址不会导致系统崩溃，但也不会        ║
// ║     产生总线错误异常。实际 SoC 中可能需要添加非法地址访问检测。               ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

module system_bus (
    input  wire        clk,
    input  wire        reset,
    // ═══════════════════════════════════════════════════════════════════
    // CPU 侧接口
    //   cpu_addr  — 32 位地址，CPU 发起访问的目标地址
    //   cpu_wdata — 32 位写数据，CPU 要写入外设的数据
    //   cpu_we    — 写使能，高有效，表示当前周期为写操作
    //   cpu_re    — 读使能，高有效，表示当前周期为读操作
    //   cpu_rdata — 32 位读数据，返回给 CPU 的读取结果
    //   cpu_ready — 总线就绪，恒为 1（零等待总线）
    // ═══════════════════════════════════════════════════════════════════
    input  wire [31:0] cpu_addr,
    input  wire [31:0] cpu_wdata,
    input  wire        cpu_we,
    input  wire        cpu_re,
    output wire [31:0] cpu_rdata,
    output wire        cpu_ready,

    // ═══════════════════════════════════════════════════════════════════
    // BRAM 块 RAM 接口 (Memory 侧)
    //   地址空间：0x0000_0000 ~ 0x0FFF_FFFF (256MB)
    //   用途：存放 CPU 指令和数据
    //   mem_we 仅当 cpu_we 有效且地址命中 BRAM 区域时才为高
    //   mem_re 同理，用于 BRAM 内部读时序控制
    // ═══════════════════════════════════════════════════════════════════
    output wire [31:0] mem_addr,
    output wire [31:0] mem_wdata,
    output wire        mem_we,
    output wire        mem_re,
    input  wire [31:0] mem_rdata,
    input  wire        mem_ready,

    // ═══════════════════════════════════════════════════════════════════
    // Timer 定时器接口
    //   地址空间：0x4000_0000 ~ 0x40FF_FFFF (16MB)
    //   注意：Timer 无读使能输出（组合读，数据始终有效）
    // ═══════════════════════════════════════════════════════════════════
    output wire [31:0] timer_addr,
    output wire [31:0] timer_wdata,
    output wire        timer_we,
    input  wire [31:0] timer_rdata,

    // ═══════════════════════════════════════════════════════════════════
    // GPIO 通用输入输出接口
    //   地址空间：0x2000_0000 ~ 0x20FF_FFFF (16MB)
    // ═══════════════════════════════════════════════════════════════════
    output wire [31:0] gpio_addr,
    output wire [31:0] gpio_wdata,
    output wire        gpio_we,
    input  wire [31:0] gpio_rdata,

    // ═══════════════════════════════════════════════════════════════════
    // UART 串口接口
    //   地址空间：0x3000_0000 ~ 0x30FF_FFFF (16MB)
    // ═══════════════════════════════════════════════════════════════════
    output wire [31:0] uart_addr,
    output wire [31:0] uart_wdata,
    output wire        uart_we,
    input  wire [31:0] uart_rdata,

    // ═══════════════════════════════════════════════════════════════════
    // PLIC 平台级中断控制器接口
    //   地址空间：0x5000_0000 ~ 0x50FF_FFFF (16MB)
    // ═══════════════════════════════════════════════════════════════════
    output wire [31:0] plic_addr,
    output wire [31:0] plic_wdata,
    output wire        plic_we,
    input  wire [31:0] plic_rdata
);

    // ╔══════════════════════════════════════════════════════════════════╗
    // ║  地址译码                                                         ║
    // ║                                                                    ║
    // ║  使用高地址位进行部分译码 (partial decoding):                       ║
    // ║    - BRAM:  仅检查 addr[31:28] == 4'h0，覆盖 0x0xxx_xxxx 共 256MB  ║
    // ║    - 外设:  检查 addr[31:24] == 8'hXX，各覆盖 16MB                  ║
    // ║                                                                    ║
    // ║  注意：BRAM 的译码范围与其他外设有重叠隐患：                          ║
    // ║    0x0000_0000 ~ 0x0FFF_FFFF 中包含了 0x00xx_xxxx，其中             ║
    // ║    0x0020_xxxx 与 GPIO  的 0x20xx_xxxx 实际上不会冲突，因为         ║
    // ║    sel_gpio 检查的是 addr[31:24] == 8'h20，而 BRAM 区域内的         ║
    // ║    addr[31:28] == 4'h0 但 addr[31:24] 可能是 0x00~0x0F，都           ║
    // ║    不等于 0x20。但为了清晰，实际使用时外设地址应从 0x2000_0000       ║
    // ║    开始，避开了 BRAM 的 0x0xxx_xxxx 区域。                           ║
    // ╚══════════════════════════════════════════════════════════════════╝
    wire sel_mem  = (cpu_addr[31:28] == 4'h0);                    // 0x0xxx_xxxx → BRAM
    wire sel_gpio = (cpu_addr[31:24] == 8'h20);                  // 0x20xx_xxxx → GPIO
    wire sel_uart = (cpu_addr[31:24] == 8'h30);                  // 0x30xx_xxxx → UART
    wire sel_timer= (cpu_addr[31:24] == 8'h40);                  // 0x40xx_xxxx → Timer
    wire sel_plic = (cpu_addr[31:24] == 8'h50);                  // 0x50xx_xxxx → PLIC

    // ╔══════════════════════════════════════════════════════════════════╗
    // ║  地址/数据/写使能路由                                               ║
    // ║                                                                    ║
    // ║  地址和数据广播到所有外设：每个外设都直接连接到 cpu_addr 和           ║
    // ║  cpu_wdata，不需要多路选择器。这简化了扇出设计，代价是信号             ║
    // ║  在所有外设端口上都有翻转（功耗略高）。                                ║
    // ║                                                                    ║
    // ║  写使能门控：cpu_we && sel_xxx。只有当地址命中某个外设时，            ║
    // ║  该外设的写使能才有效。这确保写操作仅对目标外设生效，不会              ║
    // ║  误写入其他外设的寄存器。                                           ║
    // ║                                                                    ║
    // ║  读使能门控（仅 BRAM）：cpu_re && sel_mem。BRAM 可能需要读使能        ║
    // ║  来进行内部时序控制（如 SRAM 的读脉冲）。其他外设无读使能 ——         ║
    // ║  它们的数据路径是组合逻辑，输出数据始终有效，由多路复用器选择。        ║
    // ╚══════════════════════════════════════════════════════════════════╝
    assign mem_addr   = cpu_addr;
    assign mem_wdata  = cpu_wdata;
    assign mem_we     = cpu_we && sel_mem;
    assign mem_re     = cpu_re && sel_mem;

    assign gpio_addr  = cpu_addr;
    assign gpio_wdata = cpu_wdata;
    assign gpio_we    = cpu_we && sel_gpio;

    assign uart_addr  = cpu_addr;
    assign uart_wdata = cpu_wdata;
    assign uart_we    = cpu_we && sel_uart;

    assign timer_addr = cpu_addr;
    assign timer_wdata= cpu_wdata;
    assign timer_we   = cpu_we && sel_timer;

    assign plic_addr  = cpu_addr;
    assign plic_wdata = cpu_wdata;
    assign plic_we    = cpu_we && sel_plic;

    // ╔══════════════════════════════════════════════════════════════════╗
    // ║  读数据多路复用                                                     ║
    // ║                                                                    ║
    // ║  使用优先级编码的级联三元运算符 (?:) 实现 5 选 1 多路复用器。        ║
    // ║  尽管各外设的地址空间理论上互斥（sel_xxx 互斥），但综合工具会         ║
    // ║  生成优先级链。mem 优先级最高（在链的最前端），plic 优先级最低。     ║
    // ║                                                                    ║
    // ║  默认值 32'h0：当地址未命中任何外设时返回全零。这是一种安全的         ║
    // ║  默认行为，但不会触发总线错误。实际系统中可能需要添加非法地址         ║
    // ║  访问异常（如 RISC-V 的 load access fault）。                        ║
    // ╚══════════════════════════════════════════════════════════════════╝
    assign cpu_rdata = sel_mem  ? mem_rdata   :
                       sel_gpio ? gpio_rdata  :
                       sel_uart ? uart_rdata  :
                       sel_timer? timer_rdata :
                       sel_plic ? plic_rdata  :
                       32'h0;

    // ╔══════════════════════════════════════════════════════════════════╗
    // ║  CPU 就绪信号                                                      ║
    // ║                                                                    ║
    // ║  cpu_ready 恒为 1'b1。这意味着总线永远不会向 CPU 插入等待周期       ║
    // ║  (wait state)，CPU 可以在每个时钟周期发起新的读写请求。              ║
    // ║                                                                    ║
    // ║  前提条件：                                                         ║
    // ║    1. 所有外设的读路径都是组合逻辑（地址→数据的延迟 < 1 时钟周期）   ║
    // ║    2. 写操作在下个时钟沿被外设采样，不需要握手                       ║
    // ║    3. 无跨时钟域桥接或慢速外设                                       ║
    // ║                                                                    ║
    // ║  如果未来集成需要多周期访问的外设（如 SPI Flash、I2C），需要让        ║
    // ║  cpu_ready 变为 condition-dependent，在等待期间拉低以暂停 CPU。      ║
    // ╚══════════════════════════════════════════════════════════════════╝
    assign cpu_ready = 1'b1;

endmodule
