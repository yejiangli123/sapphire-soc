//+------------------------------------------------------------------+
//| 模块名称: if_id_stage                                             |
//| 所属流水线: 取指(IF) → 译码(ID) 阶段间寄存器                       |
//| 功能概述:                                                         |
//|   - 作为经典5级流水线(RISC-V RV32I)中第一道流水线寄存器(Pipeline   |
//|     Register)，位于取指阶段(Instruction Fetch)与译码阶段           |
//|     (Instruction Decode)之间。                                     |
//|   - 在每个时钟上升沿，将来自IF阶段的PC值和指令字打入寄存器，       |
//|     供下一周期ID阶段使用。                                         |
//|   - 支持流水线阻塞(stall)和冲刷(flush)，配合冒险处理单元           |
//|     (Hazard Unit)维护程序语义正确性。                              |
//|                                                                   |
//| 在流水线中的角色:                                                  |
//|   IF_stage → [if_id_stage] → ID_stage                              |
//|                                                                   |
//|   IF阶段输出的组合逻辑信号 pc/instr，经过本寄存器的时序隔离后，     |
//|   变成稳定可靠的 if_id_pc/if_id_instr 送入ID阶段。这样，            |
//|   IF阶段和ID阶段可以"同时"处理不同指令，实现流水重叠。              |
//|                                                                   |
//| 设计要点:                                                          |
//|   1. 同步寄存: 所有输出均为寄存器输出 (reg)，确保ID阶段看到的信号   |
//|      在整个时钟周期内保持稳定，避免组合逻辑毛刺向后传播。           |
//|   2. NOP注入: 当流水线被冲刷(flush=1)时，指令字段被替换为NOP       |
//|      (addi x0,x0,0 = 32'h00000013)，实现"气泡"插入。               |
//|   3. 保持逻辑: 当阻塞(stall=1)且未冲刷(flush=0)时，寄存器值保持不变 |
//|      (即 else if 级联中的隐式保持)，使ID阶段重复处理同一指令。     |
//|   4. 复位行为: 异步复位时将指令字段复位为NOP而非全零，避免ID阶段   |
//|      将全零当作非法指令触发异常。                                   |
//+------------------------------------------------------------------+

module if_id_stage (
    //===========================================================
    //  时钟与复位
    //===========================================================
    input               clk,          // 系统时钟：所有寄存器在此时钟上升沿工作
    input               reset,        // 异步复位：高电平有效，复位后流水线为空(NOP)

    //===========================================================
    //  流水线控制信号
    //===========================================================
    input               stall,        // 阻塞信号：来自冒险处理单元(Hazard Unit)
                                      //   =1: ID阶段需要等待（如数据冒险需要等待前序指令写回；
                                      //        或结构冒险如多周期乘除法未完成），
                                      //        此时IF_ID寄存器保持当前值，相当于ID阶段"暂停"。
                                      //   =0: 流水线正常前进。
    input               flush,        // 冲刷信号：来自分支预测失败或异常处理逻辑
                                      //   =1: 当前IF阶段取出的指令不应执行（分支预测错误/
                                      //        跳转目标错误/异常/中断），需要将ID阶段
                                      //        的指令替换为NOP（"气泡"），使流水线
                                      //        中错误的指令不产生副作用。
                                      //   优先级: flush > stall，详见always块设计。
                                      //   =0: 无冲刷需求。

    //===========================================================
    //  取指阶段(IF)输入信号
    //===========================================================
    input        [31:0] pc,           // 程序计数器(Program Counter)：32位地址
                                      //   来自IF阶段，指向当前正在被取出的指令的
                                      //   字节地址（RISC-V中PC按字节寻址，但指令地址
                                      //   最低2位始终为0，因为指令必须对齐到4字节边界）。
                                      //   该值会传递到ID阶段，一方面用于分支/跳转目标
                                      //   计算(PC+offset)，另一方面用于异常/中断时的
                                      //   mepc/epc 保存。

    input        [31:0] instr,        // 指令字：32位RISC-V编码
                                      //   来自指令存储器(IMEM)或I-Cache，是当前PC
                                      //   处取出的原始32位指令码。在ID阶段将被解码为
                                      //   opcode / funct3 / funct7 / rs1 / rs2 / rd
                                      //   等字段，驱动寄存器文件读取和控制信号生成。

    //===========================================================
    //  译码阶段(ID)输出信号（寄存器输出）
    //===========================================================
    output reg   [31:0] if_id_pc,     // 已寄存的PC值：输出到ID阶段
                                      //   宽度: 32位
                                      //   方向: output reg（时序逻辑输出）
                                      //   用途: ID阶段用于分支目标计算、生成imm,
                                      //         JAL/JALR链接地址、异常返回地址。
                                      //   注意: flush时此寄存器不清零（可保留原PC用于
                                      //         debug或异常信息记录）。如需清零，取消
                                      //         相应代码行的注释即可。

    output reg   [31:0] if_id_instr   // 已寄存的指令字：输出到ID阶段
                                      //   宽度: 32位
                                      //   方向: output reg（时序逻辑输出）
                                      //   用途: ID阶段解码——提取opcode判断指令类型，
                                      //         提取寄存器地址读rf，提取立即数做符号扩展，
                                      //         生成alu_op/reg_write/mem_read等控制信号。
);

  //====================================================================
  //  本地参数: NOP 指令编码
  //====================================================================
  //  NOP = addi x0, x0, 0
  //  编码: 32'h00000013
  //  分解: funct7[31:25]=0 | rs2[24:20]=0 | rs1[19:15]=0 | funct3[14:12]=0
  //        | rd[11:7]=0 | opcode[6:0]=0010011 (OP-IMM)
  //  语义: 将立即数0加到x0再写回x0，架构上无任何可见状态改变。
  //  用途:
  //    1. 复位时填充流水线，使ID阶段看到"无害"的有效指令而非全零。
  //    2. flush时替换被冲刷的指令，实现"流水线气泡"。
  //    3. 空流水线段的默认值。
  localparam NOP = 32'h00000013;

  //====================================================================
  //  时序逻辑: 流水线寄存器的核心控制
  //====================================================================
  //  该always块描述IF→ID流水线寄存器在三种场景下的行为:
  //    场景A (reset):   异步复位，清空流水线
  //    场景B (flush):   冲刷错误指令，插入NOP气泡
  //    场景C (!stall):  正常流动，更新为新取出的指令
  //    场景D (stall且!flush): 隐式保持，ID阶段重播当前指令
  //
  //  优先级设计: reset > flush > stall
  //    - 复位优先级最高：系统初始化必须优先于所有流水线控制。
  //    - flush优先于stall：冲刷意味着当前指令是"错误的"，即使有阻塞
  //      也应先清除该指令，用NOP替代。这避免了错误指令被锁存在流水线中。
  //    - stall最后：只有既不复位也不冲刷且不阻塞时，流水线才正常前进。
  //      stall=1时寄存器隐式保持（因为所有条件分支都未满足，寄存器
  //      不会被赋值，综合工具会推断出时钟使能或反馈MUX）。
  //
  //  时序考量:
  //    - 使用非阻塞赋值(<=)是时序逻辑的标准做法，确保仿真行为
  //      与综合后电路一致（避免Verilog仿真中的竞争条件）。
  //    - 所有输出都是寄存器输出，延迟为一个时钟周期。这意味着IF阶段
  //      在第N周期取出的指令，在第N+1周期才被ID阶段解码。这是流水线
  //      设计的基本代价，也是CPI=1得以实现的基础。
  //    - 异步复位(posedge reset)虽然不利于时序收敛（FPGA中推荐同步复位），
  //      但在教育/简单设计中常用，因为无需时钟即可快速恢复初始状态。
  //====================================================================
  always @(posedge clk or posedge reset) begin
    if (reset) begin
      //----------------------------------------------------------
      //  复位处理
      //----------------------------------------------------------
      //  PC清零: 可选做法。有些设计将PC复位为第一条指令地址而非0。
      //  instr置为NOP: 关键设计选择——避免全零指令。
      //    如果指令字段复位为32'h00000000，而该编码在RISC-V中并非
      //    合法NOP（全零可能被解码器误解或触发未定义行为），使用
      //    明确的NOP编码(32'h00000013)可以让ID阶段的控制信号全部
      //    安全置零（reg_write=0, mem_read=0, branch=0等），确保
      //    复位后的第一个周期流水线不做任何有效操作。
      //----------------------------------------------------------
      if_id_pc    <= 32'h0;
      if_id_instr <= NOP;

    end else if (flush) begin
      //----------------------------------------------------------
      //  冲刷处理 (flush=1)
      //----------------------------------------------------------
      //  触发条件:
      //    a) 分支指令在EX/MEM阶段解析后，发现之前预测的跳转方向错误
      //       (branch misprediction)，此时IF阶段已取出了错误路径的
      //       指令，必须冲刷。
      //    b) JAL/JALR跳转指令在ID/EX阶段计算得到目标地址，向IF阶段
      //       发出PC重定向，同时冲刷IF_ID中已取出的顺序下一条指令。
      //    c) 异常/中断发生时，流水线中的当前指令需要被"抹除"，
      //       插入异常处理入口的取指。
      //
      //  行为:
      //    - if_id_instr <= NOP: 将指令替换为NOP，ID阶段将解码出一个
      //      "无操作"，不会写入寄存器文件、不会发起内存访问、不会
      //      改变任何架构状态。
      //    - if_id_pc 保持不变: 有意不清零。有利于debug时追踪
      //      被冲刷的指令地址，也利于异常处理时记录mepc。
      //      如需在flush时也清除PC，可取消下方注释行。
      //----------------------------------------------------------
      if_id_instr <= NOP;
      // if_id_pc <= 32'h0;  // 如需在冲刷时清除PC，取消此行注释

    end else if (!stall) begin
      //----------------------------------------------------------
      //  正常流水(非阻塞)处理 (stall=0)
      //----------------------------------------------------------
      //  IF阶段的组合逻辑结果 pc/instr 在此时钟沿被打入寄存器，
      //  下一个时钟周期ID阶段将处理这些值。
      //
      //  注意: PC值并非简单打一拍，其携带的信息有重要作用:
      //    1. pc+4 (顺次下一条指令地址): 在ID阶段通过加法器计算
      //       (pc+offset 用于分支, pc+4 用于JAL链接)。
      //    2. 指令本身的地址: 在异常发生时写入mepc作为返回地址。
      //    3. 调试/性能计数器: 采样PC用于profiling。
      //
      //  instr直接从IF阶段透传至ID阶段，不做任何修改。指令的合法性
      //  检查(如是否为非法指令)由ID阶段的解码逻辑负责。
      //----------------------------------------------------------
      if_id_pc    <= pc;
      if_id_instr <= instr;

    end
    //----------------------------------------------------------
    //  阻塞处理: stall=1 且 flush=0 (隐式行为)
    //----------------------------------------------------------
    //  当 stall=1 而 flush=0 时，以上所有分支条件均不满足，
    //  if_id_pc 和 if_id_instr 保持上一个时钟周期的值不变。
    //
    //  为什么需要保持而非打NOP？
    //    阻塞通常由数据冒险引起：当前ID/RF阶段的指令需要等待前序
    //    指令的结果写回寄存器文件。如果此时用NOP覆盖IF_ID寄存器，
    //    当前指令就会丢失，ID阶段将错误地开始解码下一条指令。
    //    正确的做法是"暂停IF阶段、保持ID阶段"，让ID阶段在当前周期
    //    "重播"同一条指令，直到数据就绪。
    //
    //  综合语义:
    //    在reset/flush/!stall三个分支都不满足时，reg变量的非阻塞赋值
    //    语句均不会执行，reg保持原值。综合工具会将此推断为:
    //    - 寄存器带时钟使能(CE)，当CE=0时保持。
    //    - 或者在寄存器输入端加一个MUX，选择反馈值或新值，
    //      这取决于目标工艺库和综合策略，功能上等价。
    //
    //  stall信号的来源与时机:
    //    stall由Hazard Unit组合逻辑生成（在同一时钟周期内），
    //    因此到达IF_ID寄存器时满足建立时间要求。典型的数据冒险场景:
    //    - Load-use冒险: lw后紧跟一条使用该结果的指令，Hazard Unit
    //      在ID阶段检测到依赖后拉高stall，IF_ID寄存器保持，ID阶段
    //      重复解码同一条需等待的指令，直到load数据写回后stall释放。
    //----------------------------------------------------------
  end

endmodule
