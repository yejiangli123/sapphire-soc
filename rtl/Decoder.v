// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                      Decoder.v — ID 阶段译码模块                             ║
// ╠══════════════════════════════════════════════════════════════════════════════╣
// ║  【模块在 CPU 流水线中的角色】                                                ║
// ║  本模块位于经典五级流水线的 ID（指令译码）阶段。                               ║
// ║  它承接 IF 阶段取出的原始 32 位指令，将指令按 RISC-V 标准编码切分为            ║
// ║  独立的字段（opcode / funct3 / funct7 / rs1 / rs2 / rd），供后续的           ║
// ║  控制单元（Control）和寄存器堆（RegFile）使用。                               ║
// ║                                                                              ║
// ║  同时，本模块也是 IF/ID 流水线寄存器：它将 IF 阶段算出的 PC 相关值            ║
// ║  （pc_imm_in、pc_4_in）一并锁存，向下游阶段传递，用于异常处理、               ║
// ║  分支跳转目标计算等。                                                        ║
// ║                                                                              ║
// ║  本模块不含复杂的控制信号译码（如 ALUOp、RegWrite 等），那些由专门的           ║
// ║  Control 模块根据 opcode/funct3/funct7 组合逻辑产生。                         ║
// ║  Decoder 的核心职责是：锁存 + 字段切分 + 流水线控制（停顿 / 冲刷 / 复位）。   ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
//  一、与其他流水线阶段的交互关系
// ============================================================================
//
//   ┌──────┐    instr    ┌─────────┐  fields + PC  ┌─────────┐
//   │  IF  │ ──────────> │ Decoder │ ────────────> │ Control │──> EX
//   │stage │  pc_imm     │  (ID)   │  opcode_out   │  Unit   │
//   │      │  pc_4       │         │  funct3/7_out │         │
//   └──────┘             │         │               └─────────┘
//                        │         │  source_reg_1/2 ────> RegFile (读端口)
//                        │         │  dest_reg ──────────> WB stage  (写回目标)
//                        │         │  load_itype ────────> Hazard Unit
//                        └─────────┘
//
//   上游（IF 阶段）：
//     - instr_in   : 指令存储器（IMEM）读出的原始 32-bit 指令字
//     - pc_imm_in  : IF 阶段计算的 PC + 立即数（分支/跳转目标地址）
//     - pc_4_in    : IF 阶段计算的 PC + 4（顺序下一条地址）
//
//   下游（EX / MEM / WB 阶段）：
//     - source_reg_1/2 → 寄存器堆的读地址，读取操作数 rs1/rs2
//     - dest_reg       → 随流水线传递至 WB 阶段，作为写回目标寄存器 rd
//     - opcode_out     → Control 单元，解码出 ALUOp / RegWrite / MemWrite 等控制信号
//     - funct_3_out    → Control 单元，参与 ALU 操作选择、分支条件选择
//     - funct_7_out    → Control 单元，区分同 opcode 下的指令变体（如 add vs sub）
//     - load_itype     → 前递/冒险检测单元（Hazard Unit），用于 load-use 数据冒险的检测与停顿
//     - pc_imm_out     → EX 阶段，用于 CSR 异常返回地址、跳转目标计算
//     - pc_4_out       → EX 阶段，用于 jal 返回地址（rd = PC+4）
//
// ============================================================================
//  二、端口详细说明
// ============================================================================
//
//  ── 输入端口 ─────────────────────────────────────────────────────────────
//  clk               : 全局时钟，上升沿触发流水线寄存器更新
//  reset             : 异步复位，高有效。复位时所有寄存器输出清零
//  instr_in   [31:0] : 来自 IF 阶段的 32-bit 原始指令字（IMEM 读出值）
//  pc_imm_in  [31:0] : 来自 IF 阶段的 PC + 符号扩展立即数（分支/跳转目标地址）
//  pc_4_in    [31:0] : 来自 IF 阶段的 PC + 4（顺序下一条指令地址）
//  stall            : 流水线停顿信号，高有效。
//                      当数据冒险需要等待（如 load-use 相关）时置高，
//                      此时 Decoder 保持当前输出不变，相当于在 ID 阶段插入一个"影子状态"
//  bypass_stall     : 与 stall 同源的备用停顿信号。
//                      在顶层 rv32i_core 中两者接同一信号 pipeline_stall，
//                      设计保留两个端口是为了兼容不同的停顿来源扩展
//  wrong_predict_in : 分支预测失败冲刷信号，高有效。
//                      当 EX 阶段发现分支预测错误时置高，
//                      Decoder 必须立即插入 NOP（气泡），将错误取入的指令抹掉
//
//  ── 输出端口 ─────────────────────────────────────────────────────────────
//  source_reg_1 [4:0] : 源寄存器 1 地址（rs1），对应 instr[19:15]
//  source_reg_2 [4:0] : 源寄存器 2 地址（rs2），对应 instr[24:20]
//  dest_reg    [4:0] : 目的寄存器地址（rd）， 对应 instr[11:7]
//  opcode_out  [6:0] : 7-bit 操作码，对应 instr[6:0]
//  funct_3_out [2:0] : 3-bit 功能码，对应 instr[14:12]
//  funct_7_out [6:0] : 7-bit 功能码，对应 instr[31:25]
//  load_itype        : 组合逻辑输出，当前锁存指令是否为 Load 类型。
//                       该信号不经过寄存器，直接由 opcode_out 组合产生，
//                       确保冒险检测单元在同一个周期就能获得 Load 信息
//  instr_out  [31:0] : 锁存后的完整 32-bit 指令，供后续 EX/MEM 阶段使用
//                      注意：原设计宽度为 [24:0]，已修正为 [31:0]（保留完整指令）
//  pc_imm_out [31:0] : 锁存后的 PC + 立即数地址
//  pc_4_out   [31:0] : 锁存后的 PC + 4 地址
//
// ============================================================================
//  三、RISC-V 指令编码基础（理解字段切分的依据）
// ============================================================================
//
//  RISC-V RV32I 基本指令格式（32-bit）：
//
//   31        25 24     20 19     15 14   12 11      7 6          0
//  ┌─────────────┬─────────┬─────────┬───────┬─────────┬───────────┐
//  │   funct7    │   rs2   │   rs1   │funct3 │   rd    │  opcode   │
//  └─────────────┴─────────┴─────────┴───────┴─────────┴───────────┘
//
//  字段切分映射（R-type 视角，I-type 的 funct7 区域变为 12-bit imm）：
//    instr_in[6:0]    → opcode_out     : 指令大类（LOAD / STORE / BRANCH / OP-IMM / ...）
//    instr_in[14:12]  → funct_3_out    : 子功能码（如 BEQ vs BNE，ADD vs SUB）
//    instr_in[31:25]  → funct_7_out    : 进一步区分子功能（如 ADD vs SUB，SRL vs SRA）
//    instr_in[19:15]  → source_reg_1   : 第一个源操作数寄存器索引
//    instr_in[24:20]  → source_reg_2   : 第二个源操作数寄存器索引
//    instr_in[11:7]   → dest_reg       : 目的寄存器索引（I-type / R-type）
//
//  为什么不在此处进一步译码？
//    本模块只做"切分"，不判断指令类型。这是因为：
//    1. 所有指令的字段位置是统一的（RISC-V 编码的设计优势）
//    2. 复杂的控制信号（RegWrite, ALUSrc, MemtoReg 等）由 Control 模块
//       根据 opcode/funct3/funct7 生成，职责分离更清晰
//    3. 字段切分是纯连线操作，无需任何组合逻辑，不影响时序
//
// ============================================================================
//  四、流水线控制策略详解（优先级：reset > flush > stall > normal）
// ============================================================================
//
//  本模块实现的流水线寄存器有四种工作状态，按优先级从高到低排列：
//
//  ┌──────────┬──────────────────────────────────────────────────────────┐
//  │ 优先级   │ 触发条件                       │ 行为                     │
//  ├──────────┼──────────────────────────────────────────────────────────┤
//  │ 1 (最高) │ reset = 1                      │ 异步清零所有输出          │
//  │ 2        │ wrong_predict_in = 1            │ 插入 NOP 气泡             │
//  │ 3        │ stall = 1 或 bypass_stall = 1   │ 保持当前输出不变          │
//  │ 4 (最低) │ 以上全为 0                      │ 正常锁存新指令            │
//  └──────────┴──────────────────────────────────────────────────────────┘
//
//  【设计理由】
//
//  优先级 1 — reset 最高：
//    异步复位是全局行为，必须优先于一切流水线控制。复位后 CPU 从零状态启动，
//    所有寄存器输出清零，确保第一条指令从正确的初始条件开始执行。
//
//  优先级 2 — flush 高于 stall：
//    分支预测失败时，IF 阶段已经取入了错误的指令。即使当前有 stall，
//    错误指令也必须被立即抹掉，否则会被下游阶段执行，产生错误结果。
//    冲刷时将指令替换为 NOP（addi x0, x0, 0 = 32'h00000013）：
//      - opcode = `OPCODE_OP_IMM (0010011)，即 ADDI 指令
//      - rs1 = x0, rd = x0, imm = 0
//      - 执行结果：x0 + 0 → x0，不改变任何寄存器或内存状态
//    选择 NOP 而非全零是因为：
//      全零指令在 RISC-V 中是非法的（opcode=0000000 未定义），
//      会导致 Control 单元产生未定义的控制信号，可能触发意外行为。
//      NOP (0x00000013) 是一个合法的、明确的无操作指令。
//    source_reg_1/2、dest_reg 清零是为了一致性（x0 是硬连线零寄存器，
//    读取 x0 不依赖寄存器堆状态，避免误读）。
//
//  优先级 3 — stall 保持：
//    当流水线需要停顿时（最常见场景：load-use 数据冒险）：
//      lw  x1, 0(x2)     ← 在 MEM 阶段才能拿到数据
//      add x3, x1, x4    ← 在 EX 阶段就需要 x1 —— 来不及！
//    解决方式：让 Decoder（及 IF 阶段）停顿一个周期，EX 阶段插入气泡。
//    保持当前输出不变意味着：相同指令在下个周期继续驻留在 ID 阶段，
//    给了前级指令足够的时间完成（load 进入 MEM 返回数据，经前递给 EX）。
//
//    为什么 stall 和 bypass_stall 两个信号？
//      - stall         : 来自冒险检测单元的流水线停顿请求
//      - bypass_stall  : 来自数据前递单元的停顿请求（当需要两个周期才能前递时）
//      两者在顶层通常接同一信号，但分离端口使得各自可独立控制，
//      方便未来扩展不同的停顿策略。
//
//  优先级 4 — 正常锁存：
//    无任何异常时，每个时钟上升沿将 IF 阶段的新指令锁存到 ID 阶段寄存器。
//    这是流水线的正常推进模式，每个周期流入一条新指令。
//
// ============================================================================
//  五、关键内部信号说明
// ============================================================================
//
//  load_itype（组合逻辑输出）：
//    assign load_itype = (opcode_out == `OPCODE_LOAD);
//
//    为什么用组合逻辑而非时序逻辑？
//      数据冒险检测（Hazard Unit）需要在同一个周期内就知道 ID 阶段的
//      指令是否为 Load。如果放在时序逻辑中，会多出一个周期的延迟，
//      导致 load-use 冒险无法及时检测，可能需要更复杂的停顿逻辑。
//
//    为什么用 opcode_out（锁存后的值）而非 instr_in[6:0]？
//      在 stall 期间，instr_in 可能已经变化（IF 阶段可能继续取指），
//      但 ID 阶段仍然在等待。如果用 instr_in，会在 stall 期间误判指令类型。
//      使用 opcode_out（锁存后的值）能保证 load_itype 与 ID 阶段当前
//      实际持有的指令一致。
//
//    该信号的使用者：
//      Hazard Unit 检测到 load_itype=1 且下一条指令用到相同 rd 时，
//      触发 stall，暂停 IF 和 ID 阶段，直到 load 数据可用。
//
// ============================================================================
//  六、时序说明
// ============================================================================
//
//  ── 复位时序 ──
//  reset 是异步置位、同步释放：
//    - reset 上升沿立即清零所有寄存器（不等待 clk 上升沿）
//    - reset 下降沿后，下一个 clk 上升沿开始正常锁存
//
//  ── 正常流水 ──
//  每个 clk 上升沿：
//    IF 阶段输出（instr_in, pc_imm_in, pc_4_in）→ 锁存到 ID 阶段寄存器
//    即：第 N 条指令在周期 T 被 IF 取出，在周期 T+1 的上升沿锁存到 Decoder，
//        在周期 T+1 期间，ID 阶段对第 N 条指令进行译码
//
//  ── 停顿 ──
//  stall=1 时，即使 clk 上升沿到来，所有寄存器保持原值。
//  相当于 ID 阶段在周期 T+1 仍然"看"第 N 条指令，
//  而 IF 阶段需要在周期 T+1 重新取第 N+1 条指令（或保持 PC 不变）。
//
//  ── 冲刷 ──
//  wrong_predict_in=1 时，当前周期结束时插入 NOP，
//  下一个周期 ID 阶段看到的是气泡而非错误指令。
//  同时 IF 阶段需要将 PC 更新为正确的分支目标。
//

`include "Def.v"

module decoder (
    // ─────────── 输入：来自 IF 阶段的指令和地址 ───────────
    input  wire [31:0] instr_in,          // 原始 32-bit 指令字
    input  wire        bypass_stall,      // 前递停顿信号（与 stall 同源）
    input  wire        clk,               // 全局时钟
    input  wire        reset,             // 异步复位
    input  wire        stall,             // 流水线停顿信号
    input  wire [31:0] pc_imm_in,         // PC + 立即数（分支/跳转目标）
    input  wire [31:0] pc_4_in,           // PC + 4（顺序下一条地址）
    input  wire        wrong_predict_in,  // 分支预测失败冲刷信号

    // ─────────── 输出：RISC-V 标准字段切分 ───────────
    output reg  [4:0]  source_reg_1,      // rs1 地址 → 寄存器堆读端口1
    output reg  [4:0]  source_reg_2,      // rs2 地址 → 寄存器堆读端口2
    output reg  [4:0]  dest_reg,          // rd  地址 → 流水线传递至 WB 写回
    output reg  [6:0]  opcode_out,        // opcode  → Control 单元译码
    output reg  [2:0]  funct_3_out,       // funct3  → Control 单元译码
    output reg  [6:0]  funct_7_out,       // funct7  → Control 单元译码

    // ─────────── 输出：冒险检测辅助信号 ───────────
    output wire        load_itype,       // 组合输出：当前指令是否为 Load
                                          // 用于 Hazard Unit 做 load-use 冒险检测

    // ─────────── 输出：锁存后的完整指令和地址（传递至下游） ───────────
    output reg  [31:0] instr_out,         // 锁存后的完整指令（已修复宽度：[24:0]→[31:0]）
    output reg  [31:0] pc_imm_out,        // 锁存后的 PC + 立即数
    output reg  [31:0] pc_4_out           // 锁存后的 PC + 4
);

    // =========================================================================
    //  Load 指令检测 — 组合逻辑（无寄存器延迟）
    //
    //  检测当前锁存指令是否为 Load 类型（opcode = 0000011）。
    //  使用 opcode_out（锁存后）而非 instr_in（输入级），原因：
    //    - stall 期间 instr_in 可能已变化（IF 继续取指），但 ID 阶段
    //      实际持有的仍是被停顿的指令，必须用锁存值才能正确反映
    //    - 组合输出确保 Hazard Unit 在同一个周期就能读到该信号，
    //      无需再等一个时钟沿
    // =========================================================================
    assign load_itype = (opcode_out == `OPCODE_LOAD);

    // =========================================================================
    //  流水线寄存器 — 带优先级的多路选择控制
    //
    //  在 clk 上升沿或 reset 上升沿触发。
    //  reset 是异步的：一旦 reset=1，立即清零所有寄存器，不等待时钟。
    //
    //  优先级设计（if-else 链，从上到下递减）：
    //    1. reset            → 全局异步清零
    //    2. wrong_predict_in → 插入 NOP 气泡（冲刷错误指令）
    //    3. stall/bypass_stall → 保持当前值（流水线停顿）
    //    4. 默认              → 正常锁存新指令（流水线推进）
    // =========================================================================
    always @(posedge clk or posedge reset) begin
        // ── 情况 1：异步复位（最高优先级） ──
        // 系统上电或外部复位时，所有流水线寄存器归零。
        // 清零后 opcode_out = 0（非法指令编码），但 CPU 会在复位释放后
        // 从 PC=0 重新取指，第一个有效指令会覆盖这些零值。
        if (reset) begin
            source_reg_1  <= 5'b0;
            source_reg_2  <= 5'b0;
            dest_reg      <= 5'b0;
            opcode_out    <= 7'b0;
            funct_3_out   <= 3'b0;
            funct_7_out   <= 7'b0;
            instr_out     <= 32'b0;
            pc_imm_out    <= 32'b0;
            pc_4_out      <= 32'b0;
        end
        // ── 情况 2：分支预测失败冲刷（第二优先级） ──
        // 当 EX 阶段的分支解析发现预测错误时，IF 阶段取入的指令序列是错误的。
        // 此时必须立即将 ID 阶段的错误指令替换为 NOP，防止其继续流入 EX 阶段。
        //
        // NOP 编码选择：addi x0, x0, 0 = 32'h0000_0013
        //   - opcode  = 0010011 (OP-IMM, 即 ADDI)
        //   - funct3  = 000    (ADDI 子类型)
        //   - funct7  = 0000000
        //   - rs1     = x0 (5'b00000)
        //   - rd      = x0 (5'b00000)
        //   - imm     = 12'b0
        //   执行效果：x0 ← x0 + 0，不改变任何架构状态
        //
        // 为什么 source_reg_1/2 和 dest_reg 都置为 x0？
        //   - source_reg 读 x0：x0 在 RISC-V 中是硬连线零寄存器，
        //     读取不依赖寄存器堆状态，不会误读任何有效寄存器
        //   - dest_reg = x0：即使写使能意外打开，写入 x0 也无效果
        else if (wrong_predict_in) begin
            // ★ 优先级高于 stall：预测失败必须立即冲刷，无论是否停顿
            source_reg_1  <= 5'b0;
            source_reg_2  <= 5'b0;
            dest_reg      <= 5'b0;
            opcode_out    <= `OPCODE_OP_IMM;           // ADDI opcode = 7'b0010011
            funct_3_out   <= 3'b0;
            funct_7_out   <= 7'b0;
            instr_out     <= 32'h00000013;             // addi x0, x0, 0 (NOP)
            pc_imm_out    <= 32'b0;
            pc_4_out      <= 32'b0;
        end
        // ── 情况 3：流水线停顿（第三优先级） ──
        // stall 或 bypass_stall 任一有效即停顿。
        //
        // 停顿时的行为：所有寄存器保持当前值不变（x <= x）。
        // 这意味着 ID 阶段的指令在下一个周期仍然是同一条指令，
        // 下游 EX 阶段将看到一个重复的指令（通常 EX 阶段会同时插入气泡）。
        //
        // 典型停顿场景：
        //   - Load-Use 数据冒险：lw 后紧跟使用该数据的指令
        //     → Hazard Unit 置高 stall，ID 暂停，EX 插入 bubble
        //   - 多周期指令阻塞：除法等指令需要多个周期
        //     → stall 冻结前方流水级
        //
        // bypass_stall 与 stall 的关系：
        //   两者在顶层 rv32i_core 中通常接同一个信号（pipeline_stall），
        //   分离端口的设计为未来可能的不同停顿来源保留了灵活性。
        else if (stall || bypass_stall) begin
            // 保持所有输出不变（流水线停顿 / 数据转发停顿）
            source_reg_1  <= source_reg_1;
            source_reg_2  <= source_reg_2;
            dest_reg      <= dest_reg;
            opcode_out    <= opcode_out;
            funct_3_out   <= funct_3_out;
            funct_7_out   <= funct_7_out;
            instr_out     <= instr_out;
            pc_imm_out    <= pc_imm_out;
            pc_4_out      <= pc_4_out;
        end
        // ── 情况 4：正常流水线推进（最低优先级） ──
        // 无任何异常事件时，将 IF 阶段的输出锁存到 ID 阶段。
        // 字段切分依据 RISC-V 标准指令编码。
        //
        // 切分逻辑与 RISC-V 指令格式的对应：
        //   source_reg_1 = instr_in[19:15] → rs1（对所有格式通用）
        //   source_reg_2 = instr_in[24:20] → rs2（R/S/B 类型使用）
        //   dest_reg     = instr_in[11:7]  → rd （R/I/U/J 类型使用）
        //   opcode_out   = instr_in[6:0]   → 指令大类
        //   funct_3_out  = instr_in[14:12] → 子类型 / 分支条件 / 访存宽度
        //   funct_7_out  = instr_in[31:25] → R-type 子类型区分（I-type 时为 imm[11:5]）
        //
        // 注意：对于 I-type 指令（如 addi, lw），funct_7_out 捕获的是
        // 12-bit 立即数的高 7 位（imm[11:5]），而非真正的 funct7。
        // Control 单元会根据 opcode_out 来区分是否将其解释为 funct7。
        else begin
            // 正常锁存新指令并切分字段
            source_reg_1  <= instr_in[19:15];
            source_reg_2  <= instr_in[24:20];
            dest_reg      <= instr_in[11:7];
            opcode_out    <= instr_in[6:0];
            funct_3_out   <= instr_in[14:12];
            funct_7_out   <= instr_in[31:25];
            instr_out     <= instr_in;
            pc_imm_out    <= pc_imm_in;
            pc_4_out      <= pc_4_in;
        end
    end

endmodule
