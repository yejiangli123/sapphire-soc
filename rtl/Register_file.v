// ============================================================================
// Register_file.v — 32-entry RISC-V 寄存器堆（Register File）
// ============================================================================
//
// 【模块在 CPU 流水线中的角色】
//   寄存器堆是 CPU 数据通路的核心存储单元，位于指令译码（ID）阶段和执行（EX）
//   阶段之间。它接收来自 ID 阶段的读地址，将操作数送往 EX 阶段的 ALU；同时
//   接收来自写回（WB）阶段的写数据和写地址，完成寄存器更新。
//
//   在经典 5 级流水线（IF → ID → EX → MEM → WB）中，本模块承担两个关键职责：
//     1. 读操作（ID 阶段）  — 根据指令的 rs1/rs2 字段输出操作数 read_data1/2
//     2. 写操作（WB 阶段）  — 将 ALU 结果或访存数据写回目的寄存器 rd
//
// 【RISC-V 规范要求】
//   - x0 寄存器硬连线为常量 0，任何写操作不应改变 x0 的值
//   - 32 个 32 位通用寄存器（RV32I 基本整数指令集）
//   - 读端口为组合逻辑输出，实现低延迟（一个周期内完成 读→ALU→写）
//
// 【与其他流水线阶段的交互】
//   - 与 IF 阶段：无直接交互（IF 阶段访问指令存储器）
//   - 与 ID 阶段：ID 阶段提供 read_address1/2（rs1/rs2 域），接收 read_data1/2
//   - 与 EX 阶段：读出的操作数直接送入 ALU 进行计算
//   - 与 MEM 阶段：无直接交互（MEM 阶段访问数据存储器）
//   - 与 WB 阶段：WB 阶段提供 write_enable/write_address/write_data 完成写回
//   - 与 Forward Unit：旁路单元比较 EX/MEM/WB 阶段的 rd 地址与 ID 阶段 rs1/rs2，
//     若匹配则绕过寄存器堆直接前递最新结果
// ============================================================================

module reg_file #(
    // ADDR_WIDTH = 5 对应 2^5 = 32 个寄存器（RISC-V RV32I 规范要求）
    // 若需要支持 RV64I，可将 DATA_WIDTH 改为 64，ADDR_WIDTH 保持 5
    parameter ADDR_WIDTH = 5,
    // DATA_WIDTH = 32 对应 RV32I 的 32 位数据通路宽度
    // 保持参数化是为了便于扩展（例如 RV64I、或非标准位宽设计）
    parameter DATA_WIDTH = 32
) (
    // =========================================================================
    // 时钟与复位
    // =========================================================================
    input  wire                     clk,
    //      clk — 系统时钟，所有时序逻辑（写操作）在上升沿触发。
    //            在同步设计中，读操作为组合逻辑，与时序无关，
    //            因此读端口不依赖 clk。

    input  wire                     reset,
    //      reset — 异步复位信号（高有效）。
    //              当 reset=1 时，所有 32 个寄存器被清零。
    //              采用异步复位可确保上电后立即进入已知状态，
    //              而不需要等待第一个时钟沿。

    // =========================================================================
    // 读端口 1（对应 RISC-V 指令的 rs1 源操作数 1）
    // =========================================================================
    input  wire [ADDR_WIDTH-1:0]    read_address1,
    //      read_address1 — 5 位读地址，通常来自指令的 rs1 字段 [19:15]。
    //                      组合逻辑读取对应寄存器的值输出到 read_data1。
    //                      若地址为 0（x0），强制输出 0。

    // =========================================================================
    // 读端口 2（对应 RISC-V 指令的 rs2 源操作数 2）
    // =========================================================================
    input  wire [ADDR_WIDTH-1:0]    read_address2,
    //      read_address2 — 5 位读地址，通常来自指令的 rs2 字段 [24:20]。
    //                      组合逻辑读取对应寄存器的值输出到 read_data2。
    //                      若地址为 0（x0），强制输出 0。

    // =========================================================================
    // 写端口（对应 RISC-V 指令的 rd 目的寄存器）
    // =========================================================================
    input  wire                     write_enable,
    //      write_enable — 写使能信号（高有效）。
    //                      由 WB 阶段的控制器产生。当指令需要写回结果
    //                      （如 R-type 运算、I-type 加载等）时置 1；
    //                      对分支、存储等无需写回的指令置 0。
    //                      这样可以避免无效写操作，降低功耗。

    input  wire [ADDR_WIDTH-1:0]    write_address,
    //      write_address — 5 位写地址，通常来自指令的 rd 字段 [11:7]。
    //                      在 clk 上升沿且 write_enable=1 时，
    //                      将 write_data 写入对应寄存器。
    //                      若写地址为 0（x0），写操作被硬件屏蔽（符合 RISC-V 规范）。

    input  wire [DATA_WIDTH-1:0]    write_data,
    //      write_data — 32 位写数据，来源可能是：
    //                    - ALU 计算结果（R-type / I-type 算术指令）
    //                    - 数据存储器读回的值（load 指令）
    //                    - PC+4 或 PC+imm（JAL / JALR 跳转指令）
    //                    - CSR 寄存器读值（CSR 指令）

    // =========================================================================
    // 读数据输出
    // =========================================================================
    output wire [DATA_WIDTH-1:0]    read_data1,
    //      read_data1 — 32 位读数据 1，送往 ALU 的源操作数 A 输入端。
    //                   对于 x0 地址，硬件强制输出 32'h0。

    output wire [DATA_WIDTH-1:0]    read_data2
    //      read_data2 — 32 位读数据 2，送往 ALU 的源操作数 B 输入端
    //                   （或送往存储单元作为 store 数据）。
    //                   对于 x0 地址，硬件强制输出 32'h0。
);

    // =========================================================================
    // 本地参数：寄存器总数 = 2^ADDR_WIDTH = 32
    // =========================================================================
    // 使用 1 << ADDR_WIDTH 而非硬编码 32，使模块对 ADDR_WIDTH 参数化友好
    localparam REG_DEPTH = 1 << ADDR_WIDTH;

    // =========================================================================
    // 寄存器阵列（32 个 DATA_WIDTH 位宽的寄存器）
    // =========================================================================
    // reg_filex 是实际的存储单元，综合后映射为触发器或 SRAM。
    // 在 FPGA 上通常映射为分布式 RAM（LUTRAM）或 Block RAM；
    // 在 ASIC 上通常映射为寄存器阵列或 SRAM 宏单元。
    reg [DATA_WIDTH-1:0] reg_filex [REG_DEPTH-1:0];

    // =========================================================================
    // 仿真初始化（仅仿真生效，方便波形调试）
    // =========================================================================
    // 设计理由：
    //   - 实际硬件上电后寄存器值不确定（X 态），但综合工具会忽略 initial 块，
    //     因此用 `ifndef SYNTHESIS 包裹，确保不影响综合结果。
    //   - 在仿真中，将寄存器初始化为 16 * index 的已知值（x0=0, x1=16, x2=32...），
    //     便于在波形中快速识别读出的数据来自哪个寄存器，
    //     帮助调试数据相关性和转发逻辑。
    //   - 不做随机初始化是为了保证仿真结果可复现（deterministic）。
    `ifndef SYNTHESIS
    integer init_i;
    initial begin
        for (init_i = 0; init_i < REG_DEPTH; init_i = init_i + 1)
            reg_filex[init_i] = 16 * init_i;
    end
    `endif

    // =========================================================================
    // 写操作：异步复位 + 条件写入（x0 写保护）
    // =========================================================================
    // 时序逻辑描述：
    //   always @(posedge clk or posedge reset)  — 异步复位风格。
    //     当时钟上升沿或复位上升沿到来时触发。
    //
    // 分支逻辑：
    //   1. if (reset)                                    → 复位分支
    //      将所有 32 个寄存器清零。
    //      为什么需要异步复位？
    //        - CPU 启动时，寄存器堆必须处于已知状态，避免 X 态传播导致
    //          后续指令计算错误。
    //        - 异步复位保证 reset 有效时立即清零，无需等待 clk。
    //
    //   2. else if (write_enable && (write_address != 0)) → 正常写分支
    //      条件解析：
    //        a) write_enable = 1：指令需要写回结果。由 WB 控制器根据
    //           指令类型（RegWrite 信号）生成。
    //        b) write_address != 0：x0 写保护。
    //           根据 RISC-V 规范，x0 寄存器硬连线为常量 0，
    //           任何对 x0 的写入都必须是 NOP（不改变 x0 的值）。
    //           这里通过比较 write_address 与全零地址来实现。
    //           即使软件误写了 x0，硬件也能保证 x0 始终为 0。
    //      操作为非阻塞赋值（<=），保证在时钟上升沿后才更新，
    //      避免读写竞争（read 看到的是写之前的值）。
    //
    //   3. 隐含的 else：无操作（保持当前值）
    //      当 write_enable=0 或写目标为 x0 时，所有寄存器保持不变。
    //      这是低功耗设计的考量——不需要写时避免不必要的翻转。
    integer i;
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // ============================================================
            // 复位：所有寄存器清零
            // ============================================================
            for (i = 0; i < REG_DEPTH; i = i + 1)
                reg_filex[i] <= {DATA_WIDTH{1'b0}};
        end
        else if (write_enable && (write_address != {ADDR_WIDTH{1'b0}})) begin
            // ============================================================
            // 正常写操作：
            //   - write_enable 有效（指令需要写回）
            //   - 且目的寄存器不是 x0（RISC-V 规范要求 x0 不可写）
            // ============================================================
            reg_filex[write_address] <= write_data;
        end
        // 隐式的 else：不满足写条件时，寄存器保持原值不变
    end

    // =========================================================================
    // 读端口（组合逻辑，x0 强制为 0 双重保险）
    // =========================================================================
    // 设计理由：
    //   1. 为什么是组合逻辑？
    //      - 读操作必须在同一个时钟周期内完成，不能插入流水级延迟。
    //        在 ID 阶段，指令译码后即刻需要操作数，若读操作为时序逻辑，
    //        则需要额外一个周期才能获得操作数，这会增加 CPI。
    //      - 综合工具通常将组合读映射为多路选择器（MUX），面积小且延迟低。
    //
    //   2. 为什么需要 x0 双重保护？
    //      - 第一重保护在写端口（write_address != 0 条件），防止写入 x0。
    //      - 第二重保护在读端口（MUX 前判断），即使 x0 被意外写入
    //        （例如因单粒子翻转 SEU 导致存储位翻转），读端口仍强制输出 0。
    //      - 这种"纵深防御"策略是可靠性设计的常见实践。
    //
    //   3. 为什么使用 assign（连续赋值）而非 always_comb？
    //      - 两种写法等效。assign 更简洁地表达了"读端口 = MUX"的意图。
    //      - 对于这种纯组合的 MUX 逻辑，assign 的仿真性能略好。
    //
    //   4. 为什么是三目运算符（?:）而非 if-else？
    //      - 三目运算符表达"二选一 MUX"的硬件结构更为直观。
    //      - 综合结果完全一致（都是 MUX + 比较器）。
    assign read_data1 = (read_address1 == {ADDR_WIDTH{1'b0}})
                      ? {DATA_WIDTH{1'b0}}
                      : reg_filex[read_address1];
    //      read_data1 — 若读地址为 0（x0），强制输出 0；
    //                   否则从寄存器阵列中读取对应寄存器的值。

    assign read_data2 = (read_address2 == {ADDR_WIDTH{1'b0}})
                      ? {DATA_WIDTH{1'b0}}
                      : reg_filex[read_address2];
    //      read_data2 — 若读地址为 0（x0），强制输出 0；
    //                   否则从寄存器阵列中读取对应寄存器的值。

endmodule
