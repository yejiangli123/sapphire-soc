// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                    axi_lite_manager.v — AXI4-Lite 管理器                      ║
// ║                    CPU 简单内存接口 ↔ AXI4-Lite 总线桥                         ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ================================================================================
// 一、模块定位与架构角色
// ================================================================================
// 本模块是 SoC 内部 CPU Core（rv32i_core）与片外/片上 AXI4-Lite 互联总线之间的
// 协议转换桥。CPU 侧发出最简单的三信号请求（addr, wdata/rdata, we/re），本模块
// 将其翻译为 AXI4-Lite 标准 5 通道握手协议。
//
//   CPU 侧接口（简单）                AXI4-Lite 侧接口（标准 5 通道）
//   ┌──────────┐                    ┌──────────────────────┐
//   │ addr     │──┐              ┌──│ AWADDR/AWVALID/AWREADY │  写地址通道
//   │ wdata    │  │   ┌──────┐   │  │ WDATA/WSTRB/WVALID/WREADY│  写数据通道
//   │ we/re    │──┼──▶│ 本   │──▶│  │ BRESP/BVALID/BREADY    │  写响应通道
//   │ rdata    │◀─┼───│ 模块 │◀──┼──│ ARADDR/ARVALID/ARREADY │  读地址通道
//   │ ready    │◀─┘   └──────┘   └──│ RDATA/RRESP/RVALID/RREADY│  读数据通道
//   └──────────┘                    └──────────────────────┘
//
// 在 SoC 系统中的位置：
//   rv32i_core → axi_lite_manager → AXI Interconnect → 各外设（UART/GPIO/RAM等）
//
// ================================================================================
// 二、设计原则与关键决策
// ================================================================================
//
// 2.1 读写分离状态机
//   - 写事务和读事务各自独立的状态机并行运行，互不阻塞对方的状态跳转。
//   - 但通过 write_active 标志实现互斥仲裁：同一时刻只允许一个事务占用 CPU 侧
//     地址/数据锁存通道。
//   - 仲裁策略：写优先（写事务进行中时，读事务不可启动）。
//
// 2.2 地址/数据锁存机制
//   - CPU 侧的 addr/wdata 是组合逻辑信号，仅在 we/re 有效时稳定。
//   - 必须在其有效边沿立即锁存到内部寄存器（awaddr_reg / wdata_reg / araddr_reg），
//     防止后续状态切换时原始信号变化导致 AXI 侧输出错误。
//   - 锁存条件：we/re 有效 且 对应状态机处于 IDLE 且 无冲突事务占用。
//
// 2.3 Ready 单周期脉冲
//   - ready 信号仅在事务完成的那一个周期拉高，形成脉冲。
//   - 写完成：W_RESP 状态且 BVALID 有效 → ready=1（1 周期）。
//   - 读完成：R_RDATA 状态且 RVALID 有效 → ready=1 + rdata 锁存（1 周期）。
//   - 其余所有时刻 ready=0。这样 CPU 侧只需在任意时刻采样 ready 即可判断事务
//     是否完成，无需状态机跟踪。
//
// 2.4 AXI 握手协议实现
//   - VALID 由主设备（本模块）控制：进入对应状态时拉高，收到 READY 后拉低。
//   - READY 由从设备（外设）控制：本模块在对应状态等待 READY 后推进。
//   - 遵循 AXI4-Lite 规范：VALID 不可依赖 READY 才拉高（但可同时）。
//
// 2.5 异步复位（高有效）
//   - 复位极性为高有效，与 rv32i_core 保持一致，避免顶层混用正/负复位。
//   - 所有寄存器在复位时清零，确保上电后状态确定。
//
// 2.6 AXI 错误响应检测
//   - BRESP ≠ 0（写响应错误）或 RRESP ≠ 0（读响应错误）时，通过 $display
//     打印仿真告警，但不阻塞状态机推进（事务仍正常结束）。
//   - 这是仿真辅助手段，综合时 $display 通常被忽略或替换为用户自定义错误处理。
//
// 2.7 字节选通（WSTRB）
//   - CPU 当前仅支持 32 位字访问，因此 WSTRB 固定为 4'b1111（4 字节全部有效）。
//   - 若未来 CPU 支持 sub-word 访问（lh/lb/sb/sh），需要将 WSTRB 改为根据
//     地址低 2 位动态生成。
//
// ================================================================================
// 三、状态机编码
// ================================================================================
// 写状态机（4 个状态）：
//   W_IDLE  (2'd0): 空闲，等待 CPU 写请求（we=1 且无冲突）
//   W_AW    (2'd1): 写地址握手——驱动 AWADDR/AWVALID，等待 AWREADY
//   W_WDATA (2'd2): 写数据握手——驱动 WDATA/WSTRB/WVALID，等待 WREADY
//   W_RESP  (2'd3): 写响应握手——驱动 BREADY，等待 BVALID，检测 BRESP
//
// 读状态机（3 个状态）：
//   R_IDLE  (2'd0): 空闲，等待 CPU 读请求（re=1 且 we=0，写优先）
//   R_AR    (2'd1): 读地址握手——驱动 ARADDR/ARVALID，等待 ARREADY
//   R_RDATA (2'd2): 读数据握手——驱动 RREADY，等待 RVALID，锁存 RDATA，检测 RRESP
//
// ================================================================================
// 四、修复清单（相对旧版）
// ================================================================================
//   [1] 补齐读状态机（旧版注释 "类似" 但逻辑不完整，缺少完整三态跳转）
//   [2] 修复地址/数据寄存器在非 IDLE 状态被意外覆盖的问题（增加 w_state==W_IDLE /
//       r_state==R_IDLE 条件保护）
//   [3] 同时读写互斥仲裁（写优先，通过 write_active 实现）
//   [4] ready 改为脉冲信号（仅 1 周期有效），避免持续高导致 CPU 误判多笔完成
//   [5] 异步复位（高有效，与 CPU 一致）
//   [6] AXI 错误响应检测（BRESP/RRESP ≠ 0 → $display 告警）
//   [7] 所有内部寄存器在复位时初始化为 0

module axi_lite_manager (
    // ==================================================================
    // 时钟与复位
    // ==================================================================
    input  wire        clk,               // 系统时钟（与 CPU 同频同相）
    input  wire        reset,             // 异步复位，高有效（与 CPU 一致）

    // ==================================================================
    // CPU 侧简单接口（5 个信号，极简设计）
    // ==================================================================
    // addr[31:0]：CPU 发出的读写目标地址（字节寻址，32 位对齐）
    // wdata[31:0]：CPU 发出的写入数据（仅 we=1 时有效）
    // we：写使能，高有效。与 re 互斥，同时有效时 we 优先
    // re：读使能，高有效。仅在 we=0 时被响应
    // rdata[31:0]：返回给 CPU 的读取数据（读完成时更新）
    // ready：事务完成脉冲。1 周期高电平，指示 CPU 可发起下一请求
    input  wire [31:0] addr,
    input  wire [31:0] wdata,
    input  wire        we,                // 写使能（高有效）
    input  wire        re,                // 读使能（高有效）
    output reg  [31:0] rdata,
    output reg         ready,             // 事务完成脉冲（单周期）

    // ==================================================================
    // AXI4-Lite 写地址通道（AW — Write Address）
    // ==================================================================
    // AWADDR[31:0]：写事务目标地址，在 W_AW 状态驱动
    // AWVALID：写地址有效标志，主设备（本模块）控制在 W_AW 状态拉高
    // AWREADY：从设备准备好接收写地址，由外设控制
    output reg  [31:0] AWADDR,
    output reg         AWVALID,
    input  wire        AWREADY,

    // ==================================================================
    // AXI4-Lite 写数据通道（W — Write Data）
    // ==================================================================
    // WDATA[31:0]：写入数据，在 W_WDATA 状态驱动
    // WSTRB[3:0]：字节选通，每个 bit 对应一个字节写使能。当前固定 4'b1111
    // WVALID：写数据有效标志，在 W_WDATA 状态拉高
    // WREADY：从设备准备好接收写数据，由外设控制
    output reg  [31:0] WDATA,
    output reg  [3:0]  WSTRB,             // 字节选通（固定 4'b1111，字访问）
    output reg         WVALID,
    input  wire        WREADY,

    // ==================================================================
    // AXI4-Lite 写响应通道（B — Write Response）
    // ==================================================================
    // BRESP[1:0]：写响应状态码（00=OK, 10=SLVERR, 11=DECERR），从设备返回
    // BVALID：写响应有效标志，从设备控制在响应时拉高
    // BREADY：主设备准备好接收写响应，本模块在 W_RESP 状态拉高
    input  wire [1:0]  BRESP,
    input  wire        BVALID,
    output reg         BREADY,

    // ==================================================================
    // AXI4-Lite 读地址通道（AR — Read Address）
    // ==================================================================
    // ARADDR[31:0]：读事务目标地址，在 R_AR 状态驱动
    // ARVALID：读地址有效标志，在 R_AR 状态拉高
    // ARREADY：从设备准备好接收读地址，由外设控制
    output reg  [31:0] ARADDR,
    output reg         ARVALID,
    input  wire        ARREADY,

    // ==================================================================
    // AXI4-Lite 读数据通道（R — Read Data）
    // ==================================================================
    // RDATA[31:0]：读取返回数据，从设备驱动，事务完成时锁存到 rdata
    // RRESP[1:0]：读响应状态码（00=OK, 10=SLVERR, 11=DECERR）
    // RVALID：读数据有效标志，从设备控制在返回数据时拉高
    // RREADY：主设备准备好接收读数据，本模块在 R_RDATA 状态拉高
    input  wire [31:0] RDATA,
    input  wire [1:0]  RRESP,
    input  wire        RVALID,
    output reg         RREADY
);

    // ============================================================================
    // 内部信号定义
    // ============================================================================

    // ------------------------------------------------------------------
    // 状态机编码 — 使用 localparam 定义状态常量，便于仿真波形查看
    // ------------------------------------------------------------------
    // 写状态机：4 个状态，2 bit 编码
    localparam W_IDLE  = 2'd0;            // 空闲，等待写请求
    localparam W_AW    = 2'd1;            // 写地址握手
    localparam W_WDATA = 2'd2;            // 写数据握手
    localparam W_RESP  = 2'd3;            // 等待/接收写响应

    // 读状态机：3 个状态，2 bit 编码（注意：读状态机独立于写，状态值可重叠）
    localparam R_IDLE  = 2'd0;            // 空闲，等待读请求
    localparam R_AR    = 2'd1;            // 读地址握手
    localparam R_RDATA = 2'd2;            // 等待/接收读数据

    // ------------------------------------------------------------------
    // 状态寄存器 — 当前状态与下一状态（标准两段式状态机）
    // ------------------------------------------------------------------
    // w_state / r_state：当前状态，由时序逻辑驱动
    // w_next  / r_next ：下一状态，由组合逻辑计算
    reg [1:0] w_state, w_next;            // 写状态机寄存器
    reg [1:0] r_state, r_next;            // 读状态机寄存器

    // ------------------------------------------------------------------
    // 地址/数据锁存寄存器 — 在事务发起瞬间锁存 CPU 侧的 addr/wdata
    // ------------------------------------------------------------------
    // awaddr_reg：写地址锁存。在 W_IDLE 且 we=1 时锁存 addr，防止被后续 addr
    //            变化（可能指向下一次请求）覆盖
    // wdata_reg：写数据锁存。与 awaddr_reg 同时锁存，同理保护
    // araddr_reg：读地址锁存。在 R_IDLE 且 re=1 时锁存 addr
    reg [31:0] awaddr_reg;                // 写地址锁存
    reg [31:0] wdata_reg;                 // 写数据锁存
    reg [31:0] araddr_reg;                // 读地址锁存

    // ------------------------------------------------------------------
    // 互斥仲裁标志
    // ------------------------------------------------------------------
    // write_active：写事务正在执行标志。
    //   置位：we=1 且 w_state==W_IDLE 且 !write_active（发起写事务）
    //   清零：w_state==W_RESP 且 BVALID==1（写事务完成）
    //   作用：阻断读事务发起（写优先策略），防止读写同时占用总线导致混乱
    reg        write_active;


    // ============================================================================
    // 时序逻辑 1：状态机当前状态更新（复位 + 正常推进）
    // ============================================================================
    // 这是标准两段式状态机的"状态寄存器段"：
    //   - 复位时将 w_state/r_state 置为 IDLE
    //   - 每个时钟上升沿将组合逻辑计算的 w_next/r_next 打入当前状态
    // 采用异步复位（posedge reset）：复位信号一到立即清零，不等待时钟沿
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            w_state      <= W_IDLE;       // 写状态机复位到空闲
            r_state      <= R_IDLE;       // 读状态机复位到空闲
            awaddr_reg   <= 32'b0;        // 写地址锁存清零
            wdata_reg    <= 32'b0;        // 写数据锁存清零
            araddr_reg   <= 32'b0;        // 读地址锁存清零
            AWADDR       <= 32'b0;        // AXI 写地址输出清零
            AWVALID      <= 1'b0;         // AXI 写地址有效清零
            WDATA        <= 32'b0;        // AXI 写数据输出清零
            WSTRB        <= 4'b0;         // AXI 字节使能清零
            WVALID       <= 1'b0;         // AXI 写数据有效清零
            BREADY       <= 1'b0;         // AXI 写响应就绪清零
            ARADDR       <= 32'b0;        // AXI 读地址输出清零
            ARVALID      <= 1'b0;         // AXI 读地址有效清零
            RREADY       <= 1'b0;         // AXI 读数据就绪清零
            rdata        <= 32'b0;        // CPU 读数据输出清零
            ready        <= 1'b0;         // CPU 就绪信号清零
            write_active <= 1'b0;         // 写事务激活标志清零
        end
        else begin
            w_state <= w_next;            // 写状态机推进
            r_state <= r_next;            // 读状态机推进
        end
    end


    // ============================================================================
    // 组合逻辑：写状态机下一状态计算
    // ============================================================================
    // 写事务流程（4 步，严格顺序）：
    //   W_IDLE → W_AW → W_WDATA → W_RESP → W_IDLE
    //
    // 每步推进条件：
    //   W_IDLE → W_AW   : we=1 且无写事务进行中（write_active=0）
    //   W_AW   → W_WDATA : AWREADY=1（从设备已接收地址）
    //   W_WDATA → W_RESP : WREADY=1（从设备已接收数据）
    //   W_RESP  → W_IDLE : BVALID=1（从设备返回响应）→ 同时检测 BRESP 错误
    //
    // 注意：状态机不会自动回退；若握手条件不满足，则保持当前状态等待
    always @(*) begin
        w_next = w_state;                 // 默认保持当前状态
        case (w_state)
            W_IDLE:  if (we && !write_active)             w_next = W_AW;
            W_AW:    if (AWREADY)                         w_next = W_WDATA;
            W_WDATA: if (WREADY)                          w_next = W_RESP;
            W_RESP:  if (BVALID) begin
                // $display 为仿真辅助：检测从设备返回的写错误响应
                // BRESP: 00=OKAY, 01=EXOKAY(不支持), 10=SLVERR, 11=DECERR
                if (BRESP != 2'b00) $display("AXI Write Error: BRESP=%b", BRESP);
                w_next = W_IDLE;
            end
        endcase
    end


    // ============================================================================
    // 组合逻辑：读状态机下一状态计算
    // ============================================================================
    // 读事务流程（3 步，严格顺序）：
    //   R_IDLE → R_AR → R_RDATA → R_IDLE
    //
    // 每步推进条件：
    //   R_IDLE → R_AR   : re=1 且 we=0（写优先：若同时有写请求，写先被服务）
    //   R_AR   → R_RDATA : ARREADY=1（从设备已接收读地址）
    //   R_RDATA → R_IDLE : RVALID=1（从设备返回读数据）→ 同时检测 RRESP 错误
    //
    // 写优先（we 优先）设计理由：
    //   - 若 CPU 发出的是 sw 指令（store），数据已锁存到 wdata_reg，延迟写入
    //     可能导致数据不一致（如后续 lw 读到旧值）。
    //   - 若 CPU 发出的是 lw 指令（load），读事务延迟最多增加几个周期延迟，
    //     但不会导致数据错误。
    //   - 因此写优先是更安全的选择。
    always @(*) begin
        r_next = r_state;                 // 默认保持当前状态
        case (r_state)
            R_IDLE:  if (re && !we)                       r_next = R_AR;
            R_AR:    if (ARREADY)                         r_next = R_RDATA;
            R_RDATA: if (RVALID) begin
                // $display 为仿真辅助：检测从设备返回的读错误响应
                // RRESP: 00=OKAY, 01=EXOKAY(不支持), 10=SLVERR, 11=DECERR
                if (RRESP != 2'b00) $display("AXI Read Error: RRESP=%b", RRESP);
                r_next = R_IDLE;
            end
        endcase
    end


    // ============================================================================
    // 时序逻辑 2：写通道控制（地址锁存 + AXI 握手信号生成）
    // ============================================================================
    // 本 always 块管理所有写通道的 AXI 输出信号以及写事务互斥标志。
    // 设计要点：
    //   1. 地址/数据锁存发生在 W_IDLE 状态，仅此一次，后续状态使用锁存值。
    //      这是关键设计——防止在 W_AW/W_WDATA 状态时 addr/wdata 变化导致
    //      输出到 AXI 总线上的地址/数据中途改变。
    //   2. AXI VALID 信号的拉高条件精确匹配状态（w_state == W_AW / W_WDATA），
    //      拉低条件是离开对应状态或收到 READY。
    //   3. write_active 标志贯穿整个写事务，在 W_IDLE 置位，在 W_RESP 完成时清零。
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            AWADDR       <= 32'b0;
            AWVALID      <= 1'b0;
            WDATA        <= 32'b0;
            WVALID       <= 1'b0;
            BREADY       <= 1'b0;
            write_active <= 1'b0;
        end
        else begin
            // ----------------------------------------------------------------
            // 步骤 1：地址/数据锁存（仅在空闲且收到写请求时执行一次）
            // ----------------------------------------------------------------
            // 条件：we=1 且写状态机空闲 且 无写事务正在执行
            // 为什么检查 write_active？因为写状态机和 write_active 由两个不同
            // always 块驱动，w_state==W_IDLE 并不意味着 write_active 已清零
            // （可能在同一个周期刚从 W_RESP 跳转，write_active 尚未更新）。
            // 双重检查确保绝不在事务进行中重新锁存。
            if (we && w_state == W_IDLE && !write_active) begin
                awaddr_reg   <= addr;     // 锁存写目标地址（来自 CPU addr 总线）
                wdata_reg    <= wdata;    // 锁存写数据（来自 CPU wdata 总线）
                write_active <= 1'b1;     // 标记写事务激活，阻止读事务
            end

            // ----------------------------------------------------------------
            // 步骤 2：AW（写地址）通道控制
            // ----------------------------------------------------------------
            // 仅在 W_AW 状态驱动 AWADDR 和 AWVALID。
            // 离开 W_AW 状态或收到 AWREADY 后，AWVALID 拉低。
            // AXI 规范要求：VALID 一旦拉高，必须保持直到握手完成（READY 同时有效）。
            // 这里使用 "else if (w_state != W_AW || AWREADY)" 来确保：
            //   - 仍在 W_AW 但 AWREADY=1 → 握手完成，下周期拉低 AWVALID
            //   - 已离开 W_AW → 立即拉低 AWVALID
            if (w_state == W_AW) begin
                AWADDR  <= awaddr_reg;    // 驱动已锁存的写地址到 AXI 总线
                AWVALID <= 1'b1;          // 通知从设备：地址已就绪
            end
            else if (w_state != W_AW || AWREADY) begin
                AWVALID <= 1'b0;          // 退出或握手完成后拉低
            end

            // ----------------------------------------------------------------
            // 步骤 3：W（写数据）通道控制
            // ----------------------------------------------------------------
            // 仅在 W_WDATA 状态驱动 WDATA、WSTRB 和 WVALID。
            // WSTRB 固定为 4'b1111，表示 32 位全字写入。
            // 若未来 CPU 支持字节/半字访问，需根据地址低 2 位和访问宽度动态生成：
            //   字节写（sb）：WSTRB = 4'b0001 << addr[1:0]
            //   半字写（sh）：WSTRB = 4'b0011 << addr[1:0]（要求地址对齐）
            //   字写（sw）  ：WSTRB = 4'b1111
            if (w_state == W_WDATA) begin
                WDATA  <= wdata_reg;      // 驱动已锁存的写数据到 AXI 总线
                WSTRB  <= 4'b1111;        // 字节使能：全 4 字节有效（字访问）
                WVALID <= 1'b1;           // 通知从设备：数据已就绪
            end
            else if (w_state != W_WDATA || WREADY) begin
                WVALID <= 1'b0;           // 退出或握手完成后拉低
                WSTRB  <= 4'b0;           // 非写数据状态时字节使能清零
            end

            // ----------------------------------------------------------------
            // 步骤 4：B（写响应）通道控制
            // ----------------------------------------------------------------
            // BREADY 在 W_RESP 状态持续拉高，表示主设备准备好接收响应。
            // 从设备在 BVALID 拉高且 BREADY 也为高时完成握手。
            // 简化设计：BREADY 直接等于 (w_state == W_RESP)，不依赖其他条件。
            BREADY <= (w_state == W_RESP);

            // ----------------------------------------------------------------
            // 步骤 5：写事务结束 — 清除互斥标志
            // ----------------------------------------------------------------
            // 当写响应握手完成（W_RESP 状态且 BVALID 有效），释放总线互斥锁。
            // 此后读事务可被允许发起。
            if (w_state == W_RESP && BVALID)
                write_active <= 1'b0;
        end
    end


    // ============================================================================
    // 时序逻辑 3：读通道控制（地址锁存 + AXI 握手信号生成）
    // ============================================================================
    // 本 always 块管理所有读通道的 AXI 输出信号。
    // 设计原理与写通道相同：在 R_IDLE 锁存地址，之后用锁存值驱动 ARADDR。
    //
    // 注意：读通道不需要互斥标志——读事务发起条件中已包含 !we 检查（写优先），
    // 且读事务本身不修改数据，不会有 write_active 那样的阻塞需求。
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            ARADDR  <= 32'b0;
            ARVALID <= 1'b0;
            RREADY  <= 1'b0;
        end
        else begin
            // ----------------------------------------------------------------
            // 步骤 1：读地址锁存（仅在空闲且收到纯读请求时执行一次）
            // ----------------------------------------------------------------
            // 条件：re=1 且 we=0 且读状态机空闲
            // we=0 检查实现写优先：若同一周期 we 也为 1，则不锁存读地址，
            // 让写事务先使用 addr 总线。
            if (re && !we && r_state == R_IDLE)
                araddr_reg <= addr;       // 锁存读目标地址

            // ----------------------------------------------------------------
            // 步骤 2：AR（读地址）通道控制
            // ----------------------------------------------------------------
            // 仅在 R_AR 状态驱动 ARADDR 和 ARVALID。
            // 握手完成后（ARREADY=1）或离开 R_AR 状态后拉低 ARVALID。
            if (r_state == R_AR) begin
                ARADDR  <= araddr_reg;    // 驱动已锁存的读地址到 AXI 总线
                ARVALID <= 1'b1;          // 通知从设备：读地址已就绪
            end
            else if (r_state != R_AR || ARREADY) begin
                ARVALID <= 1'b0;          // 退出或握手完成后拉低
            end

            // ----------------------------------------------------------------
            // 步骤 3：R（读数据）通道控制
            // ----------------------------------------------------------------
            // RREADY 在 R_RDATA 状态持续拉高，表示主设备准备好接收读数据。
            // 当 RREADY 和 RVALID 同时为高时，RDATA 被锁存到 CPU 侧 rdata。
            // 简化设计：RREADY 直接等于 (r_state == R_RDATA)。
            RREADY <= (r_state == R_RDATA);
        end
    end


    // ============================================================================
    // 时序逻辑 4：CPU 侧输出生成（rdata + ready）
    // ============================================================================
    // 这是 CPU 看到的唯一反馈：
    //   - rdata：读取结果（仅在读完成时更新，其他时刻保持上一次值）
    //   - ready：事务完成脉冲（仅 1 个周期高电平）
    //
    // 设计要点：
    //   1. ready 是脉冲信号，不是电平信号。这确保 CPU 每笔请求只看到一次
    //      ready=1，不会把一次完成误解为多次。
    //   2. 写完成和读完成的 ready 产生条件不同，但最终都驱动同一个 ready 输出
    //      （CPU 不需要区分是哪种事务完成——它自己知道发了什么请求）。
    //   3. rdata 直接从 AXI 的 RDATA 总线取值（在 RVALID 有效的同一周期锁存），
    //      这是 AXI 规范规定的：RVALID 和 RDATA 在同一周期有效。
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            rdata <= 32'b0;               // 复位时读数据清零
            ready <= 1'b0;                // 复位时 ready 拉低
        end
        else begin
            // ----------------------------------------------------------------
            // 写完成：ready 脉冲
            // ----------------------------------------------------------------
            // 在写响应的握手周期（W_RESP 且 BVALID），拉高 ready 通知 CPU 写完成。
            // 下一个周期 ready 自动归零（因为 else 分支覆盖）。
            if (w_state == W_RESP && BVALID)
                ready <= 1'b1;

            // ----------------------------------------------------------------
            // 读完成：ready 脉冲 + rdata 锁存
            // ----------------------------------------------------------------
            // 在读数据的握手周期（R_RDATA 且 RVALID），同时完成两件事：
            //   1. 将 AXI 总线上的 RDATA 锁存到 CPU 侧 rdata 输出
            //   2. 拉高 ready 通知 CPU 读完成
            else if (r_state == R_RDATA && RVALID) begin
                rdata <= RDATA;           // 从 AXI 读数据总线取值给 CPU
                ready <= 1'b1;            // 读完成脉冲
            end

            // ----------------------------------------------------------------
            // 其他所有情况：ready = 0
            // ----------------------------------------------------------------
            else
                ready <= 1'b0;            // 非完成时刻 ready 为低
        end
    end

endmodule
