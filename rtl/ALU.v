// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                  ALU.v — 32位算术逻辑单元（Arithmetic Logic Unit）            ║
// ║  所属项目: Sapphire-SoC RISC-V 处理器 (RV32IM)                               ║
// ║  模块定位: 五级流水线 EX（执行）阶段的核心组合逻辑                             ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ================================================================================
//  一、模块在 CPU 五级流水线中的角色与定位
// ================================================================================
//
//  本模块是标准 RISC-V 五级流水线中 EX（Execute，执行）阶段的核心计算部件。
//  流水线数据通路顺序为:
//
//        IF ────► ID ────► EX ────► MEM ────► WB
//       取指      译码     执行      访存      写回
//                           ↑
//                       【本模块】
//
//  ALU 位于 EX 阶段，承担以下职责:
//    1. 算数/逻辑指令的计算: ADD/SUB/XOR/OR/AND/SLL/SRL/SRA/SLT/SLTU 共10种运算
//    2. 访存指令的地址计算: Load/Store 指令的"基址寄存器 + 符号扩展偏移"加法
//    3. 分支指令的比较: BEQ/BNE 依赖于 SUB（差是否为0），BLT/BGE/BLTU/BGEU 依赖
//       于 SLT/SLTU（比较结果）
//    4. 跳转指令的地址计算: JAL/JALR 的"PC + 偏移"加法
//    5. LUI/AUIPC 的立即数传递: 通过 ADD（与0相加或与PC相加）直通立即数
//
//  数据流向简述:
//    - 输入端: operand_1 来自 rs1（经转发多路选择器后的值），operand_2 来自 rs2
//      或立即数（由 ex_alu_src 控制的多路选择器选择），aluop 来自 ID 阶段的
//      ALU_Control 译码器
//    - 输出端: out 直接驱动 EX/MEM 流水线寄存器的 ex_alu_result 输入端
//    - 同时 out 也连接到 EX 阶段的 Branch_unit（用于分支条件判断）和转发路径
//      （用于解决数据冒险的 bypass 网络）
//
// ================================================================================
//  二、端口信号详细说明
// ================================================================================
//
//  ┌─────────────────────────────────────────────────────────────────────────┐
//  │  输入端口（3个，全部来自 ID/EX 流水线寄存器输出）                          │
//  ├─────────────────────────────────────────────────────────────────────────┤
//  │                                                                          │
//  │  operand_1  [31:0] input wire                                           │
//  │    第一操作数（源操作数 A）。                                             │
//  │    数据来源: 寄存器文件 rs1 端口读出值，经过 EX 阶段的转发多路选择器       │
//  │    （由 ex_fwd1[1:0] 控制）选择最终的 operand_1:                          │
//  │      - fwd1=00: 使用 ID/EX 寄存器中的 ex_rs1_data（寄存器文件读出值）     │
//  │      - fwd1=01: 转发 EX/MEM 阶段的 ALU 结果（上一条指令刚算完的值）       │
//  │      - fwd1=10: 转发 MEM/WB 阶段的写回数据（再前一条指令的结果）           │
//  │    用途举例:                                                             │
//  │      - ADD rd, rs1, rs2  → operand_1 = rs1 的值                          │
//  │      - ADDI rd, rs1, imm → operand_1 = rs1 的值                          │
//  │      - SW rs2, offset(rs1) → operand_1 = rs1 的值（基地址）              │
//  │      - LUI rd, imm → operand_1 = 0（由 alu_src 逻辑控制）                │
//  │      - AUIPC rd, imm → operand_1 = PC（由 alu_src 逻辑控制）             │
//  │                                                                          │
//  │  operand_2  [31:0] input wire                                           │
//  │    第二操作数（源操作数 B）。                                             │
//  │    数据来源: 由 EX 阶段的 alu_src 多路选择器（由 ex_alu_src 控制）选择:    │
//  │      - alu_src=0: 使用 rs2 数据（经转发多路选择器 ex_fwd2 选择后的值）    │
//  │      - alu_src=1: 使用符号扩展后的立即数 ex_imm                           │
//  │    用途举例:                                                             │
//  │      - R-type (ADD/SLT/XOR等): alu_src=0, operand_2 = rs2 的值           │
//  │      - I-type (ADDI/SLTI等):  alu_src=1, operand_2 = 符号扩展立即数       │
//  │      - Load/Store:             alu_src=1, operand_2 = 偏移量立即数        │
//  │      - LUI:                    alu_src=1, operand_2 = U-type 立即数       │
//  │      - AUIPC:                  alu_src=1, operand_2 = U-type 立即数       │
//  │      - JAL/JALR:   alu_src=1, operand_2 = 分支偏移/立即数                 │
//  │                                                                          │
//  │  aluop  [3:0]  input wire                                               │
//  │    ALU 操作选择码（4位编码）。                                            │
//  │    数据来源: ID 阶段的 ALU_Control 译码器根据指令的 opcode[6:0]、          │
//  │    funct3[2:0]、funct7[5] 译码产生，经 ID/EX 流水线寄存器锁存后到达。      │
//  │    编码定义（来自 Def.v 全局宏，与 ALU_Control / Control_unit 一致）:      │
//  │      `ALU_ADD  = 4'b0000  加法                                           │
//  │      `ALU_SUB  = 4'b0001  减法                                           │
//  │      `ALU_XOR  = 4'b0010  按位异或                                        │
//  │      `ALU_OR   = 4'b0011  按位或                                          │
//  │      `ALU_AND  = 4'b0100  按位与                                          │
//  │      `ALU_SLL  = 4'b0101  逻辑左移                                        │
//  │      `ALU_SRL  = 4'b0110  逻辑右移                                        │
//  │      `ALU_SRA  = 4'b0111  算术右移                                        │
//  │      `ALU_SLT  = 4'b1000  有符号小于比较                                   │
//  │      `ALU_SLTU = 4'b1001  无符号小于比较                                   │
//  │    注意: M-extension（乘除法）的 aluop 编码（4'b1010~4'b1111）不经过       │
//  │    本 ALU 模块处理，而是由独立的 mul_div_unit 多周期单元处理。              │
//  │                                                                          │
//  └─────────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────────┐
//  │  输出端口（1个，驱动 EX/MEM 流水线寄存器）                                  │
//  ├─────────────────────────────────────────────────────────────────────────┤
//  │                                                                          │
//  │  out  [31:0]  output reg                                                │
//  │    ALU 运算结果（32位）。                                                 │
//  │    数据去向:                                                             │
//  │      1. 作为 ex_alu_result 锁存到 EX/MEM 流水线寄存器中                    │
//  │         - 若为算术逻辑指令（wbmux=00），最终在 WB 阶段写回寄存器文件        │
//  │         - 若为 Load/Store 指令，在 MEM 阶段用作数据存储器地址              │
//  │      2. 连接到 EX 阶段的 Branch_unit（分支判断单元）                       │
//  │         - SUB 结果为 0 ⇒ BEQ 条件成立                                     │
//  │         - SUB 结果非 0 ⇒ BNE 条件成立                                     │
//  │         - SLT/SLTU 结果配合 funct3 判断 BLT/BGE/BLTU/BGEU                 │
//  │      3. 连接到转发路径（bypass/forwarding 网络）                          │
//  │         - EX 阶段的 ALU 结果可转发给下一条指令的 operand_1 或 operand_2    │
//  │         - 转发路径: EX/MEM.alu_result → EX阶段转发MUX (fwd=01)            │
//  │    注意: out 被声明为 reg 类型，在 always @(*) 组合逻辑块中赋值。           │
//  │    虽然是 reg，但由于 always @(*) 对所有输入敏感，综合后是纯组合逻辑。      │
//  │                                                                          │
//  └─────────────────────────────────────────────────────────────────────────┘
//
// ================================================================================
//  三、内部信号与逻辑详解
// ================================================================================
//
//  本模块采用"全并行预计算 + 多路选择"架构:
//    - 所有10种运算在组合逻辑中同时计算（assign 连续赋值语句）
//    - aluop 通过 case 语句多路选择最终输出
//    - 这种"面积换速度"的设计使每种运算的延迟独立，关键路径仅为:
//      max(各运算单元延迟) + MUX延迟，而非级联的运算+MUX
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.1 算术运算: 加法器 addr_in 与减法器 sub_in
// ────────────────────────────────────────────────────────────────────────────────
//
//  wire [31:0] addr_in;
//  assign addr_in = operand_1 + operand_2;
//
//    加法器用途（覆盖 RV32I 中绝大多数指令的地址/数据计算）:
//      - ADD 指令:  rs1 + rs2  (R-type 算术加法)
//      - ADDI指令:  rs1 + imm  (I-type 立即数加法)
//      - Load指令:  rs1 + offset  (基地址 + 偏移，如 LW rd, offset(rs1))
//      - Store指令: rs1 + offset  (基地址 + 偏移，如 SW rs2, offset(rs1))
//      - JALR指令:  rs1 + imm  (跳转目标地址 = 寄存器值 + 立即数)
//      - LUI指令:   0 + imm  (加载高位立即数，通过 operand_1=0 传递)
//      - AUIPC指令: PC + imm  (PC相对地址，通过 operand_1=PC 传递)
//    设计理由:
//      - 使用 Verilog 的 + 运算符，综合工具自动推断为 FPGA 的专用进位链
//        （如 Xilinx CARRY4），延迟极小（通常在亚纳秒级别）
//      - 减法通过独立的 sub_in 计算，避免在关键路径上插入"操作数取反+MUX"的
//        额外延迟
//
//  wire [31:0] sub_in;
//  assign sub_in = operand_1 - operand_2;
//
//    减法器用途:
//      - SUB 指令:  rs1 - rs2  (R-type 算术减法)
//      - BEQ 分支:  rs1 - rs2  (差为 0 ⇒ 两数相等 ⇒ 分支成立)
//      - BNE 分支:  rs1 - rs2  (差非 0 ⇒ 两数不等 ⇒ 分支成立)
//    设计理由:
//      - 单独的减法器避免了在 ADD 路径上增加操作数取反逻辑
//      - 对于分支指令，减法结果直接送到 Branch_unit: 判断 result==0
//      - 综合工具通常将减法映射为"加法器 + 借位逻辑"，与加法器结构对称
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.2 按位逻辑运算: xor_in / or_in / and_in
// ────────────────────────────────────────────────────────────────────────────────
//
//  assign xor_in = operand_1 ^ operand_2;   // 按位异或: XOR / XORI
//  assign or_in  = operand_1 | operand_2;   // 按位或:   OR / ORI
//  assign and_in = operand_1 & operand_2;   // 按位与:   AND / ANDI
//
//    覆盖指令: XOR / XORI / OR / ORI / AND / ANDI（R-type 和 I-type）
//    设计理由:
//      - 每位独立运算，无进位传播，延迟极小（单个 LUT 即可完成）
//      - 三种运算分别用独立的 assign 实现，综合工具各自映射为 LUT
//      - 不存在资源共享的必要（现代 FPGA 的 LUT 资源充裕，独立运算避免 MUX 延迟）
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.3 移位运算: sll_in / srl_in / sra_in
// ────────────────────────────────────────────────────────────────────────────────
//
//  assign sll_in = operand_1 << operand_2[4:0];    // 逻辑左移: SLL / SLLI
//  assign srl_in = operand_1 >> operand_2[4:0];    // 逻辑右移: SRL / SRLI
//  assign sra_in = rs_op1 >>> operand_2[4:0];       // 算术右移: SRA / SRAI
//
//    移位量只取 operand_2 的低5位 [4:0]:
//      RISC-V 架构规定移位量仅由 rs2（或立即数）的低5位决定，即移位范围
//      0~31 位。这是 RV32I 的明确规范，忽略高位可以避免未定义行为。
//
//    逻辑左移 (<<):
//      低位移入0，高位移出丢弃。用于 SLL / SLLI 指令。
//      等效于无符号乘以 2^shift_amount。
//
//    逻辑右移 (>>):
//      高位移入0，低位移出丢弃。用于 SRL / SRLI 指令。
//      等效于无符号除以 2^shift_amount。
//
//    算术右移 (>>>):
//      ★ 关键设计点: 必须使用 signed 操作数！
//      rs_op1 是 operand_1 的 signed 版本（wire signed [31:0] rs_op1）。
//      如果 operand_1 被声明为 unsigned（默认），Verilog 的 >>> 运算符
//      会退化为逻辑右移（>>），无法在高位填充符号位。
//      通过将 rs_op1 声明为 signed，>>> 运算符正确地:
//        - 当 operand_1[31]=0 (正数): 高位填充 0
//        - 当 operand_1[31]=1 (负数): 高位填充 1
//      这符合 RISC-V 规范对 SRA / SRAI 指令的要求。
//
//    ★ 综合实现:
//      对于 FPGA，移位操作通常映射为:
//        - 左移: 专用的桶形移位器（通过布线实现，零延迟）
//        - 右移: 桶形移位器 + 符号扩展逻辑（SRA 时额外的符号位填充）
//      若 FPGA 上没有硬件桶形移位器，综合工具会使用 MUX 树实现。
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.4 比较运算: slt_in / sltu_in
// ────────────────────────────────────────────────────────────────────────────────
//
//  assign slt_in  = (rs_op1 < $signed(operand_2));
//    有符号小于比较: 用于 SLT / SLTI / BLT / BGE 指令
//    将 operand_2 也视为 signed 进行比较（$signed 系统函数）。
//    当 operand_1 < operand_2（有符号）时 slt_in=1，否则 slt_in=0。
//    ★ BLT 直接使用 slt_in; BGE 使用 ~slt_in（在 Branch_unit 中取反）。
//
//  assign sltu_in = (operand_1 < operand_2);
//    无符号小于比较: 用于 SLTU / SLTIU / BLTU / BGEU 指令
//    两个操作数都作为 unsigned 比较。
//    当 operand_1 < operand_2（无符号）时 sltu_in=1，否则 sltu_in=0。
//    ★ BLTU 直接使用 sltu_in; BGEU 使用 ~sltu_in（在 Branch_unit 中取反）。
//
//    设计理由:
//      - 比较结果仅为 1-bit 布尔值（0 或 1），而非完整 32-bit
//      - 这符合 RISC-V 规范: SLT/SLTU 指令将目标寄存器置为 0 或 1
//      - 在 case 语句中使用位拼接 {31'b0, slt_in} 将 1-bit 扩展为 32-bit
//      - 综合工具会将 < 运算符映射为减法器的借位输出，硬件开销为一个减法器
//        （可与 sub_in 共享硬件，具体取决于综合工具的优化策略）
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.5 有符号操作数: rs_op1
// ────────────────────────────────────────────────────────────────────────────────
//
//  wire signed [31:0] rs_op1;
//  assign rs_op1 = operand_1;
//
//    用途:
//      rs_op1 是 operand_1 的 signed 重声明版本，用于两个关键场景:
//      1. 算术右移 (>>>):  确保高位填充符号位而非 0
//      2. 有符号比较 (<):  确保 operand_1 被视为有符号数进行比较
//    设计理由:
//      虽然 operand_1 自身是 wire [31:0]（无符号），但通过 signed 的 rs_op1
//      重新解释后，Verilog 的运算符（>>> 和 <）会根据操作数的 signed 属性
//      选择正确的行为。这比在运算时使用 $signed() 系统函数更简洁。
//    为什么不把 operand_1 直接声明为 signed?
//      因为对于大多数运算（ADD/SUB/XOR/OR/AND/SLL/SRL/SLTU），操作数应被
//      视为无符号。仅在 SRA 和 SLT 中需要 signed 语义。需要哪种语义取决于
//      指令类型（由 aluop 选择），所以采用"unsiged 主线 + signed 分支"策略。
//
// ────────────────────────────────────────────────────────────────────────────────
//  3.6 多路选择: case (aluop)
// ────────────────────────────────────────────────────────────────────────────────
//
//  always @(*) begin
//      case (aluop)
//          `ALU_ADD:  out = addr_in;     // 4'b0000 — 加法
//          `ALU_SUB:  out = sub_in;      // 4'b0001 — 减法
//          `ALU_XOR:  out = xor_in;      // 4'b0010 — 按位异或
//          `ALU_OR:   out = or_in;       // 4'b0011 — 按位或
//          `ALU_AND:  out = and_in;      // 4'b0100 — 按位与
//          `ALU_SLL:  out = sll_in;      // 4'b0101 — 逻辑左移
//          `ALU_SRL:  out = srl_in;      // 4'b0110 — 逻辑右移
//          `ALU_SRA:  out = sra_in;      // 4'b0111 — 算术右移
//          `ALU_SLT:  out = {31'b0, slt_in};   // 4'b1000 — 有符号小于
//          `ALU_SLTU: out = {31'b0, sltu_in};  // 4'b1001 — 无符号小于
//          default:   out = 32'h0000_0000;     // 未定义编码 → 安全回退为 0
//      endcase
//  end
//
//    always @(*) 敏感列表:
//      使用 * 通配符表示对所有输入信号敏感（aluop, addr_in, sub_in ...）。
//      这是组合逻辑 always 块的标准写法，综合后生成纯组合电路（无锁存器）。
//
//    为什么用 case 而非多级 MUX?
//      case 语句在综合时转换为多路选择器（MUX），10路输入的 32-bit MUX。
//      Verilog 的 case 比手写嵌套条件运算符 (?:) 更清晰，综合结果相同。
//
//    为什么每个 case 分支选择的是预计算的 wire 而不是直接写表达式?
//      1. 可读性: 每种运算的实现和选择分离，更易理解和维护
//      2. 调试性: 在仿真波形中可以独立观察每个运算的中间结果
//      3. 综合友好: 综合工具可以独立优化每个运算单元，再合并 MUX
//      4. 无性能损失: 预计算是并行的，总延迟为 max(各运算延迟) + MUX延迟
//
//    SLT/SLTU 的位扩展:
//      {31'b0, slt_in} 将 1-bit 比较结果扩展为 32-bit:
//      - 高31位固定为 0（r concatentation）
//      - 最低位为比较结果 (0 或 1)
//      - 这完全符合 RISC-V 规范: rd = (rs1 < rs2) ? 1 : 0
//
//    default 分支:
//      当 aluop 取值为未定义编码（4'b1010~4'b1111，即 M-extension 编码）时:
//      - out 固定为 32'h0000_0000（全零）
//      - 设计理由: 安全回退策略。M-extension 指令不应该通过本 ALU 模块，
//        因为它们的 aluop 编码(1010~1111)在 ID 阶段就会被识别并引导到
//        mul_div_unit。如果由于某种原因进入了 default 分支，输出 0 是最
//        安全的做法（不会产生非确定性行为）。
//      - 对于 FPGA 综合，default 避免了锁存器推断（latch inference），
//        确保 case 被综合为纯组合逻辑。
//
// ================================================================================
//  四、编码设计原理: 为什么采用这批编码值？
// ================================================================================
//
//  ┌─────────┬──────────┬──────────────────────────────────────────────────┐
//  │ aluop   │ 宏名称    │ 编码理由                                         │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b0000 │ ALU_ADD  │ 最常用操作(Load/Store/JALR/LUI/AUIPC也用ADD)，    │
//  │         │          │ 编码0便于作为默认值（flush后alu_ctrl=0即NOP）      │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b0001 │ ALU_SUB  │ ADD的反操作，只需最低位翻转(0000→0001)            │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b0010 │ ALU_XOR  │ 按位运算组: XOR=0010, OR=0011, AND=0100          │
//  │ 4'b0011 │ ALU_OR   │ 低位连续编码，便于 case 综合时优化为 MUX 树       │
//  │ 4'b0100 │ ALU_AND  │                                                   │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b0101 │ ALU_SLL  │ 移位运算组: SLL=0101, SRL=0110, SRA=0111         │
//  │ 4'b0110 │ ALU_SRL  │ 最低位对应 funct3[0]: SLL→1(SLLI funct3=001),    │
//  │ 4'b0111 │ ALU_SRA  │ SRL→0(SRLI funct3=101但funct7区分),              │
//  │         │          │ SRA→1(SRAI funct3=101且funct7[5]=1)               │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b1000 │ ALU_SLT  │ 比较运算组: SLT=1000, SLTU=1001, 最高位=1标志     │
//  │ 4'b1001 │ ALU_SLTU │ "比较类"运算，最低位区分有符号(0)/无符号(1)        │
//  ├─────────┼──────────┼──────────────────────────────────────────────────┤
//  │ 4'b1010~│ M-extension│ 1010~1111 保留给乘除法单元(mul_div_unit)       │
//  │ 4'b1111 │          │ 最高位=1且在SLT/SLTU之后，不经过本ALU模块         │
//  └─────────┴──────────┴──────────────────────────────────────────────────┘
//
//  编码一致性保障:
//    - 所有模块（ALU.v / ALU_Control.v / Control_unit.v / Def.v）使用统一的
//      `ALU_* 宏定义，保证编码完全一致
//    - 宏定义集中在 Def.v 中，所有模块通过 `include "Def.v" 引用
//    - 若需修改编码，只需修改 Def.v 一处即可
//
// ================================================================================
//  五、与其他流水线阶段的交互关系
// ================================================================================
//
//  上游（ID 阶段 → EX 阶段）:
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │ ID 阶段                          EX 阶段 (本模块所在)                 │
//  ├──────────────────────────────────────────────────────────────────────┤
//  │ ALU_Control 根据 opcode+funct3    aluop[3:0] ────► ALU.v 的          │
//  │ +funct7 译码产生 aluop[3:0]      (经 ID/EX 锁存)   aluop 输入端口     │
//  │                                                                    │
//  │ 寄存器文件读出 rs1_data          operand_1 ────► ALU.v 的            │
//  │ (经 ID/EX 锁存，EX阶段转发MUX)    (经转发MUX后)    operand_1 输入端口   │
//  │                                                                    │
//  │ 寄存器文件读出 rs2_data          operand_2 ────► ALU.v 的            │
//  │ 或立即数 imm                     (经 alu_src     operand_2 输入端口   │
//  │ (经 ID/EX 锁存，EX阶段MUX选择)    MUX 选择后)                        │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  同阶段（EX 阶段内部）:
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │ ALU.v out ──► Branch_unit: 分支条件判断 (BEQ/BNE/BLT/BGE/BLTU/BGEU)  │
//  │ ALU.v out ──► 转发路径: 旁路到下一指令的 operand_1/operand_2 MUX     │
//  │ ALU.v out ──► EX/MEM 流水线寄存器: ex_alu_result 输入端               │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  下游（EX 阶段 → MEM 阶段）:
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │ ALU.v out ─► EX/MEM 寄存器 ─► MEM 阶段                               │
//  │                                                                       │
//  │  在 MEM 阶段的用途:                                                   │
//  │    - 若为 Load/Store: out 作为数据存储器的地址 (mem_addr)             │
//  │    - 若为算术逻辑: out 继续传递到 MEM/WB 寄存器，最终写回寄存器文件     │
//  │      (当 wbmux=00 时，WB阶段选择 ALU 结果写回)                         │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  下游（EX 阶段 → WB 阶段，间接）:
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │ ALU.v out ─► EX/MEM ─► MEM/WB ─► WB 阶段写回 MUX ─► 寄存器文件       │
//  │  对于 R-type/I-type 算术逻辑指令，wbmux=00，最终选择 ALU 结果写回     │
//  │  对于 Load 指令，wbmux=01，ALU 计算的是访存地址，最终选择 mem_rdata   │
//  │  对于 JAL/JALR，wbmux=10，ALU 计算的是跳转目标地址，写回的是 PC+4     │
//  └──────────────────────────────────────────────────────────────────────┘
//
// ================================================================================
//  六、时序与综合注意事项
// ================================================================================
//
//  1. 关键路径分析:
//     - 本模块是纯组合逻辑，位于 EX 阶段的关键路径上
//     - 最长路径: ID/EX 寄存器输出 → 转发MUX → ALU → EX/MEM 寄存器输入
//     - n 位加法器是延迟最大的运算单元（进位链传播延迟），但现代 FPGA 有
//       专用的快速进位链（如 Xilinx CARRY4/CARRY8），通常能在 1~2ns 完成
//     - 移位器若不使用桶形移位器硬件，可能使用 MUX 树，延迟也需关注
//
//  2. 面积与功耗:
//     - 所有10种运算并行计算 → 面积较大但延迟最优
//     - 若面积受限，可考虑多周期复用单个加法器（如对 SUB 使用取反的 operand_2 +
//       ADD），但会增加 MUX 延迟和控制复杂度
//     - 未选中的运算路径在本周期内仍会翻转 → 产生动态功耗。可通过门控时钟
//       （在 ALU 输入端而非本模块）来降低非活跃路径的翻转率
//
//  3. 综合优化建议:
//     - 综合工具通常会自动合并加法器和减法器（共享进位链）
//     - case 语句会自动被提取为 MUX 树或查找表
//     - 若时序不满足，可考虑在 ALU 输出端插入一级流水寄存器（将 EX 阶段
//       拆分为 EX1/EX2），代价是增加分支惩罚周期
//
// ================================================================================
//  七、指令覆盖汇总（本 ALU 直接服务的所有 RV32I 指令）
// ================================================================================
//
//  R-type (OPCODE_OP=0110011):
//    ADD(000) SUB(000+f7) SLL(001) SLT(010) SLTU(011)
//    XOR(100) SRL(101+f7) SRA(101+f7) OR(110) AND(111)
//
//  I-type ALU (OPCODE_OP_IMM=0010011):
//    ADDI(000) SLLI(001) SLTI(010) SLTIU(011)
//    XORI(100) SRLI(101+f7) SRAI(101+f7) ORI(110) ANDI(111)
//
//  I-type Load (OPCODE_LOAD=0000011):  LW/LH/LB/LHU/LBU — 地址加法
//  I-type JALR (OPCODE_JALR=1100111):  JALR — 跳转目标加法
//  S-type (OPCODE_STORE=0100011):      SW/SH/SB — 地址加法
//  B-type (OPCODE_BRANCH=1100011):     BEQ/BNE/BLT/BGE/BLTU/BGEU — 比较
//  U-type LUI (OPCODE_LUI=0110111):    LUI — 立即数传递
//  U-type AUIPC (OPCODE_AUIPC=0010111):AUIPC — PC+立即数
//  J-type JAL (OPCODE_JAL=1101111):    JAL — 跳转目标加法（注:JAL的ALU计算
//                                       通常不被使用，跳转目标由BPU计算）
//
//  不覆盖的指令:
//    M-extension (MUL/DIV/REM等): 由 mul_div_unit 独立处理
//    FENCE / ECALL / EBREAK / CSRR*: 不需要 ALU 计算
//
// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                            End of ALU.v Header                              ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

`include "Def.v"

module ALU (
    // ============================================================================
    //  输入端口
    // ============================================================================
    input  wire [31:0] operand_1,   // 第一操作数(rs1值，经转发MUX选择后)
    input  wire [31:0] operand_2,   // 第二操作数(rs2值或立即数，经alu_src MUX后)
    input  wire [3:0]  aluop,       // ALU操作选择码(来自ALU_Control，经ID/EX锁存)
    output reg  [31:0] out          // ALU运算结果(→ EX/MEM寄存器 + Branch_unit + 转发)
);

    // ============================================================================
    //  内部并行预计算信号 — 所有10种运算结果在同一周期内并行计算
    //  设计策略: 面积换速度。每种运算独立计算，case 仅做多路选择。
    // ============================================================================

    // ── 算术运算 ──
    wire [31:0] addr_in;    // 加法结果: operand_1 + operand_2
    wire [31:0] sub_in;     // 减法结果: operand_1 - operand_2

    // ── 按位逻辑运算 ──
    wire [31:0] xor_in;     // 按位异或: operand_1 XOR operand_2
    wire [31:0] or_in;      // 按位或:   operand_1 OR  operand_2
    wire [31:0] and_in;     // 按位与:   operand_1 AND operand_2

    // ── 移位运算 ──
    wire [31:0] sll_in;     // 逻辑左移: operand_1 << shamt (移入0)
    wire [31:0] srl_in;     // 逻辑右移: operand_1 >> shamt (移入0)
    wire [31:0] sra_in;     // 算术右移: operand_1 >>> shamt (移入符号位)

    // ── 比较运算 ──
    wire        slt_in;     // 有符号小于: operand_1 < operand_2 (signed)   → 1-bit
    wire        sltu_in;    // 无符号小于: operand_1 < operand_2 (unsigned) → 1-bit

    // ── 有符号操作数 ──
    // ★ 关键: operand_1 在模块端口为 unsigned wire，但 SRA 和 SLT 需要 signed 语义。
    //   通过本 wire 将 operand_1 重新解释为 signed，确保 >>> 和 < 行为正确。
    wire signed [31:0] rs_op1;

    assign rs_op1 = operand_1;  // signed 重声明，无额外硬件开销

    // ============================================================================
    //  并行预计算 assign 语句
    // ============================================================================

    // ── 加法器 ──
    // 覆盖: ADD/ADDI/load/store/JALR/LUI/AUIPC 的地址/数据加法
    // 综合: FPGA 专用进位链 (CARRY4/CARRY8)，延迟 ~1ns
    assign addr_in = operand_1 + operand_2;

    // ── 减法器 ──
    // 覆盖: SUB 指令 以及 BEQ/BNE 分支判断（Branch_unit 检查结果是否为0）
    // 综合: 可与 addr_in 共享进位链硬件（综合工具自动优化）
    assign sub_in  = operand_1 - operand_2;

    // ── 按位逻辑 ──
    // 覆盖: XOR/XORI, OR/ORI, AND/ANDI
    // 综合: 每位独立 LUT，32个LUT并行，延迟可忽略
    assign xor_in  = operand_1 ^ operand_2;
    assign or_in   = operand_1 | operand_2;
    assign and_in  = operand_1 & operand_2;

    // ── 移位运算 ──
    // 移位量截取 operand_2 的低5位 [4:0]，符合 RISC-V 规范（移位范围 0~31）
    // 综合: 桶形移位器（硬件）或 MUX 树（逻辑），取决于 FPGA 架构
    assign sll_in  = operand_1 << operand_2[4:0];   // 逻辑左移: 低位移入0
    assign srl_in  = operand_1 >> operand_2[4:0];   // 逻辑右移: 高位移入0
    assign sra_in  = rs_op1 >>> operand_2[4:0];     // 算术右移: 高位填充符号位
    // ★ sra_in 使用 rs_op1 (signed) 而非 operand_1 (unsigned)。
    //    若 error 使用 operand_1，>>> 退化为逻辑右移，SRA 指令将错误。

    // ── 比较运算 ──
    // 结果为 1-bit 布尔值: 1=真(operand_1 < operand_2), 0=假
    // $signed(operand_2) 确保两侧均视为有符号数进行 SLT 比较
    assign slt_in  = (rs_op1 < $signed(operand_2));   // 有符号小于
    assign sltu_in = (operand_1 < operand_2);          // 无符号小于

    // ============================================================================
    //  多路选择: case(aluop) — 根据4位操作码选择对应的预计算结果
    // ============================================================================
    //  always @(*) 表示对任何输入变化做出响应，综合为纯组合逻辑（无锁存器）
    //  所有 case 分支都使用 Def.v 中定义的 `ALU_* 宏，确保跨模块编码一致
    always @(*) begin
        case (aluop)
            // ── 算术 ──
            `ALU_ADD:  out = addr_in;     // 4'b0000: 加法 — 最常用操作
            `ALU_SUB:  out = sub_in;      // 4'b0001: 减法 — 用于 SUB 和分支比较

            // ── 按位逻辑 ──
            `ALU_XOR:  out = xor_in;      // 4'b0010: 异或
            `ALU_OR:   out = or_in;       // 4'b0011: 或
            `ALU_AND:  out = and_in;      // 4'b0100: 与

            // ── 移位 ──
            `ALU_SLL:  out = sll_in;      // 4'b0101: 逻辑左移
            `ALU_SRL:  out = srl_in;      // 4'b0110: 逻辑右移
            `ALU_SRA:  out = sra_in;      // 4'b0111: 算术右移 — 保留符号位

            // ── 比较 ──
            // 将 1-bit 比较结果扩展为 32-bit:
            //   高31位 = 0, 最低位 = 比较结果(0或1)
            // 符合 RISC-V 规范: rd = (rs1 < rs2) ? 1 : 0
            `ALU_SLT:  out = {31'b0, slt_in};   // 4'b1000: 有符号小于
            `ALU_SLTU: out = {31'b0, sltu_in};  // 4'b1001: 无符号小于

            // ── 安全回退 ──
            // 处理未定义的 aluop 编码（包括 M-extension 的 1010~1111 等）:
            //   - M-extension 指令不经过本 ALU，由 mul_div_unit 独立处理
            //   - 若因 bug 或异常进入此分支，输出 0 是最安全的回退值
            //   - default 分支同时避免 case 被综合为锁存器（latch inference）
            default:   out = 32'h0000_0000;     // 未定义操作 → 安全输出 0
        endcase
    end

endmodule
