// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  mem_pipeline.v — MEM/WB 流水线寄存器（第4级流水线寄存器）                    ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ── 模块功能概述 ───────────────────────────────────────────────────────────────
//  本模块是经典5级单发射顺序流水线中的第4级（即最后一个）流水线寄存器，
//  位于 MEM（存储器访问）阶段与 WB（写回）阶段之间。
//
//  它在每个时钟上升沿将 MEM 阶段产生的计算结果、访存数据和控制信号锁存起来，
//  供下一周期的 WB 阶段使用。WB 阶段根据这些信号通过纯组合逻辑（MUX选择）
//  决定写回寄存器堆的数据和地址。
//
//  5级流水线结构回顾：
//    IF ──[if_id]──► ID ──[id_ex]──► EX ──[ex_mem]──► MEM ──[mem_wb]──► WB
//             ↑ 第1级          ↑ 第2级          ↑ 第3级          ↑ 第4级（本模块）
//    ──► 组合逻辑段  ──► 寄存器锁存  ──► 组合逻辑段  ──► ...
//
//  5级流水线 = 5段组合逻辑 + 4级流水线寄存器。本模块是4级寄存器中的最后一级。
//
// ── 设计决策：为什么需要这一级寄存器 ────────────────────────────────────────────
//  1. 时序隔离：MEM阶段是访存的关键路径（可能涉及数据SRAM读取），
//     需要一级寄存器将访存结果与后续写回逻辑隔离开，避免组合逻辑路径过长
//     导致关键路径违例。
//  2. 流水线一致性：每条指令都必须经历相同数量的流水级，以保证指令间的
//     数据依赖关系可预测。省略此寄存器会破坏流水线深度一致性。
//  3. 写回数据选择：WB阶段需要同时看到ALU结果、访存结果和PC+4三种候选，
//     只有通过寄存器将所有候选对齐到同一拍，MUX才能正确选择。
//  4. 异常/冲刷支持：寄存器提供 flush 和 stall 控制点，使流水线能够
//     在MEM阶段发生异常或数据冲突时正确插入气泡或冻结。
//
// ── 设计决策：Flush/Stall 行为与时序理由 ────────────────────────────────────────
//  优先级: reset > flush > stall > 正常锁存
//
//  【Reset】异步复位（posedge reset 触发，不等 clk）：
//    将所有输出清零。数据通路和控制信号均清零，使 WB 阶段不产生任何写操作。
//    采用异步复位是为了在系统上电时立即进入已知状态，不依赖时钟。
//
//  【Flush】同步冲刷（仅在 clk 上升沿生效）：
//    仅清零控制信号（reg_write=0, wbmux=0），保留数据通路信号不变。
//    为什么要区分 flush 和 reset？
//    - 数据通路信号保留原值并非功能需要（reg_write=0 时写被禁止无所谓），
//      而是为了降低翻转功耗：保持数据通路不变，避免大位宽寄存器
//      （32bit x3）的无谓翻转，减少动态功耗。
//    - flush 场景：分支预测失败、异常、中断等需要取消MEM阶段之后的指令。
//      被冲刷的指令不应修改寄存器堆，因此 reg_write 必须清零。
//    - 注意：flush 是同步的（在 always @(posedge clk) 内经由 else if 判断），
//      这和许多教科书上的设计一致：冲刷信号由ID/EX阶段产生，经过组合逻辑
//      在同一个时钟沿到达各级寄存器。
//
//  【Stall】流水线冻结：
//    当 stall=1 时，所有输出保持当前值不变（if (!stall) 条件不满足）。
//    数据冒险（如前一条指令的 load-use 冲突）会导致流水线停顿，
//    此时EX/MEM寄存器和本寄存器都需要冻结，让前面的指令完成访存后再继续。
//    注意：stall 优先级低于 flush——如果同时发生，优先执行冲刷。
//    这是因为冲刷通常意味着指令本身无效了，不需要冻结等待。
//
// ── 信号说明 ───────────────────────────────────────────────────────────────────
//  本模块传递的信号分为两大类：数据通路信号和控制信号。
//  WB 阶段是纯组合逻辑，仅用到以下信号：
//
//  ★ 数据通路信号（32位或5位宽，来自 MEM 阶段，去往 WB 阶段）：
//    - alu_result：ALU运算最终结果（R型指令的计算结果、访存地址等）
//    - load_data：数据存储器读出的32位数据（仅 lw 等 load 指令有效）
//    - pc_plus_4：PC+4（用于 JAL/JALR 指令的返回地址写回）
//    - rd_addr：目标寄存器编号（5位，RISC-V 32个寄存器）
//
//  ★ 控制信号（来自 MEM 阶段经流水线传递的控制信息）：
//    - reg_write：寄存器堆写使能（1=写，0=不写）
//    - wbmux：写回数据源选择（00=ALU结果，01=MEM读数据，10=PC+4）
//
// ★ 注意：原 mem_pip_reg 已被此模块取代。旧版 wb_pipeline.v 中的
//   pip_reg4 也已被此模块取代，统一为 mem_wb_stage。
//
// ★ WB 阶段是纯组合逻辑（MUX 选择），本模块是第 4 个也是最后一个
//   流水线寄存器。5 级流水线 = 4 个寄存器 + 5 段组合逻辑。

module mem_wb_stage (
    // =========================================================================
    // 时钟与复位
    // =========================================================================
    input  wire        clk,           // 系统主时钟，所有同步逻辑在上升沿触发
    input  wire        reset,         // 异步复位（高有效），posedge触发，清零所有输出
    input  wire        stall,         // 流水线冻结信号（高有效），stall=1时输出保持不变
    input  wire        flush,         // 流水线冲刷信号（高有效），仅清零控制信号，数据通路保持不变以省功耗

    // =========================================================================
    // 数据通路输入 —— 来自 MEM 阶段
    // =========================================================================
    input  wire [31:0] mem_alu_result, // ALU运算结果（32位），来自EX/MEM寄存器
                                       // 包含：算术/逻辑运算结果、load/store访存地址、
                                       //       分支/跳转计算的目标地址等
    input  wire [31:0] mem_load_data,  // 数据存储器读出数据（32位），来自MEM阶段的数据SRAM
                                       // 仅在 load 指令时有效（lw/lh/lb/lhu/lbu）；
                                       // store 指令不需要此数据
    input  wire [31:0] mem_pc_plus_4,  // PC+4（32位），来自EX/MEM寄存器
                                       // 用于 JAL/JALR 指令的链接地址写回：rd = PC+4
    input  wire [4:0]  mem_rd_addr,    // 目标寄存器地址（5位），来自EX/MEM寄存器
                                       // RISC-V ISA 支持32个通用寄存器（x0-x31），
                                       // 5位刚好编码0-31；x0硬连线为0不可写，写使能可忽略

    // =========================================================================
    // 控制信号输入 —— 来自 MEM 阶段
    // =========================================================================
    input  wire        mem_reg_write,  // 寄存器写使能（1位），来自EX/MEM的控制通路
                                       // 1=当前指令需要写回寄存器堆，0=不需要（如store/分支/无目标）
    input  wire [1:0]  mem_wbmux,      // 写回数据源选择（2位），来自EX/MEM的控制通路
                                       // 编码定义：
                                       //   00 (WBMUX_ALU)  — 选择 alu_result（R型/I型运算指令）
                                       //   01 (WBMUX_MEM)  — 选择 load_data（load指令）
                                       //   10 (WBMUX_PC4)  — 选择 pc_plus_4（JAL/JALR返回地址）
                                       //   11              — 保留/未使用

    // =========================================================================
    // 输出 —— 去往 WB 阶段（纯组合逻辑写回选择）
    // =========================================================================
    output reg  [31:0] wb_alu_result,  // 锁存后的ALU结果，供WB阶段MUX使用
    output reg  [31:0] wb_load_data,   // 锁存后的访存数据，供WB阶段MUX使用（load指令用）
    output reg  [31:0] wb_pc_plus_4,   // 锁存后的PC+4，供WB阶段MUX使用（JAL/JALR用）
    output reg  [4:0]  wb_rd_addr,     // 锁存后的目标寄存器地址，接寄存器堆的写地址端口
    output reg         wb_reg_write,   // 锁存后的寄存器写使能，接寄存器堆的写使能端口
    output reg  [1:0]  wb_wbmux        // 锁存后的写回数据源选择，接WB阶段写回数据MUX的选择端
);

    // =========================================================================
    // 流水线寄存器 主逻辑
    // =========================================================================
    //
    // 这是一个单 always 块的时序逻辑，在时钟上升沿或异步复位上升沿触发。
    //
    // 为什么用单一 always 块而不是多个？
    //   将所有输出统一在一个 always 块中管理，确保所有寄存器的复位/冲刷/
    //   冻结行为完全一致，避免分散管理导致的不一致行为（例如某些寄存器
    //   被flush清零而另一些没有）。
    //
    // 为什么输出使用 reg 类型（即真正的D触发器）？
    //   这是流水线寄存器的本质——每个输出在物理上都是一个D触发器，
    //   在时钟沿采样输入并保持到下一个时钟沿。这提供了MEM和WB阶段之间
    //   的一周期隔离，是流水线正确操作的基础。
    //
    // 优先级设计理由：
    //   reset > flush > stall > 正常
    //   1. reset 最高——系统复位必须压倒一切，立即将所有状态归零
    //   2. flush 优先于 stall——如果指令需要被冲刷，冻结它没有意义；
    //      冲刷清除了控制信号，相当于注入一条"无操作"的NOP指令
    //   3. stall 优先于正常——数据冒险时，必须等待前序指令完成，不能锁存新数据
    //   4. 正常锁存——无冲突时，每个周期将MEM阶段输出传递到WB阶段

    // 优先级: reset > flush > stall > 正常
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // ── 异步复位分支 ─────────────────────────────────────────────────
            // 触发条件：reset 信号上升沿（不管 clk 状态）
            // 行为：所有输出强制清零
            //
            // 数据通路清零：alu_result=0, load_data=0, pc_plus_4=0
            //   虽然在 reg_write=0 时数据通路值无关紧要（写被禁止），
            //   但复位时清零可确保仿真时X态不传播，综合时也可优化掉冗余逻辑。
            //
            // 控制信号清零：reg_write=0, wbmux=0
            //   确保复位后第一个周期 WB 阶段不写寄存器堆。
            //
            // rd_addr 清零：指向 x0（zero寄存器），即使 reg_write 意外为1
            //   也不会错误地修改任何架构寄存器（x0硬连线为0，写入无效果）。
            wb_alu_result <= 32'b0;   // ALU结果清零
            wb_load_data  <= 32'b0;   // 访存数据清零
            wb_pc_plus_4  <= 32'b0;   // PC+4清零
            wb_rd_addr    <= 5'b0;    // 目标寄存器地址清零（指向x0）
            wb_reg_write  <= 1'b0;    // 寄存器写使能清零（禁止写）
            wb_wbmux      <= 2'b0;    // 写回数据源选择清零（默认ALU）
        end
        else if (flush) begin
            // ── 同步冲刷分支 ─────────────────────────────────────────────────
            // 触发条件：clk 上升沿且 flush=1（且 reset=0）
            // 行为：仅清零控制信号，数据通路信号保持原值不变
            //
            // 为什么只清零控制信号？
            //   flush 的本质是将当前已进入流水线的指令"标记为无效"。
            //   最简单的方式是将 reg_write 清零——这样WB阶段就不会写寄存器，
            //   该指令的副作用被完全消除，等价于一条 NOP。
            //
            // 为什么保留数据通路信号不置零？
            //   1. 功能上无必要：reg_write=0 已经阻止了写操作
            //   2. 降低功耗：32位寄存器的翻转需要消耗动态功耗，
            //      保持原值比全部清零翻转更少，尤其在连续冲刷时
            //   3. 调试友好：保留数据便于在波形中观察被冲刷的指令是什么
            //
            // flush 来源场景：
            //   - 分支预测失败（EX/MEM阶段发现实际分支方向与预测不符）
            //   - 异常/中断（需要取消流水线中当前 MEM 及之后的指令）
            //   - 流水线中检测到需要冲刷的结构性冲突
            //
            // 插入气泡：仅清零控制信号
            wb_reg_write  <= 1'b0;    // 禁止写寄存器——等效于NOP
            wb_wbmux      <= 2'b0;    // 清零写回选择（无关紧要，因reg_write已为0）
        end
        else if (!stall) begin
            // ── 正常锁存分支 ─────────────────────────────────────────────────
            // 触发条件：clk 上升沿且 flush=0 且 stall=0（且 reset=0）
            // 行为：将 MEM 阶段的所有输入锁存到输出寄存器中
            //
            // 这是正常的流水线推进：每个时钟周期，MEM阶段产生的结果被采样
            // 并传递到WB阶段，供下一个时钟周期的写回操作使用。
            //
            // 时序关系：
            //   周期 N：MEM 阶段组合逻辑产生 mem_* 信号（在 clk 上升沿前稳定）
            //           └─ 本 always 块在 clk 上升沿采样 mem_* → wb_*
            //   周期 N+1：WB 阶段组合逻辑使用 wb_* 信号进行写回MUX选择
            //           └─ 寄存器堆在 clk 上升沿写入（如果 wb_reg_write=1）
            //
            // 全部信号都需要传递——WB阶段是纯组合逻辑，需要这6个信号完整：
            //   wb_alu_result + wb_load_data + wb_pc_plus_4 → 三选一MUX数据输入
            //   wb_wbmux → MUX选择端（决定三选一的结果）
            //   wb_rd_addr → 寄存器堆写地址
            //   wb_reg_write → 寄存器堆写使能

            // 正常锁存：所有信号从 MEM 输入端传递到 WB 输出端
            wb_alu_result <= mem_alu_result;  // 锁存ALU结果
            wb_load_data  <= mem_load_data;   // 锁存存储器读出数据
            wb_pc_plus_4  <= mem_pc_plus_4;   // 锁存PC+4
            wb_rd_addr    <= mem_rd_addr;     // 锁存目标寄存器地址
            wb_reg_write  <= mem_reg_write;   // 锁存寄存器写使能
            wb_wbmux      <= mem_wbmux;       // 锁存写回数据源选择
        end
        // ── 隐式冻结分支（stall=1 且 flush=0 且 reset=0） ────────────────────
        // 当 stall=1 时：if-reset 为假，if-flush 为假，if-!stall 为假
        // → 不执行任何赋值，所有输出寄存器保持当前值不变
        //
        // 为什么要保持而不是清零？
        //   stall 表示流水线因数据冒险而暂时停顿（例如 load-use 冲突），
        //   并不是要取消当前指令。当前 MEM 阶段的指令仍然有效，只是需要
        //   等待一个或多个周期才能继续推进。
        //
        // 冻结期间的行为：
        //   - WB 阶段持续看到同一组 wb_* 信号（上一周期锁存的值）
        //   - 如果之前的 wb_reg_write=1，WB 阶段会重复尝试写同一个寄存器
        //     但寄存器堆的写使能由 wb_reg_write 控制，不会造成功能错误
        //     （写入相同值到同一寄存器多次是无害的——幂等操作）
        //   - 更重要的是：stall 时前面的 EX/MEM 寄存器也已冻结，
        //     MEM 阶段的组合逻辑输入不变，mem_* 信号保持稳定
        //
        // stall=1 → 保持当前输出不变
    end

endmodule
