// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                    PLIC.v — 平台级中断控制器（Platform-Level Interrupt Controller）
// ║                         基于 RISC-V PLIC 规范简化实现
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ┌──────────────────────────────────────────────────────────────────────────────┐
// │ 一、模块功能概述                                                             │
// ├──────────────────────────────────────────────────────────────────────────────┤
// │                                                                            │
// │  PLIC（平台级中断控制器）是 RISC-V 系统中负责收集、仲裁和分发外部中断的      │
// │  核心外设。本模块为简化实现，支持 32 个中断源（实际有效源 1~31，源 0 保留）。│
// │                                                                            │
// │  核心功能：                                                                 │
// │    1. 中断锁存     — 检测外部中断源的上升沿，锁存到 pending 寄存器           │
// │    2. 优先级过滤   — 只允许优先级高于 threshold 的中断通过                   │
// │    3. 优先级仲裁   — 在多个有效中断中选出优先级最高的一个                     │
// │    4. Claim 机制   — CPU 读取 CLAIM 寄存器获取中断 ID，同时清除 pending      │
// │    5. Complete 机制— CPU 处理完毕后写回 CLAIM/COMPLETE 寄存器表示完成        │
// │                                                                            │
// │  中断优先级规则：编号越大优先级越高（中断源 31 > 中断源 1）                   │
// │                                                                            │
// └──────────────────────────────────────────────────────────────────────────────┘
//
// ┌──────────────────────────────────────────────────────────────────────────────┐
// │ 二、寄存器映射（Register Map）                                                │
// ├──────────┬─────────┬──────┬─────────────────────────────────────────────────┤
// │  偏移地址 │  寄存器  │ 宽度 │  描述                                            │
// ├──────────┼─────────┼──────┼─────────────────────────────────────────────────┤
// │  0x0000  │ PENDING │ 32b  │ 中断等待寄存器（只读）。bit[n]=1 表示中断源 n     │
// │          │         │      │ 有中断等待处理。由硬件上升沿自动置位，             │
// │          │         │      │ CPU 读 CLAIM 时自动清除对应位。                    │
// ├──────────┼─────────┼──────┼─────────────────────────────────────────────────┤
// │  0x0004  │ ENABLE  │ 32b  │ 中断使能寄存器（可读写）。bit[n]=1 使能中断源 n。 │
// │          │         │      │ 复位值：0x00000000（全部禁用）                     │
// ├──────────┼─────────┼──────┼─────────────────────────────────────────────────┤
// │  0x0020  │THRESHOLD│  8b  │ 优先级阈值寄存器（可读写）。只有优先级 > threshold │
// │          │         │      │ 的中断才能通过。threshold=0 时允许所有中断通过；   │
// │          │         │      │ threshold=255 时禁止所有中断。                     │
// │          │         │      │ 复位值：0x00                                      │
// ├──────────┼─────────┼──────┼─────────────────────────────────────────────────┤
// │0x0040-   │PRIORITY │ 8b  │ 优先级寄存器组（可读写），每个中断源占用 1 个字。   │
// │ 0x007C   │ [1..31] │  each│ PRIORITY[n] 的地址 = 0x0040 + (n-1)*4。          │
// │          │         │      │ 优先级值越大优先级越高。复位值：0x00               │
// ├──────────┼─────────┼──────┼─────────────────────────────────────────────────┤
// │  0x0080  │ CLAIM/  │ 32b  │ Claim/Complete 寄存器。                           │
// │          │ COMPLETE│      │ ★ 读操作（Claim）：返回最高优先级中断 ID，         │
// │          │         │      │   并在下一个时钟周期自动清除对应的 pending 位。    │
// │          │         │      │ ★ 写操作（Complete）：中断处理完毕通知，           │
// │          │         │      │   本简化设计中写操作无硬件副作用                    │
// │          │         │      │   （pending 已在 Claim 时清除）。                  │
// └──────────┴─────────┴──────┴─────────────────────────────────────────────────┘
//
// ┌──────────────────────────────────────────────────────────────────────────────┐
// │ 三、接口说明                                                                 │
// ├──────────────────────────────────────────────────────────────────────────────┤
// │                                                                            │
// │  【总线侧接口（CPU 通过系统总线访问）】                                      │
// │    input  [31:0] addr    — 字节地址，低 8 位用于寄存器选择                   │
// │    input  [31:0] wdata   — 写数据（仅低 8 位有效，因寄存器均为 8 位宽）      │
// │    input         we      — 写使能，高电平有效                                │
// │    output [31:0] rdata   — 读数据（组合逻辑输出，当周期有效）                 │
// │                                                                            │
// │  【外部中断源接口】                                                          │
// │    input  [31:0] irq_sources — 32 个电平敏感中断源输入。                     │
// │                                 PLIC 内部通过上升沿检测将其转换为锁存事件。   │
// │                                 bit[0] 对应中断源 0（通常保留不用）。          │
// │                                                                            │
// │  【中断输出接口（连接到 CPU 核）】                                           │
// │    output        plic_irq    — 中断请求信号。高电平表示至少有一个有效中断。   │
// │    output [4:0]  plic_irq_id — 当前最高优先级中断的 ID（5 位，0~31）。       │
// │                                 仅在 plic_irq 有效时 ID 才有意义。           │
// │                                                                            │
// └──────────────────────────────────────────────────────────────────────────────┘
//
// ┌──────────────────────────────────────────────────────────────────────────────┐
// │ 四、时序与同步设计                                                           │
// ├──────────────────────────────────────────────────────────────────────────────┤
// │                                                                            │
// │  【时钟域】                                                                  │
// │    本模块工作在单一时钟域 clk 下，所有寄存器由同一 clk 的正沿驱动。          │
// │    外部 irq_sources 被视为与 clk 异步，本模块对其做了一级同步（irq_sources_d）│
// │    再做上升沿检测，以避免亚稳态。                                            │
// │                                                                            │
// │  【中断锁存时序】                                                            │
// │    Cycle 0: 外设拉高 irq_sources[n]                                         │
// │    Cycle 1: irq_sources_d 采样到高电平                                      │
// │    Cycle 2: irq_rising[n] = 1 → pending[n] 被置位                           │
// │    → 中断锁存延迟为 2 个时钟周期                                             │
// │                                                                            │
// │  【有效优先级计算（组合逻辑）】                                              │
// │    effective_prio[i] = (prio[i] > threshold) && pending[i] && enable[i]    │
// │    三个条件同时满足：                                                        │
// │      1) 优先级高于阈值   (prio[i] > threshold)                              │
// │      2) 中断正在等待     (pending[i] == 1)                                  │
// │      3) 中断已使能       (enable[i] == 1)                                   │
// │    该信号为纯组合逻辑，寄存器更新后立即生效，无额外延迟。                      │
// │                                                                            │
// │  【优先级编码器（组合逻辑）】                                                │
// │    从编号 31 向下遍历到 0，找到第一个 effective_prio[j]=1 的源。             │
// │    编号大的优先级高 → 嵌套循环中先遇到的高编号胜出。                          │
// │    当没有任何有效中断时，highest_irq = 0。                                   │
// │                                                                            │
// │  【Claim 机制时序】                                                          │
// │    Cycle 0: CPU 发起读 0x80，地址出现在 addr 上，we=0                       │
// │             → 组合逻辑立即返回 rdata = {27'd0, highest_irq}（当前周期可用）   │
// │             → claim_read 标志在本周期结束时被置为 1                          │
// │    Cycle 1: claim_read=1 生效                                               │
// │             → pending <= pending & ~(1 << claim_id)  清除对应 pending 位    │
// │             → claim_read 自动清零                                           │
// │    注意：pending 清除发生在 Claim 读的下一个周期，而非当周期。                │
// │          这保证了读出的 highest_irq 对应的 pending 在清除前被正确返回。       │
// │                                                                            │
// │  【Complete 机制】                                                           │
// │    CPU 写 0x80（写入中断 ID）表示中断处理完成。                              │
// │    本简化设计中，pending 已在 Claim 时清除，因此 Complete 写无额外硬件动作。  │
// │    但软件仍需执行 Complete 写以符合 RISC-V PLIC 规范。                       │
// │                                                                            │
// │  【复位】                                                                    │
// │    同步复位（reset 为高时，在 clk 上升沿执行复位）：                         │
// │      - enable    清零（所有中断禁用）                                        │
// │      - threshold 清零（允许所有优先级 > 0 的中断）                            │
// │      - pending   清零（无等待中断）                                          │
// │      - prio[0..31] 全部清零（所有源优先级为最低）                             │
// │      - claim_read / claim_id 清零                                           │
// │                                                                            │
// └──────────────────────────────────────────────────────────────────────────────┘
//
// ┌──────────────────────────────────────────────────────────────────────────────┐
// │ 五、中断处理完整流程                                                          │
// ├──────────────────────────────────────────────────────────────────────────────┤
// │                                                                            │
// │  1. 初始化                                                                  │
// │     CPU 配置 PRIORITY[n] 和 ENABLE[n]，设置 THRESHOLD                        │
// │                                                                            │
// │  2. 中断触发                                                                │
// │     外设拉高 irq_sources[n] → 2 周期后 pending[n] 锁存为 1                   │
// │                                                                            │
// │  3. 优先级仲裁                                                              │
// │     优先级编码器从所有 effective_prio[n]=1 的源中选出编号最大的               │
// │     → plic_irq = 1, plic_irq_id = 最高优先级中断 ID                         │
// │                                                                            │
// │  4. CPU 响应中断                                                            │
// │     CPU 读取 CLAIM 寄存器 (0x80)                                            │
// │     → 获得中断 ID（如 5）                                                   │
// │     → 下一周期 pending[5] 自动清除                                           │
// │                                                                            │
// │  5. CPU 处理中断                                                            │
// │     CPU 执行中断服务程序（ISR）                                              │
// │                                                                            │
// │  6. 中断完成                                                                │
// │     CPU 将中断 ID 写回 CLAIM/COMPLETE 寄存器 (0x80)                          │
// │     → 通知 PLIC 该中断已处理完毕（本设计中为无操作）                          │
// │                                                                            │
// │  注意：如果 irq_sources[n] 仍然为高，则会再次触发上升沿（需先变低再变高）。   │
// │        PLIC 设计为电平敏感 + 上升沿锁存，防止电平持续为高时重复触发中断。     │
// │                                                                            │
// └──────────────────────────────────────────────────────────────────────────────┘

module plic (
    input  wire        clk,
    input  wire        reset,
    // ── 总线接口（CPU 通过存储器映射访问 PLIC 寄存器） ──
    input  wire [31:0] addr,          // 字节地址，低 8 位选择寄存器
    input  wire [31:0] wdata,         // 写数据（有效位宽为寄存器位宽）
    input  wire        we,            // 写使能（1=写，0=读）
    output reg  [31:0] rdata,         // 读数据（组合逻辑输出，读操作当周期有效）
    // ── 外部中断源输入（电平敏感，内部上升沿检测锁存） ──
    input  wire [31:0] irq_sources,   // 32 个中断源输入
    // ── 中断输出（连接到 CPU 核的中断引脚） ──
    output wire        plic_irq,      // 中断请求（高有效）
    output wire [4:0]  plic_irq_id    // 当前最高优先级中断 ID（assign 驱动，须为 wire）
);

    // ═══════════════════════════════════════════════════════════════════════════
    // 寄存器定义
    // ═══════════════════════════════════════════════════════════════════════════

    // pending:  中断等待寄存器
    //   bit[n] = 1 表示中断源 n 有待处理的中断请求
    //   写入路径有两个：
    //     (a) 上升沿锁存：pending <= pending | irq_rising（中断源上升沿置位）
    //     (b) Claim 清除：pending <= pending & ~(1 << claim_id)（CPU 读 CLAIM 后清除）
    //   注意：位或 (|) 先执行，然后位与 (& ~(...)) 清除，确保同周期置位优先级高于清除
    reg [31:0] pending;

    // enable: 中断使能寄存器（地址 0x04）
    //   bit[n] = 1 表示中断源 n 被使能
    //   复位后所有中断源默认禁用
    reg [31:0] enable;

    // prio[0..31]: 优先级寄存器组（地址 0x40 ~ 0x7C）
    //   每个中断源 8 位优先级值，存储在 32 位字的低 8 位
    //   值越大优先级越高；0 为最低优先级
    reg [7:0]  prio [31:0];

    // threshold: 优先级阈值寄存器（地址 0x20）
    //   只有 prio[n] > threshold 的中断才能通过优先级过滤
    //   threshold = 0 时允许任何优先级 > 0 的中断
    //   threshold = 255 时禁止所有中断
    reg [7:0]  threshold;

    // ═══════════════════════════════════════════════════════════════════════════
    // 上升沿检测 — 将电平敏感的外部中断转换为锁存事件
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // 设计原理：
    //   外部中断源 irq_sources 是电平敏感信号（外设拉高表示有中断请求）。
    //   如果直接用电平驱动 pending，则中断会反复触发直到外设拉低。
    //   PLIC 标准做法是锁存中断事件：只在上升沿（0→1）时置位 pending，
    //   pending 清除后如果 irq_sources 仍为高也不会再次置位，
    //   除非 irq_sources 先拉低再拉高（产生新的上升沿）。
    //
    // 时序：
    //   irq_sources_d 是 irq_sources 打一拍后的同步版本
    //   irq_rising = irq_sources & ~irq_sources_d  — 经典的上升沿检测
    //   当 irq_sources=1 且上一拍 irq_sources_d=0 时，检测到上升沿
    reg [31:0] irq_sources_d;            // irq_sources 的延迟一拍版本（用于上升沿检测）
    wire [31:0] irq_rising = irq_sources & ~irq_sources_d;  // 上升沿脉冲（持续 1 周期）

    always @(posedge clk) begin
        irq_sources_d <= irq_sources;    // 打一拍同步，同时消除亚稳态
    end

    // ═══════════════════════════════════════════════════════════════════════════
    // 有效优先级计算 — 综合优先级过滤、pending 状态和使能状态
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // effective_prio[i] = 1 当且仅当以下三个条件同时满足：
    //   1) prio[i] > threshold  — 优先级高于阈值（注意严格大于，等于不算）
    //   2) pending[i] == 1      — 中断源 i 有等待处理的中断
    //   3) enable[i] == 1      — 中断源 i 已被使能
    //
    // 该信号为组合逻辑，所有输入变化后立即稳定（无时钟延迟）。
    // 使用 generate 生成 32 个并行比较器，每个 1 位宽。
    wire [31:0] effective_prio;
    genvar i;
    generate
        for (i = 0; i < 32; i = i + 1) begin : gen_eff
            assign effective_prio[i] = (prio[i] > threshold)
                                     && pending[i] && enable[i];
        end
    endgenerate

    // ═══════════════════════════════════════════════════════════════════════════
    // 优先级编码器 — 选出有效中断中优先级最高的一个
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // 仲裁策略：固定优先级，编号越大优先级越高。
    //   从 j=31 向下遍历到 j=0，每遇到 effective_prio[j]=1 就更新 highest_irq。
    //   因为遍历顺序是从高编号到低编号，最后匹配的将是最高编号的有效源，
    //   但由于高编号优先级高，一旦分配就不会被低编号覆盖（本实现直接使用 for 循环，
    //   综合后的逻辑等效于优先级编码器）。
    //
    // 注意：当没有任何有效中断时，highest_irq 保持为 0。
    //       中断源 0 通常保留（在 RISC-V 规范中 0 表示"无中断"），
    //       因此 highest_irq=0 既可能表示无中断，也可能表示源 0 中断。
    //       调用方应结合 plic_irq 信号判断。
    reg [4:0] highest_irq;
    integer j;
    always @(*) begin
        highest_irq = 5'd0;
        for (j = 31; j >= 0; j = j - 1) begin
            if (effective_prio[j]) highest_irq = j[4:0];
        end
    end

    // ═══════════════════════════════════════════════════════════════════════════
    // 读数据逻辑 — 组合逻辑实现寄存器读操作
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // 读操作与系统总线时序：
    //   CPU 在总线时钟上升沿发出地址和读命令 → 组合逻辑在同一个周期内返回 rdata
    //   → CPU 在下一个时钟上升沿采样 rdata。这是典型的同步 SRAM 式读时序。
    //
    // 寄存器地址解码（使用 addr[7:0] 低 8 位）：
    //   0x00       → pending        返回 32 位 pending 寄存器
    //   0x04       → enable         返回 32 位 enable 寄存器
    //   0x20       → threshold      返回 {24'h0, threshold}（高 24 位补 0）
    //   0x40~0x7F  → prio[n]        返回 {24'h0, prio[addr[7:2]-16]}
    //                                  addr[7:2] 取出字地址，减 16 得到中断源编号
    //   0x80       → CLAIM（仅当 we=0 时）
    //                 返回 {27'd0, highest_irq}，即当前最高优先级中断 ID
    //                 注意：CLAIM 读仅在非写周期（!we）生效，
    //                 如果同时 we=1 且 addr=0x80，不返回 CLAIM 值
    //   其他地址   → 返回 32'h0（未映射地址返回 0）
    always @(*) begin
        if (!we && addr[7:0] == 8'h80)
            // ── CLAIM 读：返回当前最高优先级中断 ID ──
            // 同时 claim_read 标志将在本周期结束时置 1（见时序逻辑段）
            rdata = {27'd0, highest_irq};
        else if (addr[7:0] == 8'h00)
            rdata = pending;
        else if (addr[7:0] == 8'h04)
            rdata = enable;
        else if (addr[7:0] == 8'h20)
            rdata = {24'h0, threshold};
        else if (addr[7:0] >= 8'h40 && addr[7:0] < 8'h80)
            // addr[7:2] 范围：16~31（对应字地址 0x40/4=16 到 0x7C/4=31）
            // 减 16 得到中断源索引：1~15 对应地址 0x44~0x7C，
            // 但实际上 0x40 对应源 0：addr[7:2]=16, 16-16=0
            rdata = {24'h0, prio[addr[7:2] - 6'd16]};
        else
            rdata = 32'h0;
    end

    // ═══════════════════════════════════════════════════════════════════════════
    // 写 / pending 更新 / Claim-Complete 时序逻辑
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // 本 always 块管理以下寄存器的更新（均在 clk 上升沿）：
    //   - enable, threshold, prio[]   ← CPU 写操作
    //   - pending                     ← 上升沿锁存 + Claim 清除
    //
    // pending 更新公式解析：
    //   pending <= (pending | irq_rising)                    — 步骤 1: 上升沿置位
    //              & ~(claim_read ? (32'd1 << claim_id) : 32'd0); — 步骤 2: Claim 清除
    //
    //   步骤 1 将检测到上升沿的中断源对应的 pending 位置 1（或保持不变）。
    //   步骤 2 如果上一周期 claim_read=1（即 CPU 读过 CLAIM），
    //          则清除 claim_id 对应的 pending 位。
    //
    //   两个操作在同周期进行时，步骤 1 的置位优先于步骤 2 的清除：
    //   (pending | irq_rising) 先执行，然后 & ~(1<<claim_id) 后执行，
    //   因此即使同一个中断源在 claim 清除的同周期又检测到上升沿，
    //   也会被置位（因为 | 在 & 之前计算）。
    //
    // 写操作（we=1 时）：
    //   0x04             → enable   <= wdata
    //   0x20             → threshold <= wdata[7:0]
    //   0x40 <= addr < 0x80 → prio[addr[7:2]-16] <= wdata[7:0]
    //   0x80（Complete）→ 无硬件操作（pending 已在 Claim 时清除）
    //   其他地址          → 无操作
    reg [4:0] claim_id;
    reg       claim_read;           // =1 表示上一周期 CPU 读了 CLAIM 寄存器

    always @(posedge clk) begin
        if (reset) begin
            // ── 同步复位：所有寄存器回到默认状态 ──
            enable    <= 32'h0;          // 全部禁用
            threshold <= 8'h0;           // 阈值 0（允许所有优先级的有效中断）
            pending   <= 32'h0;          // 无等待中断
            for (integer k = 0; k < 32; k = k + 1)
                prio[k] <= 8'h0;        // 所有源优先级为 0（最低）
        end
        else begin
            // ── pending 更新（每个周期都执行）──
            // 1) irq_rising 置位：检测到的上升沿对应的 pending 位被置 1
            // 2) Claim 清除：若上一拍 claim_read=1，清除指定 pending 位
            pending <= (pending | irq_rising)
                       & ~(claim_read ? (32'd1 << claim_id) : 32'd0);

            // ── 寄存器写操作（仅在 we=1 时执行）──
            if (we) begin
                if (addr[7:0] == 8'h04)
                    enable <= wdata;
                else if (addr[7:0] == 8'h20)
                    threshold <= wdata[7:0];
                else if (addr[7:0] >= 8'h40 && addr[7:0] < 8'h80)
                    prio[addr[7:2] - 6'd16] <= wdata[7:0];
            end
        end
    end

    // ═══════════════════════════════════════════════════════════════════════════
    // Claim / Complete 机制 — 符合 RISC-V PLIC 规范
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // RISC-V PLIC 规范定义 CLAIM/COMPLETE 为同一地址（0x80）的两种操作：
    //   - Claim（读）：CPU 读取该地址以获取待处理的最高优先级中断 ID，
    //                  硬件在读取时原子性地清除对应的 pending 位。
    //   - Complete（写）：CPU 处理完中断后，将中断 ID 写回该地址，
    //                     通知 PLIC 该中断已处理完毕。
    //
    // 本实现的 Claim 时序（两个阶段）：
    //   【阶段 1 — 当前周期（组合逻辑 + 时序逻辑采样）】
    //     条件：!we && addr[7:0] == 8'h80
    //     → rdata 组合逻辑返回 {27'd0, highest_irq}（CPU 当周期获得中断 ID）
    //     → 本 always 块在 clk 上升沿将 claim_read 置 1，claim_id 锁存 highest_irq
    //
    //   【阶段 2 — 下一周期（pending 清除）】
    //     claim_read=1 → 上一级 always 块中 pending 对应位被清除
    //     claim_read 自动清零（除非 CPU 又发起了新的 Claim 读）
    //
    // 注意：claim_read 和 claim_id 仅在 !we && addr==0x80 的周期锁存，
    //        下一周期自动清零（else 分支），确保 pending 清除只发生一次。
    always @(posedge clk) begin
        if (reset) begin
            claim_read <= 1'b0;
            claim_id   <= 5'd0;
        end
        else begin
            // 读 CLAIM 寄存器：锁存最高优先级中断 ID，触发 pending 清除
            // 注意：此时 rdata 已通过组合逻辑返回了 highest_irq，
            //       此处锁存的 claim_id 将在下一周期用于清除 pending
            if (!we && addr[7:0] == 8'h80) begin
                claim_read <= 1'b1;
                claim_id   <= highest_irq;
            end
            else begin
                claim_read <= 1'b0;      // 单周期脉冲，下一拍自动清零
            end
        end
    end

    // ═══════════════════════════════════════════════════════════════════════════
    // 中断输出 — 驱动到 CPU 核的中断请求线和中断 ID 线
    // ═══════════════════════════════════════════════════════════════════════════
    //
    // plic_irq:    中断请求信号。当 effective_prio 中任意位为 1 时有效。
    //              使用缩减或 (|) 运算符，等效于 effective_prio != 0。
    //              CPU 应在 plic_irq=1 时发起 Claim 读以获取中断 ID。
    //
    // plic_irq_id: 最高优先级中断编号。当 plic_irq=1 时输出 highest_irq；
    //              当 plic_irq=0 时输出 0（表示无中断）。
    //              注意：plic_irq_id 仅在 plic_irq=1 时有效。
    assign plic_irq    = (|effective_prio);                        // 缩减或：任一有效即可
    assign plic_irq_id = (|effective_prio) ? highest_irq : 5'd0;  // 有效时输出 ID，否则为 0

endmodule
