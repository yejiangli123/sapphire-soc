// ============================================================================
// 一、模块在流水线中的定位与角色
// ============================================================================
//
//   经典的 5 级 RISC-V 流水线结构如下：
//
//      IF ──→ ID ──→ EX ──→ MEM ──→ WB
//      (取指)  (译码)  (执行)  (访存)  (写回)
//        ↑       ↑       ↑       ↑       ↑
//      [reg1]  [reg2]  [reg3]  [reg4]  (纯组合)
//       if_id   id_ex   ex_mem  mem_wb   无寄存器
//
//   本模块（wb_pipeline / pip_reg4 / mem_wb_stage）是流水线中 **第 4 个也是
//   最后一个** 流水线寄存器，位于 MEM（访存）阶段与 WB（写回）阶段之间。
//
//   ★ 5 级流水线 = 4 组寄存器 + 5 段组合逻辑。写回阶段本身是纯组合逻辑
//     （一个多路选择器 MUX，选择写回寄存器的数据来源），不需要寄存器。
//
//   ★ 本寄存器的作用：在 MEM 阶段结束时锁存所有 WB 阶段需要的数据和控制信号，
//     使得 WB 阶段可以在下一个时钟周期独立使用这些稳定值。
//
//   ★ WB 阶段完成的工作：
//       - 根据 wb_select / wbmux 信号选择写回数据源：
//           00 → ALU 运算结果 (R-type / I-type 算术指令)
//           01 → 存储器读出数据 (load 指令)
//           10 → PC+4 (JAL / JALR 跳转返回地址)
//       - 将选中的数据写入目标寄存器 rd
//       - 产生寄存器文件写使能 (reg_write / werf_enable)
//
// ============================================================================
// 二、历史演变与当前状态
// ============================================================================
//
//   ★ 本文件已被废弃。原 pip_reg4 模块已被 mem_wb_stage（位于 mem_pipeline.v）
//     取代。保留此文件仅为兼容可能存在的旧引用路径。
//
//   演变过程：
//     pip_reg4 (本文件原内容)
//       → 废弃原因见下
//       → mem_wb_stage (mem_pipeline.v) — 新设计请使用此模块
//
//   废弃 pip_reg4 的原因：
//
//     1. 命名不一致：
//        - 旧名 pip_reg4（模糊，仅表示"第4个流水线寄存器"）
//        - 新名 mem_wb_stage（自文档化，明确指出 MEM→WB 阶段边界）
//
//     2. 缺少 stall/flush 控制：
//        - pip_reg4 只是一个简单的 always@(posedge clk) 锁存器，
//          无条件每个周期都锁存新值。
//        - mem_wb_stage 支持三级优先级控制：
//            reset > flush > stall > 正常锁存
//        - 流水线冒险（数据冒险、控制冒险）需要 stall（暂停）和 flush（冲刷）
//          机制来保证正确性，pip_reg4 无法胜任。
//
//     3. 复位方式不同：
//        - pip_reg4 无复位逻辑（或仅依赖默认值）
//        - mem_wb_stage 使用异步复位 (posedge reset)，上电后立即进入
//          已知状态，避免不定态传播。
//
//     4. 大量无用端口：
//        - pip_reg4 有 pc_plus_4_in/out、immu_in/out、pc_plus_immu_in/out
//        - 其中 immu 和 pc_plus_immu 在 rv32i_core 顶层实例化时
//          接入常量 0，实际上是死逻辑
//        - mem_wb_stage 精简为仅保留 WB 阶段真正需要的信号：
//            alu_result, load_data, pc_plus_4, rd_addr, reg_write, wbmux
//
