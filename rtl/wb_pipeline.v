// 四、原始 pip_reg4 内部逻辑说明（仅供参考）
// ============================================================================
//
//   pip_reg4 的内部实现极其简单：
//
//     always@(posedge clk) begin
//         pc_plus_4_out    <= pc_plus_4_in;
//         immu_out         <= immu_in;
//         pc_plus_immu_out <= pc_plus_immu_in;
//         read_mem_out     <= read_mem_in;
//         alu_result_out   <= alu_result_in;
//         load_out         <= load_in;
//         wb_select_out    <= wb_select_in;
//         addr_wb_out      <= addr_wb_in;
//         werf_enable_out  <= werf_enable_in;
//     end
//
//   设计分析：
//     - 这是一个纯锁存器，所有输入在 clk 上升沿被采样到输出
//     - 使用非阻塞赋值 (<=)，符合时序逻辑设计规范
//     - 无复位逻辑：上电后输出为不定态 x，依赖后续有效数据覆盖
//     - 无 stall 支持：每个周期都无条件更新，无法响应流水线暂停
//     - 无 flush 支持：分支预测失败时无法冲刷错误指令的数据
//
// ============================================================================
// 五、为何需要 MEM/WB 流水线寄存器（设计原理）
// ============================================================================
//
//   5.1 流水线分段的必要性
//
//     在 5 级流水线中，每个阶段需要一个时钟周期完成其组合逻辑。
//     若没有寄存器在阶段之间隔离，组合逻辑路径将从 IF 一直贯通到 WB，
//     形成超长关键路径，限制时钟频率。
//
//     MEM 阶段的组合逻辑（数据存储器访问）通常是最慢的路径之一。
//     将该阶段的输出用寄存器锁存后，WB 阶段的组合逻辑（MUX 选择 +
//     寄存器文件写入）可以在下一个周期独立完成，不增加 MEM 阶段的延迟。
//
//   5.2 为什么 WB 阶段不需要输出寄存器
//
//     WB 阶段是纯组合逻辑：一个 3 选 1 多路选择器根据 wbmux 选择
//     alu_result / load_data / pc_plus_4，输出到寄存器文件写数据端口。
//
//     寄存器文件的写入本身是在时钟沿完成的（同步写），所以 WB 阶段的
//     组合逻辑输出在时钟沿被寄存器文件采样，不需要额外的流水线寄存器。
//
//     即：WB = MUX（组合）→ regfile（时钟沿写入）
//
//   5.3 控制冒险与 flush
//
//     当分支预测失败或发生异常时，需要冲刷流水线中错误指令的影响。
//     flush 信号会使本寄存器的控制信号（reg_write, wbmux）清零：
//       - reg_write = 0 → 不写寄存器，错误的写回被抑制
//       - wbmux = 0 → 无害的默认值
//     数据通路信号（alu_result 等）可以保持或清零，因为 reg_write=0
//     确保它们不会被写入寄存器文件。
//
//   5.4 数据冒险与 stall
//
//     当后续指令依赖当前 load 指令的结果时（load-use 数据冒险），
//     需要暂停流水线。stall 信号使本寄存器保持当前值不变：
//       - 相当于在 MEM/WB 边界插入一个"气泡"（bubble）
//       - 前面的 MEM/EX/ID 阶段也同步 stall，保持整个流水线一致性
//
//   5.5 优先级设计：reset > flush > stall > 正常
//
//     - reset 优先级最高：系统复位时所有寄存器必须清零，进入安全初始状态
//     - flush 次之：流水线冲刷是临时性的（1-2 周期），需要在复位不活跃
//       时工作。flush 仅清零控制信号（不浪费功耗清零数据通路）
//     - stall 再次：保持当前值，用于插入等待周期
//     - 正常锁存优先级最低：默认行为
//
// ============================================================================
// 六、当前替代模块 mem_wb_stage 接口说明（新设计请使用此模块）
// ============================================================================
//
//   模块名：mem_wb_stage（位于 mem_pipeline.v）
//
//   ┌─────────────────────────────────────────────────────────────────┐
//   │ 端口名            │ 位宽  │ 方向  │ 说明                         │
//   ├─────────────────────────────────────────────────────────────────┤
//   │ clk               │ 1     │ input │ 全局时钟                     │
//   │ reset             │ 1     │ input │ 异步复位，高有效             │
//   │ stall             │ 1     │ input │ 流水线暂停，高有效           │
//   │ flush             │ 1     │ input │ 流水线冲刷，高有效           │
//   │ mem_alu_result    │ 32    │ input │ MEM 阶段的 ALU 运算结果      │
//   │ mem_load_data     │ 32    │ input │ MEM 阶段的存储器读出数据     │
//   │ mem_pc_plus_4     │ 32    │ input │ MEM 阶段的 PC+4 值           │
//   │ mem_rd_addr       │ 5     │ input │ MEM 阶段的目标寄存器地址     │
//   │ mem_reg_write     │ 1     │ input │ MEM 阶段的寄存器写使能       │
//   │ mem_wbmux         │ 2     │ input │ MEM 阶段的写回数据选择       │
//   │ wb_alu_result     │ 32    │ output│ 锁存后的 ALU 结果 → WB 阶段  │
//   │ wb_load_data      │ 32    │ output│ 锁存后的 load 数据 → WB 阶段 │
//   │ wb_pc_plus_4      │ 32    │ output│ 锁存后的 PC+4 → WB 阶段      │
//   │ wb_rd_addr        │ 5     │ output│ 锁存后的 rd 地址 → WB 阶段   │
//   │ wb_reg_write      │ 1     │ output│ 锁存后的写使能 → WB 阶段     │
//   │ wb_wbmux          │ 2     │ output│ 锁存后的写回选择 → WB 阶段   │
//   └─────────────────────────────────────────────────────────────────┘
//
//   内部 always 块行为：
//
//     always @(posedge clk or posedge reset)
//       if (reset)           → 所有输出清零
//       else if (flush)      → 仅控制信号（reg_write, wbmux）清零
//       else if (!stall)     → 正常锁存所有 mem_* 到 wb_*
//       // stall=1           → 保持当前值不变
//
