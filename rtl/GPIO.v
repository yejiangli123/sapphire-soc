// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  模块名称: GPIO.v                                                            ║
// ║  功能描述: 通用输入输出控制器 (General Purpose I/O)                              ║
// ║                                                                            ║
// ║  【外设用途】                                                                ║
// ║  GPIO 是最基础的外设模块，用于控制芯片引脚的数字输入/输出方向与电平。               ║
// ║  每个引脚可独立配置为输入或输出模式：                                           ║
// ║    - 输出模式: CPU 通过写 OUT 寄存器驱动引脚电平（高/低）。                       ║
// ║    - 输入模式: CPU 通过读 IN 寄存器获取外部引脚当前电平。                         ║
// ║  典型应用场景：LED 控制、按键读取、片选信号生成、与其他芯片的 GPIO 直连通信。       ║
// ║                                                                            ║
// ║  【寄存器映射 (Register Map)】                                                ║
// ║  基地址 + 偏移量，每个寄存器 32 位，每一位对应一个 GPIO 引脚：                     ║
// ║                                                                            ║
// ║  偏移量   名称    访问权限   复位值     描述                                   ║
// ║  ──────────────────────────────────────────────────────────────────────     ║
// ║  0x00     DIR     读写(R/W) 32'h0     方向寄存器                              ║
// ║             bit[n] = 0 → 引脚 n 为输入模式（默认）                             ║
// ║             bit[n] = 1 → 引脚 n 为输出模式                                    ║
// ║                                                                            ║
// ║  0x04     OUT     读写(R/W) 32'h0     输出数据寄存器                           ║
// ║             写: 设置输出引脚的电平值（仅在 DIR[n]=1 时对外部有效）                ║
// ║             读: 回读上一次写入的值                                             ║
// ║                                                                            ║
// ║  0x08     IN      只读(R)   —         输入状态寄存器                           ║
// ║             反映外部 gpio_in 引脚经过两级同步后的电平                            ║
// ║             写操作无效（被硬件忽略）                                            ║
// ║                                                                            ║
// ║  其他偏移量       只读(R)   32'h0     未定义地址默认返回 0                       ║
// ║                                                                            ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

module gpio (
    // ────────────────────────────────────────────────────────────────────────
    // 时钟与复位
    // ────────────────────────────────────────────────────────────────────────
    input  wire        clk,          // 系统时钟，所有寄存器在该时钟上升沿动作
    input  wire        reset,        // 同步复位（高有效），复位后 DIR=0, OUT=0

    // ────────────────────────────────────────────────────────────────────────
    // 总线侧接口 (Bus Interface)
    // 本模块作为从设备挂载到 SoC 内部总线上，CPU 通过该接口访问 GPIO 寄存器。
    // 接口协议: 简单的同步读写，无握手/等待信号，单周期完成。
    // ────────────────────────────────────────────────────────────────────────
    input  wire [31:0] addr,         // 地址总线，仅低 4 位 [3:0] 用于寄存器选择
    input  wire [31:0] wdata,        // 写数据总线，CPU 写入的 32 位数据
    input  wire        we,           // 写使能（高有效），=1 表示当前周期为写事务
    output wire [31:0] rdata,        // 读数据总线，组合逻辑输出，当前地址对应的寄存器值

    // ────────────────────────────────────────────────────────────────────────
    // 外部引脚侧接口 (External Pin Interface)
    // 连接到芯片物理 I/O 焊盘（PAD），是 GPIO 控制器与外部世界的物理边界。
    // ────────────────────────────────────────────────────────────────────────
    output wire [31:0] gpio_out,     // 输出引脚驱动值，直接由 out_reg 组合输出
    input  wire [31:0] gpio_in       // 输入引脚电平，来自芯片外部，属于异步信号
);

    // ╔══════════════════════════════════════════════════════════════════════════╗
    // ║  内部寄存器                                                                ║
    // ╚══════════════════════════════════════════════════════════════════════════╝

    reg [31:0] dir_reg;              // 0x00: 方向寄存器 (Direction)
                                     //       复位值 32'h0，即所有引脚默认为输入
                                     //       bit[n]=0: 输入  bit[n]=1: 输出

    reg [31:0] out_reg;              // 0x04: 输出数据寄存器 (Output Data)
                                     //       复位值 32'h0，所有引脚输出低电平
                                     //       该值直接驱动 gpio_out 端口

    // ── 两级同步器 (Two-Stage Synchronizer) ─────────────────────────────────
    // gpio_in 来自芯片外部，相对于 clk 是完全异步的信号。
    // 如果直接用 clk 采样异步信号，当采样时刻发生在信号跳变沿附近时，
    // 触发器可能进入"亚稳态"（输出既非 0 也非 1 的中间电平），
    // 导致 downstream 逻辑出现不可预测的行为甚至芯片功能失效。
    //
    // 解决方案: 使用两级串联的 D 触发器对异步输入进行同步。
    //   Stage 1 (in_sync1): 第一级采样，可能产生亚稳态，但经过一个周期后
    //                        大概率已稳定（MTBF 随工艺和频率而不同）。
    //   Stage 2 (in_sync2): 第二级采样，此时 in_sync1 几乎确定已稳定，
    //                        输出 in_sync2 是干净的数字信号。
    //   CPU 读取 IN 寄存器时实际读到的是 in_sync2，保证读到的是确定值。
    //
    // 代价: 输入信号到被 CPU 可读取之间有 2 个时钟周期的延迟。
    //       对于 GPIO 这种低速信号（按键、LED 等），此延迟可忽略。
    // ─────────────────────────────────────────────────────────────────────────
    reg [31:0] in_sync1;             // 同步器第一级
    reg [31:0] in_sync2;             // 同步器第二级 (最终稳定输出，供 CPU 读取)

    // ╔══════════════════════════════════════════════════════════════════════════╗
    // ║  读数据通路 (Read Data Path)                                               ║
    // ║  组合逻辑译码，无时钟延迟，cpu 在发起读请求的同一个周期获得 rdata。              ║
    // ║  未定义的地址偏移量默认返回 32'h0（安全行为，避免 X 态传播）。                  ║
    // ╚══════════════════════════════════════════════════════════════════════════╝

    assign rdata = (addr[3:0] == 4'h0) ? dir_reg  :   // 读 DIR
                   (addr[3:0] == 4'h4) ? out_reg  :   // 读 OUT
                   (addr[3:0] == 4'h8) ? in_sync2 :   // 读 IN (同步后的值)
                   32'h0;                              // 未定义地址 → 0

    // ╔══════════════════════════════════════════════════════════════════════════╗
    // ║  写数据通路 (Write Data Path)                                               ║
    // ║                                                                           ║
    // ║  时序说明:                                                                  ║
    // ║    - 时钟上升沿采样 addr, wdata, we                                        ║
    // ║    - 当 we=1 时，根据 addr[3:0] 将 wdata 写入对应寄存器                        ║
    // ║    - 当 reset=1 时，dir_reg 和 out_reg 异步复位                              ║
    // ║    - IN 寄存器不可写（硬件同步器自动更新）                                     ║
    // ║                                                                           ║
    // ║  设计要点:                                                                  ║
    // ║    1. DIR 和 OUT 寄存器均可读可写，但方向寄存器不应频繁切换。                     ║
    // ║    2. 写 DIR=0 （输入）后，对应的 gpio_out[x] 应立即变为高阻（由外部 PAD 处理）。 ║
    // ║       本模块仅负责发出 out_reg 值，三态控制由顶层 PAD 模块根据 dir_reg 实现。    ║
    // ║    3. 复位优先于写: reset 有效时忽略 we 信号，寄存器强制归零。                    ║
    // ╚══════════════════════════════════════════════════════════════════════════╝

    always @(posedge clk) begin
        if (reset) begin
            // 复位: 所有引脚恢复为输入模式，输出数据清零
            dir_reg <= 32'h0;
            out_reg <= 32'h0;
        end else if (we) begin
            // 写操作: 仅 DIR (0x00) 和 OUT (0x04) 可写
            case (addr[3:0])
                4'h0: dir_reg <= wdata;   // 写入方向寄存器
                4'h4: out_reg <= wdata;   // 写入输出数据寄存器
                // 其他地址 (含 0x08 IN) 写操作被忽略，保持当前值不变
            endcase
        end
        // else: 无写操作且无复位 → 寄存器保持当前值
    end

    // ╔══════════════════════════════════════════════════════════════════════════╗
    // ║  输入同步器 (Input Synchronizer)                                           ║
    // ║                                                                           ║
    // ║  两级触发器串联，将异步的 gpio_in 信号同步到 clk 时钟域:                        ║
    // ║                                                                           ║
    // ║    gpio_in (异步) ──► [D-Q stage1] ──► in_sync1 ──► [D-Q stage2] ──► in_sync2 ║
    // ║                           ↑                      ↑                         ║
    // ║                     可能有亚稳态              已稳定                         ║
    // ║                                                                           ║
    // ║  时序约束 (SDC):                                                            ║
    // ║    - gpio_in 对 clk 应设为 false_path，因为它是异步输入                        ║
    // ║    - in_sync1 → in_sync2 的路径是正常的同步路径，满足 setup/hold 时序要求        ║
    // ║    - 如果需要更高速率，可增加同步级数（如三级同步器）或使用异步 FIFO                ║
    // ║                                                                           ║
    // ║  注: 每个 clk 周期都会采样 gpio_in，因此同步器始终在运行，与 CPU 读写无关。        ║
    // ║       CPU 读取 IN 寄存器时得到的是最近一次采样并同步后的值。                      ║
    // ╚══════════════════════════════════════════════════════════════════════════╝

    always @(posedge clk) begin
        if (reset) begin
            // 复位时同步器清零，确保复位释放后输出确定值
            in_sync1 <= 32'b0;
            in_sync2 <= 32'b0;
        end else begin
            // 第一级: 直接采样异步输入 (MTBF 关键点)
            in_sync1 <= gpio_in;
            // 第二级: 采样已稳定的第一级输出，得到干净的同步信号
            in_sync2 <= in_sync1;
        end
    end

    // ╔══════════════════════════════════════════════════════════════════════════╗
    // ║  输出驱动 (Output Driver)                                                  ║
    // ║                                                                           ║
    // ║  gpio_out 直接由 out_reg 组合输出，无额外寄存器延迟。                          ║
    // ║  实际的引脚三态控制（输入/高阻/输出）由芯片顶层 PAD 模块完成：                     ║
    // ║    - 如果 DIR[n]=1 (输出): PAD 将 gpio_out[n] 驱动到引脚焊盘                  ║
    // ║    - 如果 DIR[n]=0 (输入): PAD 将引脚设为高阻，gpio_out[n] 不驱动焊盘           ║
    // ║  本模块仅提供数据和控制信号，不直接处理电气层面的三态逻辑。                        ║
    // ╚══════════════════════════════════════════════════════════════════════════╝

    assign gpio_out = out_reg;

endmodule
