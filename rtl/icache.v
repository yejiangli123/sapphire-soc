// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
//  一、模块功能概述
// ============================================================================
//
//  本模块为 RV32IM 处理器核心提供一个 2KB 的直接映射 (Direct-Mapped) 指令缓存。
//  它位于取指级 (IF Stage) 与系统总线之间，通过缓存最近访问的指令行来减少
//  取指延迟，提升执行效率。
//
//  【缓存组织结构】
//    - 行数 (NUM_LINES):     64 行
//    - 每行字数 (LINE_WORDS): 8 字 (32 字节)
//    - 总容量:                64 × 8 × 4 = 2048 字节 (2KB)
//    - 地址映射: TAG(21) | INDEX(6) | WORD(3) | BYTE(2)
//        INDEX:  6 bits → 64 行索引
//        WORD:   3 bits → 行内 0..7 字选择
//        BYTE:   2 bits → 字内字节偏移 (指令取指忽略)
//        TAG:   21 bits → 高 21 位用于命中比较
//
//  【总线接口协议 — 简单 req/ack 握手】
//    - CPU 侧:  cpu_addr, cpu_req, cpu_rdata, cpu_ready
//    - 存储侧:  mem_addr, mem_req, mem_rdata, mem_ack
//    - 在 SoC 顶层通过 axi_lite_manager 或 bus_arbiter 转换为 AXI4-Lite
//
//  【性能特征】
//    - 命中延迟 (Hit latency):     1 个周期 (组合读出)
//    - 缺失惩罚 (Miss penalty):    1 + N 周期 (N = 8 × 存储延迟)
//    - 关键路径:                   TAG 比较 + 数据 MUX → cpu_rdata
//
// ============================================================================
//  二、设计决策说明
// ============================================================================
//
//  2.1  为何选择直接映射 (Direct-Mapped)？
//  ─────────────────────────────────────
//    - 面积最小：每行仅需 1 个 TAG 比较器 (64 个 TAG → 64 个比较器)
//    - 时序最佳：直接映射的命中判断路径最短 (1 级 MUX + 比较)
//    - 适合教学/简单 SoC：不需要复杂的 LRU/PLRU 替换策略
//    - 局限性：冲突缺失 (conflict miss) 率高于组相联，但指令访存
//      通常具有良好的空间局部性，在 2KB 容量下直接映射足够
//
//  2.2  为何 8 字/行 (32 字节)？
//  ─────────────────────────────────────
//    - 匹配典型 AXI burst 长度 (8 拍 × 4 字节 = 32 字节)
//    - 32 字节是常见的 cache line 大小，与 DRAM 突发/BRAM 宽度对齐
//    - 更大的行可捕获更多空间局部性，但增加缺失惩罚
//
//  2.3  为何使用两组存储阵列 (tags + data) 而非单一阵列？
//  ─────────────────────────────────────
//    - 分离 tag 和 data 允许并行访问: tag 比较可与 data 读出同时进行
//    - data[][] 为 2D 寄存器阵列: data[line][word]，综合工具可推断 BRAM
//    - valid[] 单独管理，方便快速全清零 (复位时) 和行失效
//
//  2.4  为何在缺失起始时刻捕获 miss_tag？
//  ─────────────────────────────────────
//    - 行填充 (refill) 需要 8+ 个周期，期间 cpu_addr 可能变化
//      (虽然 IF 阶段通常会被 stall 住，但捕获可增加鲁棒性)
//    - miss_tag 保存了缺失时的 TAG 值，确保填充完成后写入的是
//      正确的 TAG，而非在填充完成那一刻 cpu_addr 上的 TAG
//    - miss_index 同理：锁存缺失时刻的行索引
//    - refill_base 锁存行对齐的基地址，用于计算后续字的地址
//
//  2.5  为何无 critical-word-first (关键字优先) ？
//  ─────────────────────────────────────
//    - 简化设计: 始终从 word 0 开始填充，状态机仅需 refill_cnt 递增
//    - 对于指令缓存，缺失发生在分支目标地址时，该地址可能在行内
//      任意位置。无 CWF 意味着即使缺失地址是 word 3，CPU 也必须等
//      待 word 0..3 全部取回后才能获得所需指令
//    - 这是一个已知的性能限制 (见第七节)
//
// ============================================================================
//  三、状态机设计
// ============================================================================
//
//  【状态编码】
//    S_IDLE  = 2'd0  — 空闲，等待 CPU 请求
//    S_REFILL = 2'd1 — 行填充，从存储器读取 8 个字
//
//  【状态转移图】
//
//                  ┌─────────────────────────────┐
//                  │                             │
//        ┌─────┐   │ cpu_req && !hit    ┌────────┴────┐
//   reset│     │   │ ─────────────────> │             │
//   ────>│IDLE │                       │   REFILL    │
//        │     │<──────────────────────│             │
//        └─────┘   refill_cnt==7 &&    └─────────────┘
//                    mem_ack
//
//  【各状态详述】
//
//  3.1  S_IDLE (空闲态)
//  ─────────────────────────────────────
//  功能:
//    - 持续拉低 mem_req (解除总线请求)
//    - 监听 cpu_req，若命中 (hit=1) 则组合逻辑直接通过 cpu_rdata 和
//      cpu_ready 返回数据，不触发状态转移
//    - 若缺失 (cpu_req=1 && hit=0)，则:
//        · 锁存缺失信息: miss_index, miss_tag, refill_base
//        · 发起存储器读请求: mem_addr = 行对齐基地址, mem_req = 1
//        · 转移至 S_REFILL
//
//  3.2  S_REFILL (行填充态)
//  ─────────────────────────────────────
//  功能:
//    - 等待 mem_ack 握手信号
//    - 每收到一个 mem_ack:
//        · 将 mem_rdata 写入 data[miss_index][refill_cnt]
//        · refill_cnt 自增 (0 → 1 → ... → 7)
//        · 若 refill_cnt == 7:
//            → 写入 tag: tags[miss_index] <= miss_tag
//            → 置有效:   valid[miss_index] <= 1
//            → 拉低 mem_req，返回 S_IDLE
//        · 否则:
//            → 计算下一字地址: refill_base + (refill_cnt+1) << 2
//            → 保持 mem_req = 1
//
//  3.3  状态机设计要点
//  ─────────────────────────────────────
//  - 两态设计极简：无写操作 (I$ 只读)，无需回写 (writeback)，
//    无需无效化状态 (假设无自修改代码)
//  - mem_req 在 REFILL 期间连续保持有效: 存储器侧通过 mem_ack
//    控制节奏，形成天然的背压 (back-pressure) 机制
//
// ============================================================================
//  四、时序分析
// ============================================================================
//
//  4.1  命中路径 (Hit Path) — 单周期读
//  ─────────────────────────────────────
//  周期 N-1:  cpu_addr 提供地址
//  周期 N:   组合逻辑:
//             (1) index = cpu_addr 的位段提取 (无寄存器)
//             (2) tags[index] 读出并比较 tag_in
//             (3) data[index][word] 读出
//             (4) cpu_rdata = hit ? data[index][word] : mem_rdata
//             (5) cpu_ready = hit || ... = 1
//             → CPU 在同一周期获得指令
//
//  关键路径 (Critical Path):
//    cpu_addr → index decode → data MUX[word] → cpu_rdata MUX → cpu_rdata
//    预计延迟: ~3-4 ns (FPGA) / ~1.5 ns (ASIC 28nm)
//    此路径决定了 icache 的 fmax
//
//  4.2  缺失路径 (Miss Path) — 多周期行填充
//  ─────────────────────────────────────
//  周期 0:  检测缺失 (S_IDLE)
//              → 锁存 miss_info, 发起 mem_req, 进入 S_REFILL
//  周期 1:  (存储器仲裁/响应延迟, mem_ack=0)
//              → 等待... 状态保持 S_REFILL
//  周期 N:  mem_ack=1 → 存储 word 0, refill_cnt→1, 发起 word 1 请求
//           同时: cpu_ready=1, cpu_rdata=mem_rdata (旁路)
//  周期 N+1..N+7: 重复, 每次 mem_ack 存储一个字
//  周期 N+7: refill_cnt=7, mem_ack=1
//              → 存储 word 7, 写 tag, 置 valid, 返回 S_IDLE
//
//  注意: mem_ack 可能不是每个周期都有效。存储器可能需要多周期响应。
//        典型 BRAM 延迟为 1-2 周期，AXI 总线可能更多。
//
//  4.3  流水线停顿处理 (Stall Handling)
//  ─────────────────────────────────────
//  - cpu_ready = hit || (S_REFILL && mem_ack)
//  - 缺失时 cpu_ready 拉低 → IF 级流水线停顿 (stall=1)
//  - 每次 mem_ack 到达时，若仍在 S_REFILL，cpu_ready 短暂拉高一周期
//  - 此设计允许"流式"交付: 第一个 mem_ack 到达时 CPU 即可获得 word 0，
//    无需等待整行填充完成
//  - 局限性: 这要求 CPU 请求恰好是 word 0 地址。若请求 word 3，
//    旁路交付的是 word 0 (错误数据) — 见第七节
//
// ============================================================================
//  五、SoC 集成方案
// ============================================================================
//
//  【当前状态 (rv32i_core.v / rv32im_core)】
//  ─────────────────────────────────────
//  - icache 模块已定义但**未集成**到核心中
//  - 核心当前使用 instruction_memory 模块进行直接取指 (旁路缓存)
//  - 原因: rv32im_core 使用统一总线 (unified bus)，未实现 I$/D$ 分离
//  - 若启用 icache，需要:
//      1. 替换 instruction_memory 为 icache 实例
//      2. 将 icache 的 mem_* 端口连接到 bus_arbiter (多主仲裁器)
//      3. 更新 stall_unit 使用 icache_ready 信号
//      4. 处理 dcache 与 icache 存储侧总线的仲裁
//
//  【目标集成架构】
//  ─────────────────────────────────────
//                     ┌─────────────┐
//          PC ───────>│             │
//                     │   icache    │── mem_req/addr ─┐
//          instr <────│   (I$)     │<─ mem_rdata/ack  │
//                     │             │                  │
//                     └─────────────┘      ┌───────────▼──────────┐
//                                          │    bus_arbiter       │
//                     ┌─────────────┐      │   (多主仲裁 + 地址   │
//          addr ─────>│             │      │    译码 + AXI转换)   │
//                     │   dcache    │──┬───│                      │
//          rdata <────│   (D$)     │<─┘   └──────────────────────┘
//                     │             │
//                     └─────────────┘
//
//  【与 dcache.v 的对比】
//  ─────────────────────────────────────
//  特性          │ icache (本模块) │ dcache.v
//  ─────────────┼─────────────────┼─────────────────
//  写支持        │ 无 (只读)       │ 有 (写穿透)
//  状态数        │ 2 (IDLE,REFILL) │ 4 (IDLE,WRITE,RD_MISS,RD_REFILL)
//  dirty 位      │ 无              │ 有 (未使用)
//  cpu_ready     │ 组合逻辑        │ 组合逻辑
//  行填充策略    │ 从 word 0 开始  │ 从 word 0 开始 (同)
//  tag 捕获      │ 有              │ 有
//
// ============================================================================
//  六、参数与宏定义
// ============================================================================
//
//  所有参数通过 `include "Def.v" 中的宏定义提供缺省值，并可在实例化时覆盖:
//
//  `define ICACHE_NUM_LINES  64    // 行数
//  `define ICACHE_WORD_BITS  3     // 行内字偏移位宽 (log2(8) = 3)
//  `define ICACHE_INDEX_BITS 6     // 行索引位宽 (log2(64) = 6)
//  `define ICACHE_TAG_BITS   21    // 标签位宽 (32-2-3-6 = 21)
//
//  地址位域布局 (32-bit 字节地址):
//  ┌──────────┬──────────┬──────────┬──────────┐
//  │  TAG[20:0] │ INDEX[5:0] │ WORD[2:0] │ BYTE[1:0]│
//  │   21 bits   │   6 bits   │   3 bits   │  2 bits  │
//  └──────────┴──────────┴──────────┴──────────┘
//   31        11 10        5 4         2 1        0
//
// ============================================================================
//  七、已知问题与局限性
// ============================================================================
//
//  7.1  无关键字优先 (No Critical-Word-First)
//  ─────────────────────────────────────
//  问题: 缺失填充始终从 word 0 开始，而非从 CPU 请求的字 (critical word) 开始。
//        例如 CPU 请求地址 0x104 (word 1)，缓存缺失后先从 0x100 (word 0) 开始读。
//  影响: 第一个 mem_ack 到达时 cpu_ready=1, cpu_rdata=mem_rdata=word 0。
//        若 IF 级在此周期锁存 instr，将获得错误指令 (word 0 而非 word 1)。
//  缓解: 目前的假设是取指地址总是行对齐的 (word 0)，这在顺序执行中通常成立，
//        但分支目标可能不满足此假设。需要 CPU 侧配合或修改 FSM 支持 CWF。
//
//  7.2  不支持新请求的中途处理
//  ─────────────────────────────────────
//  问题: S_REFILL 期间，即使 cpu_req 有效且命中其他已缓存行，FSM 也忽略。
//        cpu_ready 被 S_REFILL && mem_ack 驱动，而非响应该命中。
//  影响: 在行填充期间，CPU 无法从缓存中读取其他已缓存的行。
//  缓解: 由于缺失时 IF 级被 stall，CPU 不会发出新请求。但若未来支持
//        非阻塞缓存 (non-blocking cache)，需要重构状态机。
//
//  7.3  mem_req 持续保持问题
//  ─────────────────────────────────────
//  当前行为: 在 S_REFILL 期间 mem_req 保持为 1，即使在等待 mem_ack 的间隙。
//  风险: 若存储系统期望 mem_req 以脉冲形式 (单周期 pulse) 发出，持续高电平
//        可能导致重复事务。当前 BRAM 和 AXI 控制器均支持持续请求模式，故无问题，
//        但与非标准存储接口连接时需注意。
//
//  7.4  无缓存一致性 / 无刷新机制
//  ─────────────────────────────────────
//  问题: 无可编程的缓存失效 (invalidate) 或刷新 (flush) 接口。
//  影响: 若存在自修改代码 (self-modifying code) 或 DMA 修改指令内存，
//        无法使缓存中的过时行失效。
//  缓解: 当前 SoC 不支持自修改代码或 DMA 到指令空间，此限制可接受。
//        若未来需要，可增加 invalidate 输入端口 (按地址或全刷)。
//
//  7.5  复位依赖 initial 块
//  ─────────────────────────────────────
//  问题: initial 块用于仿真中初始化 valid[] 为 0，但 FPGA 综合工具通常忽略
//        initial 块中的寄存器赋值 (仅接受 $readmemh 等存储器初始化)。
//  缓解: always @(posedge reset) 块中也有 valid[] 清零循环，确保硬件复位正确。
//        initial 块主要是仿真保障，可安全地由综合工具忽略。
//
//  7.6  bypss 路径的语义限制
//  ─────────────────────────────────────
//  问题: cpu_rdata = hit ? data[index][word] : mem_rdata 意味着非命中时
//        cpu_rdata 始终跟随 mem_rdata (存储器读数据总线)。
//  影响: 若由于某种原因 (如总线桥错误) mem_rdata 上出现无关数据，CPU 将获得
//        错误指令而无任何错误指示。无奇偶校验或 ECC 保护。
//  缓解: 这是教学级 SoC 的合理取舍，生产级设计应添加错误检测。
//
// ============================================================================
//
`include "Def.v"

module icache #(
    // ---------------------------------------------------------------------------
    // 参数列表 — 均从 Def.v 宏定义获取默认值，可在实例化时覆盖
    // ---------------------------------------------------------------------------
    parameter NUM_LINES  = `ICACHE_NUM_LINES,   // 缓存行数 (默认 64)
    parameter LINE_WORDS = 8,                    // 每行字数 (固定 8 = 32 字节)
    parameter WORD_BITS  = `ICACHE_WORD_BITS,    // 字偏移位宽 (log2(8) = 3)
    parameter TAG_BITS   = `ICACHE_TAG_BITS,     // 标签位宽 (默认 21)
    parameter INDEX_BITS = `ICACHE_INDEX_BITS    // 索引位宽 (log2(64) = 6)
) (
    // ===========================================================================
    //  端口列表
    // ===========================================================================
    input  wire        clk,            // 系统时钟，上升沿触发
    input  wire        reset,          // 异步复位，高有效

    // --- CPU 侧接口 (连接 IF 取指级) ---
    // cpu_addr:  来自 PC 的 32-bit 字节地址
    // cpu_req:   取指请求有效 (IF 级发出请求)
    // cpu_rdata: 返回的 32-bit 指令数据
    // cpu_ready: 握手信号: 1=数据有效/可接受新请求, 0=忙 (需停顿)
    input  wire [31:0] cpu_addr,
    input  wire        cpu_req,
    output wire [31:0] cpu_rdata,
    output wire        cpu_ready,

    // --- 存储器侧接口 (连接系统总线/仲裁器) ---
    // mem_addr:  存储器读地址 (32-bit 字节地址)
    // mem_req:   读请求有效 (脉冲或持续，取决于总线协议)
    // mem_rdata: 存储器返回的读数据
    // mem_ack:   存储器握手应答 (1=当前周期 mem_rdata 有效)
    output reg  [31:0] mem_addr,
    output reg         mem_req,
    input  wire [31:0] mem_rdata,
    input  wire        mem_ack
);

    // ===========================================================================
    //  缓存存储阵列
    // ===========================================================================
    // tags[]  — 标签阵列: 每行存储高 21-bit 地址标签，用于命中比较
    // valid[] — 有效位阵列: 1=该行包含有效数据, 0=无效 (上电/复位后全零)
    // data[]  — 数据阵列: 2D 结构 [行号][行内字偏移]，每项 32-bit
    //
    // 综合提示: data[0:NUM_LINES-1][0:LINE_WORDS-1] 这种 2D 寄存器阵列
    //           在大多数 FPGA 综合工具中可被推断为 Block RAM (若位宽/深度合适)
    //           tags[] 和 valid[] 也通常映射为分布式 RAM 或 LUT RAM
    // ===========================================================================
    reg [TAG_BITS-1:0]         tags    [0:NUM_LINES-1];
    reg                        valid   [0:NUM_LINES-1];
    reg [31:0]                 data    [0:NUM_LINES-1][0:LINE_WORDS-1];

    // ===========================================================================
    //  地址解码 — 组合逻辑
    // ===========================================================================
    // 将 CPU 字节地址拆解为三个字段:
    //
    // index:  行索引。从 32-bit 地址中提取 INDEX_BITS(6) bit，选定 64 行之一
    //         使用 part-select: cpu_addr[INDEX_BITS+WORD_BITS+2 -: INDEX_BITS]
    //         即 cpu_addr[6+3+2-1:6+3+2-6] = cpu_addr[10:5]
    //
    // word:   行内字偏移。提取 WORD_BITS(3) bit，选定行内 0..7 字
    //         cpu_addr[WORD_BITS+2-1:2] = cpu_addr[4:2]
    //
    // tag_in: 标签字段。提取高 TAG_BITS(21) bit 用于命中比较
    //         cpu_addr[31:31-TAG_BITS+1] = cpu_addr[31:11]
    //
    // 注意: 低 2 bit (BYTE) 被忽略，因为指令总是 32-bit 对齐
    // ===========================================================================
    wire [INDEX_BITS-1:0]  index  = cpu_addr[INDEX_BITS+WORD_BITS+2 -: INDEX_BITS];
    wire [WORD_BITS-1:0]   word   = cpu_addr[WORD_BITS+2-1:2];
    wire [TAG_BITS-1:0]    tag_in = cpu_addr[31:31-TAG_BITS+1];

    // ===========================================================================
    //  缺失处理状态机 (Miss FSM)
    // ===========================================================================
    // 两态设计:
    //   S_IDLE   = 2'd0 — 空闲，等待 CPU 请求
    //   S_REFILL = 2'd1 — 行填充：从存储器逐字读取 8 个字填入缓存行
    //
    // 缺失处理寄存器:
    //   refill_cnt  — 填充计数器 (0..7)，指示当前正在填充行内第几个字
    //   refill_base — 行对齐基地址 = {cpu_addr[31:WORD_BITS+2], 5'b0}
    //                  用于计算后续字的地址 (base + 0, +4, +8, ...)
    //   miss_index  — ★ 缺失时锁存的行索引 (防止 cpu_addr 在填充期间变化)
    //   miss_tag    — ★ 缺失时锁存的标签值 (关键！确保填充完成后写入正确的 tag)
    //
    //  ★ tag 捕获的意义:
    //    填充需要多个周期才能完成。如果不在缺失发生时锁存 tag_in，等到
    //    填充完成那一刻才读取 cpu_addr 上的 tag，此时 cpu_addr 可能已
    //    经指向了不同的行 (例如 CPU 被 un-stall 并前进了)，导致写入
    //    错误的 TAG。miss_tag 和 miss_index 的锁存是数据一致性的关键保障。
    // ===========================================================================
    localparam S_IDLE = 2'd0, S_REFILL = 2'd1;
    reg [1:0]  state;
    reg [2:0]  refill_cnt;           // 0..7 (填充 8 个字)
    reg [31:0] refill_base;          // 行对齐基地址
    reg [INDEX_BITS-1:0] miss_index;
    reg [TAG_BITS-1:0]   miss_tag;   // ★ 缺失起始时锁存的 TAG
    reg [WORD_BITS-1:0]  miss_word;  // ★【Bug 修复】缺失起始时锁存的 word 偏移
                                      //   实现 critical-word-first：从 CPU 请求的字
                                      //   开始 refill，而非始终从 word 0 开始

    // ===========================================================================
    //  命中判断 — 组合逻辑
    // ===========================================================================
    // 命中条件 (两条件必须同时满足):
    //   1. valid[index] = 1        — 该行含有效数据
    //   2. tags[index] == tag_in   — 标签匹配 (地址高位一致)
    //
    // 注意: 此命中信号基于**当前** cpu_addr 的实时解码，而非缺失时的锁存值。
    //       在 S_REFILL 期间，cpu_addr 可能因 IF 级停顿而保持为缺失地址，
    //       此时 hit 可能为 0 (因为目标行尚未完成填充)。这是正常的。
    // ===========================================================================
    wire hit  = valid[index] && (tags[index] == tag_in);

    // ===========================================================================
    //  CPU 读数据与就绪信号 — 组合逻辑
    // ===========================================================================
    // cpu_rdata:
    //   命中时 → 从缓存数据阵列直接读出 data[index][word]
    //   缺失时 → 旁路到 mem_rdata (在 mem_ack 有效时获取填充数据)
    //
    //   组合读出路径: cpu_addr → index/word 解码 → data[index][word] → cpu_rdata
    //   此路径为整个 icache 的关键时序路径 (critical path)
    //
    // cpu_ready:
    //   命中时 → 立即就绪 (hit=1)
    //   缺失时 → 在 mem_ack 有效时短暂就绪 (允许旁路交付)
    //   其他情况 → 拉低, 停顿 CPU IF 级
    //
    // 设计意图: 旁路路径允许 CPU 在第一个填充字到达时立即获得数据,
    //           无需等待全部 8 个字填充完成。这是典型的"尽早重启"
    //           (early restart) 策略的变体。
    // ===========================================================================
    assign cpu_rdata = hit ? data[index][word] : mem_rdata;
    assign cpu_ready = hit || (state == S_REFILL && mem_ack);

    // ===========================================================================
    //  仿真初始化块
    // ===========================================================================
    // 将所有 valid[] 清零，确保上电/仿真起始时缓存为空。
    // 注意: FPGA 综合工具通常忽略此 initial 块中对 reg 阵列的赋值，
    //       但 always 块中的 reset 分支会处理硬件复位。
    //       这里保留 initial 块主要是为了仿真确定性 (避免 X 态)。
    // ===========================================================================
    integer i;
    initial begin
        for (i = 0; i < NUM_LINES; i = i + 1) valid[i] = 1'b0;
    end

    // ===========================================================================
    //  主时序逻辑 — 异步复位 + 状态机
    // ===========================================================================
    // 使用 always @(posedge clk or posedge reset) 实现异步复位:
    //   - 复位时立即进入 S_IDLE，清除所有控制寄存器
    //   - valid[] 全清零 (硬件确保复位后缓存为空)
    //   - 正常工作时根据状态机转移
    //
    // 时序说明 (详见第三节):
    //   S_IDLE:
    //     - 清零 mem_req (确保不产生意外的总线事务)
    //     - 检测 cpu_req && !hit → 发起行填充
    //     - 锁存 miss_index, miss_tag, refill_base, mem_addr
    //     - 置 mem_req = 1 启动存储器读
    //
    //   S_REFILL:
    //     - 等待 mem_ack 握手
    //     - 每收到一个 mem_ack: 写入一个字到 data[miss_index][refill_cnt]
    //     - refill_cnt 自增, 计算下一字地址
    //     - refill_cnt == 7 时完成: 写入 tag, 置 valid, 返回 S_IDLE
    //
    // 关键: mem_addr 的更新在 mem_ack 周期中进行, 提前为下一个字准备好地址。
    //       因为 mem_addr 是 reg 类型, 在当前周期末被赋值, 下一个时钟沿生效。
    //       mem_req 保持为 1 直到整行填充完成 (连续请求模式)。
    // ===========================================================================
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // --- 异步复位分支 ---
            // 所有控制寄存器恢复到初始值，valid[] 全清零
            // 注意: data[][] 和 tags[] 不复位 (减少复位扇出, 且无功能影响)
            state      <= S_IDLE;
            refill_cnt <= 3'd0;
            mem_req    <= 1'b0;
            mem_addr   <= 32'h0;
            miss_index <= 0;
            // 硬件复位: 遍历清除所有有效位, 确保上电后缓存无残留数据
            for (i = 0; i < NUM_LINES; i = i + 1) valid[i] <= 1'b0;
        end
        else begin
            case (state)

                // ===============================================================
                //  S_IDLE — 空闲态: 等待 CPU 请求, 检测命中/缺失
                // ===============================================================
                S_IDLE: begin
                    // 清除 mem_req: 确保非请求周期不对总线产生干扰
                    mem_req <= 1'b0;

                    if (cpu_req && !hit) begin
                        // ---------- 缺失处理 ----------
                        // ★ 关键: 在此刻锁存所有缺失相关信息
                        //   因为下一个周期 cpu_addr 可能变化
                        //   (尽管 IF 级会被 stall, 锁存仍增加鲁棒性)
                        state       <= S_REFILL;        // 进入行填充态
                        refill_cnt  <= 3'd0;             // 填充计数器归零
                        miss_index  <= index;            // 锁存行索引
                        miss_tag    <= tag_in;           // ★ 锁存当前 TAG
                        miss_word   <= word;             // ★【Bug 修复】锁存请求的 word 偏移
                        // refill_base — 行对齐基地址:
                        //   清零低 5 bit (WORD + BYTE 字段), 保留高位 (TAG + INDEX)
                        //   例如 cpu_addr=0x00000134 → refill_base=0x00000120
                        refill_base <= {cpu_addr[31:WORD_BITS+2], {(WORD_BITS+2){1'b0}}};
                        // mem_addr — 存储器读起始地址 (★ 从请求的字开始, 而非 word 0)
                        //   【Bug 修复】原代码总是从行基地址 (word 0) 开始读取，
                        //   导致 bypass 路径在第一个 mem_ack 时返回 word 0，
                        //   而非 CPU 请求的字。修复后从请求字地址开始。
                        mem_addr    <= {cpu_addr[31:2], 2'b00};
                        mem_req     <= 1'b1;             // 发起存储器读请求
                    end
                    // 注: 命中时无需任何操作 — 组合逻辑 cpu_rdata/cpu_ready 已处理
                end

                // ===============================================================
                //  S_REFILL — 行填充态: 从存储器逐字读取并写入缓存行
                // ===============================================================
                S_REFILL: begin
                    if (mem_ack) begin
                        // ---------- 存储器握手应答 ----------
                        // ★【Bug 修复】使用 wrapping 计算写入位置：
                        //   data_pos = (miss_word + refill_cnt) & 3'd7
                        //   这样第一个 refill 的字写入 miss_word 位置（CPU 请求的字），
                        //   后续字依次写入后续位置，到达 word 7 后 wrap 到 word 0。
                        //   例如 miss_word=3:
                        //     cnt 0 → pos 3, cnt 1 → pos 4, ..., cnt 4 → pos 7,
                        //     cnt 5 → pos 0 (wrap), cnt 6 → pos 1, cnt 7 → pos 2
                        data[miss_index][(miss_word + refill_cnt) & 3'd7] <= mem_rdata;

                        // refill_cnt 自增
                        refill_cnt <= refill_cnt + 3'd1;

                        if (refill_cnt == 3'd7) begin
                            // -------- 最后一个字 --------
                            // 整行填充完成 (8 个字全部写入)
                            state   <= S_IDLE;
                            mem_req <= 1'b0;
                            tags[miss_index]  <= miss_tag;    // ★ 使用锁存的 TAG
                            valid[miss_index] <= 1'b1;        // 标记行有效
                        end
                        else begin
                            // -------- 非最后一个字 --------
                            // 计算下一个字的存储器地址 (带 wrapping):
                            //   next_pos = (miss_word + refill_cnt + 1) & 3'd7
                            //   mem_addr = refill_base + next_pos * 4
                            // 当 next_pos wrap 到 0 时, 地址回到行基地址,
                            // 继续填充剩余的 words 0..(miss_word-1)
                            mem_addr <= refill_base +
                                        {29'd0, (miss_word + refill_cnt + 3'd1) & 3'd7, 2'b00};
                            mem_req  <= 1'b1;                // 保持读请求有效
                        end
                    end
                    // 注: 若 mem_ack = 0, 则所有寄存器保持当前值,
                    //     mem_req 继续保持 1, mem_addr 保持不变。
                    //     存储器侧通过延时 mem_ack 来控制数据返回的节奏。
                end

            endcase
        end
    end

endmodule
