// ╔══════════════════════════════════════════════════════════════════╗
// ║  bram.v — 统一块内存 (64KB)                                       ║
// ║                                                                  ║
// ║  【模块用途】                                                       ║
// ║  本模块实现处理器核心（Sapphire SoC）的统一指令/数据紧耦合内存。          ║
// ║  它既是指令存储器也是数据存储器，提供组合读（零等待周期）、                  ║
// ║  同步写（单周期吞吐）、字节/半字/字粒度写入，以及仿真阶段的                 ║
// ║  firmware 自动加载。典型容量 64KB（可参数化调整）。                      ║
// ║                                                                  ║
// ║  【存储器组织与寻址】                                                 ║
// ║  - 深度 SIZE = 16384 条目，每条目 32 位宽 → 总计 64KB。               ║
// ║  - ADDR_BITS = 14 (ceil(log2(16384)))，即 2^14 = 16384。           ║
// ║  - 外部接口地址 addr[31:0] 是字节地址。内部通过 word_addr =              ║
// ║    addr[ADDR_BITS+1:2] 将字节地址转换为字地址（32-bit word）。          ║
// ║    推导：addr[15:2] → 14 bits，丢弃低 2 位（4 字节对齐）。               ║
// ║    这意味着 addr 必须字对齐访问（低 2 位 = 00），否则会丢失低位信息。        ║
// ║  - 存储器声明为 reg [31:0] mem [0:SIZE-1]，用 word_addr 索引。        ║
// ║                                                                  ║
// ║  【读写时序与协议】                                                   ║
// ║  - 组合读：rdata 由组合逻辑 assign rdata = mem[word_addr] 产生。        ║
// ║    这意味着在同一个时钟周期内，地址变化后 rdata 立即更新（零延迟）。          ║
// ║    FPGA/ASIC 综合时此模式会推断为 block RAM 的读端口不经输出寄存器。        ║
// ║  - 同步写：在 clk 上升沿写入。we=1 时根据 be 执行全字或部分字节写入。       ║
// ║  - 异步复位：posedge reset 触发 always 块。复位不清零内存内容，             ║
// ║    目的是保留已加载的 firmware 不被破坏（处理器复位后从 0x00000000 取指）。  ║
// ║  - 写-读冲突（同一地址同一周期）：由于读是组合的而写是同步的，                 ║
// ║    当前周期读到的仍是旧值（写前读行为），下一周期读到新值。                   ║
// ║                                                                  ║
// ║  【设计选择说明】                                                     ║
// ║  1. 组合读 vs 寄存器读：                                              ║
// ║     组合读使处理器能在取指/加载指令的同一周期获得数据，无需插入                ║
// ║     等待周期。这对于单周期或低 CPI 的简单 RISC-V 核至关重要。               ║
// ║     代价是路径延迟受存储器读访问时间限制，频率上限比寄存器读低。               ║
// ║  2. 字节使能 (be) 设计：                                              ║
// ║     RISC-V 支持 SB（存字节）、SH（存半字）、SW（存字）指令。                ║
// ║     be[0]→byte0(addr+0), be[1]→byte1(addr+1),                     ║
// ║     be[2]→byte2(addr+2), be[3]→byte3(addr+3)。                     ║
// ║     当 be == 4'b1111 时做全字写（SW），否则按位逐字节写入（SB/SH）。          ║
// ║     这种"读-改-写"风格确保未被使能的字节保持原值。                         ║
// ║  3. 复位不清零内存：                                                   ║
// ║     Block RAM 在 FPGA 上通常不支持异步复位（复位清内存会综合为               ║
// ║     大量 LUT 触发器而非 block RAM）。即使支持也不清内存，因为                ║
// ║     firmware 已通过 $readmemh 加载到 mem 中，复位后 core 必须              ║
// ║     能从 PC=0x00000000 正常取指运行。                                  ║
// ║  4. 仿真初始化策略：                                                   ║
// ║     先用 NOP 指令 (0x00000013 = addi x0,x0,0) 填充整个内存，             ║
// ║     再用 $readmemh 从 INIT_FILE 覆盖实际程序区域。                        ║
// ║     这样未使用区域执行的是 NOP 而不是随机垃圾，避免仿真时出现 X 态传播。       ║
// ╚══════════════════════════════════════════════════════════════════╝

module bram #(
    parameter SIZE      = 16384,           // 存储器深度：16K 条目 × 32bit = 64KB
    parameter ADDR_BITS = 14,              // 地址位宽 = ceil(log2(SIZE)) = 14
    parameter INIT_FILE = "firmware.hex"   // 仿真初始化文件（Intel HEX 或 Verilog hex 格式）
) (
    input  wire        clk,                // 时钟：写操作在上升沿触发
    input  wire        reset,              // ★ 异步复位：posedge 触发，不清零内存（保留 firmware）
    input  wire [31:0] addr,               // 字节地址输入（32-bit，低 2 位被忽略，要求字对齐）
    input  wire [31:0] wdata,              // 写数据输入（32-bit）
    input  wire        we,                 // 写使能：1=写，0=读（与 be 配合确定写入粒度）
    input  wire [3:0]  be,                 // ★ 字节使能：bit[0]=byte0, bit[1]=byte1, bit[2]=byte2, bit[3]=byte3
                                           //   1=写入该字节，0=保持该字节原值（用于 SB/SH 指令）
    output wire [31:0] rdata               // 读数据输出（组合逻辑，零延迟）
);

    // ── 存储器阵列 ──
    // 声明 16K×32bit 的寄存器阵列。FPGA 综合时映射为 block RAM，ASIC 映射为 SRAM 宏。
    reg [31:0] mem [0:SIZE-1];

    // ── 地址转换：字节地址 → 字地址 ──
    // addr 是字节地址（如 0x00000004 表示第 4 个字节即第 2 个 32-bit 字）。
    // word_addr 截取 addr[ADDR_BITS+1:2] = addr[15:2]（14 bits），
    // 等价于 addr[31:0] >> 2 的低 ADDR_BITS 位。
    // 注意：低 2 位 addr[1:0] 被丢弃，要求访问必须是字对齐的。
    wire [ADDR_BITS-1:0] word_addr = addr[ADDR_BITS+1:2];

    // ═══════════════════════════════════════════════════════════════
    // 组合读路径（零延迟）
    // ═══════════════════════════════════════════════════════════════
    // rdata 是 mem[word_addr] 的组合输出：addr 变化后，rdata 在同一周期内
    // 即更新。这是"零等待状态"存储器设计的关键——取指和加载指令无需插入
    // 等待周期即可获得数据。
    // 代价：组合路径延迟 = 地址解码延迟 + 存储器读访问时间，
    //       限制了最高时钟频率。若频率受限可改为寄存器读（增加 1 周期延迟）。
    assign rdata = mem[word_addr];

    // ═══════════════════════════════════════════════════════════════
    // 同步写路径（字节使能）
    // ═══════════════════════════════════════════════════════════════
    // 写操作在 clk 上升沿执行，与绝大多数同步数字设计保持一致。
    // 采用"非阻塞赋值 (<=)"以正确建模时序逻辑行为。
    //
    // 【复位行为】
    // reset=1 时进入 always 块，但不执行任何清零操作。
    // 设计理由：
    //   1. FPGA block RAM 通常不支持异步复位数据内容，若强加复位会综合为
    //      分布式 RAM（LUT 实现），浪费大量逻辑资源且频率更低。
    //   2. firmware 已在仿真阶段通过 $readmemh 预加载，复位不应破坏它。
    //   3. 处理器复位后 PC=0x00000000，直接从 firmware 起始地址取指运行。
    //
    // 【写使能与字节使能的配合】
    // we=1 时使能写入。具体写入粒度由 be 决定：
    //   - be=4'b1111 → 全字写（SW 指令）：32-bit 整体写入。
    //   - be=4'b0001 → 写 byte0（SB 到地址 0/4/8/...）。
    //   - be=4'b0011 → 写 byte0+byte1（SH 到地址 0/4/8/...）。
    //   - be=4'b1100 → 写 byte2+byte3（SH 到地址 2/6/A/...）。
    //   未被 be 使能的字节在 mem 中保持原值（读-改-写语义）。
    //
    // 【全字写优化】
    // be=4'b1111 时直接 mem[word_addr] <= wdata，避免拆成 4 条逐字节
    // 赋值。这在仿真和综合时都更高效（单次 32-bit 赋值 vs 4 次 8-bit 赋值）。
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // 复位时不清零内存（保留 firmware），仅确保写使能无效
        end
        else if (we) begin
            // ★ 字节使能写入（读-改-写模式）
            if (be == 4'b1111) begin
                mem[word_addr] <= wdata;                   // SW: 全字写（32-bit 整体写入）
            end
            else begin
                // SB/SH: 部分字节写 —— 仅更新 be 选中的字节通道
                if (be[0]) mem[word_addr][7:0]   <= wdata[7:0];
                if (be[1]) mem[word_addr][15:8]  <= wdata[15:8];
                if (be[2]) mem[word_addr][23:16] <= wdata[23:16];
                if (be[3]) mem[word_addr][31:24] <= wdata[31:24];
            end
        end
    end

    // ═══════════════════════════════════════════════════════════════
    // 仿真初始化
    // ═══════════════════════════════════════════════════════════════
    // initial 块仅在仿真时执行，综合工具忽略。
    // 步骤 1：用 NOP 指令 (0x00000013 = addi x0, x0, 0) 填充全部 16K 条目。
    //         这确保未初始化区域（包括 firmware 未覆盖的地址）在仿真中行为
    //         确定，避免出现 'bx/'bz（未知态/高阻态）传播导致仿真失败。
    // 步骤 2：$readmemh 从 INIT_FILE 读取 hex 文件内容，覆盖对应的 mem 条目。
    //         hex 文件中每行一个 32-bit 十六进制数（无 0x 前缀），
    //         按地址从小到大依次写入 mem[0], mem[1], ...。
    //         编译器/链接器生成的 firmware.hex 应从地址 0x00000000 开始，
    //         包含中断向量表（mtvec）、复位向量和用户程序代码。
    integer i;
    initial begin
        for (i = 0; i < SIZE; i = i + 1)
            mem[i] = 32'h00000013;           // NOP 填充：确保未编程区域安全执行（无副作用）
        $readmemh(INIT_FILE, mem);           // 从 hex 文件加载实际程序/数据
    end

endmodule
