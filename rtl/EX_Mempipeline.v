// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  EX_Mempipeline.v — EX/MEM 流水线寄存器                                     ║
// ║  功能: 锁存 EX 阶段数据+控制信号，输出到 MEM 阶段                              ║
// ║  支持 reset(清零) / flush(控制清零) / stall(保持) 三态                        ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ═══════════════════════════════════════════════════════════════════════════════
// 【模块定位与流水线角色】
// ═══════════════════════════════════════════════════════════════════════════════
//
// 本模块是五级流水线处理器中位于 EX（执行）与 MEM（访存）阶段之间的流水线寄存器。
// 在经典 RISC-V 五级流水线中，数据通路顺序为：
//   IF → ID → EX → MEM → WB
// 本模块恰好处于 EX 与 MEM 之间，承担"承上启下"的角色：
//
//   EX 阶段产出:
//     - ALU 计算结果 (ex_alu_result)
//     - 存储器写入数据 (ex_mem_write_data)
//     - 目标寄存器地址 (ex_rd)
//     - 访存控制信号 (ex_mem_write / ex_mem_read)
//     - 写回控制信号 (ex_reg_write / ex_wbmux)
//     - PC+4 地址 (ex_pc_plus_4) — 供 JAL/JALR 链接地址写回
//
//   本模块将这些信号（数据 + 控制）同步寄存一拍后送入 MEM 阶段，
//   确保 MEM 阶段看到的信号在整周期内保持稳定。
//
// ═══════════════════════════════════════════════════════════════════════════════
// 【设计决策：为什么要在 EX/MEM 边界插入寄存器】
// ═══════════════════════════════════════════════════════════════════════════════
//
// 1. 时序隔离 (Timing Isolation):
//    EX 阶段的 ALU 计算属于组合逻辑路径（可能包含加法器、移位器、乘法器等），
//    其延迟通常是整条流水线中最长的关键路径。如果在 EX 输出端不插入寄存器，
//    ALU 的组合延迟将直接叠加到 MEM 阶段的地址计算/数据对齐逻辑上，
//    导致单周期时钟频率严重受限。
//    插入本寄存器后，关键路径被切割为：
//       ID寄存器 → ALU → EX/MEM寄存器 （EX阶段路径）
//       EX/MEM寄存器 → 数据存储器 → MEM/WB寄存器 （MEM阶段路径）
//    两条路径分别独立约束，时钟周期只受最长单段限制，显著提升主频。
//
// 2. 控制信号对齐:
//    控制信号（mem_write, mem_read, reg_write, wbmux）在 ID 阶段译码产生，
//    经过 ID/EX 寄存器后在 EX 阶段伴随数据通路流动。
//    若控制信号与数据不同步（例如控制信号提前到达 MEM 阶段而数据尚未就绪），
//    将导致存储器误写入或误读取。本模块统一寄存数据与控制，保证二者严格对齐。
//
// 3. 异常与冲刷支持:
//    流水线在遇到分支预测失败、异常、中断等情况时需要"冲刷"（flush）错误指令。
//    flush 信号可以有效清零本模块输出的控制信号，将正在流向 MEM 阶段的
//    错误指令变为 NOP（气泡），而无需等待该指令走完整个流水线。
//
// ═══════════════════════════════════════════════════════════════════════════════
// 【三态控制逻辑：reset > flush > stall > 正常 — 优先级设计原理】
// ═══════════════════════════════════════════════════════════════════════════════
//
// 优先级链条:  reset > flush > stall > 正常锁存
//
// ┌─────────┬────────────────────────────────────────────────────────────────┐
// │ 条件     │ 行为                                                           │
// ├─────────┼────────────────────────────────────────────────────────────────┤
// │ reset   │ 全部寄存器异步清零。复位具有最高优先级：无论 flush/stall 状态     │
// │         │ 如何，系统复位必须立刻将所有状态归零，使处理器进入确定初始态。     │
// ├─────────┼────────────────────────────────────────────────────────────────┤
// │ flush   │ 仅清零控制信号（mem_write/mem_read/reg_write/wbmux/pc_plus_4）。 │
// │         │ 数据通路信号（alu_result/mem_write_data/rd）保持不变。            │
// │         │ 设计意图：数据通路不关心（下一阶段不会使用无效数据），              │
// │         │ 保持数据不变可避免不必要的翻转功耗；                              │
// │         │ 控制信号必须清零以确保被冲刷的指令不会触发副作用                  │
// │         │ （如误写存储器、误写寄存器文件）。                                │
// ├─────────┼────────────────────────────────────────────────────────────────┤
// │ stall   │ 所有寄存器保持当前值不变。当流水线因数据冒险、结构冒险等原因       │
// │         │ 需要暂停时，MEM 阶段可能正在执行多周期操作（如访存），              │
// │         │ EX 阶段需要等待。此时 EX/MEM 寄存器暂停更新，                    │
// │         │ 保持上一次有效数据直到 stall 解除。                               │
// ├─────────┼────────────────────────────────────────────────────────────────┤
// │ 正常     │ 所有寄存器正常锁存 EX 阶段的输入信号。                            │
// └─────────┴────────────────────────────────────────────────────────────────┘
//
// 优先级逻辑实现说明：
//   - Verilog 的 if-else if-else 链天然实现了优先级编码。
//   - reset 用异步 posedge reset 触发（本条 always 的敏感列表同时包含
//     posedge clk 和 posedge reset），保证复位在任何时钟沿之前响应。
//   - flush 次高优先级：冲刷请求意味着当前 EX 指令无效，必须阻止其控制效果。
//   - stall 再次：暂停时数据不能丢失，必须保持，故放在正常锁存之前判断。
//   - 正常锁存：只有三个条件均不满足时才发生。
//
// ═══════════════════════════════════════════════════════════════════════════════
// ★ 传递信号速查
// ═══════════════════════════════════════════════════════════════════════════════
//   数据通路: alu_result, mem_write_data, rd, pc_plus_4
//   控制信号: mem_write, mem_read, reg_write
//   写回选择: wbmux[1:0]  — ★ 新增，供 WB 阶段选择数据来源
//
// ═══════════════════════════════════════════════════════════════════════════════
// 端口详细说明
// ═══════════════════════════════════════════════════════════════════════════════

module ex_mem_stage (
    // ─────────────────────────────────────────────────────────────────────────
    // 系统信号
    // ─────────────────────────────────────────────────────────────────────────

    // clk: 系统时钟 (input, 1-bit)
    //   全局同步时钟。所有状态寄存器的更新（正常锁存、flush 清零、stall 保持）
    //   均在 clk 的上升沿触发。
    input  wire        clk,

    // reset: 异步复位 (input, 1-bit)
    //   高电平有效。复位时所有输出寄存器清零。
    //   作为 always 块的异步敏感信号（posedge reset），不依赖 clk 即可触发。
    //   优先级最高：无论 clk 状态如何，复位立即生效。
    input  wire        reset,

    // stall: 流水线暂停信号 (input, 1-bit)
    //   高电平有效。stall=1 时，EX/MEM 寄存器保持当前值不变（不更新）。
    //   典型使用场景:
    //     - MEM 阶段正在执行多周期访存操作（如缓存未命中），EX 阶段需等待。
    //     - 数据冒险：EX 阶段需要 MEM 或 WB 阶段的结果（前递 + 暂停）。
    //     - 结构冒险：共享资源（如单端口存储器）被占用。
    //   注意: stall 的优先级低于 reset 和 flush。
    input  wire        stall,

    // flush: 流水线冲刷信号 (input, 1-bit)
    //   高电平有效。冲刷（flush）时仅清零控制信号，数据通路信号保持原值。
    //   典型触发场景:
    //     - 分支预测失败：IF/ID 和 ID/EX 中错误路径的指令被冲刷。
    //     - 异常/中断：当前及后续指令被冲刷，流水线转向异常处理入口。
    //     - 流水线陷阱（如 ecall/ebreak）：后续指令需被取消。
    //   设计细节见上文"三态控制逻辑"。
    input  wire        flush,

    // ─────────────────────────────────────────────────────────────────────────
    // EX 阶段输入 — 数据通路
    // ─────────────────────────────────────────────────────────────────────────

    // ex_alu_result: EX 阶段 ALU 运算结果 (input, 32-bit)
    //   由 EX 阶段的 ALU 产生，携带如下可能的值:
    //     - 算术/逻辑指令的计算结果 (add/sub/and/or/sll/srl/slt 等)
    //     - 访存指令的地址计算结果 (lw/sw 等 — 基址+偏移)
    //     - 分支/跳转的目标地址或比较结果
    //   在 MEM 阶段，该信号将被用作:
    //     - 数据存储器的地址总线 (mem_addr)
    //     - 或直接传递到 WB 阶段作为写回数据 (当 wbmux=00 时)
    input  wire [31:0] ex_alu_result,

    // ex_mem_write_data: EX 阶段存储器写入数据 (input, 32-bit)
    //   来自寄存器文件的 rs2 读出值（经过 ID/EX 寄存器）。
    //   在 store 指令 (sw/sh/sb) 中，此数据将被写入数据存储器。
    //   在 MEM 阶段可能经过字节/半字对齐和符号扩展处理后再写入存储器。
    input  wire [31:0] ex_mem_write_data,

    // ex_rd: EX 阶段目标寄存器地址 (input, 5-bit)
    //   指向指令的目标寄存器（rd 字段）。
    //   RISC-V 有 32 个通用寄存器 (x0-x31)，故地址宽度为 5-bit。
    //   注意: x0 寄存器硬连线为 0，即使 rd=x0 也不产生副作用，
    //   但该信号仍需传递以支持 reg_write 控制信号的一致性判断。
    input  wire [4:0]  ex_rd,

    // ─────────────────────────────────────────────────────────────────────────
    // EX 阶段输入 — 控制信号
    // ─────────────────────────────────────────────────────────────────────────

    // ex_mem_write: EX 阶段存储器写使能 (input, 1-bit)
    //   高电平指示当前指令是 store 类型 (sw/sh/sb)。
    //   MEM 阶段将据此信号使能数据存储器的写操作。
    //   flush 时被清零，防止错误指令误写入存储器。
    input  wire        ex_mem_write,

    // ex_mem_read: EX 阶段存储器读使能 (input, 1-bit)
    //   高电平指示当前指令是 load 类型 (lw/lh/lb/lhu/lbu)。
    //   MEM 阶段将据此信号使能数据存储器的读操作。
    //   读出的数据将在 WB 阶段写回寄存器文件。
    //   flush 时被清零，防止错误指令触发不必要的存储器读。
    input  wire        ex_mem_read,

    // ex_reg_write: EX 阶段寄存器写使能 (input, 1-bit)
    //   高电平指示当前指令需要写回目标寄存器。
    //   适用于: R-type (算术逻辑), I-type (立即数算术/load), JAL/JALR, LUI/AUIPC 等。
    //   不适用于: S-type (store), B-type (分支), 以及 rd=x0 的情况
    //            （虽然后者在译码阶段已通过将 reg_write 置 0 来优化）。
    //   flush 时被清零: 被冲刷的指令不得修改寄存器文件状态。
    input  wire        ex_reg_write,

    // ─────────────────────────────────────────────────────────────────────────
    // EX 阶段输入 — 写回选择 + PC（★ 新增信号）
    // ─────────────────────────────────────────────────────────────────────────

    // ex_wbmux: EX 阶段写回数据选择控制 (input, 2-bit)
    //   定义 WB 阶段写回寄存器文件的数据来源:
    //     2'b00 → ALU 结果 (ex_alu_result)      — R-type / I-type 算术逻辑
    //     2'b01 → 存储器读出数据 (mem_read_data) — load 指令
    //     2'b10 → PC+4 (ex_pc_plus_4)           — JAL / JALR 链接地址
    //     2'b11 → 保留 (未使用)
    //   该信号在 ID 阶段由译码逻辑产生，经 ID/EX 寄存器传入 EX 阶段，
    //   再由本模块寄存后送入 MEM 阶段并最终在 WB 阶段使用。
    //   flush 时被清零 (2'b00)，默认选择 ALU 结果，配合 reg_write=0 无副作用。
    input  wire [1:0]  ex_wbmux,           // 写回数据选择 (00=ALU 01=MEM 10=PC+4)

    // ex_pc_plus_4: EX 阶段 PC+4 值 (input, 32-bit)
    //   当前指令的 PC+4，即下一条指令的地址。
    //   用于 JAL (Jump And Link) 和 JALR (Jump And Link Register) 指令:
    //     这两条指令将 PC+4 (返回地址) 写入 rd 寄存器。
    //   该值从 IF 阶段开始伴随指令流动，经 ID/EX → 本模块 → MEM → WB，
    //   最终在 WB 阶段通过 wbmux=2'b10 选择写回寄存器文件。
    //   flush 时被清零: 被冲刷的指令不应保留无效的 PC+4。
    input  wire [31:0] ex_pc_plus_4,       // PC+4 (JAL/JALR 写回目标)

    // ─────────────────────────────────────────────────────────────────────────
    // MEM 阶段输出 — 所有输出均为寄存器类型 (reg)
    // ─────────────────────────────────────────────────────────────────────────
    //
    // 以下输出信号均为时序逻辑驱动（always @(posedge clk or posedge reset)），
    // 即每个输出端口都由一个 D 触发器驱动。这样做的好处:
    //   1. 输出信号在整周期内稳定，不受输入毛刺影响。
    //   2. 下游 MEM 阶段的组合逻辑路径更短（从寄存器输出开始，而非从 EX 阶段
    //      组合逻辑输出开始）。
    //   3. 综合工具可以对输出寄存器进行复制/重定时优化，进一步改善时序。

    // ex_mem_alu_result: MEM 阶段 ALU 结果 (output, 32-bit)
    //   EX 阶段 ALU 运算结果的寄存版本。在 MEM 阶段将用作:
    //     - 数据存储器的地址 (配合 mem_read / mem_write)
    //     - 传递到 WB 阶段的通用数据 (当 wbmux=2'b00 时)
    output reg  [31:0] ex_mem_alu_result,

    // ex_mem_mem_write_data: MEM 阶段存储器写入数据 (output, 32-bit)
    //   经过寄存的 store 数据。在 MEM 阶段连接到数据存储器的写数据端口。
    //   对于 store 指令，此数据将被写入存储器。
    output reg  [31:0] ex_mem_mem_write_data,

    // ex_mem_rd: MEM 阶段目标寄存器地址 (output, 5-bit)
    //   经过寄存的 rd 字段。在 MEM 阶段之后传递到 MEM/WB 寄存器，
    //   最终在 WB 阶段用作寄存器文件的写地址。
    //   虽然 MEM 阶段本身不使用 rd，但需要将其传递给下一阶段。
    output reg  [4:0]  ex_mem_rd,

    // ex_mem_mem_write: MEM 阶段存储器写使能 (output, 1-bit)
    //   寄存后的 mem_write 控制信号。连接到数据存储器的写使能。
    //   flush 时清零 → 阻止错误指令的 store 操作。
    output reg         ex_mem_mem_write,

    // ex_mem_mem_read: MEM 阶段存储器读使能 (output, 1-bit)
    //   寄存后的 mem_read 控制信号。连接到数据存储器的读使能。
    //   flush 时清零 → 阻止错误指令触发不必要的读操作。
    output reg         ex_mem_mem_read,

    // ex_mem_reg_write: MEM 阶段寄存器写使能 (output, 1-bit)
    //   寄存后的 reg_write 控制信号。传递到 MEM/WB 寄存器，
    //   最终在 WB 阶段控制寄存器文件的写使能。
    //   flush 时清零 → 阻止错误指令修改寄存器文件。
    output reg         ex_mem_reg_write,

    // ex_mem_wbmux: MEM 阶段写回数据选择 (output, 2-bit)
    //   寄存后的 wbmux 信号。传递到 MEM/WB 寄存器，
    //   在 WB 阶段选择写回数据来源 (ALU/MEM/PC+4)。
    //   flush 时清零 (2'b00)。
    output reg  [1:0]  ex_mem_wbmux,

    // ex_mem_pc_plus_4: MEM 阶段 PC+4 值 (output, 32-bit)
    //   寄存后的 PC+4 值。传递到 MEM/WB 寄存器，
    //   在 WB 阶段用于 JAL/JALR 指令的链接地址写回。
    //   flush 时清零。
    output reg  [31:0] ex_mem_pc_plus_4
);

    // ═════════════════════════════════════════════════════════════════════════
    // 核心时序逻辑: always @(posedge clk or posedge reset)
    // ═════════════════════════════════════════════════════════════════════════
    //
    // 本 always 块是整个模块唯一的行为描述，实现所有寄存器输出的更新逻辑。
    //
    // 敏感列表: posedge clk or posedge reset
    //   - posedge clk: 正常同步操作（锁存、flush、stall）的触发条件。
    //   - posedge reset: 异步复位触发条件。这意味着:
    //       * 复位不需要等待时钟沿，reset 上升沿即刻触发。
    //       * 综合工具会推断出带异步复位/同步释放的 D 触发器。
    //       * FPGA 上的大多数逻辑单元原生支持这种结构。
    //
    // 所有赋值使用非阻塞赋值 (<=):
    //   - 这是时序逻辑的标准写法，确保仿真行为与综合后的硬件行为一致
    //     （所有 <= 赋值在 always 块结束时同时更新，避免赋值顺序依赖）。
    //   - 对于流水线寄存器而言，这模拟了多级 D 触发器组的同时采样行为。
    //
    // ═════════════════════════════════════════════════════════════════════════
    // 优先级: reset > flush > stall > 正常
    // ═════════════════════════════════════════════════════════════════════════

    always @(posedge clk or posedge reset) begin
        // ─────────────────────────────────────────────────────────────────────
        // 情况 1: reset = 1 — 异步复位 (最高优先级)
        // ─────────────────────────────────────────────────────────────────────
        // 所有输出寄存器清零。
        // 为什么全部清零而非部分:
        //   - 复位后处理器从确定的零状态启动，所有流水级均为 NOP。
        //   - 控制信号清零 → MEM 阶段不会产生任何访存操作。
        //   - 数据清零 → 避免不确定值 (X) 在下游传播。
        //   - rd=5'b0 (x0) → 即使 reg_write 被意外使能也不会污染寄存器文件。
        // 综合注意: 并非所有 FPGA 都支持异步复位，某些架构可能需要同步复位。
        //   若目标器件不支持异步复位，综合工具会自动转换为同步复位。
        if (reset) begin
            ex_mem_alu_result     <= 32'b0;
            ex_mem_mem_write_data <= 32'b0;
            ex_mem_rd             <= 5'b0;
            ex_mem_mem_write      <= 1'b0;
            ex_mem_mem_read       <= 1'b0;
            ex_mem_reg_write      <= 1'b0;
            ex_mem_wbmux          <= 2'b0;
            ex_mem_pc_plus_4      <= 32'b0;
        end
        // ─────────────────────────────────────────────────────────────────────
        // 情况 2: flush = 1 (且 reset = 0) — 流水线冲刷 (次高优先级)
        // ─────────────────────────────────────────────────────────────────────
        // 仅清零控制信号，数据通路信号保持原值不变。
        //
        // 设计原理:
        //   控制信号决定了指令在后续阶段是否会"做事情":
        //     - reg_write=0 → WB 阶段不会写寄存器文件
        //     - mem_write=0 → MEM 阶段不会写数据存储器
        //     - mem_read=0  → MEM 阶段不会读数据存储器
        //     - wbmux=00    → 即使写回数据选择 ALU 也无害，因 reg_write=0
        //   因此被冲刷的指令等效于 NOP，在 MEM 和 WB 阶段不产生任何架构可见的副作用。
        //
        //   数据通路保持原值（不归零）的原因:
        //     1. 下级逻辑（MEM 阶段）只看控制信号: mem_write/mem_read=0 时
        //        数据通路值不论如何都不会被使用。
        //     2. 减少信号翻转 (toggling): 如果在 flush 时将数据通路也归零，
        //        每个 flush 周期都会产生大量额外的信号跳变，增加动态功耗。
        //        保持原值意味着 flush 周期内数据通路信号的翻转率为 0。
        //     3. 时序优化: 数据通路寄存器在 flush 时不需要额外的复位逻辑，
        //        综合后的电路更简洁，路径延迟更小。
        //
        //   pc_plus_4 也在此清零: 该信号本质上是 JAL/JALR 指令的"写回数据值"，
        //     flush 后没有合法指令需要写回 PC+4，清零避免误用。
        else if (flush) begin
            // 插入气泡：仅清零控制信号（数据通路保持，不影响）
            ex_mem_mem_write      <= 1'b0;
            ex_mem_mem_read       <= 1'b0;
            ex_mem_reg_write      <= 1'b0;
            ex_mem_wbmux          <= 2'b0;
            ex_mem_pc_plus_4      <= 32'b0;
        end
        // ─────────────────────────────────────────────────────────────────────
        // 情况 3: stall = 0 (且 reset=0, flush=0) — 正常锁存
        // ─────────────────────────────────────────────────────────────────────
        // 所有输入信号采样到输出寄存器，实现流水线推进。
        //
        // 注意条件表达式是 `!stall` 而非 `stall`:
        //   - stall=0 → 流水线正常前进，寄存器更新。
        //   - stall=1 → 不进入本条分支，也不进入前面的 if/else if 分支，
        //               整个 always 块对输出寄存器无赋值操作，
        //               寄存器的值自然保持不变。
        //
        //   这种写法（if else if (!stall) begin ... end，省略最后的 else）
        //   利用了 Verilog 的寄存器保持语义:
        //     当 always 块没有对某个 reg 进行赋值时，该 reg 保持上一次的值。
        //     综合工具将其映射为带时钟使能 (clock enable) 的 D 触发器:
        //       DFF with CE: Q <= CE ? D : Q
        //     其中 CE = ~stall & ~flush & ~reset。
        //
        //   这种隐式保持的写法比显式写 `if (stall) begin ... end` 更简洁，
        //   且在综合时能得到相同的电路结构（带使能的寄存器）。
        else if (!stall) begin
            // 正常锁存：所有信号从 EX 输入采样到 MEM 输出
            ex_mem_alu_result     <= ex_alu_result;
            ex_mem_mem_write_data <= ex_mem_write_data;
            ex_mem_rd             <= ex_rd;
            ex_mem_mem_write      <= ex_mem_write;
            ex_mem_mem_read       <= ex_mem_read;
            ex_mem_reg_write      <= ex_reg_write;
            ex_mem_wbmux          <= ex_wbmux;
            ex_mem_pc_plus_4      <= ex_pc_plus_4;
        end
        // ─────────────────────────────────────────────────────────────────────
        // 隐式情况 4: stall = 1 (且 reset=0, flush=0) — 流水线暂停
        // ─────────────────────────────────────────────────────────────────────
        // 不执行任何赋值操作 → 所有输出寄存器保持当前值不变。
        //
        // 为什么 stall 要阻止寄存器更新:
        //   MEM 阶段可能正在执行多周期操作（如数据缓存未命中导致的总线等待），
        //   此时 EX 阶段的指令已经执行完毕，但不能送入 MEM 阶段
        //   （因为 MEM 阶段还在忙碌），否则会覆盖掉 MEM 阶段正在处理的数据。
        //
        //   stall=1 时，不仅本模块暂停，上游模块（IF/ID, ID/EX）也同步暂停，
        //   下游模块（MEM/WB）可能继续前进或也暂停，具体取决于冒险处理策略。
        //
        //   stall 与 flush 的区别:
        //     - stall: 暂时保持，等待条件解除后继续执行当前指令。
        //     - flush: 永久丢弃，当前指令被取消（变为 NOP），不会再恢复。
        //
        //   硬件实现: 综合工具将 !stall 条件映射为寄存器的时钟使能 (CE) 引脚。
        //     FPGA 的多数逻辑单元 (如 Xilinx Slice, Intel ALM) 原生支持 CE，
        //     因此 stall 功能的硬件开销极小（仅增加 CE 控制的布线，不增加额外 LUT）。
        // stall=1 → 保持当前输出不变
    end

endmodule
