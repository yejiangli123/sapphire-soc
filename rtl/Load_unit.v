// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                    Load_unit.v — Load 指令数据对齐 + 符号/零扩展               ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ==============================================================================
// 1. 模块在 CPU 流水线中的角色
// ==============================================================================
//
//   Load_unit 位于 MEM（访存）阶段与 WB（写回）阶段之间，是 Load 数据通路的
//   关键组件。在经典五级流水线中：
//
//     IF → ID → EX → MEM → WB
//                        ↑
//                   Load_unit 在此
//
//   当一条 Load 指令（LB/LH/LW/LBU/LHU）完成存储器访问后，从数据存储器读出的
//   总是完整的 32-bit 字（data_in）。但 RISC-V 的 Load 指令可能只需要其中的
//   一个字节（byte）或半字（halfword），并且需要根据指令类型决定对高位进行
//   符号扩展（sign-extension）还是零扩展（zero-extension）。
//
//   Load_unit 的职责：
//   (a) 根据地址低 2-bit（addr_low）从不对齐的 32-bit 字中挑选正确的字节/半字；
//   (b) 根据 funct3 决定对选出的数据进行符号扩展还是零扩展；
//   (c) 将结果扩展为完整的 32-bit 字，送往 WB 阶段写入寄存器堆；
//   (d) 生成 load_mux 控制信号，告诉 WB 阶段的 MUX 选择 Load 数据（而非 ALU
//       结果）作为写回数据源。
//
//   与之协作的模块：
//   - EX/MEM 流水线寄存器：提供 addr_low（访存地址低 2 位）
//   - 数据存储器（D-MEM）：提供 data_in（读出的 32-bit 原始数据）
//   - 译码阶段（ID）：提供 load_funct3_in（funct3 字段）和 load_in（Load 使能）
//   - WB 阶段的写回多路选择器：使用 load_mux 选择 load_output 或 ALU 结果
//
// ==============================================================================
// 2. RISC-V Load 指令 funct3 编码规则
// ==============================================================================
//
//   RISC-V RV32I 规范中 Load 指令的 funct3 编码如下：
//
//   ┌──────────┬───────┬──────┬─────────────────────────────────────┐
//   │  指令     │ funct3 │ 宽度  │  扩展方式                           │
//   ├──────────┼───────┼──────┼─────────────────────────────────────┤
//   │  LB      │  000   │ byte │ 符号扩展：byte[7] → [31:8]           │
//   │  LH      │  001   │ half │ 符号扩展：half[15] → [31:16]         │
//   │  LW      │  010   │ word │ 不扩展，直接 32-bit                   │
//   │  LBU     │  100   │ byte │ 零扩展：{24'b0, byte}                │
//   │  LHU     │  101   │ half │ 零扩展：{16'b0, half}                │
//   └──────────┴───────┴──────┴─────────────────────────────────────┘
//
//   设计要点 —— 为什么这套编码可以直接用于硬件判断：
//
//   ★ funct3[2]：
//       0 → 有符号扩展（LB=000, LH=001, LW=010）
//       1 → 零扩展（LBU=100, LHU=101）
//       这种编码不是巧合，而是 RISC-V 指令集的精心设计：bit[2] 恰好区别
//       "符号/零扩展"，可以直接用作 MUX 选择信号，无需额外的译码逻辑。
//
//   ★ funct3[1:0]：
//       00 → byte 操作（LB/LBU）
//       01 → half 操作（LH/LHU）
//       10 → word 操作（LW）
//       11 → 保留（RV32I 未定义 Load 类型），本模块与 LW 同等处理
//       同样可以直接驱动 case 语句，无需译码。
//
//   ★ 注意：LW（funct3=010）的 bit[2]=0，但符号扩展逻辑不会影响 LW，
//       因为 LW 走 default 分支直接使用 data_in，sign_byte/sign_half
//       对 LW 结果无影响。
//
// ==============================================================================
// 3. 为什么需要 addr_low（地址低 2 位）
// ==============================================================================
//
//   RISC-V 支持非对齐 Load（misaligned access），即使地址不是自然对齐的。
//   数据存储器总是返回以字对齐地址读出的完整 32-bit 字。例如：
//
//   - 若访存地址为 0x1003（addr_low = 2'b11），读 LB：
//     存储器返回地址 0x1000 开始的 32-bit 字：
//       data_in[31:24] ← 0x1003 的字节
//       data_in[23:16] ← 0x1002 的字节
//       data_in[15:8]  ← 0x1001 的字节
//       data_in[7:0]   ← 0x1000 的字节
//     LB 应取 0x1003 的字节，即 data_in[31:24]，这需要 addr_low=2'b11。
//
//   addr_low[1:0] 映射到 data_in 字节位置：
//     2'b00 → data_in[7:0]    （地址末两位 00，字内偏移 0）
//     2'b01 → data_in[15:8]   （地址末两位 01，字内偏移 1）
//     2'b10 → data_in[23:16]  （地址末两位 10，字内偏移 2）
//     2'b11 → data_in[31:24]  （地址末两位 11，字内偏移 3）
//
//   对于半字操作（LH/LHU），只需 addr_low[1]：
//     addr_low[1]=0 → 低半字 data_in[15:0]
//     addr_low[1]=1 → 高半字 data_in[31:16]
//
// ==============================================================================
// 4. 模块接口定义
// ==============================================================================

module load_unit (
    // ---- 控制输入（来自 ID 阶段，经流水线寄存器传递）----

    // funct3 字段，识别 Load 类型：
    //   LB(LB=000, LH=001, LW=010, LBU=100, LHU=101)
    //   直接来自指令编码 inst[14:12]
    input  wire [2:0]  load_funct3_in,

    // Load 使能信号：当前指令是否为 Load 类型
    //   来自主译码器（main decoder）的 MemRead 控制信号
    //   用于：(a) 门控 load_output（非 Load 指令时输出 0）
    //         (b) 传递为 load_mux，控制 WB 阶段写回数据源选择
    input  wire        load_in,

    // ---- 数据输入（来自 MEM 阶段）----

    // 从数据存储器读出的完整 32-bit 字
    //   该字是以字对齐地址（addr & ~0x3）从 D-MEM 读出的
    //   包含目标字节但位置由 addr_low 决定
    input  wire [31:0] data_in,

    // 访存地址的低 2 位（字节偏移）
    //   来自 ALU 计算出的访存地址的低 2-bit
    //   用于在 data_in 中选择正确的字节/半字位置
    //   ★ 关键信号：没有它就无法处理非对齐 Load
    input  wire [1:0]  addr_low,

    // ---- 数据输出（送往 WB 阶段）----

    // 对齐 + 扩展后的最终数据
    //   宽度恒为 32-bit：字节/半字已扩展至 32-bit
    //   当 load_in=0（非 Load 指令）时输出全 0，避免无效数据翻转功耗
    //   WB 阶段将此值作为写回数据源之一
    output wire [31:0] load_output,

    // Load 数据有效指示（透传 load_in）
    //   用于 WB 阶段的写回多路选择器控制：
    //     load_mux=1 → 写回数据选 load_output（Load 指令）
    //     load_mux=0 → 写回数据选 ALU 结果（非 Load 指令）
    //   ★ 命名中的 "mux" 指其在 WB 阶段驱动 MUX 选择端
    output wire        load_mux
);

// ==============================================================================
// 5. 内部信号定义与设计理由
// ==============================================================================

    // --------------------------------------------------------------------------
    // 5.1 字节选择 (byte_sel)
    // --------------------------------------------------------------------------
    //   根据 addr_low 从 32-bit 字中选出目标字节。
    //
    //   为什么用组合逻辑（assign）而非时序逻辑？
    //   → Load_unit 本身就是组合逻辑模块，位于 MEM 和 WB 之间的组合路径上。
    //     流水线寄存器在 Load_unit 之前和之后，这里不需要额外打拍。
    //
    //   为什么使用三目运算符链而非 case 语句？
    //   → (a) 对字节选择这种简单映射，三目运算符综合结果与 case 等价；
    //     (b) 三目运算符表达优先级明确，避免综合出意外的锁存器。
    //
    //   MUX 优先级：addr_low = 00 → 01 → 10 → 11（else）
    //   每个分支对应一个字节 lane，逻辑完备（所有 4 种情况均已覆盖）。
    //
    wire [7:0]  byte_sel;

    assign byte_sel = (addr_low == 2'b00) ? data_in[7:0]   :  // 字内偏移 0 的字节
                      (addr_low == 2'b01) ? data_in[15:8]  :  // 字内偏移 1 的字节
                      (addr_low == 2'b10) ? data_in[23:16] :  // 字内偏移 2 的字节
                                            data_in[31:24];    // 字内偏移 3 的字节

    // --------------------------------------------------------------------------
    // 5.2 半字选择 (half_sel)
    // --------------------------------------------------------------------------
    //   根据 addr_low[1] 从 32-bit 字中选出目标半字。
    //
    //   为什么只需 addr_low[1]？
    //   → 半字为 2 字节对齐，只有两种有效位置：
    //     · addr_low[1]=0 → 低半字 data_in[15:0]（地址 xx...x00 或 xx...x01）
    //     · addr_low[1]=1 → 高半字 data_in[31:16]（地址 xx...x10 或 xx...x11）
    //     注意：若地址末位为 1，访问的不是自然对齐半字（misaligned half），
    //     按 RISC-V 规范取包含该地址的半字。
    //
    //   为什么偶地址和奇地址都取同一半字？
    //   → 例如地址 0x1001（addr_low=2'b01, bit[1]=0），目标字节在 data_in[15:8]。
    //     LH 指令需要返回以该地址开始的 2 字节半字 data_in[23:8]（跨越两个字节 lane）。
    //     本模块将 low-half data_in[15:0] 作为半字返回——这实际包含地址 0x1000
    //     和 0x1001 两个字节，而 0x1001 确实在其中，但起始点不对！
    //
    //     ★ 重要说明：当前实现假定半字地址已对齐（addr_low[0]=0）。
    //       若需支持非对齐半字访问（如地址 0x1001 的 LH），需要更复杂的
    //       字节旋转逻辑或分两次访存。RISC-V 规范允许硬件不支持非对齐访问，
    //       转而触发异常由软件处理。本模块实现的是简化版本。
    //
    wire [15:0] half_sel;

    assign half_sel = addr_low[1] ? data_in[31:16] : data_in[15:0];

    // --------------------------------------------------------------------------
    // 5.3 符号/零扩展选择信号
    // --------------------------------------------------------------------------
    //   sign_byte: 字节扩展时填充高 24-bit 的位值
    //     - 有符号（funct3[2]=0）：取 byte_sel[7]（符号位），即 byte 的最高位
    //     - 零扩展（funct3[2]=1）：固定为 1'b0
    //
    //   sign_half: 半字扩展时填充高 16-bit 的位值
    //     - 有符号（funct3[2]=0）：取 half_sel[15]（符号位），即半字最高位
    //     - 零扩展（funct3[2]=1）：固定为 1'b0
    //
    //   为什么用三目运算符而非 if-else？
    //   → 这是纯组合逻辑，assign + 三目运算符比 always_comb + if-else 更
    //     简洁且综合结果相同（综合器均映射为 MUX）。
    //
    //   为什么 byte_sel[7] / half_sel[15] 可以作为符号位？
    //   → 在二进制补码表示中，最高有效位（MSB）就是符号位：
    //     · byte:  bit[7] =1 表示负数，=0 表示非负
    //     · half:  bit[15]=1 表示负数，=0 表示非负
    //     符号扩展即用该符号位填充所有高位，保持数值不变。
    //
    wire sign_byte = load_funct3_in[2] ? 1'b0 : byte_sel[7];
    wire sign_half = load_funct3_in[2] ? 1'b0 : half_sel[15];

    // ==========================================================================
    // 6. 数据拼装逻辑（组合逻辑 always 块）
    // ==========================================================================
    //
    //   使用组合 always 块实现 MUX：根据 funct3[1:0] 选择：
    //     - 00：字节操作（LB/LBU）→ {24个符号/零扩展位, 选出的字节}
    //     - 01：半字操作（LH/LHU）→ {16个符号/零扩展位, 选出的半字}
    //     - 10/11：字操作（LW 或未定义）→ 直接使用 32-bit data_in
    //
    //   为什么用 reg 类型 + always @(*)？
    //   → (a) always @(*) 描述组合逻辑，综合为纯组合电路（无锁存器）；
    //     (b) case 语句可读性优于 assign 下的嵌套三目运算符；
    //     (c) default 分支确保无锁存器产生，覆盖了未使用的编码（funct3[1:0]=11）。
    //
    //   为什么 LW（字操作）直接透传 data_in？
    //   → 字操作已经是 32-bit 宽度，不需要任何截取或扩展。数据存储器的
    //     32-bit 输出就是最终结果。这也是为什么 LW 可以忽略 sign_byte/sign_half。
    //
    //   复制运算符 {N{bit}} 的语义：
    //   - {24{sign_byte}}：将 sign_byte 复制 24 次，形成 24-bit 扩展字段
    //   - {16{sign_half}}：将 sign_half 复制 16 次，形成 16-bit 扩展字段
    //   Verilog 要求复制次数必须是常量；24 和 16 来自 32-bit 宽度减去数据宽度。
    //
    reg [31:0] load_inter;
    always @(*) begin
        case (load_funct3_in[1:0])
            2'b00:   load_inter = {{24{sign_byte}}, byte_sel};  // LB 或 LBU：
                                                                 //   高 24-bit 由 sign_byte 填充
                                                                 //   低 8-bit 为选出的字节
            2'b01:   load_inter = {{16{sign_half}}, half_sel};  // LH 或 LHU：
                                                                 //   高 16-bit 由 sign_half 填充
                                                                 //   低 16-bit 为选出的半字
            default: load_inter = data_in;                      // LW（或未定义 Load 类型）：
                                                                 //   无需扩展，直接透传 32-bit 字
        endcase
        // ★ case 已通过 default 覆盖所有情况，不会产生锁存器
    end

    // ==========================================================================
    // 7. 输出驱动逻辑
    // ==========================================================================

    // --------------------------------------------------------------------------
    // 7.1 load_output —— 受 load_in 门控的最终数据
    // --------------------------------------------------------------------------
    //
    //   功能：当且仅当当前指令为 Load 类型（load_in=1）时，将扩展后的数据
    //         load_inter 输出；否则输出全 0。
    //
    //   为什么需要门控？
    //   → 非 Load 指令（如算术指令、Store 指令）不需要 load_output 参与
    //     写回。若不门控，load_inter 可能存在无效翻转（由 data_in 和
    //     load_funct3_in 的任意值驱动），导致不必要的动态功耗。输出固定
    //     为 0 可以消除下游组合逻辑的无用翻转。
    //
    //   为什么是 assign 而不是 always？
    //   → 简单二选一 MUX，assign + 三目运算符最为直接。
    //
    assign load_output = load_in ? load_inter : 32'b0;

    // --------------------------------------------------------------------------
    // 7.2 load_mux —— WB 阶段写回数据源选择信号
    // --------------------------------------------------------------------------
    //
    //   功能：将 load_in 透传为 load_mux，供 WB 阶段的写回多路选择器使用。
    //
    //   在 WB 阶段，寄存器堆的写回数据来自两个可能的数据源：
    //     数据源 A：ALU 计算结果（如加法、逻辑运算的结果）
    //     数据源 B：Load 数据（load_output，即本模块的输出）
    //
    //   WB 阶段的 MUX 选择逻辑为：
    //     if (load_mux == 1'b1)
    //         regfile_wdata = load_output;   // 写回 Load 数据
    //     else
    //         regfile_wdata = alu_result;    // 写回 ALU 结果
    //
    //   为什么不是输出一个新的译码信号而是直接透传 load_in？
    //   → load_in 本身含义就是"当前指令是 Load"，与 load_mux 需要的语义
    //     完全一致。透传比重新译码更简单、更一致，避免了信号语义的重复推断。
    //
    //   为什么不直接用 load_in 而另起 load_mux 这个名字？
    //   → (a) 提高可读性：load_mux 明确表达了该信号在 WB 阶段的用途；
    //     (b) 信号命名反映使用场景，便于理解数据通路拓扑；
    //     (c) 允许未来若需修改选择逻辑（如增加其他写回源），可以仅修改
    //         此 assign 而不影响其他使用 load_in 的模块。
    //
    assign load_mux   = load_in;

endmodule
