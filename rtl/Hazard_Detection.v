// ============================================================================
//  一、流水线冒险类型总览
// ============================================================================
//
//  在经典五级流水线中，存在三类冒险，本模块处理其中两类：
//
//  ┌────────────────┬──────────────────┬──────────────────────────────────┐
//  │  冒险类型       │  典型场景          │  解决方式                         │
//  ├────────────────┼──────────────────┼──────────────────────────────────┤
//  │  1. 数据冒险    │  RAW (写后读)      │  Bypass_unit: 转发 (Forwarding)   │
//  │     (RAW)      │  非 Load 指令      │  从 EX/MEM 或 MEM/WB 旁路到 ID     │
//  │                ├──────────────────┼──────────────────────────────────┤
//  │                │  Load-Use 特殊情况  │  Hazard_Detection: 停顿 1 周期     │
//  │                │  lw x1,0(x2)     │  + Bypass_unit: 停顿后转发          │
//  │                │  add x3,x1,x4    │  （本模块检测停顿条件）              │
//  ├────────────────┼──────────────────┼──────────────────────────────────┤
//  │  2. 控制冒险    │  分支/跳转发生     │  Hazard_Detection: 冲刷 ID/EX     │
//  │     (Control)  │  预测错误/无条件跳  │  + Decoder: 在 ID 阶段输出 NOP      │
//  ├────────────────┼──────────────────┼──────────────────────────────────┤
//  │  3. 结构冒险    │  单端口存储器冲突   │  Stall_unit: 总线仲裁停顿         │
//  │     (Structural)│  I$ 和 D$ 同时访问  │  （本模块不处理）                  │
//  └────────────────┴──────────────────┴──────────────────────────────────┘
//
// ============================================================================
//  二、Load-Use 数据冒险 —— 为什么无法通过转发解决
// ============================================================================
//
//  2.1 问题本质：时序不可调和
//
//  考虑如下指令序列：
//      lw  x1, 0(x2)     // 指令A: EX阶段计算地址, MEM阶段读存储器
//      add x3, x1, x4    // 指令B: EX阶段需要 x1 的值作为ALU输入
//
//  五级流水线的时间推进（以时钟周期为单位）：
//
//           CC1     CC2     CC3     CC4     CC5     CC6
//  指令A:   IF      ID      EX      MEM     WB
//  指令B:           IF      ID      EX      MEM     WB
//                           ▲               ▲
//                           │               │
//                      B需要x1         A的x1在MEM末尾才有效！
//
//  关键矛盾：指令B在 CC3 的 EX 阶段就需要 x1 的值，
//           但指令A的 x1 值在 CC4 的 MEM 阶段末尾（存储器读出后）才有效。
//           即使在 MEM 阶段一读出数据就立刻转发，也来不及给 CC3 的 EX 使用。
//
//  2.2 与非Load RAW冒险的对比
//
//  非 Load 指令（如 ALU 指令）的 RAW 冒险：
//      add x1, x2, x3    // 指令A: EX阶段计算, 结果在EX末尾就有效
//      sub x4, x1, x5    // 指令B: EX阶段需要x1
//
//  时间线：
//           CC1     CC2     CC3     CC4
//  指令A:   IF      ID      EX      MEM
//                             ↓
//                          结果有效 ────► 转发给B的EX（同一周期或下一周期）
//  指令B:           IF      ID      EX
//                                   ▲
//                              B需要x1（转发来得及！）
//
//  非 Load 的 ALU 结果在 EX 末尾就已经稳定，可以通过 forwarding path
//  直接旁路给下一条指令的 EX 阶段使用。而 Load 的结果必须等 MEM 阶段
//  存储器读出，至少晚一个周期。这就是 Load-Use 冒险必须停顿的根本原因。
//
//  2.3 停顿策略
//
//  检测到 Load-Use 冒险后：
//    - PC_write = 0:      冻结 PC，下一周期重新取同一条指令（指令B）
//    - IF_ID_write = 0:   冻结 IF/ID 管道寄存器，指令B保留在 ID 阶段
//    - control_mux_sel = 1: ID/EX 管道寄存器插入气泡（NOP）
//
//  停顿一个周期后的时间线：
//           CC1     CC2     CC3     CC4     CC5     CC6
//  指令A:   IF      ID      EX      MEM     WB
//  指令B:           IF      ID      ID(停)  EX      MEM     WB
//                                          ▲
//                                     x1从MEM转发到EX，来得及！
//
//  停顿一周期后，指令B 的 EX 阶段推迟到 CC5，此时指令A 已经在 MEM 阶段
//  读出了 x1 的值，可以通过 MEM/WB 转发路径（Bypass_unit）旁路给 B。
//
// ============================================================================
//  三、控制冒险 —— 分支/跳转冲刷机制
// ============================================================================
//
//  3.1 冒险来源
//
//  分支指令在 EX 阶段才解析"是否跳转"，而 ID 阶段已经取入了分支后面的指令。
//  如果分支发生（或预测错误），ID 和 ID/EX 中的指令是错误路径上的，必须冲刷。
//
//  无条件跳转（JAL/JALR）：必然跳转 → 必须冲刷紧跟其后的指令。
//  条件分支（BEQ/BNE等）：EX 阶段解析出 branch_taken=1 → 冲刷。
//
//  3.2 冲刷信号时序
//
//  控制冒险的处理：
//    - PC_write = 1:      允许 PC 更新到跳转目标地址（新PC = branch_target/jalr_target）
//    - IF_ID_write = 1:   允许 IF/ID 更新 → Decoder 输出的 NOP 进入 IF/ID 寄存器
//    - control_mux_sel = 1: ID/EX 插入气泡（清零ID/EX的控制信号）
//
//  关键设计：控制冒险时 IF_ID_write=1 (非0)，不同于 Load-Use 的 IF_ID_write=0。
//  原因：
//    控制冒险时，PC 已经指向正确的目标地址，IF 阶段取到的是正确的下一条指令。
//    但紧跟分支的那条指令仍在流水线内部（ID→EX），必须被冲刷。
//    同时 Decoder 在 wrong_predict=1 时会将 id_instr 覆写为 NOP，
//    所以 IF_ID_write=1 让 NOP 进入管道，后续阶段看到的是无害的 NOP。
//
//  ┌─────────────────────────────────────────────────────────────┐
//  │ 冲刷范围示意（分支发生，CC3解析出跳转）:                       │
//  │                                                              │
//  │         CC1      CC2      CC3      CC4                       │
//  │ 分支:   IF ───►  ID ───►  EX ───►  MEM    (解析: 跳转!)      │
//  │ 指令+1:          IF ───►  ID ───►  NOP    (被冲刷)            │
//  │ 指令+2:                  IF ───►  NOP    (被冲刷)             │
//  │ 目标:                            IF ───►  ID    (正确路径)     │
//  │                                ▲                              │
//  │                          PC在此周期指向目标                    │
//  └─────────────────────────────────────────────────────────────┘
//
// ============================================================================
//  四、转发路径 (Bypass_unit) —— ex_hit vs mem_hit 的编码与优先级
// ============================================================================
//
//  4.1 转发单元架构
//
//  本模块 (Hazard_Detection_Unit) 只负责停顿和冲刷，不直接参与数据转发。
//  数据转发由 Bypass_unit 完成。两个模块在 ID 阶段并列运行：
//
//      ID 阶段 ─┬─ Bypass_unit: 检测转发条件 → 产生 fwd1/fwd2 选择码
//               │                检测 Load-Use → 产生 bypass_stall
//               │
//               └─ Hazard_Detection_Unit: 检测 Load-Use → 产生 stall/flush
//                                         检测控制冒险 → 产生 flush
//               (两个模块的 Load-Use 检测是冗余/互补的)
//
//  4.2 Bypass_unit 的转发编码
//
//  Bypass_unit 为每个源操作数产生 2 位转发选择码：
//
//    fwd[1:0] │ 含义
//    ─────────┼─────────────────────────────────────────────────
//      00     │ 无转发：使用寄存器文件读出的值 (RF[rs])
//      01     │ EX/MEM 转发：使用 EX/MEM 管道寄存器中的 ALU 结果
//      10     │ MEM/WB 转发：使用 MEM/WB 管道寄存器中的写回数据
//      11     │ 保留
//
//  对应到 rv32i_core 中的转发 MUX：
//      ex_fwd_rs1 = (fwd1==01) ? mem_alu_result   // EX/MEM 阶段的值
//                 : (fwd1==10) ? wb_write_data     // MEM/WB 阶段的值
//                 : ex_rs1_data;                    // 寄存器文件
//
//  4.3 ex_hit（EX/MEM转发）vs mem_hit（MEM/WB转发）—— 优先级与场景
//
//  ex_hit 条件（Bypass_unit 第 49-50 行）：
//      ex_hit_rs1 = ex_reg_write && (ex_rd == rs1) && (|ex_rd)
//      → EX 阶段的指令会写寄存器，且其 rd 匹配 ID 阶段指令的 rs1，且 rd≠x0
//
//  mem_hit 条件（Bypass_unit 第 52-53 行）：
//      mem_hit_rs1 = mem_reg_write && (mem_rd == rs1) && (|mem_rd)
//      → MEM 阶段的指令会写寄存器，且其 rd 匹配 ID 阶段指令的 rs1，且 rd≠x0
//
//  ★ 优先级：ex_hit > mem_hit
//
//  编码逻辑（Bypass_unit 第 61-66 行）：
//      fwd = ex_hit ? 2'b01 : mem_hit ? 2'b10 : 2'b00;
//
//  优先级理由：
//    考虑三条连续指令：
//      add x1, x2, x3    // 指令A（CC3: EX, CC4: MEM, CC5: WB）
//      sub x1, x4, x5    // 指令B（CC4: EX, CC5: MEM, CC6: WB）
//      and x6, x1, x7    // 指令C（CC5: ID, 需要x1的值）
//
//    在 CC5 时刻：
//      - mem_hit 匹配：指令A在MEM阶段，rd=x1 → 值在 mem_alu_result
//      - ex_hit 匹配：  指令B在EX阶段，rd=x1 → 值在 ex_alu_result（更新！）
//
//    指令C真正需要的是指令B的 x1（最新的值），不是指令A的 x1。
//    因为指令B在程序顺序上更接近指令C，其写入覆盖了指令A的写入。
//    所以 ex_hit（更接近"现在"的指令）必须具有更高优先级。
//
//  4.4 转发时序路径
//
//  转发的物理路径：
//
//  ┌─────────────────────────────────────────────────────────────────┐
//  │                                                                  │
//  │  EX/MEM转发 (fwd=01):                                            │
//  │    EX阶段 ALU 输出 ─► ex_alu_result ─► EX/MEM管道寄存器          │
//  │                       (CC3末尾)        mem_alu_result (CC4)      │
//  │                                      ─► 转发MUX ─► EX阶段操作数  │
//  │                                                                  │
//  │  MEM/WB转发 (fwd=10):                                            │
//  │    MEM阶段 存储器/LoadUnit输出 ─► MEM/WB管道寄存器                │
//  │         (CC4末尾)                   wb_write_data (CC5)          │
//  │                                   ─► 转发MUX ─► EX阶段操作数     │
//  │                                                                  │
//  │  设计要点：fwd1/fwd2的选择码在ID阶段确定，锁存到ID/EX管道寄存器    │
//  │  中，然后在EX阶段驱动转发MUX。这是因为EX阶段的组合逻辑在同一周期   │
//  │  内完成，不能等待另一个时钟周期来计算转发条件。                    │
//  └─────────────────────────────────────────────────────────────────┘
//
//  4.5 为什么 Load-Use 不能用转发解决（再次强调）
//
//  回到 2.1 节的时间线：
//    lw x1, 0(x2) 的 x1 值在 MEM 末尾（CC4末尾）才出现在 wb_write_data 中。
//    但 add x3, x1, x4 的 EX 阶段在 CC3，此时 wb_write_data 还是旧值。
//
//  如果 lw 的结果可以在 EX 阶段末尾获得（像 ALU 指令那样），
//  那么 ex_hit 转发就能解决。但 Load 必须经过 MEM 阶段的存储器访问，
//  这是物理上无法绕过的延迟。这就是 Load-Use 必须停顿的根本原因。
//
// ============================================================================
//  五、端口信号说明与命名澄清
// ============================================================================
//
//  5.1 信号来源（重要：端口名与实际连接的信号不完全对应）
//
//  端口名中包含 "ID_EX_" 和 "EX_MEM_" 前缀，但实际连接到的是
//  ID 阶段和 EX 阶段的组合信号，而非管道寄存器的输出。详见下表：
//
//  ┌─────────────────────┬──────────────────────────────────────────┐
//  │  Hazard_Detection   │  rv32i_core 实际连接                       │
//  │  端口名              │  （见 rv32i_core.v 第 191-198 行）         │
//  ├─────────────────────┼──────────────────────────────────────────┤
//  │  ID_EX_rs1          │  id_rs1_addr — ID 阶段译码出的 rs1 地址     │
//  │  ID_EX_rs2          │  id_rs2_addr — ID 阶段译码出的 rs2 地址     │
//  │  ID_EX_mem_read     │  ex_mem_read  — EX 阶段的 Load 标志！       │
//  │                     │  ★ 注意：不是 ID 阶段的 Load 标志           │
//  ├─────────────────────┼──────────────────────────────────────────┤
//  │  EX_MEM_rd          │  ex_rd  — EX 阶段指令的目的寄存器            │
//  │  EX_MEM_mem_read    │  ex_mem_read — EX 阶段的 Load 标志          │
//  │                     │  （与 ID_EX_mem_read 连接相同信号！）        │
//  │  MEM_WB_rd          │  mem_rd — MEM 阶段指令的目的寄存器           │
//  ├─────────────────────┼──────────────────────────────────────────┤
//  │  branch_taken       │  branch_taken_ex — EX 阶段分支解析结果      │
//  │  jump               │  jalr_enable — ID 阶段跳转使能              │
//  └─────────────────────┴──────────────────────────────────────────┘
//
//  5.2 输出信号作用
//
//  ┌──────────────────┬───────┬──────────────────────────────────────┐
//  │  输出信号         │ 默认值 │  功能说明                              │
//  ├──────────────────┼───────┼──────────────────────────────────────┤
//  │  PC_write        │  1    │  1=PC 更新到下一值; 0=PC冻结           │
//  │                  │       │  → 连接到 prog_counter 的 stall_in    │
//  ├──────────────────┼───────┼──────────────────────────────────────┤
//  │  IF_ID_write     │  1    │  1=IF/ID管道寄存器更新; 0=IF/ID冻结    │
//  │                  │       │  → 连接到 if_id_stage 的 stall 端口    │
//  ├──────────────────┼───────┼──────────────────────────────────────┤
//  │  control_mux_sel │  0    │  1=ID/EX插入气泡(清零控制信号)         │
//  │                  │       │  → 连接到 id_ex_stage 的 flush 端口    │
//  └──────────────────┴───────┴──────────────────────────────────────┘
//
// ============================================================================
//  六、检测逻辑详解
// ============================================================================
//
//  6.1 Load-Use 冒险检测
//
//  wire load_use_hazard = EX_MEM_mem_read       // 条件1: EX阶段是Load指令
//                       && (|EX_MEM_rd)          // 条件2: rd ≠ x0
//                       && ((ID_EX_rs1 == EX_MEM_rd)   // 条件3: ID阶段rs1匹配
//                        || (ID_EX_rs2 == EX_MEM_rd)); //   或 ID阶段rs2匹配
//
//  逐条件分析：
//
//  【条件1: EX_MEM_mem_read = 1】
//   确保 EX 阶段的指令确实是 Load（lw/lh/lb/lbu/lhu 等）。
//   只有 Load 指令才需要停顿——ALU指令的结果在EX末尾就有效，可以通过转发解决。
//
//  【条件2: (|EX_MEM_rd) = 1】
//   rd ≠ x0（即 rd != 5'b00000）。RISC-V 约定 x0 恒为 0，不可写。
//   如果 Load 的目标是 x0（实际上不会发生，但为完备性检查），
//   则不存在真正的数据依赖，不需要停顿。
//
//  【条件3: ID 阶段指令的源寄存器与 EX 阶段 Load 指令的目的寄存器匹配】
//   只有当 ID 阶段的指令确实使用了 Load 将要写入的寄存器时，才存在 RAW 冒险。
//   如果 ID 阶段指令的 rs1 和 rs2 都不等于 Load 的 rd，则不存在数据依赖。
//
//  【为什么使用 EX_MEM_mem_read 而不是 ID_EX_mem_read？】
//   这是本模块的一个关键设计细节。load_use_hazard 检测的是：
//     "EX 阶段的指令是 Load" 且 "ID 阶段的指令使用其 rd"
//   即 A ← B 依赖，其中 A 在 ID 阶段，B 在 EX 阶段。
//
//   如果错误地使用 ID_EX_mem_read（ID 阶段是否为 Load），则检测的是：
//     "ID 阶段的指令是 Load" 且 "ID 阶段的指令使用其自己的 rd"
//   这是毫无意义的情况——一条 Load 指令不会同时读和写同一个寄存器
//   （它读取的是 rs1（基址寄存器），写入的是 rd，两者不同）。
//   因此必须使用 EX_MEM_mem_read = ex_mem_read（EX 阶段 Load 标志）。
//
//  6.2 控制冒险检测
//
//  wire control_hazard = branch_taken || jump;
//
//  【branch_taken】
//   来自 EX 阶段的 branch_unit 输出。在 EX 阶段比较 rs1 和 rs2 后确定
//   条件分支是否发生（BEQ/BNE/BLT/BGE/BLTU/BGEU）。如果发生，
//   则 ID 和 ID/EX 中的指令是错误路径上的，需要冲刷。
//
//  【jump】
//   来自 ID 阶段的 jalr_enable 信号。JAL 和 JALR 是无条件跳转，
//   紧跟其后的指令一定不在正确路径上。注意 JAL 在 ID 阶段就被识别
//   （由 Decoder 译码产生），所以 jump 信号在 ID 阶段就已经有效。
//
//   为什么 JAL 的控制冒险在 ID 阶段而不是 EX 阶段？
//   因为 Decoder 直接译码 opcode=JAL，不需要等到 EX 阶段计算。
//   但 PC 跳转目标（branch_target / jalr_target）可能在 EX 阶段
//   才最终确定（尤其是 JALR: target = rs1 + imm，需要读寄存器+加法）。
//
// ============================================================================
//  七、输出信号的三态控制逻辑
// ============================================================================
//
//  always @(*) 组合逻辑，根据冒险类型产生不同的控制信号组合：
//
//  状态1: Load-Use 冒险 (load_use_hazard = 1)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  PC_write       = 0    ← 冻结PC，不取新指令                       │
//  │  IF_ID_write    = 0    ← 冻结IF/ID，保留当前ID阶段的指令           │
//  │  control_mux_sel = 1   ← ID/EX插入气泡（NOP），EX阶段空闲一个周期   │
//  │                                                                  │
//  │  效果: ID阶段指令等待一个周期，EX阶段执行NOP（什么都不做）。         │
//  │        下一个周期，Load的MEM阶段完成，数据可通过转发给ID阶段指令。   │
//  │                                                                  │
//  │  流水线快照（停顿周期）:                                           │
//  │    IF: [指令C] ← 不变（PC冻结）                                    │
//  │    ID: [指令B] ← 不变（IF/ID冻结），即使用Load结果的指令             │
//  │    EX: [NOP]   ← 气泡（ID/EX被冲洗）                               │
//  │    MEM: [Load] ← 指令A正在读存储器                                 │
//  │    WB: [上一条]                                                    │
//  └──────────────────────────────────────────────────────────────────┘
//
//  状态2: 控制冒险 (control_hazard = 1, load_use_hazard = 0)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  PC_write       = 1    ← 允许PC更新到跳转目标                     │
//  │  IF_ID_write    = 1    ← 允许IF/ID更新（NOP或正确路径指令进入）     │
//  │  control_mux_sel = 1   ← ID/EX插入气泡，冲刷错误路径指令           │
//  │                                                                  │
//  │  效果: PC跳转到目标地址，IF取出正确指令。IF/ID中的错误指令被        │
//  │        Decoder输出的NOP覆盖（wrong_predict=1触发）。                │
//  │        ID/EX中的错误指令被flush清零控制信号。                      │
//  │                                                                  │
//  │  关键: IF_ID_write=1 不同于 Load-Use 的 IF_ID_write=0。           │
//  │        控制冒险时 IF 已经取到了正确路径的指令，需要让它进入          │
//  │        IF/ID 寄存器。Load-Use 时需要保持当前指令不变。             │
//  └──────────────────────────────────────────────────────────────────┘
//
//  状态3: 正常执行 (无冒险)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  PC_write       = 1    ← PC正常递增（+4）                         │
//  │  IF_ID_write    = 1    ← IF/ID正常锁存新指令                      │
//  │  control_mux_sel = 0   ← ID/EX正常传递，不插入气泡                 │
//  │                                                                  │
//  │  效果: 流水线正常推进，每个阶段处理其对应的指令。                   │
//  │        Bypass_unit 负责检测并解决非 Load 的 RAW 数据冒险。          │
//  └──────────────────────────────────────────────────────────────────┘
//
//  优先级：load_use_hazard > control_hazard > 正常
//  if...else if...else 结构保证了 Load-Use 优先于控制冒险。
//  如果同一周期同时发生 Load-Use 和控制冒险，优先处理 Load-Use。
//  实际上 Load 指令不会同时是分支指令，但 JALR 可能在使用 Load 结果
//  的寄存器作为基址（如 lw x1,0(x2); jalr x0,x1,0），
//  此时应先解决 Load-Use 停顿，再处理跳转。
//
// ============================================================================
//  八、与流水线各阶段的交互关系
// ============================================================================
//
//  8.1 与 IF 阶段 (PC) 的交互
//
//  PC_write → prog_counter 的 stall_in 端口
//    - PC_write=0: PC 冻结，IF 阶段不做任何操作，下一周期重新取同一地址
//    - PC_write=1: PC 正常更新（+4 或 branch_target 或 jalr_target）
//
//  ● Load-Use 时 PC_write=0，防止取入新指令覆盖 IF/ID 的内容。
//    因为 IF/ID 也需要冻结（IF_ID_write=0），IF 阶段取新指令也没地方放。
//  ● 控制冒险时 PC_write=1，PC 需要跳转到正确目标地址取指令。
//
//  8.2 与 IF/ID 管道寄存器的交互
//
//  IF_ID_write → if_id_stage 的 stall 端口
//    - IF_ID_write=0: 冻结 IF/ID，保持当前值
//    - IF_ID_write=1: 锁存 IF 阶段的新值
//
//  ● Load-Use 时 IF_ID_write=0，保留 ID 阶段的指令不变。
//    该指令需要停顿一个周期等待 Load 数据。
//  ● 控制冒险时 IF_ID_write=1，让 Decoder 的 NOP 或正确路径指令进入。
//    Decoder 在 wrong_predict=1 时会将输出覆盖为 NOP，
//    所以 IF/ID 寄存器收到的是无害的 NOP 指令。
//
//  8.3 与 ID/EX 管道寄存器的交互
//
//  control_mux_sel → id_ex_stage 的 flush 端口
//    - control_mux_sel=0: 正常传递 ID 阶段信号到 EX 阶段
//    - control_mux_sel=1: 清零 ID/EX 的控制信号 (reg_write=0, mem_write=0 等)
//                         数据通路保持原值但已是 don't care
//
//  ● 这是插入流水线气泡（NOP）的机制。无论 Load-Use 还是控制冒险，
//    都需要在 ID/EX 插入气泡，让 EX 阶段执行一个无操作周期。
//  ● ID/EX 的 flush 实现（id_ex_stage 第 384-408 行）：
//    只清零控制信号，不清零数据通路。因为控制信号全关断后，
//    EX 阶段不会产生任何副作用（不写寄存器、不访问存储器）。
//
//  8.4 与 Decoder 的交互
//
//  Decoder 在 wrong_predict=1 时会输出 NOP（所有控制信号清零）。
//  wrong_predict 信号由 rv32i_core 中的分支预测比较产生：
//      wrong_predict = branch_taken_d ^ (branch_taken_ex | jalr_enable_ex)
//  即：ID阶段预测 != EX阶段实际结果 → 预测错误。
//
//  Hazard_Detection 的输出不直接控制 Decoder，但间接影响：
//    控制冒险 → IF_ID_write=1 → NOP 从 Decoder 进入 IF/ID
//    Load-Use → IF_ID_write=0 → Decoder 输出被忽略（IF/ID冻结）
//
//  8.5 与 Bypass_unit 的交互
//
//  ┌────────────────────────────────────────────────────────────────┐
//  │                                                                 │
//  │  Bypass_unit（转发）:          Hazard_Detection（停顿/冲刷）:     │
//  │                                                                 │
//  │  处理:                         处理:                             │
//  │    - ALU → ALU RAW (ex_hit)     - Load → ALU RAW (load_use)     │
//  │    - ALU → ALU RAW (mem_hit)    - 控制冒险 (branch/jump)         │
//  │    - Load-Use 停顿请求          - 流水线冲刷/冻结                 │
//  │      (bypass_stall)                                               │
//  │                                                                 │
//  │  输出: stall_out                输出: PC_write, IF_ID_write,     │
//  │        irmux1, irmux2                 control_mux_sel            │
//  │                                                                 │
//  └────────────────────────────────────────────────────────────────┘
//
//  两个模块在 Load-Use 检测上有重叠：
//    - Bypass_unit: load_use_hazard = ex_load && (ex_hit_rs1 || ex_hit_rs2)
//    - Hazard_Detection: load_use_hazard = EX_MEM_mem_read && |EX_MEM_rd
//                                         && (rs1==rd || rs2==rd)
//    两者检测条件等价，Bypass_unit 的输出 stall_out 连接到 Stall_unit，
//    最终合并为 pipeline_stall。Hazard_Detection 的输出则直接控制
//    PC、IF/ID 和 ID/EX 的 stall/flush。
//
//  8.6 与 Stall_unit 的交互
//
//  Stall_unit 接收多个停顿源：
//    - bypass_stall (来自 Bypass_unit)
//    - m_unit_stall (乘法/除法单元多周期停顿)
//    - imem_ready / dmem_ready (指令/数据存储器就绪)
//
//  输出统一的 pipeline_stall 信号，该信号连接到所有管道寄存器的
//  stall 端口以及 PC 的 stall_in。Hazard_Detection 产生的
//  PC_write/IF_ID_write 与 pipeline_stall 是独立的两套控制路径，
//  分别处理不同类型的停顿需求。
//
// ============================================================================
//  九、设计约束与边缘情况
// ============================================================================
//
//  9.1 x0 寄存器处理
//    (|EX_MEM_rd) 确保 rd≠x0 时才算有效匹配。
//    RISC-V 规范：x0 恒为 0，不可写。如果 Load 的目标是 x0，
//    不存在真正的 RAW 依赖，不需要停顿。
//    Bypass_unit 中的 ex_hit/mem_hit 同样包含 (|rd) 检查。
//
//  9.2 无寄存器写使能的指令
//    Store（S-type）、Branch（B-type）、FENCE 等指令不写寄存器文件。
//    这些指令的 rd 字段实际上编码的是 funct3 或其它信息，
//    不能作为 RAW 冒险的检测依据。但本模块的 EX_MEM_mem_read
//    已经限定了只有 Load 指令才触发，所以不写寄存器的指令不会
//    被误判为 Load-Use 冒险。
//
//  9.3 多周期指令
//    M-extension 的乘法/除法指令（mul/div/rem）需要多个周期完成。
//    这些指令的停顿由 Stall_unit 的 m_unit_stall 处理，
//    本模块不介入。mul_div_unit 在 busy 期间会拉高 m_unit_stall，
//    导致整个流水线暂停。
//
//  9.4 Cache Miss 停顿
//    I-Cache 和 D-Cache miss 导致的停顿由 Stall_unit 统一管理。
//    在 Cache miss 期间，pipeline_stall 会阻止所有管道寄存器更新，
//    包括本模块的输出实际上也被间接"忽略"了（因为管道寄存器不采样新值）。
//
// ============================================================================
//  十、信号速查表
// ============================================================================
//
//  ┌─────────────────────┬────────┬───────────────────────────────────┐
//  │  信号               │  位宽   │  简要说明                          │
//  ├─────────────────────┼────────┼───────────────────────────────────┤
//  │  ID_EX_rs1          │  5     │  ID阶段指令的rs1地址                │
//  │  ID_EX_rs2          │  5     │  ID阶段指令的rs2地址                │
//  │  EX_MEM_rd          │  5     │  EX阶段指令的rd地址                 │
//  │  MEM_WB_rd          │  5     │  MEM阶段指令的rd地址（保留端口）     │
//  │  ID_EX_mem_read     │  1     │  EX阶段为Load标志（非ID阶段！）      │
//  │  EX_MEM_mem_read    │  1     │  EX阶段为Load标志（同上，冗余连接）   │
//  │  branch_taken       │  1     │  EX阶段分支发生标志                 │
//  │  jump               │  1     │  ID阶段无条件跳转标志                │
//  │  PC_write           │  1     │  PC更新使能 → prog_counter         │
//  │  IF_ID_write        │  1     │  IF/ID更新使能 → if_id_stage       │
//  │  control_mux_sel    │  1     │  ID/EX冲刷使能 → id_ex_stage       │
//  └─────────────────────┴────────┴───────────────────────────────────┘
//
// ============================================================================
//  十一、冒险处理决策树
// ============================================================================
//
//  ID阶段发现数据依赖？
//  │
//  ├── 依赖 EX 阶段 Load 的结果？
//  │   └── YES → Load-Use 冒险
//  │        ├── PC_write = 0    (冻结PC)
//  │        ├── IF_ID_write = 0 (冻结IF/ID)
//  │        └── control_mux_sel = 1 (ID/EX插入气泡)
//  │        → 停顿1周期后，通过MEM/WB转发解决
//  │
//  ├── 依赖 EX 阶段非Load指令的结果？
//  │   └── YES → RAW 冒险（由Bypass_unit处理）
//  │        └── ex_hit → fwd = 2'b01 → 从EX/MEM转发
//  │        → 无需停顿
//  │
//  └── 依赖 MEM 阶段指令的结果？
//      └── YES → RAW 冒险（由Bypass_unit处理）
//           └── mem_hit → fwd = 2'b10 → 从MEM/WB转发
//           → 无需停顿
//
//  控制冒险？
//  │
//  ├── 分支发生 (branch_taken = 1) 或 无条件跳转 (jump = 1)
//  │   └── YES → 控制冒险
//  │        ├── PC_write = 1    (允许PC更新到目标)
//  │        ├── IF_ID_write = 1 (允许NOP/正确指令进入IF/ID)
//  │        └── control_mux_sel = 1 (ID/EX插入气泡)
//  │        → 错误路径指令被冲刷
//  │
//  └── 无冒险 → 正常流水推进
//       ├── PC_write = 1
//       ├── IF_ID_write = 1
//       └── control_mux_sel = 0
//

`include "Def.v"

module Hazard_Detection_Unit (
    // ========================================================================
    //  ID 阶段源寄存器地址 — 当前在 ID 阶段的指令的 rs1 和 rs2
    // ========================================================================
    input  wire [4:0] ID_EX_rs1,        // ID 阶段 rs1 地址（rv32i_core: id_rs1_addr）
    input  wire [4:0] ID_EX_rs2,        // ID 阶段 rs2 地址（rv32i_core: id_rs2_addr）

    // ========================================================================
    //  EX/MEM 阶段目的寄存器地址
    // ========================================================================
    input  wire [4:0] EX_MEM_rd,        // EX 阶段 rd 地址（rv32i_core: ex_rd）
    input  wire [4:0] MEM_WB_rd,        // MEM 阶段 rd 地址（保留端口，未用于检测逻辑）

    // ========================================================================
    //  Load 指令标志 — 实际连接的都是 EX 阶段的 Load 标志
    // ========================================================================
    input  wire       ID_EX_mem_read,   // EX 阶段 Load 标志（rv32i_core: ex_mem_read）
    input  wire       EX_MEM_mem_read,  // EX 阶段 Load 标志（rv32i_core: ex_mem_read）
                                        // ★ 两个端口连接同一信号，前者为冗余连接

    // ========================================================================
    //  控制冒险检测输入
    // ========================================================================
    input  wire       branch_taken,     // 分支发生标志（rv32i_core: branch_taken_ex）
                                        //   来自 EX 阶段 branch_unit，条件分支满足时 =1
    input  wire       jump,             // 无条件跳转标志（rv32i_core: jalr_enable）
                                        //   来自 ID 阶段 Decoder，JAL/JALR 指令时 =1

    // ========================================================================
    //  流水线控制输出 — 控制 PC、IF/ID、ID/EX 的行为
    // ========================================================================
    output reg        PC_write,         // PC 写使能: 1=PC更新 0=PC冻结
                                        //   → prog_counter.stall_in（低有效冻结）
    output reg        IF_ID_write,      // IF/ID 写使能: 1=IF/ID更新 0=IF/ID冻结
                                        //   → if_id_stage.stall（高有效冻结，需取反）
    output reg        control_mux_sel   // ID/EX 冲刷选择: 1=插入气泡(flush) 0=正常
                                        //   → id_ex_stage.flush
);

    // ========================================================================
    //  Load-Use 数据冒险检测
    // ========================================================================
    //
    //  检测条件三要素：
    //    1. EX_MEM_mem_read = 1: EX 阶段的指令是 Load（lw/lh/lb/lbu/lhu）
    //       只有 Load 才需要停顿，因为数据在 MEM 末尾才有效。
    //       ALU 指令的结果在 EX 末尾就有效，可通过转发解决。
    //
    //    2. (|EX_MEM_rd) = 1: Load 的目标寄存器 rd ≠ x0（5'b00000）
    //       x0 恒为 0 且不可写，不需要停顿。按位或(|) 检查 rd 是否有非零位。
    //
    //    3. ID 阶段指令的 rs1 或 rs2 与 EX 阶段 Load 的 rd 匹配
    //       只有 ID 阶段的指令确实读取了 Load 的目标寄存器时，
    //       才存在真正的 RAW（读后写）数据依赖。
    //
    //  为什么使用 EX_MEM_mem_read（EX 阶段 Load 标志）而不是
    //  ID_EX_mem_read（ID 阶段 Load 标志）？
    //    因为需要检测的是 "EX 阶段的 Load 与 ID 阶段的消费者" 之间的冒险。
    //    如果使用 ID 阶段的 Load 标志，则只能检测 "ID 阶段自己与自己" 的
    //    匹配，这是无意义的——Load 指令不会同时读写同一寄存器。
    //
    wire load_use_hazard = EX_MEM_mem_read         // EX 阶段是 Load 指令
                         && (|EX_MEM_rd)           // rd ≠ x0（x0不可写）
                         && ((ID_EX_rs1 == EX_MEM_rd)   // ID.rs1 == EX.rd
                          || (ID_EX_rs2 == EX_MEM_rd)); // 或 ID.rs2 == EX.rd

    // ========================================================================
    //  控制冒险检测
    // ========================================================================
    //
    //  条件分支发生（branch_taken = 1）：EX 阶段 branch_unit 解析出分支条件
    //    满足，需要跳转。ID 和 ID/EX 中的指令是顺序取入的错误路径指令。
    //
    //  无条件跳转（jump = 1）：JAL 或 JALR 指令一定跳转。由于 JAL 在 ID 阶段
    //    就被 Decoder 译码识别，jump 信号在 ID 阶段就已经有效。
    //    紧跟跳转指令之后的指令一定不在正确路径上。
    //
    wire control_hazard = branch_taken || jump;

    // ========================================================================
    //  输出信号生成 — 三态控制逻辑（组合逻辑）
    // ========================================================================
    //
    //  优先级: load_use_hazard > control_hazard > 正常执行
    //  这样做是因为 Load-Use 冒险需要冻结整条流水线来保护数据依赖关系，
    //  而控制冒险只是冲刷错误路径。如果两者同时发生，应先解决数据依赖。
    //
    always @(*) begin
        if (load_use_hazard) begin
            // ── 状态1: Load-Use 冒险 ──
            // 目标：停顿流水线 1 个周期，等待 Load 完成 MEM 读操作
            //
            // PC_write = 0: 冻结 PC，不取新指令
            //   原因：下一周期需要重新执行当前 ID 阶段的指令（使用 Load 结果的指令），
            //   不能取入新指令覆盖它。同时 IF/ID 也需要冻结来保存该指令。
            //
            // IF_ID_write = 0: 冻结 IF/ID 管道寄存器
            //   原因：保留当前 ID 阶段的指令不变。该指令需要等待一个周期。
            //   下一周期它将再次出现在 ID 阶段，此时 Load 已完成 MEM 阶段，
            //   数据可通过 MEM/WB 转发路径（Bypass_unit）获取。
            //
            // control_mux_sel = 1: ID/EX 插入气泡（NOP）
            //   原因：EX 阶段当前正在执行 Load 指令，需要一个周期完成 MEM 访问。
            //   下一周期 EX 阶段应执行之前停顿在 ID 阶段的消费者指令，
            //   因此当前周期 ID/EX 寄存器应传递一个空的 NOP 到 EX 阶段。
            //   flush 清零 reg_write/mem_write/mem_read 等控制信号，
            //   确保 EX 阶段不产生任何副作用。
            //
            PC_write        = 1'b0;
            IF_ID_write     = 1'b0;
            control_mux_sel = 1'b1;
        end
        else if (control_hazard) begin
            // ── 状态2: 控制冒险 ──
            // 目标：冲刷错误路径上的指令，将流水线重定向到正确目标
            //
            // PC_write = 1: 允许 PC 更新到跳转目标地址
            //   原因：分支/跳转已发生，PC 必须指向目标地址取正确指令。
            //   PC 的新值由 prog_counter 内部根据 branch_taken_in、
            //   immbj（分支偏移）、jalr_type_in（JALR 目标）等信号计算。
            //
            // IF_ID_write = 1: 允许 IF/ID 更新
            //   ★ 关键设计点：控制冒险时 IF_ID_write=1，不是 0。
            //   这与 Load-Use 不同！
            //   原因：虽然 IF/ID 中当前有一条错误指令需要丢弃，
            //   但 Decoder 在 wrong_predict=1 时会将输出覆写为 NOP，
            //   同时 IF 阶段已经取到了正确路径的指令（PC已更新）。
            //   所以 IF_ID_write=1 让 NOP（或正确指令）进入 IF/ID，
            //   下一个周期正常流水继续。
            //   实际上在这个周期的初始 half-cycle 内：
            //     (a) Decoder 看到 wrong_predict=1 → 输出 NOP
            //     (b) IF_ID_write=1 → NOP 被锁存到 IF/ID 寄存器
            //     (c) IF 阶段取出正确目标地址的指令（由已更新的 PC 驱动）
            //
            // control_mux_sel = 1: ID/EX 插入气泡
            //   原因：ID/EX 中锁存的是紧跟分支的错误路径指令，
            //   必须通过 flush 清除其控制信号（reg_write=0, mem_write=0等），
            //   使其变成无操作的 NOP。数据通路不清零但已是 don't care。
            //
            PC_write        = 1'b1;
            IF_ID_write     = 1'b1;
            control_mux_sel = 1'b1;
        end
        else begin
            // ── 状态3: 正常执行（无冒险）──
            // 流水线正常推进，所有阶段各司其职
            //
            // PC_write = 1: PC 正常递增（+4，无跳转时）
            // IF_ID_write = 1: IF/ID 正常锁存新取出的指令
            // control_mux_sel = 0: ID/EX 正常传递 ID 阶段的信号到 EX 阶段
            //
            // 此状态下，Bypass_unit 负责处理非 Load 的 RAW 数据冒险：
            //   通过 fwd1/fwd2 选择码控制 EX 阶段的转发 MUX，
            //   从 EX/MEM 或 MEM/WB 旁路数据到 ALU 输入。
            //
            PC_write        = 1'b1;
            IF_ID_write     = 1'b1;
            control_mux_sel = 1'b0;
        end
    end

endmodule
