// ║  Stall.v — 5 级流水线统一停顿控制器（Unified Pipeline Stall Controller）      ║
// ╟──────────────────────────────────────────────────────────────────────────────╢
// ║  所属模块: Sapphire-SoC RV32IM 五级流水线 RISC-V 处理器核心                    ║
// ║  核心功能: 汇集 4 类停顿来源 → 统一 OR 归约 → 输出全局流水线停顿信号           ║
// ║  设计原则: 集中式停顿仲裁 —— 任何停顿源触发，整条流水线冻结                     ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
//  一、模块处理的流水线冒险类型（Pipeline Hazards）
// ============================================================================
//
//  本模块处理的停顿来源覆盖了三类流水线冒险中的结构冒险和数据冒险：
//
//  ┌──────────────────┬──────────────────┬──────────────────────────────────┐
//  │  冒险类型         │  对应信号         │  产生原因                         │
//  ├──────────────────┼──────────────────┼──────────────────────────────────┤
//  │  结构冒险         │  ~imem_ready     │  指令存储器未就绪（取指阶段无法     │
//  │  (Structural)     │                  │  在一个周期内提供新指令）           │
//  │                   ├──────────────────┼──────────────────────────────────┤
//  │                   │  ~dmem_ready     │  数据存储器未就绪（Load/Store      │
//  │                   │                  │  需要 >1 周期完成访存）            │
//  ├──────────────────┼──────────────────┼──────────────────────────────────┤
//  │  数据冒险         │  load_use_stall  │  Load-Use 数据冒险：EX 阶段是      │
//  │  (Data Hazard)    │                  │  Load 指令，且 ID 阶段下一条指令    │
//  │                   │                  │  恰好使用该 Load 的目标寄存器 rd。  │
//  │                   │                  │  转发（Forwarding/Bypass）无法     │
//  │                   │                  │  解决这种冒险，必须停顿 1 个周期。   │
//  ├──────────────────┼──────────────────┼──────────────────────────────────┤
//  │  结构冒险/        │  multi_cycle_    │  多周期运算单元（mul_div_unit）     │
//  │  数据冒险         │  stall           │  正在执行乘除指令，尚未产出结果。    │
//  │  (M-extension)    │                  │  后续依赖指令必须等待。             │
//  └──────────────────┴──────────────────┴──────────────────────────────────┘
//
//  注意：控制冒险（分支/跳转）不通过本模块处理。控制冒险由 Hazard_Detection_Unit
//  直接产生 PC_write、IF_ID_write 和 control_mux_sel 信号，在 IF 和 ID/EX
//  阶段插入气泡（NOP）来冲刷错误路径的指令。
//
// ============================================================================
//  二、检测逻辑详解 —— 为什么这些条件会触发停顿？
// ============================================================================
//
//  2.1 指令存储器未就绪（~imem_ready）
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │  imem_ready = 0 表示指令存储器（或 I-Cache）无法在当前周期提供指令。      │
//  │                                                                       │
//  │  触发场景：                                                            │
//  │    - I-Cache 未命中，需要从外部 AXI 总线读取指令行                       │
//  │    - 外部指令存储器响应延迟 > 1 个时钟周期                               │
//  │    - 系统总线仲裁导致取指请求被延迟                                      │
//  │                                                                       │
//  │  停顿理由：                                                            │
//  │    IF 阶段必须等待有效的指令才能推进。如果此时不冻结 PC 和 IF/ID 寄存器，  │
//  │    则 IF/ID 流水线寄存器将锁存一个无效/过期的指令字，ID 阶段会基于错误     │
//  │    的指令进行译码，导致整个流水线执行垃圾指令。                            │
//  │                                                                       │
//  │  影响范围：                                                            │
//  │    - PC 冻结（Program_counter 内部 stall_in=1 时 PC 保持不变）          │
//  │    - IF/ID 流水线寄存器冻结（stall=1 时保持原值不更新）                    │
//  │    - ID/EX 及之后阶段不受直接影响（它们继续处理已锁存的有效指令）           │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  2.2 数据存储器未就绪（~dmem_ready）
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │  dmem_ready = 0 表示数据存储器（或 D-Cache）无法在当前周期完成 Load 或    │
//  │  Store 操作。                                                          │
//  │                                                                       │
//  │  触发场景：                                                            │
//  │    - D-Cache 未命中，需要从外部 AXI 总线读取/写入数据行                   │
//  │    - 外部数据存储器响应延迟 > 1 个时钟周期                               │
//  │    - 总线仲裁冲突（多主设备争用数据总线）                                 │
//  │                                                                       │
//  │  停顿理由：                                                            │
//  │    MEM 阶段是 Load/Store 指令的执行阶段。如果数据尚未就绪就允许流水线      │
//  │    推进，则：                                                          │
//  │    ① Load 指令会将未定义的 mem_rdata 锁存进 MEM/WB 寄存器，并在 WB 阶段   │
//  │       写回垃圾数据到寄存器文件；                                        │
//  │    ② Store 指令可能写入不完整的数据到存储器，破坏内存一致性。              │
//  │                                                                       │
//  │  影响范围：                                                            │
//  │    - 全局 stall=1，冻结 PC、IF/ID、ID/EX（mem_pipeline 和 wb_pipeline   │
//  │      也接收 stall 信号，但它们的实现在 rv32i_core 中 ... 实际上 EX/MEM    │
//  │      和 MEM/WB 在 dmem_ready=0 时也需要保持，否则 MEM 阶段的结果会丢失）   │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  2.3 Load-Use 数据冒险（load_use_stall）
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │  这是经典的"Load-Use 数据冒险"，是五级流水线中唯一一种转发无法解决的数据   │
//  │  冒险。                                                                │
//  │                                                                       │
//  │  冒险产生的时序分析：                                                   │
//  │                                                                       │
//  │    时钟周期:    N       N+1       N+2       N+3       N+4              │
//  │    ───────────────────────────────────────────────────                 │
//  │    lw x1,... :  IF  →  ID  →  EX  →  MEM  →  WB                      │
//  │    add x2,x1,x3:       IF  →  ID  →  EX  →  MEM                      │
//  │                         ↑                                               │
//  │                         └─ 此时 x1 的值还在 MEM 阶段读取中！              │
//  │                           寄存器文件未更新，转发也无法提供（数据尚未到达）  │
//  │                                                                       │
//  │  为什么转发（Forwarding/Bypass）无法解决？                              │
//  │    - EX/MEM 转发（2'b01）：lw 在 EX 阶段并没有产生最终结果 ——             │
//  │      lw 的 EX 阶段只是计算了访存地址（addr = rs1 + imm），不是数据值       │
//  │    - MEM/WB 转发（2'b10）：lw 的数据在 MEM 阶段末尾才从数据存储器读出，    │
//  │      比 add 需要的时机晚一个周期                                         │
//  │    - 因此 add 指令不可能从任何转发路径获得正确的 x1 值                    │
//  │                                                                       │
//  │  检测条件（由 Bypass_unit 产生）：                                      │
//  │    load_use_hazard = ex_load && ex_hit_rs1 || ex_hit_rs2              │
//  │    即：EX 阶段是 Load 指令，且 Load 的目标寄存器 rd 匹配 ID 阶段指令的    │
//  │    rs1 或 rs2（且 rd ≠ x0，因为 x0 是零寄存器永不改变）                   │
//  │                                                                       │
//  │  停顿效果：                                                            │
//  │    load_use_stall=1 → pipeline_stall=1 → PC 冻结 + IF/ID 冻结          │
//  │    + ID/EX flush（通过 control_mux_sel=1），插入一个 NOP 气泡             │
//  │    这相当于让 add 指令在 ID 阶段多停留 1 个周期，等 lw 完成 MEM 阶段      │
//  │    后，在下一周期通过 MEM/WB 转发路径将正确的 x1 值转发给 add。            │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  2.4 多周期运算停顿（multi_cycle_stall）
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │  信号由 mul_div_unit（乘除法单元）产生。RV32M 扩展指令（MUL、MULH、        │
//  │  MULHU、DIV、REM 等）无法在单周期内完成。                                  │
//  │                                                                       │
//  │  触发条件：                                                            │
//  │    mul_div_unit 内部状态机进入 MD_BUSY 状态时，busy 输出保持为 1。        │
//  │    - 乘法通常需要 1~4 个周期（具体取决于实现：booth 乘法器、                  │
//  │      迭代乘法器等）                                                    │
//  │    - 除法通常需要 1~32 个周期（逐位迭代除法算法）                        │
//  │                                                                       │
//  │  停顿理由：                                                            │
//  │    多周期运算未完成时，ALU 输出 m_unit_result 是中间值而非最终结果。如果     │
//  │    不暂停流水线，EX/MEM 寄存器将锁存错误的中间结果，后续指令如果通过转发     │
//  │    使用该结果也会得到错误值。同时乘除法单元的输入（操作数）必须在运算期间     │
//  │    保持稳定。                                                          │
//  │                                                                       │
//  │  与 Load-Use 停顿的交互：                                              │
//  │    如果一条 Load 指令的下一条指令是乘法，且乘法使用 Load 的目标寄存器：     │
//  │    - 先产生 load_use_stall（1 周期）                                    │
//  │    - 然后 multi_cycle_stall 持续多周期                                  │
//  │    - stall_unit 的 OR 逻辑确保在所有停顿条件解除后流水线才恢复推进         │
//  └──────────────────────────────────────────────────────────────────────┘
//
// ============================================================================
//  三、转发路径（Forwarding/Bypass Paths）与停顿的关系
// ============================================================================
//
//  本模块自身不实现转发逻辑（转发在 Bypass_unit.v 中实现），但停顿逻辑与转发
//  逻辑紧密配合，共同解决数据冒险。理解转发路径有助于理解"为什么有些情况需要停顿，
//  而有些情况只需要转发"。
//
//  3.1 转发路径架构
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │                                                                       │
//  │   EX/MEM 转发路径（ex_hit，编码 2'b01）：                                │
//  │   ┌────────┐    ┌────────┐    ┌────────┐    ┌────────┐    ┌────────┐  │
//  │   │   IF   │ → │   ID   │ → │   EX   │ → │  MEM   │ → │   WB   │  │
//  │   └────────┘    └───┬────┘    └───┬────┘    └───┬────┘    └────────┘  │
//  │                     │             │             │                      │
//  │                     │   rs1/rs2   │  mem_alu_result (EX/MEM 流水线输出)│
//  │                     │   匹配?     │             │                      │
//  │                     │             └──── 转发 ──→│ (ALU 结果)            │
//  │                     │   2'b01 = EX/MEM 转发     │                      │
//  │                                                                       │
//  │   MEM/WB 转发路径（mem_hit，编码 2'b10）：                               │
//  │   ┌────────┐    ┌────────┐    ┌────────┐    ┌────────┐    ┌────────┐  │
//  │   │   IF   │ → │   ID   │ → │   EX   │ → │  MEM   │ → │   WB   │  │
//  │   └────────┘    └───┬────┘    └────────┘    └───┬────┘    └───┬────┘  │
//  │                     │   rs1/rs2                 │  wb_write_data       │
//  │                     │   匹配?                   └──── 转发 ──→│         │
//  │                     │   2'b10 = MEM/WB 转发     │  (已完成的 Load/ALU)  │
//  │                                                                       │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  3.2 编码选择与优先级：为什么 ex_hit 优先于 mem_hit？
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │                                                                       │
//  │  转发编码（2-bit，来自 Bypass_unit 的 irmux1/irmux2）：                  │
//  │    2'b00 — 无转发：使用寄存器文件读出的值（ex_rs1_data / ex_rs2_data）   │
//  │    2'b01 — EX/MEM 转发：使用 mem_alu_result（EX 阶段指令的结果）         │
//  │    2'b10 — MEM/WB 转发：使用 wb_write_data（MEM 阶段指令的结果）         │
//  │                                                                       │
//  │  ★ 优先级：EX/MEM > MEM/WB（通过三元运算符的书写顺序实现）                │
//  │                                                                       │
//  │  为什么 EX 优先？                                                      │
//  │    考虑以下指令序列：                                                   │
//  │      add x1, x2, x3     // EX 阶段，x1 = x2+x3                        │
//  │      add x1, x4, x5     // MEM 阶段，x1 = x4+x5（覆盖了上一条的 x1）   │
//  │      sub x6, x1, x7     // ID 阶段，需要 x1 的值                       │
//  │                                                                       │
//  │    sub 需要的是 x4+x5（最新值），而不是 x2+x3（过期值）。                  │
//  │    EX 阶段指令比 MEM 阶段指令晚发出，其结果是逻辑上"更新"的值。             │
//  │    如果 MEM 的匹配优先于 EX，sub 会错误地获得 x2+x3。                     │
//  │                                                                       │
//  │    更一般地说：同一寄存器在连续多条指令中被写入时，最后写入的值               │
//  │    才是当前有效的值。在流水线中，EX 阶段的指令在 MEM 阶段的指令之后发出，   │
//  │    所以 EX 的结果是最新的。                                             │
//  │                                                                       │
//  │  为什么不是 3 条转发路径（为什么不从 WB 阶段转发）？                      │
//  │    WB 阶段的数据（wb_write_data）在时钟上升沿写入寄存器文件。             │
//  │    ID 阶段的寄存器读取在同一个时钟周期内通过组合逻辑完成。                  │
//  │    如果 WB 阶段在本周期写 x1，而 ID 阶段在本周期读 x1：                    │
//  │    - 理想情况下，寄存器文件应该"写优先"或"写穿通"                         │
//  │    - 如果寄存器文件实现了写穿通（write-through），那实际上就是一条           │
//  │      隐式的"WB→ID 转发路径"，无需额外硬件                                │
//  │    - 如果寄存器文件是简单的读优先实现，则需要 WB→ID 转发                   │
//  │    - 本设计中，WB 数据在上升沿写入，ID 读取在同一周期的组合路径中完成，     │
//  │      寄存器文件的写操作实际上覆盖了 WB 阶段的转发需求                     │
//  │    - 如果寄存器文件不支持写穿通，则需要添加第三条转发路径（编码 2'b11）。    │
//  │      当前设计的编码空间预留了 2'b11 可用于此扩展。                        │
//  └──────────────────────────────────────────────────────────────────────┘
//
//  3.3 转发与停顿的协同
//  ┌──────────────────────────────────────────────────────────────────────┐
//  │                                                                       │
//  │  数据冒险的处理策略分层：                                               │
//  │                                                                       │
//  │  冒险场景                              │ 处理方式      │ 开销          │
//  │  ─────────────────────────────────────┼─────────────┼────────────── │
//  │  ID.rs 匹配 MEM.rd（非 Load 指令）      │ MEM/WB 转发  │ 0 周期停顿     │
//  │  ID.rs 匹配 EX.rd（非 Load/Store 指令） │ EX/MEM 转发  │ 0 周期停顿     │
//  │  ID.rs 匹配 EX.rd（Load 指令）          │ 停顿 1 周期   │ 1 周期停顿     │
//  │                                       │ + MEM/WB 转发 │               │
//  │  ID.rs 匹配 EX.rd（Store 指令）         │ 无冒险        │ 0 周期停顿     │
//  │                                       │（Store 不写 rd）│              │
//  │                                                                       │
//  │  工作流程：                                                            │
//  │    1. Bypass_unit 在 ID 阶段检测所有数据依赖关系                         │
//  │    2. 对于非 Load 的 RAW 冒险 → 产生转发选择码（irmux1/irmux2）           │
//  │    3. 对于 Load-Use 冒险 → 产生 load_use_hazard → 设置 load_use_stall    │
//  │    4. stall_unit 将 load_use_stall 合并到全局 pipeline_stall             │
//  │    5. 停顿 1 周期后，Load 数据通过 MEM/WB 转发路径送达                   │
//  │                                                                       │
//  │  ★ Bypass_unit 和 Hazard_Detection_Unit 双重检测：                      │
//  │    两个模块都检测了 Load-Use 冒险，这是冗余设计确保安全。                  │
//  │    - Bypass_unit 产生 stall_out → stall_unit → pipeline_stall          │
//  │    - Hazard_Detection 产生 PC_write/IF_ID_write/control_mux_sel         │
//  │    - 两者共同作用：stall 冻结 IF 阶段 + flush 清空 ID/EX 插入气泡          │
//  └──────────────────────────────────────────────────────────────────────┘
//
// ============================================================================
//  四、与流水线各阶段的交互
// ============================================================================
//
//  pipeline_stall 信号驱动整条流水线进入停顿状态。以下是各个阶段的具体行为：
//
//  ┌────────────────┬─────────────────────────────────────────────────────┐
//  │  流水线阶段      │  pipeline_stall=1 时的行为                          │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  IF (取指)      │  Program_counter: PC 保持不变（stall_in=1，输出       │
//  │                │  不更新）。                                         │
//  │                │  instruction_memory: 继续提供当前地址的指令（PC 未变  │
//  │                │  所以地址端口稳定，在停顿周期内指令输出保持）。          │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  IF/ID 寄存器   │  if_id_stage: stall=1 → 保持当前锁存的指令不变。       │
//  │                │  这确保了 ID 阶段在停顿期间看到的指令不会改变，避免了   │
//  │                │  错误地消费一个尚未就绪的新指令。                       │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  ID (译码)      │  组合逻辑，无状态。Decoder、control_unit、reg_file     │
//  │                │  继续根据锁存的指令产生控制信号和寄存器数据。            │
//  │                │  Bypass_unit: stall_out 在此周期仍然有效（load_use_   │
//  │                │  stall 信号可能仍然为 1），保持停顿状态。               │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  ID/EX 寄存器   │  id_ex_stage: stall=1 → 保持当前锁存的值不变。          │
//  │                │  ★ 同时在 Load-Use 冒险中，control_mux_sel=1 会导致    │
//  │                │    flush=1，即 ID/EX 控制信号被清零（插入 NOP 气泡），   │
//  │                │    数据通路保持。这确保 EX 阶段在停顿周期执行一次 NOP。   │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  EX (执行)      │  ALU: 在停顿周期仍然计算（基于锁存的 ID/EX 输出）。      │
//  │                │  mul_div_unit: 如果正在执行多周期运算，此周期继续计算；  │
//  │                │    如果 multi_cycle_stall 是停顿源，则 busying 状态     │
//  │                │    保持不变直到完成。                                 │
//  │                │  branch_unit: 在停顿周期继续判断分支（不影响，因为        │
//  │                │    PC 已被冻结不会执行跳转）。                          │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  EX/MEM 寄存器  │  ex_mem_stage: stall=1 → 保持当前值不变（和 ID/EX     │
//  │                │    类似）。                                           │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  MEM (访存)     │  Load_unit / Store_unit: 组合逻辑，无独立状态。         │
//  │                │  在 dmem_ready=0 的情况下（本模块的停顿来源之一），      │
//  │                │  mem_rdata 可能尚未有效，但 Load_unit 不依赖状态，      │
//  │                │  只是等待输入稳定；EX/MEM 寄存器冻结保证输入稳定。      │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  MEM/WB 寄存器  │  mem_wb_stage: stall=1 → 保持当前值不变。               │
//  ├────────────────┼─────────────────────────────────────────────────────┤
//  │  WB (写回)      │  纯组合逻辑 MUX，无状态。wb_write_data 根据 wb_wbmux    │
//  │                │  选择 ALU 结果 / Load 数据 / PC+4。                     │
//  │                │  在 dmem_ready=0 的停顿中，wb_write_data 来自上一周期    │
//  │                │  锁存的有效数据，不会出现 Load 数据未就绪的问题（因为        │
//  │                │  MEM/WB 寄存器被冻结了）。                              │
//  └────────────────┴─────────────────────────────────────────────────────┘
//
//  关键设计要点：
//  - pipeline_stall 冻结整个流水线的前端（IF、IF/ID、ID/EX），后端（EX/MEM、
//    MEM、MEM/WB、WB）同时也被冻结或不受影响（通过控制信号关闭产生副作用）。
//  - stall=1 时，所有流水线寄存器的输出保持稳定，EX 阶段的组合逻辑持续计算
//    但结果不会被锁存进下一个流水线寄存器（因为下一个寄存器也被冻结了）。
//  - 停顿释放后，流水线从断点继续执行，不会丢失任何指令的状态。
//
// ============================================================================
//  五、信号约定与扩展方式
// ============================================================================
//
//  ★ 电平约定（重要）：
//    - 所有 *_ready 信号：高有效，1=就绪 / 0=未就绪（需停顿）
//    - 所有 *_stall 信号：高有效，1=请求停顿 / 0=正常操作
//    - 输出 stall：        高有效，1=停顿流水线 / 0=正常推进
//
//  ★ 停顿来源汇总（共 4 类，任何一项触发即全局停顿）：
//    ┌──────┬───────────────────┬──────────┬─────────────────────┐
//    │ 编号  │  信号              │  有效电平  │  来源模块            │
//    ├──────┼───────────────────┼──────────┼─────────────────────┤
//    │  1   │  imem_ready       │  0=停顿   │  instruction_memory  │
//    │  2   │  dmem_ready       │  0=停顿   │  数据存储器/AXI 总线  │
//    │  3   │  load_use_stall   │  1=停顿   │  Bypass_unit         │
//    │  4   │  multi_cycle_stall│  1=停顿   │  mul_div_unit        │
//    └──────┴───────────────────┴──────────┴─────────────────────┘
//
//  ★ 扩展方式：
//    新增停顿来源只需：
//    1. 添加一个输入端口（单 bit 或向量）
//    2. 在 assign stall 行追加一个 OR 项
//    3. 单 bit 信号可直接 OR（& | ^）
//       多 bit 向量需先做归约（如 |my_vector），即任何一位为 1 则认为有效
//    4. 示例：要添加"UART 发送忙碌"停顿：
//       .uart_tx_busy(uart_tx_busy),   // 新增端口
//       assign stall = ... | uart_tx_busy;   // 追加 OR 项
//

module stall_unit (
    // ========================================================================
    //  外部存储器就绪信号（高有效：1=就绪，0=等待/忙碌）
    //  ★ 这些信号低有效时表示"未就绪"，所以内部使用 ~ 取反后参与 OR
    // ========================================================================
    input  wire imem_ready,          // 指令存储器就绪（来自 instruction_memory 或 I-Cache）
    input  wire dmem_ready,          // 数据存储器就绪（来自 D-Cache 或 AXI 总线 acknowledge）

    // ========================================================================
    //  内部流水线冒险停顿请求（高有效：1=请求停顿，0=正常）
    //  ★ 这些信号是"断言为 1"时请求停顿，可以直接参与 OR，无需取反
    // ========================================================================
    input  wire load_use_stall,      // Load-Use 数据冒险停顿请求（来自 Bypass_unit / Hazard_Detection_Unit）
                                     // 条件：EX 阶段是 Load 且其 rd 匹配 ID 阶段 rs1/rs2 且 rd≠x0
    input  wire multi_cycle_stall,   // 多周期运算未完成（来自 mul_div_unit 的 busy 输出）
                                     // 条件：乘除法单元内部状态机处于 MD_BUSY 状态

    // ========================================================================
    //  统一停顿输出（高有效：1=暂停流水线，0=正常推进）
    //  连接目标：
    //    - Program_counter.stall_in（冻结 PC）
    //    - IF_IDpipeline.stall（冻结 IF/ID 寄存器）
    //    - ID_EXpipeline.stall（冻结 ID/EX 寄存器）
    //    - EX_Mempipeline.stall（冻结 EX/MEM 寄存器）
    //    - mem_pipeline.stall（冻结 MEM/WB 寄存器）
    // ========================================================================
    output wire stall
);

    // ========================================================================
    //  统一 OR 归约逻辑
    // ========================================================================
    //
    //  逻辑表达式：
    //    stall = ~imem_ready | ~dmem_ready | load_use_stall | multi_cycle_stall
    //
    //  逐项分析：
    //    ~imem_ready:       imem_ready=0 时此项为 1 → 取指未就绪，需停顿
    //                       典型场景：I-Cache miss、外部存储器访问延迟
    //    ~dmem_ready:       dmem_ready=0 时此项为 1 → 访存未就绪，需停顿
    //                       典型场景：D-Cache miss、Load/Store 等待总线响应
    //    load_use_stall:    来自 Bypass_unit 的 Load-Use 数据冒险检测
    //                       当 bypass_unit 检测到 EX 阶段是 Load 且其 rd
    //                       被 ID 阶段指令使用时置 1
    //    multi_cycle_stall: 来自 mul_div_unit 的 busy 信号
    //                       当乘除法单元正在迭代计算时置 1，确保结果稳定
    //
    //  为什么使用按位 OR（|）而非逻辑 OR（||）？
    //    - 这里所有操作数都是单 bit 信号，| 和 || 等价
    //    - 使用 | 是出于编码风格的一致性（该设计在信号归约时统一使用 |）
    //    - 如果将来多 bit 信号参与归约（如 |multi_bit_signal），| 语法可以直接
    //      扩展而无需修改运算符
    //
    //  组合逻辑特性：
    //    - 本模块纯组合逻辑，无时钟输入，无状态寄存器
    //    - stall 输出是输入的即时函数，延迟为一个 OR 门 + 两个 NOT 门的传播延迟
    //    - 这保证了停顿请求的快速响应：在同一个时钟周期内，停顿源的变化立即反映
    //      到流水线控制信号上
    //
    assign stall = ~imem_ready          // 指令存储器等待（结构冒险）
                 | ~dmem_ready          // 数据存储器等待（结构冒险）
                 | load_use_stall       // Load-Use 冒险（数据冒险，转发无法解决）
                 | multi_cycle_stall;   // 多周期运算（M 扩展乘除指令，结构/数据冒险）

endmodule
