// ============================================================
//  mul_div_unit.v — RV32M 乘除法单元 (Multiply/Divide Unit)
//
//  功能概述:
//    本模块实现 RISC-V RV32M 标准扩展的全部 8 条指令:
//      MUL / MULH / MULHSU / MULHU  — 单周期组合逻辑乘法
//      DIV / DIVU / REM / REMU      — 多周期迭代除法
//
//  设计决策:
//    1. 乘法采用 Verilog 内建 '*' 运算符, 综合工具将其映射到 DSP 块或
//       组合逻辑乘法器。乘法结果在同周期内通过组合逻辑输出,
//       因此乘法不产生流水线停顿 (busy 保持为 0)。
//
//    2. 除法采用 32 位恢复余数法 (restoring division), 每周期产生 1 位商,
//       共需 33 个时钟周期 (32 次迭代 + 1 个 DONE 周期)。
//       选择该算法的原因:
//       (a) 面积最小 — 仅需一个 32 位减法器和一个 64 位移位寄存器;
//       (b) 控制逻辑简单, 易于验证;
//       (c) 除法指令在典型工作负载中占比较低,
//           面积优先于吞吐率是合理的取舍。
//
//    3. 结果输出采用混合方式: 乘法结果为组合逻辑输出 (同周期可用),
//       除法结果为寄存器输出 (多周期计算完成后锁存)。通过 result 端口的
//       MUX 统一对外接口, 上层只需检查 busy 信号即可。
//
//    4. 模块内部自行处理绝对值、符号翻转、除零和溢出等边界条件,
//       完全符合 RISC-V 特权架构规范。
//
//  状态机概览:
//    S_IDLE (2'd0) → S_CALC (2'd1) → S_DONE (2'd2) → S_IDLE
//
//    乘法路径: S_IDLE 内组合逻辑计算完即返回, 不进入后续状态。
//    除法路径: S_IDLE(锁存) → S_CALC(32次迭代) → S_DONE(符号修正) → S_IDLE
//
//  集成接口:
//    - 上层控制器应在 busy=1 期间保持流水线停顿,
//      且不得在此期间发出新的 start 脉冲。
//    - result 端口在 busy=0 且本次运算完成后方可采样。
// ============================================================

`include "Def.v"

module mul_div_unit (
    input  wire        clk,
    input  wire        reset,
    input  wire [31:0] op_a,          // 操作数 A (被乘数 / 被除数)
    input  wire [31:0] op_b,          // 操作数 B (乘数 / 除数)
    input  wire [3:0]  md_op,         // 微操作码, 来自 Def.v 中的 ALU_MUL/.../ALU_REMU 宏定义
    input  wire [2:0]  funct3_in,     // ★ funct3 字段，用于区分 DIV(funct3=100) 和 REM(funct3=110)
                                       //   【Bug 修复】原设计中 DIV/REM 共用 aluop 编码，
                                       //   导致 mul_div_unit 无法区分除法与取余。
                                       //   新增 funct3_in 端口解决此问题。
    input  wire        start,         // 启动脉冲, 高有效, 由译码级发出
    output wire [31:0] result,        // ★ 乘法: 组合逻辑输出 (同周期); 除法: 寄存器输出 (多周期后)
    output reg         busy           // 忙标志: 为 1 时流水线必须停顿, 且不得再次 start
);

    // ============================================================
    //  乘法逻辑 — 单周期组合路径
    //
    //  同时生成三种乘积 (有符号×有符号、无符号×无符号、有符号×无符号),
    //  由操作码选择对应结果的低 32 位或高 32 位。
    //
    //  MUL:    有符号低 32 位 (有符号乘积截断)
    //  MULH:   有符号高 32 位
    //  MULHSU: 有符号×无符号高 32 位
    //  MULHU:  无符号高 32 位
    //
    //  注: 有符号与无符号乘积的低 32 位完全相同(二进制等价),
    //       因此 MUL 可以直接使用 signed×signed 乘积的低 32 位。
    // ============================================================

    wire signed [31:0] sa = op_a;
    wire signed [31:0] sb = op_b;
    wire [63:0] prod_ss = sa * sb;                 // 有符号 × 有符号 = 64 位
    wire [63:0] prod_uu = op_a * op_b;             // 无符号 × 无符号 = 64 位
    wire [63:0] prod_su = sa * {1'b0, op_b};       // 有符号 × 无符号 = 64 位
                                                    //   (op_b 高位补 0 视为无符号)

    // 操作码判别: 是否为乘法指令
    wire is_mul = (md_op == `ALU_MUL) || (md_op == `ALU_MULH) ||
                  (md_op == `ALU_MULHSU) || (md_op == `ALU_MULHU);

    // 根据操作码选择乘法结果
    wire [31:0] mul_res =
        (md_op == `ALU_MUL)    ? prod_ss[31:0]  :   // 有符号低 32 位
        (md_op == `ALU_MULH)   ? prod_ss[63:32] :   // 有符号高 32 位
        (md_op == `ALU_MULHSU) ? prod_su[63:32] :   // s×u 高 32 位
        (md_op == `ALU_MULHU)  ? prod_uu[63:32] :   // 无符号高 32 位
                                 32'h0;              // 除法时此信号无意义

    wire is_div = !is_mul;       // 除法指令标志

    // ============================================================
    //  除法 FSM — 恢复余数法 (Restoring Division)
    //
    //  状态编码:
    //    S_IDLE (2'd0): 空闲态, 等待 start 脉冲
    //    S_CALC  (2'd1): 迭代计算态, 每周期产生 1 位商
    //    S_DONE (2'd2): 完成态, 符号修正后回到 IDLE
    //
    //  算法原理 (恢复余数法):
    //    设被除数为 N (32位), 除数为 D (32位), 则除法等价于求解:
    //      N = Q × D + R,  其中 0 ≤ R < |D|
    //
    //    维护 64 位寄存器对 {A, Q}, 其中:
    //      A (32位) — 余数寄存器
    //      Q (32位) — 商寄存器 (逐位构建)
    //
    //    每轮迭代:
    //      1. {A, Q} 联合左移 1 位, 被除数的高位逐位移入 A
    //      2. 从 A 中减去除数 D
    //      3. 若差 ≥ 0 → Q[0] = 1, 保留减法结果 (A = A - D)
    //         若差 < 0  → Q[0] = 0, 恢复 A     (A 保持移位值)
    //      4. 重复 32 次, Q 中即为商, A 中即为余数
    //
    //  符号处理策略:
    //    内部始终以无符号绝对值运算; 在 S_IDLE 中将被除数和除数
    //    取绝对值存入 Q 和 D; 在 S_DONE 中根据原始符号翻转结果。
    //    这个策略避免了在迭代过程中处理符号, 大幅简化控制逻辑。
    //
    //  时序 (除法):
    //    start 有效 → 下一拍进入 S_CALC (cnt=0)
    //    经过 32 个 S_CALC 周期 (cnt=0 到 cnt=31)
    //    第 33 拍进入 S_DONE, 完成符号修正
    //    第 34 拍回到 S_IDLE, busy 清零, 结果可用
    //    总计: 从 start 到结果可用 = 33 个周期 (含最后一拍锁存)
    // ============================================================

    localparam S_IDLE = 2'd0, S_CALC = 2'd1, S_DONE = 2'd2;

    reg [1:0]  s;                   // 当前状态
    reg [5:0]  cnt;                 // 迭代计数器, 0..31 (32 次迭代)
    reg [31:0] A;                   // 余数寄存器 (upper 32 of {A,Q})
    reg [31:0] Q;                   // 商寄存器   (lower 32 of {A,Q})
    reg [31:0] D;                   // 除数 (取绝对值后的正值)
    reg        calc_rem;            // 1=REM/REMU (返回余数), 0=DIV/DIVU (返回商)
    reg        flip_quo;            // 商需要取反 (有符号除法且符号位相异)
    reg        flip_rem;            // 余数需要取反 (有符号取余且被除数为负)
    reg [31:0] div_result_reg;      // 除法结果寄存器 (乘法也会锁存一份至此)

    // ============================================================
    //  结果输出 MUX
    //
    //  乘法: S_IDLE 状态下 start 有效且 is_mul 时,
    //        直接输出组合逻辑乘法结果 (同周期可用)。
    //  除法及其他情况: 输出 div_result_reg (寄存器值)。
    //
    //  这个设计保证了乘法结果零延迟 (组合路径直通),
    //  而除法结果在 DONE 态锁存后稳定输出。
    // ============================================================
    assign result = (s == S_IDLE && start && is_mul) ? mul_res : div_result_reg;

    // ============================================================
    //  主时序逻辑 — FSM + 数据通路
    // ============================================================
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // 复位: 回到 IDLE, 清空所有寄存器和控制标志
            s <= S_IDLE;
            busy <= 1'b0;
            div_result_reg <= 32'h0;
            cnt <= 6'd0;
            A <= 32'h0;
            Q <= 32'h0;
            D <= 32'h0;
            calc_rem <= 1'b0;
            flip_quo <= 1'b0;
            flip_rem <= 1'b0;
        end
        else begin
            case (s)

                // --------------------------------------------------
                //  S_IDLE — 空闲状态
                //
                //  行为:
                //    - 保持 busy=0, 表示可以接受新指令
                //    - 乘法: 组合逻辑已在 result 端口就绪,
                //            此处将 mul_res 锁存到 div_result_reg
                //            (寄存器版本也保存, 便于统一调试/追踪)
                //    - 除法: 锁存操作数、取绝对值、设置符号标志,
                //            处理特殊边界条件 (除零、溢出);
                //            正常情况下将 busy 置 1 并跳转 S_CALC。
                //
                //  转移条件:
                //    start && is_mul → 保持在 S_IDLE (单周期完成)
                //    start && is_div → 特殊情况: 留在 S_IDLE;
                //                       普通情况: → S_CALC
                // --------------------------------------------------
                S_IDLE: begin
                    busy <= 1'b0;                    // 空闲态, 流水线不阻塞
                    if (start && is_mul) begin
                        // 乘法: 同一拍内组合逻辑已有结果,
                        //       此处额外锁存一份到寄存器方便 tracing
                        div_result_reg <= mul_res;
                    end
                    else if (start && is_div) begin
                        // — 确定操作类型 —
                        // ★【Bug 修复】原代码用 md_op 判断 REM/DIV:
                        //   calc_rem <= (md_op == ALU_REM) || (md_op == ALU_REMU);
                        //   但 ALU_DIV == ALU_REM (4'b1110) 且 ALU_DIVU == ALU_REMU (4'b1111),
                        //   导致 DIV 也被误判为取余。现改用 funct3_in 区分。
                        // REM / REMU (funct3=110/111): 最终返回余数 (A)
                        // DIV / DIVU (funct3=100/101): 最终返回商   (Q)
                        calc_rem <= (funct3_in == `MULDIV_REM) || (funct3_in == `MULDIV_REMU);

                        // — 确定结果符号是否需要翻转 (仅有符号操作) —
                        // 商的符号 = 被除数符号 XOR 除数符号 (标准补码除法)
                        flip_quo <= (funct3_in == `MULDIV_DIV) && (op_a[31] ^ op_b[31]);
                        // 余数的符号 = 被除数符号 (RISC-V 规范)
                        flip_rem <= (funct3_in == `MULDIV_REM) && op_a[31];

                        // — 被除数取绝对值存入 Q —
                        // 仅在有符号操作 (DIV/REM) 且符号位为 1 时取反,
                        // 无符号操作 (DIVU/REMU) 直接原值存入。
                        if ((funct3_in == `MULDIV_DIV || funct3_in == `MULDIV_REM) && op_a[31])
                            Q <= ~op_a + 1'b1;       // 二进制补码取反: -x = ~x + 1
                        else
                            Q <= op_a;

                        // — 除数取绝对值存入 D —
                        // 同上, 有符号且符号位为 1 时取反。
                        if ((funct3_in == `MULDIV_DIV || funct3_in == `MULDIV_REM) && op_b[31])
                            D <= ~op_b + 1'b1;
                        else
                            D <= op_b;

                        A <= 32'h0;                  // 余数初值为 0
                        cnt <= 6'd0;                 // 迭代计数器清零

                        // — 特殊边界条件处理 —
                        //
                        // 1. 除零 (op_b == 0):
                        //    RISC-V 规范: DIV/DIVU 返回全 1 (0xFFFFFFFF),
                        //                REM/REMU 返回被除数
                        //    此时运算已完成, 不进入 S_CALC, busy 保持 0。
                        //
                        // 2. 有符号溢出 (-2^31 / -1):
                        //    0x80000000 / 0xFFFFFFFF, 理论结果为 +2^31
                        //    但 32 位补码无法表示 +2^31 (最大正值 2^31-1),
                        //    RISC-V 规范: DIV 返回 0x80000000 (即 -2^31, 饱和),
                        //                REM 返回 0
                        //    同样直接完成, 不进入迭代。
                        if (op_b == 32'h0) begin
                            // 除零处理
                            if (md_op == `ALU_REM || md_op == `ALU_REMU)
                                div_result_reg <= op_a;          // REM: 返回被除数
                            else
                                div_result_reg <= 32'hFFFF_FFFF; // DIV: 返回全 1
                        end
                        else if ((md_op == `ALU_DIV) && op_a == 32'h8000_0000 && op_b == 32'hFFFF_FFFF) begin
                            // 有符号溢出: -2^31 / -1
                            div_result_reg <= 32'h8000_0000;     // 商 = -2^31 (饱和)
                        end
                        else if ((md_op == `ALU_REM) && op_a == 32'h8000_0000 && op_b == 32'hFFFF_FFFF) begin
                            // 有符号溢出: -2^31 % -1
                            div_result_reg <= 32'h0;             // 余数 = 0
                        end
                        else begin
                            // 普通除法: 设置 busy 并进入迭代
                            busy <= 1'b1;
                            s <= S_CALC;
                        end
                    end
                end

                // --------------------------------------------------
                //  S_CALC — 除法迭代计算状态
                //
                //  算法步骤 (恢复余数法, 每周期 1 位):
                //
                //  第 1 步: 联合左移 {A, Q}
                //    将 {A[30:0], Q[31]} 构成新的 A (移位后的高 32 位),
                //    Q[30:0] 左移并在 LSB 填 0 构成新的低 32 位。
                //    效果: 被除数的高位逐位"喂入"余数寄存器。
                //
                //    实现: {A, Q} <= {A[30:0], Q[31], Q[30:0], 1'b0};
                //    这是非阻塞赋值, 先于条件判断执行。
                //
                //  第 2 步: 比较与减法 (使用移位前的老值计算移位后的 A)
                //    比较对象: shifted_A = {old_A[30:0], old_Q[31]} vs old_D
                //    若 shifted_A >= D:
                //      → Q[0] = 1, A = shifted_A - D
                //        减法"成功", 新余数为正或零, 保留减法结果。
                //    若 shifted_A < D:
                //      → Q[0] = 0, A = shifted_A (不变, "恢复"到移位值)
                //        减法"失败", 恢复余数为移位前的值。
                //
                //    Q[0] = 0 的恢复动作由第 1 步的非阻塞赋值隐式完成:
                //    因条件为假时 if 分支不执行, A 保持 {A, Q} <= ... 的
                //    非阻塞赋值结果 (即纯移位值), 等价于"减 D → 发现为负
                //    → 加回 D"的恢复操作, 但省去了一次加法器。
                //
                //  迭代计数:
                //    cnt 从 0 递增到 31, 每周期产生 1 位商存入 Q 的 LSB。
                //    当 cnt == 31 (第 32 次迭代) 的时钟边沿到来时,
                //    下一状态为 S_DONE。
                //
                //  循环次数: 32 次迭代 (cnt = 0,1,...,31)
                //  原因: 32 位被除数需要 32 次左移才能完全移入余数寄存器,
                //        每次移位后比较一次, 共产生 32 位商。
                //
                //  转移条件:
                //    cnt == 31 → S_DONE   (迭代完成)
                //    否则       → 保持 S_CALC
                // --------------------------------------------------
                S_CALC: begin
                    // 第 1 步: {A, Q} 联合左移 1 位
                    // 移位操作使用旧值计算, 非阻塞赋值到 {A, Q}
                    {A, Q} <= {A[30:0], Q[31], Q[30:0], 1'b0};

                    // 第 2 步: 比较移位后的 A (用老值表达) 与除数 D
                    // $signed 比较, 兼容有符号无符号统一处理 (D 已是正数)
                    // {A[30:0], Q[31]} 就是移位操作后新 A 的值 (用老变量表达)
                    if ($signed({A[30:0], Q[31]}) >= $signed(D)) begin
                        // 减法成功: A = shifted_A - D, Q[0] = 1
                        A <= {A[30:0], Q[31]} - D;
                        Q <= {Q[30:0], 1'b1};
                    end
                    // else: 减法失败, A 保持移位值 (Q[0] 已被移位填 0),
                    //       非阻塞赋值自动实现"恢复"

                    cnt <= cnt + 6'd1;

                    if (cnt == 6'd31) begin
                        // 已完成 32 轮迭代 (cnt 0→31),
                        // 下一拍进入 DONE 做符号修正
                        s <= S_DONE;
                    end
                end

                // --------------------------------------------------
                //  S_DONE — 除法完成 & 符号修正
                //
                //  功能:
                //    经过 S_CALC 的 32 次迭代:
                //      Q 中存有商的绝对值 (无符号形式)
                //      A 中存有余数的绝对值 (无符号形式)
                //    本状态根据之前锁存的符号标志, 将结果翻转为
                //    正确的有符号表示, 并写入 div_result_reg。
                //
                //  输出选择:
                //    若 calc_rem == 1 (REM/REMU) → 输出余数 A
                //    若 calc_rem == 0 (DIV/DIVU) → 输出商 Q
                //
                //  符号修正:
                //    仅当相应的 flip_quo / flip_rem 为 1 时取反,
                //    无符号操作时这些标志恒为 0, 结果不变。
                //
                //  转移条件:
                //    无例外 → S_IDLE (下一拍空闲)
                // --------------------------------------------------
                S_DONE: begin
                    s <= S_IDLE;
                    busy <= 1'b0;                    // 解除流水线阻塞

                    if (calc_rem) begin
                        // 余数指令: 输出 A, 视需要翻转符号
                        div_result_reg <= flip_rem ? (~A + 1'b1) : A;
                    end
                    else begin
                        // 除法指令: 输出 Q, 视需要翻转符号
                        div_result_reg <= flip_quo ? (~Q + 1'b1) : Q;
                    end
                end

            endcase
        end
    end

    // ============================================================
    //  关键路径 (Critical Path) 分析
    //
    //  乘法路径 (组合逻辑):
    //    op_a, op_b → 32×32 乘法器(→64位) → MUX → result
    //    此路径无寄存器, 是所有运算中最长的组合路径,
    //    决定了模块能达到的最高时钟频率。
    //    若时序违例, 可考虑在乘法输出端加一级流水寄存器,
    //    但这会使乘法也变为多周期 (需要修改上层接口)。
    //
    //  除法路径 (寄存器到寄存器):
    //    {A, Q} 移位 → 比较器(32位) → 减法器(32位) → MUX → A/Q寄存器
    //    此路径较短, 通常不会成为时序瓶颈。
    //
    //  result MUX:
    //    s, start, md_op → 控制逻辑 → MUX选择 → result
    //    控制路径较浅, 不影响整体时序。
    // ============================================================

    // ============================================================
    //  集成说明 (Integration)
    //
    //  1. 与执行单元的接口:
    //     - 执行单元在译码级检测到 RV32M 指令后,
    //       将 op_a, op_b, md_op 送入本模块,
    //       同时拉起 start 信号 (持续 1 个周期)。
    //     - 执行单元必须采样 busy 信号:
    //         busy=0 时: 乘法结果同周期可用, 除法结果在 busy 下降沿可用
    //         busy=1 时: 流水线必须停顿, 且不得发起新的 start
    //
    //  2. 与流水线控制器的交互:
    //     - 建议在流水线 stall 逻辑中加入:
    //         stall |= (mul_div_unit_busy);
    //     - 乘法不产生停顿, 除法停顿 33 个周期。
    //
    //  3. 结果采样:
    //     - 乘法: result 在 start 同周期有效, 组合逻辑输出。
    //     - 除法: 采样时机为 busy 下降沿所在周期 (S_DONE → S_IDLE),
    //            此时 div_result_reg 已更新, result 端口稳定。
    //
    //  4. 操作码宏定义 (Def.v 中定义):
    //       `ALU_MUL    4'b1010
    //       `ALU_MULH   4'b1011
    //       `ALU_MULHSU 4'b1100
    //       `ALU_MULHU  4'b1101
    //       `ALU_DIV    4'b1110  (与 ALU_REM 共用编码，通过 funct3_in 区分)
    //       `ALU_DIVU   4'b1111  (与 ALU_REMU 共用编码，通过 funct3_in 区分)
    //       `ALU_REM    4'b1110
    //       `ALU_REMU   4'b1111
    //     MULDIV 宏: MULDIV_MUL=3'b000, MULDIV_MULH=3'b001, ..., MULDIV_REMU=3'b111
    // ============================================================

    // ============================================================
    //  已知局限与改进方向 (Known Issues / Limitations)
    //
    //  1. 非流水线化, 不可重入 (No pipelining / No re-entrancy):
    //     除法运算期间 (busy=1), 本模块不能接受新的除法请求。
    //     若上层控制器未正确发出停顿, 会导致状态混乱。
    //
    //  2. 除法吞吐率低 (Low throughput):
    //     每 33 个周期只能完成一次 32 位除法, 等效吞吐率为 ~0.03 IPC
    //     (除法指令占比)。对于除法密集的工作负载 (如密码学),
    //     可考虑升级为基-4 或基-16 除法器, 或使用乘法器实现 Newton-Raphson
    //     除法以利用已有 DSP 资源。
    //
    //  3. 乘法组合路径可能成为频率瓶颈:
    //     32×32 乘法器的组合延迟在低频设计中 (如 ~50 MHz FPGA) 通常可接受,
    //     但在高频 (>200 MHz) 或 ASIC 工艺下可能需要额外流水级。
    //     当前设计没有在乘法输出端加寄存器以保持单周期吞吐。
    //
    //  4. 恢复余数法的面积-性能权衡:
    //     恢复余数法面积最小但速度最慢。如果面积预算允许,
    //     可采用不恢复余数法 (non-restoring) 减少每次迭代的
    //     比较逻辑, 或采用 SRT 除法器实现更高的基值。
    //
    //  5. 无形式验证属性:
    //     当前代码中未包含 SystemVerilog Assertions (SVA),
    //     建议在验证环境中添加:
    //       - busy 与状态一致性断言
    //       - 除法结果正确性 (与参考模型比对)
    //       - 除零行为断言
    //       - 溢出饱和断言
    //
    //  6. 无低功耗优化:
    //     乘法器在除法运算期间仍在持续组合翻转 (输入 op_a/op_b 可能变化),
    //     产生不必要的动态功耗。可在 is_div 期间通过门控关闭
    //     乘法器输入以降低功耗 (需额外 MUX/AND 门控逻辑)。
    // ============================================================

endmodule
