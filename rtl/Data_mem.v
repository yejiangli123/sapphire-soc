// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  Data_mem.v — RISC-V 数据存储器 (Data Memory)                                ║
// ║                                                                            ║
// ║  【模块用途】                                                                ║
// ║  本模块实现 RISC-V 处理器的数据存储器，用于执行 load/store 指令。               ║
// ║  支持以下访存操作：                                                           ║
// ║    LB  (3'b000): 读字节 — 符号扩展至 32 位                                   ║
// ║    LH  (3'b001): 读半字 — 符号扩展至 32 位                                   ║
// ║    LW  (3'b010): 读字   — 完整 32 位                                        ║
// ║    LBU (3'b100): 读字节 — 零扩展至 32 位                                     ║
// ║    LHU (3'b101): 读半字 — 零扩展至 32 位                                     ║
// ║    SB  (3'b000): 写字节                                                      ║
// ║    SH  (3'b001): 写半字                                                      ║
// ║    SW  (3'b010): 写字                                                        ║
// ║                                                                            ║
// ║  【访存时序】                                                                ║
// ║  - 写操作：1 个时钟周期（组合逻辑生成字节使能 + 寄存器写入）                     ║
// ║  - 读操作：2 个时钟周期（第 1 周期锁存地址，第 2 周期送出数据）                  ║
// ║  - 背靠背读：通过 pending 寄存器缓存第二个请求，流水线无气泡                     ║
// ║                                                                            ║
// ║  【设计决策说明】                                                             ║
// ║  1. 为什么读操作需要 2 周期（寄存器读，而非组合逻辑读）？                         ║
// ║     组合逻辑读（地址→数据直通）虽然只需 1 周期，但关键路径包括：                  ║
// ║       地址生成 → 存储器读出 → 对齐/扩展 → 回写寄存器                            ║
// ║     这条路径在较大的存储器或较高频率下会成为时序瓶颈。                            ║
// ║     采用寄存器读（第 1 周期锁存地址，第 2 周期从寄存器阵列读出）将路径              ║
// ║     切为两段，显著降低关键路径延迟，有利于时序收敛。                              ║
// ║     代价是增加 1 周期延迟，但在典型 RISC-V 流水线中可通过转发或停顿来吸收。        ║
// ║                                                                            ║
// ║  2. 为什么需要字节使能 (Byte Enable) 设计？                                   ║
// ║     RISC-V 的 SB/SH/SW 指令要求按字节/半字/字粒度写入存储器。                    ║
// ║     字节使能信号 (be[3:0]) 精确控制 32 位字中的哪些字节被改写，                   ║
// ║     从而实现"读-修改-写"效果：先读出整个字，用写数据的对应字节替换，               ║
// ║     再将修改后的字写回。这保证未涉及的字节不会被意外覆盖。                         ║
// ║                                                                            ║
// ║  3. 为什么需要背靠背 (Back-to-Back) 机制？                                    ║
// ║     如果处理器连续发出读请求（例如 lw x1, 0(x2); lw x3, 4(x4)），              ║
// ║     没有 pending 机制的话，第二条读指令必须等待 mem_ready 变高后才能发出，         ║
// ║     会产生一个空闲周期（气泡）。pending 寄存器允许在第一个读尚未完成时就             ║
// ║     捕获第二个读的地址/控制信息，第一个读完成后立即启动第二个读，实现流水无气泡。     ║
// ║     注意：仅当地址不同时才触发 pending（word_addr != addr_reg），                  ║
// ║     相同地址的背靠背读不需要重新访问存储器（可进一步优化为转发，但当前未实现）。      ║
// ║                                                                            ║
// ║  【存储器组织与寻址】                                                          ║
// ║  - 物理存储：mem[0:MEM_DEPTH-1]，每个元素为 32 位字                              ║
// ║  - 默认深度 256（即 256×32bit = 1KB）                                         ║
// ║  - 地址映射：32 位字节地址 addr → 字地址 word_addr = addr[ADDR_BITS+1:2]        ║
// ║    即右移 2 位（除以 4），因为每个字占 4 字节                                     ║
// ║  - 字节偏移：addr[1:0] 表示字内偏移                                              ║
// ║    00 = 最低字节 (bits 7:0),  01 = bits 15:8,                                  ║
// ║    10 = bits 23:16,           11 = 最高字节 (bits 31:24)                        ║
// ║  - 寻址空间：addr 的低 ADDR_BITS+2 位用于寻址，高位被截断                          ║
// ║    例如 ADDR_BITS=8 → addr[9:0] 有效, addr[31:10] 忽略                           ║
// ║                                                                            ║
// ║  【接口信号】                                                                 ║
// ║  - clk, reset: 时钟与异步复位                                                  ║
// ║  - addr[31:0]: 字节地址输入（来自 ALU 输出）                                     ║
// ║  - write_data[31:0]: 写数据（来自 rs2 寄存器）                                   ║
// ║  - mem_read / mem_write: 读/写使能（来自控制单元译码）                            ║
// ║  - funct3[2:0]: 访存宽度/符号模式（来自指令 funct3 字段）                          ║
// ║  - read_data[31:0]: 读数据输出（已对齐、已扩展）                                  ║
// ║  - mem_ready: 存储器就绪标志（高电平表示可接受新请求）                              ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

module data_memory #(
    // ── 参数：存储器深度（字数）与地址位宽 ──
    parameter MEM_DEPTH = 256,         // 存储器深度：256 个 32-bit 字，即 1KB
    parameter ADDR_BITS = 8            // 字地址位宽：$clog2(256) = 8 位
                                       // 字节地址需 ADDR_BITS+2 = 10 位（低 2 位为字节偏移）
) (
    // ── 时钟与复位 ──
    input  wire        clk,            // 时钟信号（上升沿触发）
    input  wire        reset,          // 异步复位（高有效），复位后存储器清零、状态机回到空闲

    // ── 访存地址与数据 ──
    input  wire [31:0] addr,           // 字节地址：来自处理器流水线的 ALU 计算结果
                                       // 低 ADDR_BITS+2 位有效，高位忽略（超出深度则回绕/截断）
    input  wire [31:0] write_data,     // 写数据：来自 rs2 寄存器的值，按字节使能部分写入

    // ── 访存控制 ──
    input  wire        mem_read,       // 读使能：高有效，来自 load 指令译码
    input  wire        mem_write,      // 写使能：高有效，来自 store 指令译码
    input  wire [2:0]  funct3,         // 访存宽度/符号模式：
                                       //   000→LB/SB  001→LH/SH  010→LW/SW
                                       //   100→LBU    101→LHU
                                       //   LB/LH 符号扩展，LBU/LHU 零扩展

    // ── 读数据与状态 ──
    output reg  [31:0] read_data,      // 读数据输出：已完成对齐和符号/零扩展，直接送入目的寄存器
    output reg         mem_ready       // 存储器就绪：1=空闲可接受请求，0=读操作进行中
                                       // 写操作不占用 mem_ready（写操作是单周期的）
);

    // ══════════════════════════════════════════════════════════════════════════
    // 存储器阵列与流水线寄存器
    // ══════════════════════════════════════════════════════════════════════════

    // ── 物理存储器阵列 ──
    // 256×32bit 寄存器阵列，综合为 block RAM 或分布式 RAM（取决于 FPGA/ASIC 工具）
    reg [31:0] mem [0:MEM_DEPTH-1];

    // ── 读流水线第 1 级寄存器（地址锁存） ──
    // 在读操作的第 1 个周期锁存以下信息，第 2 个周期用这些锁存值读出数据
    reg [ADDR_BITS-1:0] addr_reg;       // 锁存的字地址（已去除低 2 位字节偏移）
    reg [1:0]  byte_offset_reg;         // 锁存的字节偏移 addr[1:0]
    reg [2:0]  funct3_reg;              // 锁存的 funct3（用于 load_align 选择扩展模式）

    // ── 背靠背读的 pending 寄存器 ──
    // 当第一个读请求尚未完成（mem_ready=0）时，如果处理器发出第二个读请求，
    // 且第二个请求的地址与第一个不同，则将其信息暂存到 pending 寄存器组中。
    // 第一个读完成后立即启动 pending 请求，避免流水线气泡。
    reg        pending;                 // pending 标志：1=有待处理的背靠背读请求
    reg [ADDR_BITS-1:0] pending_addr;  // 暂存的第二个读请求字地址
    reg [2:0]  pending_funct3;         // 暂存的第二个读请求 funct3
    reg [1:0]  pending_byte_offset;    // 暂存的第二个读请求字节偏移

    // ══════════════════════════════════════════════════════════════════════════
    // 地址译码：字节地址 → 字地址 + 字节偏移
    // ══════════════════════════════════════════════════════════════════════════

    // ── 字地址计算（带越界保护） ──
    // 将字节地址右移 2 位（除以 4）得到字地址。
    // 取 addr[ADDR_BITS+1:2] 而非 addr[ADDR_BITS-1+2:2]，实际上截取 ADDR_BITS 位宽。
    // 例如 ADDR_BITS=8：取 addr[9:2] → 8 位字地址，可寻址 256 个字（1KB）。
    // 如果 addr 超出范围，高位被自动截断，形成自然回绕（相当于 addr % MEM_DEPTH）。
    // 注：真实 RISC-V 处理器应在此处或外部做地址对齐检查，未对齐访问会触发异常。
    wire [ADDR_BITS-1:0] word_addr = addr[ADDR_BITS+1:2];

    // ══════════════════════════════════════════════════════════════════════════
    // 函数：store_byte_enable — 生成写字节使能
    // ══════════════════════════════════════════════════════════════════════════
    // 根据 funct3 指令类型和字节偏移，生成 4 位字节使能信号 be[3:0]。
    // be[i]=1 表示写数据的第 i 个字节（bits 8i+7 : 8i）将被写入存储器。
    //
    // ┌──────────┬───────────────┬───────────────────────────────────┐
    // │  指令    │ funct3        │ be[3:0]                           │
    // ├──────────┼───────────────┼───────────────────────────────────┤
    // │ SB (字节)│ 3'b000        │ 0001 << byte_offset               │
    // │          │               │ offset=0→0001, 1→0010, 2→0100,   │
    // │          │               │ 3→1000                           │
    // ├──────────┼───────────────┼───────────────────────────────────┤
    // │ SH (半字)│ 3'b001        │ offset=0,1→0011 (低半字)           │
    // │          │               │ offset=2,3→1100 (高半字)           │
    // │          │               │ 注：RISC-V 要求半字对齐，offset     │
    // │          │               │ 只能为 0 或 2，此处容错处理         │
    // ├──────────┼───────────────┼───────────────────────────────────┤
    // │ SW (字)  │ 3'b010        │ 1111 (全字写入)                    │
    // └──────────┴───────────────┴───────────────────────────────────┘
    function [3:0] store_byte_enable;
        input [1:0] byte_offset;   // 字内字节偏移 addr[1:0]
        input [2:0] f3;            // funct3 字段
        begin
            case (f3)
                3'b000:  store_byte_enable = 4'b0001 << byte_offset;  // SB：只使能 1 个字节
                3'b001:  store_byte_enable = byte_offset[1] ? 4'b1100 : 4'b0011;
                         // SH：使能连续 2 个字节（低半字或高半字）
                         // byte_offset[1]=0 → offset 0/1 → 低半字 be=0011
                         // byte_offset[1]=1 → offset 2/3 → 高半字 be=1100
                3'b010:  store_byte_enable = 4'b1111;                 // SW：使能全部 4 字节
                default: store_byte_enable = 4'b1111;                 // 未知 funct3：默认全字写入（安全后备）
            endcase
        end
    endfunction

    // ══════════════════════════════════════════════════════════════════════════
    // 函数：load_align — 读数据对齐与符号/零扩展
    // ══════════════════════════════════════════════════════════════════════════
    // 从 32 位存储字中提取所需的字节/半字，并按指令要求做符号扩展或零扩展。
    //
    // ┌────────┬────────┬────────────────────────────────────────────┐
    // │ 指令   │ funct3 │ 操作                                       │
    // ├────────┼────────┼────────────────────────────────────────────┤
    // │ LB     │ 3'b000 │ 读字节 → 符号扩展至 32 位                   │
    // │ LH     │ 3'b001 │ 读半字 → 符号扩展至 32 位                   │
    // │ LW     │ 3'b010 │ 读字   → 直接输出 32 位                    │
    // │ LBU    │ 3'b100 │ 读字节 → 零扩展至 32 位                     │
    // │ LHU    │ 3'b101 │ 读半字 → 零扩展至 32 位                     │
    // └────────┴────────┴────────────────────────────────────────────┘
    //
    // 字节布局（小端序 Little-Endian）：
    //   mem[addr] = {b3, b2, b1, b0} = bits [31:24][23:16][15:8][7:0]
    //   偏移 0 → b0 (bits 7:0),  偏移 1 → b1 (bits 15:8)
    //   偏移 2 → b2 (bits 23:16), 偏移 3 → b3 (bits 31:24)
    function [31:0] load_align;
        input [31:0] word;         // 从存储器读出的完整 32 位字
        input [1:0]  off;          // 字节偏移
        input [2:0]  f3;           // funct3 字段
        reg [7:0]  b0, b1, b2, b3;  // 4 个字节分量
        reg [15:0] h0, h1;          // 2 个半字分量（低半字 h0=word[15:0]，高半字 h1=word[31:16]）
        begin
            // ── 将 32 位字拆解为字节和半字 ──
            b0 = word[7:0];     b1 = word[15:8];
            b2 = word[23:16];  b3 = word[31:24];
            h0 = word[15:0];   h1 = word[31:16];
            case (f3)
                3'b000:  // LB — 读字节，符号扩展
                    // 符号扩展：将字节最高位 bX[7] 复制到高 24 位
                    case (off)  // synthesis parallel_case  // 四个分支互斥，无优先编码器
                        2'b00: load_align = {{24{b0[7]}}, b0};  // 偏移 0：取 b0，符号扩展
                        2'b01: load_align = {{24{b1[7]}}, b1};  // 偏移 1：取 b1，符号扩展
                        2'b10: load_align = {{24{b2[7]}}, b2};  // 偏移 2：取 b2，符号扩展
                        2'b11: load_align = {{24{b3[7]}}, b3};  // 偏移 3：取 b3，符号扩展
                    endcase
                3'b001:  // LH — 读半字，符号扩展
                    // off[1] 区分低半字 (off=0,1) 和高半字 (off=2,3)
                    // 符号扩展：将半字最高位 hX[15] 复制到高 16 位
                    load_align = off[1] ? {{16{h1[15]}}, h1}
                                        : {{16{h0[15]}}, h0};
                3'b010:  // LW — 读字，无需扩展
                    load_align = word;
                3'b100:  // LBU — 读字节，零扩展
                    // 零扩展：高 24 位填 0
                    case (off)  // synthesis parallel_case
                        2'b00: load_align = {24'b0, b0};
                        2'b01: load_align = {24'b0, b1};
                        2'b10: load_align = {24'b0, b2};
                        2'b11: load_align = {24'b0, b3};
                    endcase
                3'b101:  // LHU — 读半字，零扩展
                    // 零扩展：高 16 位填 0
                    load_align = off[1] ? {16'b0, h1} : {16'b0, h0};
                default: // 未定义 funct3：直接输出原始字（安全后备）
                    load_align = word;
            endcase
        end
    endfunction

    // ══════════════════════════════════════════════════════════════════════════
    // 初始化：仿真时清零存储器与状态
    // ══════════════════════════════════════════════════════════════════════════
    // 在仿真开始时（time 0）执行：
    //   - 所有存储器单元清零（模拟上电复位后的未知态清除）
    //   - mem_ready 置 1（初始空闲状态，可接受访存请求）
    //   - pending 置 0（无待处理请求）
    // 注：综合时 initial 块通常被忽略（FPGA 有全局复位，ASIC 需额外处理），
    //     此处主要用于仿真行为模型。
    initial begin
        for (integer i = 0; i < MEM_DEPTH; i = i + 1)
            mem[i] = 32'h0;               // 所有存储单元初始化为 0
        mem_ready = 1'b1;                 // 初始状态：空闲，可接受请求
        pending   = 1'b0;                 // 无待处理请求
    end

    // ══════════════════════════════════════════════════════════════════════════
    // 主时序逻辑：写操作（1 周期）+ 读操作（2 周期）+ 背靠背读支持
    // ══════════════════════════════════════════════════════════════════════════
    //
    // ┌──────────────────────────────────────────────────────────────────────┐
    // │  写操作（mem_write=1）：单周期完成                                    │
    // │  第 N 周期：组合生成 be[3:0]，读 mem[word_addr] → 按 be 替换字节     │
    // │            → 写回 mem[word_addr]，同时可启动新的读操作（如有）         │
    // └──────────────────────────────────────────────────────────────────────┘
    //
    // ┌──────────────────────────────────────────────────────────────────────┐
    // │  读操作（mem_read=1）：2 周期流水线                                   │
    // │  第 N 周期（mem_ready=1）：                                           │
    // │    - 锁存 word_addr → addr_reg                                       │
    // │    - 锁存 addr[1:0] → byte_offset_reg                                │
    // │    - 锁存 funct3 → funct3_reg                                        │
    // │    - mem_ready ← 0（进入忙碌状态）                                    │
    // │                                                                      │
    // │  第 N+1 周期（mem_ready=0）：                                         │
    // │    - 从 mem[addr_reg] 读出 32 位字                                    │
    // │    - 通过 load_align() 做对齐/扩展 → read_data                        │
    // │    - mem_ready ← 1（回到空闲）                                        │
    // │    - 若 pending=1：立即启动暂存请求（回到第 N 周期状态）               │
    // └──────────────────────────────────────────────────────────────────────┘
    //
    // ┌──────────────────────────────────────────────────────────────────────┐
    // │  背靠背读（Back-to-Back Read）处理：                                  │
    // │  若第 N 周期已发出读请求（mem_ready=0），而在第 N+1 周期到来前         │
    // │  又收到新的读请求（mem_read=1），且新地址 ≠ 当前正在读的地址，         │
    // │  则将新请求的信息暂存到 pending_* 寄存器组，pending 标志置 1。         │
    // │  第 N+1 周期当前读完成后，若 pending=1，立即加载 pending 信息到        │
    // │  主流水寄存器，mem_ready 保持 0，开始执行暂存的读请求。                  │
    // │                                                                      │
    // │  时序示意（连续 3 次读，地址均不同）：                                  │
    // │  周期     │ mem_ready │ 动作                                        │
    // │  ─────────┼───────────┼─────────────────────────────────────────────│
    // │  N        │ 1 → 0    │ 锁存读 A 的地址                               │
    // │  N+1      │ 0 → 0    │ 送出 A 的数据；检测到读 B → pending ← 1       │
    // │  N+2      │ 0 → 0    │ 送出 B 的数据；检测到读 C → pending ← 1       │
    // │  N+3      │ 0 → 1    │ 送出 C 的数据；无新请求 → mem_ready ← 1       │
    // │           │           │ （注意：每次"送出数据"的同时启动下一个请求）    │
    // └──────────────────────────────────────────────────────────────────────┘
    //
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // ── 复位：所有寄存器回到初始状态 ──
            mem_ready       <= 1'b1;        // 空闲状态
            pending         <= 1'b0;        // 无待处理请求
            addr_reg        <= 0;           // 地址寄存器清零
            byte_offset_reg <= 0;           // 字节偏移清零
            funct3_reg      <= 3'b010;      // funct3 默认为 LW（字读）
            pending_addr        <= 0;
            pending_funct3      <= 3'b010;
            pending_byte_offset <= 0;
            read_data           <= 32'b0;   // 读数据输出清零
        end
        else begin
            // ── 以下为 VCS 2018 兼容性声明：块级局部变量声明在块首部 ──
            // be：组合逻辑生成的字节使能，w：读-修改-写的临时字变量
            reg [3:0] be; reg [31:0] w;                 // ★ VCS 2018: 块顶部声明

            // ================================================================
            // 阶段一：写操作处理（与读操作可同周期执行）
            // ================================================================
            // 写操作是单周期的，不依赖 mem_ready 状态。
            // 读-修改-写流程：
            //   1. 根据 funct3 和 addr[1:0] 生成字节使能 be[3:0]
            //   2. 读出目标字的当前值 w = mem[word_addr]
            //   3. 对每个 be[i]=1 的字节，用 write_data 的对应字节替换 w 的对应字节
            //   4. 将修改后的 w 写回 mem[word_addr]
            // 如果同时有读请求，写操作在读操作锁存地址之前完成，不会冲突。
            if (mem_write) begin
                // ── 生成字节使能 ──
                be = store_byte_enable(addr[1:0], funct3);
                // ── 读出当前存储字（读-修改-写的"读"阶段）──
                w = mem[word_addr];
                // ── 按字节使能部分更新 ──
                if (be[0]) w[7:0]   = write_data[7:0];     // 写第 0 字节 (bits 7:0)
                if (be[1]) w[15:8]  = write_data[15:8];    // 写第 1 字节 (bits 15:8)
                if (be[2]) w[23:16] = write_data[23:16];   // 写第 2 字节 (bits 23:16)
                if (be[3]) w[31:24] = write_data[31:24];   // 写第 3 字节 (bits 31:24)
                // ── 写回存储器（非阻塞赋值，本周期末生效）──
                mem[word_addr] <= w;
            end

            // ================================================================
            // 阶段二：读操作状态机
            // ================================================================
            // 状态编码：
            //   mem_ready=1 → IDLE：空闲，可接受新读请求
            //   mem_ready=0 → BUSY：读操作进行中
            // 状态转移：
            //   IDLE + mem_read → BUSY（锁存地址，启动读操作）
            //   BUSY → IDLE（送出数据，若 pending=0）
            //   BUSY → BUSY（送出数据 + 加载 pending 请求，若 pending=1）

            if (mem_ready) begin
                // ────────────────────────────────────────────────────────
                // IDLE 状态：空闲，可接受新读请求
                // ────────────────────────────────────────────────────────
                if (mem_read) begin
                    // 收到读请求 → 锁存地址与控制信息，进入 BUSY 状态
                    addr_reg        <= word_addr;            // 锁存字地址
                    byte_offset_reg <= addr[1:0];            // 锁存字节偏移
                    funct3_reg      <= funct3;               // 锁存 funct3（用于 load_align）
                    mem_ready       <= 1'b0;                 // 进入忙碌状态
                end
                // 若 mem_read=0：保持在 IDLE 状态，mem_ready 保持 1
            end
            else begin
                // ────────────────────────────────────────────────────────
                // BUSY 状态：完成当前读操作，并处理背靠背请求
                // ────────────────────────────────────────────────────────
                // 从存储器读出数据，经对齐/扩展后输出
                // 注：mem[addr_reg] 是组合逻辑读（从 reg 阵列读取无延迟），
                //     而 addr_reg 是上一周期锁存的，形成寄存器读时序。
                read_data <= load_align(mem[addr_reg], byte_offset_reg, funct3_reg);
                mem_ready <= 1'b1;                          // 默认回到空闲状态

                // ★ 背靠背读：如果上一个周期暂存了 pending 请求，立即启动
                // 从 pending 寄存器加载地址/控制信息，mem_ready 保持 0，
                // 下个周期将送出 pending 请求的数据。
                if (pending) begin
                    addr_reg         <= pending_addr;        // 加载暂存的字地址
                    byte_offset_reg  <= pending_byte_offset; // 加载暂存的字节偏移
                    funct3_reg       <= pending_funct3;      // 加载暂存的 funct3
                    mem_ready        <= 1'b0;                // 保持忙碌（因为 pending 请求刚启动）
                    pending          <= 1'b0;                // pending 已消费，清零
                end
            end

            // ================================================================
            // 阶段三：背靠背读检测 — 在当前读未完成时捕获新的读请求
            // ================================================================
            // 条件：
            //   1. !mem_ready：当前有读操作正在进行
            //   2. mem_read：处理器发出了新的读请求
            //   3. word_addr != addr_reg：新请求地址与当前正在读的地址不同
            //      （相同地址的背靠背读理论上可转发，但此处未优化，仍会触发
            //       pending 导致重复读；实际处理器通常由 forwarding 单元处理此情况）
            // 满足条件时：将新请求的信息暂存到 pending 寄存器组。
            if (!mem_ready && mem_read && (word_addr != addr_reg)) begin
                pending             <= 1'b1;                 // 标记有待处理请求
                pending_addr        <= word_addr;            // 暂存字地址
                pending_funct3      <= funct3;               // 暂存 funct3
                pending_byte_offset <= addr[1:0];            // 暂存字节偏移
            end
        end
    end

endmodule
