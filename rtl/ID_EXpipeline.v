// ╔══════════════════════════════════════════════════════════════════════════╗
// ║         ID_EXpipeline.v — ID阶段到EX阶段的流水线寄存器                     ║
// ╟──────────────────────────────────────────────────────────────────────────╢
// ║  所属模块: Sapphire-SoC 五级流水线 RISC-V 处理器核心                       ║
// ║  流水线位置: ID (指令译码) → EX (执行) 之间的管道寄存器                     ║
// ║  核心功能: 锁存 ID 阶段产生的数据通路和控制信号，在下一个时钟周期输出到 EX   ║
// ║            阶段。支持三态控制: reset (同步清零) / flush (插入气泡) /        ║
// ║            stall (流水线暂停/保持)。                                       ║
// ╚══════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
//  一、模块在五级流水线中的角色与定位
// ============================================================================
//  本模块是标准五级流水线的第二级管道寄存器，位于 ID (Instruction Decode) 阶段
//  与 EX (Execute) 阶段之间。在经典 RISC-V 五级流水线中:
//
//       IF ──► ID ──► [本模块] ──► EX ──► MEM ──► WB
//                 ↑                ↑
//             ID阶段            EX阶段
//          (译码+读寄存器)    (ALU执行+分支解析)
//
//  设计理由 —— 为什么需要在这个位置放置寄存器:
//  1. **时序隔离**: 将 ID 阶段的组合逻辑输出与 EX 阶段的组合逻辑输入隔离开，
//     使每个阶段可以在一个时钟周期内完成其工作，从而提升时钟频率。
//  2. **流水并行**: 允许 ID 阶段在译码指令 N+1 的同时，EX 阶段正在执行指令 N。
//  3. **冒险处理**: 寄存器提供了 stall/flush 的控制点，使得流水线控制器可以在
//     这里插入气泡(NOP)或暂停流水，以解决数据冒险和控制冒险。
//
// ============================================================================
//  二、端口与信号说明
// ============================================================================
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  时钟与复位                                                         │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  clk     (input wire)        — 系统时钟，所有寄存器在 posedge 触发   │
//  │  reset   (input wire)        — 同步复位，高有效。清零所有输出寄存器   │
//  │  stall   (input wire)        — 流水线暂停信号，高有效。=1时保持当前值  │
//  │  flush   (input wire)        — 流水线冲刷信号，高有效。插入NOP气泡      │
//  └─────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  数据通路信号 (从 ID 阶段输入，锁存后输出到 EX 阶段)                    │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  id_pc        [31:0]  input  → ex_pc        [31:0]  output          │
//  │      程序计数器(PC)值。EX阶段需要PC来计算跳转目标地址                   │
//  │      (例如 JAL/JALR 的 target = PC + imm，或分支指令的 target)。       │
//  │                                                                      │
//  │  id_rs1_data  [31:0]  input  → ex_rs1_data  [31:0]  output          │
//  │      源寄存器 rs1 的读出数据。EX阶段需要此数据作为ALU的输入操作数。      │
//  │      注意: 实际到达ALU的可能是经过转发多路选择后的值(ex_fwd1控制)。      │
//  │                                                                      │
//  │  id_rs2_data  [31:0]  input  → ex_rs2_data  [31:0]  output          │
//  │      源寄存器 rs2 的读出数据。作用同 rs1_data。                         │
//  │                                                                      │
//  │  id_imm       [31:0]  input  → ex_imm       [31:0]  output          │
//  │      立即数值。由ID阶段的立即数生成单元从指令中提取并符号扩展。          │
//  │      EX阶段将其作为ALU的第二个操作数(当alu_src=1时)。                   │
//  │                                                                      │
//  │  id_rs1       [4:0]   input  → ex_rs1       [4:0]   output          │
//  │      源寄存器rs1的地址编号(5位，对应32个通用寄存器)。                   │
//  │      EX阶段需要此地址用于转发单元的比较逻辑:                            │
//  │      "前一条指令的目的寄存器 == 本条指令的源寄存器?"                    │
//  │                                                                      │
//  │  id_rs2       [4:0]   input  → ex_rs2       [4:0]   output          │
//  │      源寄存器rs2的地址编号，用途同 rs1 地址。                           │
//  │                                                                      │
//  │  id_rd        [4:0]   input  → ex_rd        [4:0]   output          │
//  │      目的寄存器地址编号。需要逐级传递到WB阶段，用于写回寄存器文件。       │
//  │      在EX阶段也可能用于转发冒险检测。                                   │
//  └─────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  执行阶段控制信号 (ID阶段译码产生，EX阶段消费)                          │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  id_alu_ctrl  [3:0]   input  → ex_alu_ctrl  [3:0]   output          │
//  │      ALU操作选择码。4位编码，告诉ALU执行哪种运算:                       │
//  │      加法(ADD)、减法(SUB)、移位(SLL/SRL/SRA)、                        │
//  │      逻辑(AND/OR/XOR)、比较(SLT/SLTU)等。由ID阶段译码器产生。          │
//  │                                                                      │
//  │  id_alu_src   (1bit)  input  → ex_alu_src   (1bit)   output          │
//  │      ALU第二个操作数来源选择:                                          │
//  │      0 = 来自寄存器 rs2_data (R-type指令)                              │
//  │      1 = 来自立即数 imm      (I-type指令, load/store等)                │
//  │                                                                      │
//  │  id_mem_write (1bit)  input  → ex_mem_write (1bit)   output          │
//  │      存储器写使能。1=store指令，在MEM阶段将数据写入数据存储器。          │
//  │                                                                      │
//  │  id_mem_read  (1bit)  input  → ex_mem_read  (1bit)   output          │
//  │      存储器读使能。1=load指令，在MEM阶段从数据存储器读取数据。           │
//  │                                                                      │
//  │  id_reg_write (1bit)  input  → ex_reg_write (1bit)   output          │
//  │      寄存器文件写使能。1=指令有目的寄存器且需要写回。                    │
//  │      此信号会逐级传递到WB阶段，控制最终的寄存器写操作。                   │
//  └─────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  分支判断相关信号 (★ 新增)                                            │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  id_funct3    [2:0]   input  → ex_funct3    [2:0]   output          │
//  │      指令的 funct3 字段。分支指令需要 funct3 来判断分支条件类型:        │
//  │      BEQ(000), BNE(001), BLT(100), BGE(101), BLTU(110), BGEU(111)。 │
//  │      EX阶段的分支解析单元根据funct3和ALU结果决定是否跳转。              │
//  │                                                                      │
//  │  id_opcode    [6:0]   input  → ex_opcode    [6:0]   output          │
//  │      指令的 opcode 字段(7位)。用于EX阶段识别指令类型:                    │
//  │      分支(BRANCH)、跳转(JAL/JALR)、算术(OP-IMM/OP)等。                 │
//  │      分支预测错误时也需要opcode来触发正确的流水线冲刷。                  │
//  └─────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  写回选择信号                                                        │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  id_wbmux     [1:0]   input  → ex_wbmux     [1:0]   output          │
//  │      写回数据选择器控制码。2位编码，决定WB阶段写回寄存器的数据来源:      │
//  │      00 = ALU结果                                                     │
//  │      01 = 存储器读出数据 (load)                                       │
//  │      10 = PC+4 (JAL/JALR的返回地址)                                  │
//  │      11 = 保留                                                        │
//  └─────────────────────────────────────────────────────────────────────┘
//
//  ┌─────────────────────────────────────────────────────────────────────┐
//  │  转发控制信号 (转发单元在ID阶段检测，EX阶段使用)                        │
//  ├─────────────────────────────────────────────────────────────────────┤
//  │  id_fwd1      [1:0]   input  → ex_fwd1      [1:0]   output          │
//  │      rs1 的转发选择码。2位编码:                                        │
//  │      00 = 无转发，使用寄存器文件读出的 rs1_data                         │
//  │      01 = 转发来自 EX/MEM 阶段的结果 (上一条指令的ALU输出)             │
//  │      10 = 转发来自 MEM/WB 阶段的结果 (再前一条指令的结果)              │
//  │      11 = 保留                                                        │
//  │                                                                      │
//  │  id_fwd2      [1:0]   input  → ex_fwd2      [1:0]   output          │
//  │      rs2 的转发选择码，编码含义同 fwd1。                               │
//  │                                                                      │
//  │  ★ 设计要点: 转发选择必须在ID阶段就确定并锁存到管道寄存器中，           │
//  │    因为EX阶段的转发多路选择器是组合逻辑在同一个周期内完成的，             │
//  │    不存在额外的时钟周期用于计算转发条件。                                │
//  └─────────────────────────────────────────────────────────────────────┘
//
// ============================================================================
//  三、内部逻辑详解
// ============================================================================
//
//  本模块仅包含一个 always @(posedge clk or posedge reset) 块。
//  没有 assign 连续赋值语句 —— 所有输出都是 reg 类型，在时序逻辑中赋值。
//  这是经典的流水线寄存器实现方式: 纯时序逻辑 + 非阻塞赋值(<=)。
//
//  每个输出寄存器变量的声明理由:
//  - 使用 reg 类型 + 非阻塞赋值(<=)，确保所有寄存器在同一时钟沿同步更新。
//  - 不使用 wire 直接驱动输出，因为输出值必须在时钟沿才变化，
//    保证下游EX阶段的组合逻辑在一个完整的时钟周期内看到稳定的输入。
//
// ============================================================================
//  四、三态优先级控制逻辑 (reset > flush > stall > 正常传递)
// ============================================================================
//
//  if (reset)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  条件: reset = 1                                                 │
//  │  行为: 所有输出寄存器清零为 0。                                    │
//  │  理由:                                                            │
//  │  - reset 是最高优先级。无论其他控制信号如何，复位时必须将            │
//  │    整个处理器置于已知的初始状态。                                  │
//  │  - 清零控制信号(mem_write, mem_read, reg_write=0)确保复位后        │
//  │    不会产生意外的存储器访问或寄存器写入。                          │
//  │  - 清零转发控制(ex_fwd1, ex_fwd2=0)确保不会错误地转发未定义数据。  │
//  │  - 数据通路也清零是为了确定性: 避免x态传播到后续阶段。              │
//  │  - 采用同步复位(posedge reset)，保证复位释放时的时序收敛。          │
//  └──────────────────────────────────────────────────────────────────┘
//
//  else if (flush)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  条件: flush = 1 且 reset = 0                                     │
//  │  行为: 仅清零控制信号，数据通路保持原值(不清零也不更新)。            │
//  │  理由:                                                            │
//  │  - flush 的语义是"插入一个NOP气泡"，即将当前管道寄存器的控制信号     │
//  │    全部清零，使得 EX 阶段看到一条"无效指令":                         │
//  │      * reg_write=0 → 不写寄存器                                    │
//  │      * mem_write=0, mem_read=0 → 不访问存储器                      │
//  │      * alu_ctrl=0 → ALU执行加法(不影响状态)                        │
//  │  - **为什么不清零数据通路?** 数据通路不清零有两方面考虑:             │
//  │      1. 节省不必要的翻转功耗(清零再恢复会产生额外跳变)               │
//  │      2. 数据通路在控制信号全关的情况下是"don't care"，不影响         │
//  │         架构状态(寄存器文件/存储器不会改变)。                       │
//  │  - **为什么清零转发控制?** flush通常发生在分支预测错误时，           │
//  │    此时EX阶段原有的转发关系不再有效，必须清零避免错误转发。          │
//  │  - flush 的典型触发场景:                                           │
//  │      * 分支预测错误(BPU mispredict): IF/ID和ID/EX中的指令需要       │
//  │        被冲刷掉，替换为NOP。                                       │
//  │      * 异常/中断处理: 需要取消流水线中当前指令的执行。               │
//  │      * 流水线互锁需要插入气泡。                                     │
//  └──────────────────────────────────────────────────────────────────┘
//
//  else if (!stall)
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  条件: stall = 0 且 reset = 0 且 flush = 0                        │
//  │  行为: 正常流水 —— ID阶段的输入值锁存到输出寄存器。                  │
//  │  理由:                                                            │
//  │  - 这是流水线的正常推进模式。每个时钟周期，ID阶段产出的新数据        │
//  │    被锁存到管道寄存器，供EX阶段在下一个时钟周期使用。                │
//  │  - 所有信号(数据通路+控制+转发)都需要传递，因为每条指令都有          │
//  │    独特的操作语义和控制需求。                                      │
//  │  - 非阻塞赋值(<=)确保所有寄存器在同一时钟沿采样 id_* 的值，          │
//  │    不会出现"部分更新"的中间状态。                                  │
//  └──────────────────────────────────────────────────────────────────┘
//
//  (隐含) stall = 1
//  ┌──────────────────────────────────────────────────────────────────┐
//  │  条件: stall = 1 且 reset = 0 且 flush = 0                        │
//  │  行为: 不做任何赋值 —— 所有输出寄存器保持当前值不变。               │
//  │  理由:                                                            │
//  │  - stall 的语义是"流水线暂停"。EX阶段需要保持当前正在执行的指令，    │
//  │    因为目前无法继续推进(例如等待数据冒险解决、等待存储器响应等)。    │
//  │  - "什么都不做"就是保持 —— 寄存器的值不会改变，相当于流水线         │
//  │    中插入了一个"暂停周期"。                                        │
//  │  - stall 的典型触发场景:                                           │
//  │      * Load-Use 数据冒险: lw 指令在EX阶段，下一条指令需要在          │
//  │        ID阶段使用lw的目标寄存器。此时ID阶段需要stall一个周期，       │
//  │        等待lw完成MEM阶段读出数据后再通过转发解决。                   │
//  │      * 多周期操作: EX阶段需要多个周期完成(如乘法、除法)时，          │
//  │        需要stall ID/EX管道寄存器。                                 │
//  │      * 指令缓存未命中: IF阶段需要等待指令从内存取回。                │
//  └──────────────────────────────────────────────────────────────────┘
//
// ============================================================================
//  五、时序设计考量
// ============================================================================
//
//  1. 关键路径分析:
//     - 本模块本身就是时序逻辑，其时钟到Q延迟(t_clk2q)是EX阶段关键路径的
//       一部分。输出直接驱动EX阶段的组合逻辑(ALU、转发MUX、分支解析等)。
//     - ID阶段的组合逻辑(译码、立即数生成、寄存器文件读取)必须在时钟沿之前
//       稳定，满足本模块的建立时间(t_setup)。
//
//  2. 转发路径的时序:
//     - 转发多路选择发生在EX阶段的组合逻辑中，不在本模块内。
//     - ex_fwd1/ex_fwd2 控制信号与本模块的数据输出(ex_rs1_data/ex_rs2_data)
//       同时到达EX阶段的转发MUX，保证时序对齐。
//
//  3. stall 的时序:
//     - stall信号必须在时钟沿之前稳定(建立时间满足)，否则可能导致错误的
//       数据被锁存或丢失。通常 stall 由冒险检测单元在ID阶段产生。
//     - stall=1 时，本模块的ID端输入必须保持稳定(由ID阶段的stall控制保证)。
//
//  4. flush 的时序:
//     - flush信号通常由EX阶段的分支解析结果产生，在同一周期的下一个时钟沿
//       作用于本模块。这意味着分支错误预测的指令(在ID/EX寄存器中)需要
//       一个额外的周期被冲刷掉。
//     - 这是流水线分支惩罚的一个周期来源。
//
// ============================================================================
//  六、信号传递清单 (速查表)
// ============================================================================
//
//  数据通路 (7个信号):
//    pc[31:0], rs1_data[31:0], rs2_data[31:0], imm[31:0],
//    rs1[4:0], rs2[4:0], rd[4:0]
//
//  控制信号 (6个信号):
//    alu_ctrl[3:0], alu_src, mem_write, mem_read, reg_write, wbmux[1:0]
//
//  分支判断 (2个信号):
//    funct3[2:0], opcode[6:0]
//
//  转发控制 (2个信号):
//    fwd1[1:0], fwd2[1:0]
//
//  ★ 总计: 17个信号从ID阶段传递到EX阶段
//
// ============================================================================

module id_ex_stage (
    // ========================================================================
    //  时钟与复位 — 全局控制信号
    // ========================================================================
    input  wire        clk,          // 系统时钟，posedge触发
    input  wire        reset,        // 同步复位，高有效，最高优先级
    input  wire        stall,        // 暂停信号，高有效。=1时保持寄存器值不变
    input  wire        flush,        // 冲刷信号，高有效。清零控制信号(插入NOP气泡)

    // ========================================================================
    //  数据通路 — 从ID阶段传入的计算数据
    // ========================================================================
    input  wire [31:0] id_pc,        // 程序计数器值 → 用于跳转目标计算
    input  wire [31:0] id_rs1_data,  // 源寄存器rs1的读出值 → ALU操作数A
    input  wire [31:0] id_rs2_data,  // 源寄存器rs2的读出值 → ALU操作数B(或存储数据)
    input  wire [31:0] id_imm,       // 立即数(符号扩展后) → ALU操作数B(alu_src=1时)
    input  wire [4:0]  id_rs1,       // 源寄存器rs1地址 → 转发冒险检测比较
    input  wire [4:0]  id_rs2,       // 源寄存器rs2地址 → 转发冒险检测比较
    input  wire [4:0]  id_rd,        // 目的寄存器地址 → 逐级传递到WB阶段写回

    // ========================================================================
    //  执行阶段控制 — ID阶段译码产生的控制信号
    // ========================================================================
    input  wire [3:0]  id_alu_ctrl,  // ALU操作选择码[3:0] → 4位编码选择ALU功能
    input  wire        id_alu_src,   // ALU第二操作数选择 → 0=rs2_data, 1=imm
    input  wire        id_mem_write, // 存储器写使能 → 1=store指令
    input  wire        id_mem_read,  // 存储器读使能 → 1=load指令
    input  wire        id_reg_write, // 寄存器写使能 → 1=有目的寄存器写回

    // ========================================================================
    //  分支判断 — EX阶段分支解析所需的指令字段
    // ========================================================================
    input  wire [2:0]  id_funct3,    // 指令funct3字段 → 分支条件类型判断
    input  wire [6:0]  id_opcode,    // 指令opcode字段 → 指令类型识别

    // ========================================================================
    //  写回选择 — WB阶段数据来源选择(逐级传递)
    // ========================================================================
    input  wire [1:0]  id_wbmux,     // 写回数据选择码 → 00=ALU, 01=MEM, 10=PC+4

    // ========================================================================
    //  转发控制 — ID阶段检测的转发关系(EX阶段的转发MUX使用)
    // ========================================================================
    input  wire [1:0]  id_fwd1,      // rs1转发选择 → 00=无, 01=EX/MEM, 10=MEM/WB
    input  wire [1:0]  id_fwd2,      // rs2转发选择 → 00=无, 01=EX/MEM, 10=MEM/WB

    // ========================================================================
    //  输出端口 — 锁存后的信号，驱动EX阶段组合逻辑
    // ========================================================================
    output reg  [31:0] ex_pc,        // 锁存后的PC值
    output reg  [31:0] ex_rs1_data,  // 锁存后的rs1数据
    output reg  [31:0] ex_rs2_data,  // 锁存后的rs2数据
    output reg  [31:0] ex_imm,       // 锁存后的立即数
    output reg  [4:0]  ex_rs1,       // 锁存后的rs1地址
    output reg  [4:0]  ex_rs2,       // 锁存后的rs2地址
    output reg  [4:0]  ex_rd,        // 锁存后的rd地址
    output reg  [3:0]  ex_alu_ctrl,  // 锁存后的ALU控制码
    output reg         ex_alu_src,   // 锁存后的ALU源选择
    output reg         ex_mem_write, // 锁存后的存储器写使能
    output reg         ex_mem_read,  // 锁存后的存储器读使能
    output reg         ex_reg_write, // 锁存后的寄存器写使能
    output reg  [2:0]  ex_funct3,    // 锁存后的funct3
    output reg  [6:0]  ex_opcode,    // 锁存后的opcode
    output reg  [1:0]  ex_wbmux,     // 锁存后的写回选择码
    output reg  [1:0]  ex_fwd1,      // 锁存后的rs1转发选择
    output reg  [1:0]  ex_fwd2       // 锁存后的rs2转发选择
);

    // ========================================================================
    //  核心时序逻辑: 三态优先级控制的流水线寄存器
    // ========================================================================
    //
    //  优先级: reset > flush > stall > 正常锁存
    //
    //  为什么是这个优先级顺序?
    //     reset 必须最先 —— 无论什么情况，复位要能清零一切。
    //     flush 优先于 stall —— 分支预测错误时，需要立即冲刷掉错误路径的指令，
    //       即使此时有 stall 请求也应该先 flush (stall 在 flush 之后的下个周期
    //       再处理)。
    //     stall 优先于正常锁存 —— 暂停时不能更新寄存器，否则会丢失当前指令的
    //       执行状态。
    //     正常锁存是最低优先级 —— 这是流水线无冲突时的默认行为。
    //
    //  非阻塞赋值(<=)的时序语义:
    //     所有 <= 赋值在 always 块结束时统一更新。这意味着:
    //     - 在时钟沿时刻: 右侧表达式采样的是时钟沿之前的旧值
    //     - 在时钟沿之后: 左侧寄存器更新为新值
    //     - 这保证了流水线寄存器之间不会出现"穿通"现象
    //
    always @(posedge clk or posedge reset) begin
        if (reset) begin
            // ── 复位: 所有寄存器清零 ──
            // 控制信号清零确保复位后处理器处于安全状态(不写寄存器、不访问存储器)
            // 数据通路清零保证确定性的初始状态，避免X态传播
            ex_pc        <= 32'b0;
            ex_rs1_data  <= 32'b0;
            ex_rs2_data  <= 32'b0;
            ex_imm       <= 32'b0;
            ex_rs1       <= 5'b0;
            ex_rs2       <= 5'b0;
            ex_rd        <= 5'b0;
            ex_alu_ctrl  <= 4'b0;
            ex_alu_src   <= 1'b0;
            ex_fwd1      <= 2'b0;
            ex_fwd2      <= 2'b0;
            ex_mem_write <= 1'b0;
            ex_mem_read  <= 1'b0;
            ex_reg_write <= 1'b0;
            ex_funct3    <= 3'b0;
            ex_opcode    <= 7'b0;
            ex_wbmux     <= 2'b0;
        end
        else if (flush) begin
            // ── 冲刷: 仅清零控制信号(插入NOP气泡) ──
            // 核心思想: 将当前流水线槽位变成一条"无操作"指令
            // - alu_ctrl=0: ALU执行默认操作(通常是加法，结果不影响架构状态)
            // - alu_src=0:  不影响(因为mem和reg控制都已关断)
            // - mem_write=0, mem_read=0: 不产生存储器访问
            // - reg_write=0: 不写寄存器文件 → 这条"指令"对架构状态零影响
            // - wbmux=0:     不影响(reg_write已关断)
            // - funct3/opcode=0: 不影响(无控制信号需要这些字段)
            // - fwd1/fwd2=0: 清除转发关系 → 使用寄存器读出的值(但reg_write=0
            //                意味着上一条指令不写寄存器，所以无冒险)
            // ★ 数据通路不清零:
            //   - 节省功耗(减少信号翻转)
            //   - 控制信号全关断的情况下数据通路的值是don't care
            //   - 如果将来需要调试追踪冲刷的指令，保留数据通路有助于定位
            ex_alu_ctrl  <= 4'b0;
            ex_alu_src   <= 1'b0;
            ex_mem_write <= 1'b0;
            ex_mem_read  <= 1'b0;
            ex_reg_write <= 1'b0;
            ex_funct3    <= 3'b0;
            ex_opcode    <= 7'b0;
            ex_wbmux     <= 2'b0;
            ex_fwd1      <= 2'b0;
            ex_fwd2      <= 2'b0;
        end
        else if (!stall) begin
            // ── 正常流水推进: 锁存ID阶段的所有信号 ──
            // stall=0 表示流水线正常推进，将ID阶段的输出采样到管道寄存器
            // 每个信号都必须传递，因为每条指令有不同的操作语义:
            //   - pc:  每条指令有不同的PC(用于异常处理和跳转目标计算)
            //   - 数据: 不同指令的源操作数不同
            //   - 地址: rs1/rs2/rd 每条指令都可能使用不同的寄存器
            //   - 控制: 每条指令有不同的执行需求(算逻/访存/分支/跳转)
            //   - 转发: 每条指令的转发需求取决于它与前后指令的数据依赖关系
            ex_pc        <= id_pc;
            ex_rs1_data  <= id_rs1_data;
            ex_rs2_data  <= id_rs2_data;
            ex_imm       <= id_imm;
            ex_rs1       <= id_rs1;
            ex_rs2       <= id_rs2;
            ex_rd        <= id_rd;
            ex_alu_ctrl  <= id_alu_ctrl;
            ex_alu_src   <= id_alu_src;
            ex_fwd1      <= id_fwd1;
            ex_fwd2      <= id_fwd2;
            ex_mem_write <= id_mem_write;
            ex_mem_read  <= id_mem_read;
            ex_reg_write <= id_reg_write;
            ex_funct3    <= id_funct3;
            ex_opcode    <= id_opcode;
            ex_wbmux     <= id_wbmux;
        end
        // ── stall=1: 保持当前寄存器值不变 ──
        // 隐式的 else: 无任何赋值操作
        // 寄存器保持其上一周期的值，相当于流水线暂停一个周期
        // 这为Load-Use冒险、多周期EX操作等场景提供了流水线互锁机制
        // ★ 注意: 在仿真中，如果寄存器未被赋值且无初始值，将保持X态。
        //   但 reset 分支确保了在上电/reset后寄存器有确定的值，
        //   且正常流水推进后寄存器持有有效数据，因此stall保持不会引入问题。
    end

endmodule
