//==============================================================================
// 模块名称: uart_full
// 功能描述: 完整 UART（通用异步收发传输器）外设模块
//           - 支持 8 位数据、1 位起始位、1 位停止位、无校验位、LSB 先发
//           - 默认波特率: 50MHz 时钟下，baud_div=434 → 约 115200 bps
//             (公式: baud_rate = f_clk / (baud_div + 1))
//           - 包含独立的发送器 (TX) 状态机和接收器 (RX) 子模块
//           - 支持中断产生（发送完成或接收就绪时触发）
//
// 寄存器映射 (地址使用 addr[3:0]，即 16 字节地址空间内):
//   偏移量    名称       宽度    读写    描述
//   ---------------------------------------------------------------------------
//   0x0      DATA       [7:0]   R/W    发送/接收数据寄存器
//                                       写: 写入待发送的 8 位数据
//                                       读: 读取最后接收到的 8 位数据
//   0x4      CTRL       [17:0]  R/W    控制/配置寄存器
//                                       [15:0]: baud_div - 波特率分频系数
//                                       [16]:   tx_enable - 发送使能 (1=使能)
//                                       [17]:   rx_enable - 接收使能 (1=使能)
//   0x8      STAT/IRQ   [31:0]  R/W    状态/中断控制寄存器
//                                       读:
//                                         [0]:  tx_empty - 发送空闲标志
//                                         [30]: rx_ready - 接收就绪标志
//                                       写:
//                                         [31]: irq_enable - 中断使能
//
// 总线接口 (类 APB 风格，同步读写):
//   - clk:      系统时钟
//   - reset:    同步复位 (高有效)
//   - addr[31:0]: 32 位地址总线 (仅低 4 位用于寄存器选择)
//   - wdata[31:0]: 32 位写数据总线
//   - we:       写使能 (高有效)
//   - rdata[31:0]: 32 位读数据总线 (组合逻辑输出)
//
// 外部引脚:
//   - tx:       UART 发送串行输出线
//   - rx:       UART 接收串行输入线
//   - uart_irq: 中断请求输出 (高有效: irq_enable & (tx_empty | rx_ready))
//
// 时序与同步设计:
//   - 全同步设计: 所有寄存器在 clk 上升沿更新，reset 为同步复位
//   - 波特率生成: 通过 baud_div 16 位递减计数器实现，计数器从 baud_div
//     递减到 0，每个位周期为 (baud_div + 1) 个时钟周期
//   - TX 状态机 (共 13 个状态):
//      状态0:  空闲 (IDLE)            - tx=1, tx_empty=1
//      状态1:  起始位 (START)          - 拉低 tx 线一个位周期 (tx=0)
//      状态2-9: 数据位 D0~D7 (DATA)    - 逐位发送 tx_shift[0]，右移
//      状态10: 停止位 (STOP)           - tx=1 一个位周期
//      状态11: 尾随空闲 (TRAILING)     - 保持 tx=1，置 tx_empty
//      默认:  回退到空闲状态
//   - 接收器: 由 uart_rx 子模块处理 (独立的例化模块)
//   - 中断: uart_irq 为组合逻辑输出 (irq_enable & (tx_empty | rx_ready))
//     当中断使能且发送空闲或接收就绪时，中断线拉高
//==============================================================================

module uart_full(
    input  clk,                    // 系统时钟
    input  reset,                  // 同步复位 (高有效)
    input  [31:0] addr,           // 32 位地址总线 (仅低 4 位用于寄存器选择)
    input  [31:0] wdata,          // 32 位写数据总线
    input  we,                     // 写使能 (高有效)
    output [31:0] rdata,          // 32 位读数据总线 (组合逻辑输出)
    output tx,                     // UART 发送串行输出
    input  rx,                     // UART 接收串行输入
    output uart_irq               // 中断请求输出 (高有效)
);

//------------------------------------------------------------------------------
// 内部寄存器声明
//------------------------------------------------------------------------------
reg  [7:0]  tx_data,              // 待发送数据寄存器 (写 DATA 寄存器时锁存)
            rx_data_buf;           // 接收数据缓冲 (由 uart_rx 子模块更新)
reg  [15:0] baud_div;             // 波特率分频系数 (默认 434)
reg         tx_enable,             // 发送使能标志
            rx_enable,             // 接收使能标志
            tx_empty;              // 发送空闲标志 (1=空闲/完成, 0=正在发送)
wire        rx_ready;              // 接收就绪标志 (由 uart_rx 子模块驱动)
reg         irq_enable;            // 中断使能标志

//------------------------------------------------------------------------------
// TX 发送器内部信号
//------------------------------------------------------------------------------
reg  [15:0] tx_counter;           // 波特率位周期计数器 (递减)
reg  [3:0]  tx_state;             // 发送状态机状态 (0~11, >11 回退到 0)
reg         tx_reg;                // TX 输出寄存器 (驱动 tx 引脚)
reg  [7:0]  tx_shift;             // 发送移位寄存器 (LSB 先出, 右移)

//------------------------------------------------------------------------------
// UART 接收器 (RX) 子模块例化
// 接口: clk, reset, rx 输入, baud_div 波特率配置, rx_ready 输出, rx_data 输出
// rx_error 未使用 (悬空)
//------------------------------------------------------------------------------
uart_rx u_rx(
    .clk      (clk),
    .reset    (reset),
    .rx       (rx),
    .baud_div (baud_div),
    .rx_ready (rx_ready),
    .rx_data  (rx_data_buf),
    .rx_error ()                  // 接收错误状态 (未使用)
);

//------------------------------------------------------------------------------
// 读数据通路 (组合逻辑)
// 根据 addr[3:0] 选择对应的寄存器输出到 rdata 总线:
//   地址 0x0 → 返回 RX 数据缓冲 (低 8 位有效)
//   地址 0x4 → 返回控制寄存器 {rx_enable, tx_enable, baud_div}
//   地址 0x8 → 返回状态寄存器 {irq_enable, rx_ready, 1'b0, tx_empty}
//   其他    → 返回 0
//------------------------------------------------------------------------------
assign rdata = (addr[3:0] == 0)  ? {24'b0,   rx_data_buf} :
               (addr[3:0] == 4)  ? {14'b0,   rx_enable, tx_enable, baud_div} :
               (addr[3:0] == 8)  ? {28'b0,   irq_enable, rx_ready, 1'b0, tx_empty} :
                                    0;

//------------------------------------------------------------------------------
// 寄存器写逻辑 (时序逻辑)
// 复位时: rx_enable=1, tx_enable=1, baud_div=434 (默认 ~115200bps @50MHz),
//         irq_enable=0
// 写使能有效时，根据 addr[3:0] 写对应寄存器:
//   地址 0x0 → 写 tx_data (低 8 位)
//   地址 0x4 → 写 baud_div[15:0], tx_enable[16], rx_enable[17]
//   地址 0x8 → 写 irq_enable[31]
//------------------------------------------------------------------------------
always @(posedge clk) begin
    if (reset) begin
        // 复位默认值: 收发使能，波特率 ~115200，中断关闭
        rx_enable <= 1;
        tx_enable <= 1;
        baud_div  <= 434;
        irq_enable <= 0;
    end else if (we) begin
        case (addr[3:0])
            0: tx_data <= wdata[7:0];                       // 写入发送数据
            4: begin                                         // 写入控制寄存器
                baud_div  <= wdata[15:0];                    //   [15:0] 波特率分频
                tx_enable <= wdata[16];                      //   [16]   发送使能
                rx_enable <= wdata[17];                      //   [17]   接收使能
            end
            8: irq_enable <= wdata[31];                      // 写入中断使能
            default: ;                                       // 未定义地址，无操作
        endcase
    end
end

//------------------------------------------------------------------------------
// 中断产生逻辑 (组合逻辑)
// 当中断使能且 (发送空闲 或 接收就绪) 时，产生中断请求
//------------------------------------------------------------------------------
assign uart_irq = irq_enable & (tx_empty | rx_ready);

//------------------------------------------------------------------------------
// TX 发送状态机 (时序逻辑)
//
// 状态转移图:
//   IDLE(0) → START(1) → D0(2) → D1(3) → D2(4) → D3(5) → D4(6) → D5(7)
//       ↑                                                               |
//       |     TRAILING(11) ← STOP(10) ← D7(9) ← D6(8) ←───────────────┘
//       |         |
//       └─────────┘
//
// 每个状态停留 (baud_div + 1) 个时钟周期 (由 tx_counter 控制)
// 数据传输顺序: 起始位(0) → D0(LSB) → D1 → ... → D7(MSB) → 停止位(1)
//------------------------------------------------------------------------------
always @(posedge clk) begin
    if (reset) begin
        // 复位: 回到空闲状态，tx 输出高电平 (空闲态)
        tx_state   <= 0;
        tx_empty   <= 1;
        tx_reg     <= 1;
        tx_counter <= 0;
        tx_shift   <= 0;
    end else begin
        case (tx_state)
            //------------------------------------------------------------------
            // 状态 0: 空闲 (IDLE)
            // TX 线保持高电平，等待总线写入 DATA 寄存器 (addr=0)
            // 写操作触发发送: 锁存发送数据、清除 tx_empty、加载波特率计数器、进入状态1
            //------------------------------------------------------------------
            0: begin
                tx_reg   <= 1;                               // TX 线保持空闲 (高)
                tx_empty <= 1;                               // 标记发送空闲
                if (we && addr[3:0] == 0) begin
                    tx_shift   <= wdata[7:0];                // 锁存待发送数据
                    tx_empty   <= 0;                         // 发送忙 (清除空闲标志)
                    tx_state   <= 1;                         // 进入起始位状态
                    tx_counter <= baud_div;                  // 加载位周期计数器
                end
            end

            //------------------------------------------------------------------
            // 状态 1: 起始位 (START BIT)
            // 等待计数器递减到 0，然后拉低 TX 线，加载数据位周期，进入状态2
            //------------------------------------------------------------------
            1: if (tx_counter == 0) begin
                tx_reg     <= 0;                             // 发送起始位 (低电平)
                tx_counter <= baud_div;                      // 重新加载位周期
                tx_state   <= 2;                             // 进入第一个数据位
            end else begin
                tx_counter <= tx_counter - 1;                // 递减计数器
            end

            //------------------------------------------------------------------
            // 状态 2~9: 数据位 D0~D7 (DATA BITS, LSB FIRST)
            // 每个状态发送 tx_shift 的最低有效位，然后将 tx_shift 右移一位
            // 共 8 个数据位: 状态2→D0(LSB), 状态3→D1, ..., 状态9→D7(MSB)
            //------------------------------------------------------------------
            2,3,4,5,6,7,8,9: if (tx_counter == 0) begin
                tx_reg     <= tx_shift[0];                   // 发送当前 LSB
                tx_shift   <= tx_shift >> 1;                 // 右移准备下一位
                tx_counter <= baud_div;                      // 重新加载位周期
                tx_state   <= tx_state + 1;                  // 进入下一数据位状态
            end else begin
                tx_counter <= tx_counter - 1;                // 递减计数器
            end

            //------------------------------------------------------------------
            // 状态 10: 停止位 (STOP BIT)
            // 等待计数器递减到 0，然后拉高 TX 线，进入尾随空闲状态
            //------------------------------------------------------------------
            10: if (tx_counter == 0) begin
                tx_reg     <= 1;                             // 发送停止位 (高电平)
                tx_counter <= baud_div;                      // 重新加载位周期
                tx_state   <= 11;                            // 进入尾随空闲状态
            end else begin
                tx_counter <= tx_counter - 1;                // 递减计数器
            end

            //------------------------------------------------------------------
            // 状态 11: 尾随空闲 (TRAILING IDLE)
            // 等待一个完整的位周期后，置位 tx_empty，返回空闲状态
            // 此状态保证停止位后至少有一个位周期的空闲间隔
            //------------------------------------------------------------------
            11: if (tx_counter == 0) begin
                tx_empty <= 1;                               // 发送完成，置空闲标志
                tx_state <= 0;                               // 返回空闲状态
            end else begin
                tx_counter <= tx_counter - 1;                // 递减计数器
            end

            //------------------------------------------------------------------
            // 默认: 安全回退
            // 若状态机进入未定义状态 (如 SEU 翻转导致)，回退到空闲
            //------------------------------------------------------------------
            default: tx_state <= 0;
        endcase
    end
end

//------------------------------------------------------------------------------
// TX 输出引脚驱动 (连续赋值)
// 直接将 tx_reg 连接到外部 tx 引脚
//------------------------------------------------------------------------------
assign tx = tx_reg;

endmodule
