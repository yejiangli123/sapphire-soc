// ╔══════════════════════════════════════════════════════════════════════════╗
// ║              Branch_predicting_unit.v — 分支预测单元                      ║
// ║              策略: B-type 静态预测不跳 + JAL/JALR 无条件预测跳            ║
// ║              位置: IF 阶段（取指后立即预测，不等 ID 译码）                 ║
// ╚══════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
//  一、模块在 CPU 流水线中的角色
// ============================================================================
//
//  本模块位于经典五级流水线的 IF（Instruction Fetch，取指）阶段。
//  在 RISC-V 处理器中，分支/跳转指令的目标地址要到 EX（执行）阶段才能算出。
//  如果流水线停下来等待 EX 阶段的结果，每个分支都会导致 2~3 个周期的流水线
//  气泡（bubble），严重降低 IPC（每周期指令数）。
//
//  分支预测单元的作用是：在 IF 阶段拿到指令操作码（opcode）后，立即对该指令
//  是否"跳转"做出预测。预测结果用于指导 PC（程序计数器）的更新：
//    - 预测跳转  → PC 提前指向目标地址（由 Branch_target_computer 计算），
//                   流水线继续取指，不产生气泡。
//    - 预测不跳 → PC 按顺序递增（PC+4），流水线正常流动。
//
//  如果预测正确：流水线无损失，分支指令零开销。
//  如果预测错误：EX 阶段发现预测与实际不符时，必须冲刷（flush）IF/ID/EX 阶段
//                 中错误取入的指令，并从正确地址重新取指，浪费 3 个周期。
//
//  与其他流水线阶段的交互关系：
//    IF  ← 本模块输出 branch_taken_out → Branch_target_computer (IF阶段)
//                                      → PC 选择逻辑 (IF阶段)
//    EX  ← EX 阶段的 branch_unit 算出 actual_taken → 反馈给本模块的 branch_in
//    IF  ← 本模块输出 wrong_predict_out → Pipeline Control (用于冲刷流水线)
//    IF  ← 本模块输出 is_branch → 标识当前取出的指令是否为分支/跳转指令
//
// ============================================================================
//  二、预测策略详解
// ============================================================================
//
//  ★ 策略选择：静态预测（Static Prediction）
//    - 不依赖历史信息，无状态寄存器，零硬件开销。
//    - 决策依据仅取决于指令类型（opcode），不随程序行为变化。
//
//  ★ B-type (BEQ/BNE/BLT/BGE/BLTU/BGEU) → 预测不跳 (branch_taken=0)
//    理由：
//      1. 多数 B-type 指令用于循环末尾的"跳回"条件判断。循环体通常执行
//         多次，而跳出循环的条件只在最后一次满足。因此"不跳"的频率 > "跳"。
//      2. 静态"不跳"预测对 B-type 的准确率约为 60%~70%，在没有 BTB
//         （Branch Target Buffer）或动态预测器的简单设计中是最优选择。
//      3. 预测"不跳"时，PC 按顺序递增（PC+4），BTB 不需要提供目标地址，
//         硬件实现最简单。
//
//  ★ JAL / JALR → 预测跳 (branch_taken=1)
//    理由：
//      1. JAL（Jump And Link）和 JALR（Jump And Link Register）是
//         无条件跳转指令，执行时 100% 会跳转，不存在"不跳"的情况。
//      2. 如果对无条件跳转预测"不跳"，则每次遇到 JAL/JALR 都会预测失败，
//         导致 100% 的误预测率，每次都要冲刷流水线，浪费 3 个周期。
//      3. 预测"跳"是最安全的选择——零误预测。
//
//  ★ 其他指令（R-type, I-type, load, store 等）→ 预测不跳 (0)
//    这些指令根本不会改变控制流，branch_taken_out 的值无关紧要，
//    因为 is_branch=0 时会屏蔽后续预测逻辑的影响。
//
// ============================================================================
//  三、端口说明
// ============================================================================
//
//  【输入端口】
//  clock              — 系统时钟。虽然本模块为纯组合逻辑（无时序元件），
//                       但保留时钟端口以备未来扩展（如加入 2-bit 饱和计数器
//                       构成动态预测器时，需要时钟驱动状态更新）。
//
//  opcode_in[6:0]     — 当前取指阶段读出的指令操作码（inst[6:0]）。
//                       RISC-V 指令集的 opcode 固定位于指令低 7 位。
//                       宽度为 7 bits（原设计为 5 bits，已修正），与
//                       RISC-V 规范一致。
//
//  reset              — 异步/同步复位信号（由上层模块决定复位方式）。
//                       在当前纯组合逻辑中未使用，保留用于未来状态机复位。
//
//  branch_in          — 实际分支结果，来自 EX（执行）阶段的 branch_unit。
//                       值为 1 表示分支实际被采纳（taken），
//                       值为 0 表示分支实际未采纳（not taken）。
//                       该信号在 EX 阶段有效，用于与预测值比较以检测误预测。
//                       对于非分支指令，该信号的值无关紧要（被 is_branch 屏蔽）。
//
//  【输出端口】
//  branch_taken_out   — 预测结果。
//                       1 = 预测该指令会跳转（branch taken），
//                           用于：控制 PC 选择目标地址而非 PC+4；
//                           配合 Branch_target_computer 计算出的目标地址。
//                       0 = 预测该指令不会跳转（branch not taken），
//                           用于：PC 正常递增（PC+4）。
//                       该信号在 IF 阶段产出，供同阶段的 PC 逻辑使用。
//
//  wrong_predict_out  — 预测错误标志。
//                       1 = 预测结果与实际结果不符（误预测），
//                           用于：触发流水线冲刷（flush），丢弃 IF/ID/EX
//                           阶段中已错误取入的指令，从正确地址重新取指。
//                       0 = 预测正确或当前指令不涉及分支。
//                       公式：wrong_predict = is_branch AND (prediction XOR actual)
//                       仅当指令是分支/跳转且预测与实际不一致时才为 1。
//
//  is_branch          — 当前指令是否为分支或跳转指令的标志。
//                       1 = 当前指令是 B-type（条件分支）或 JAL/JALR（无条件跳转），
//                           即该指令可能改变程序控制流。
//                       0 = 当前指令不涉及控制流转移（如算术、访存等）。
//                       该信号在 IF 阶段产出，可供流水线控制逻辑使用。
//
// ============================================================================
//  四、内部信号逻辑详解
// ============================================================================

`include "Def.v"

module branch_prediction_unit (
    input  wire        clock,              // 系统时钟（保留用于未来动态预测器扩展）
    input  wire [6:0]  opcode_in,          // 当前指令操作码 inst[6:0]，7 bits 匹配 RISC-V 规范
    input  wire        reset,              // 复位信号（保留用于未来状态机复位）
    input  wire        branch_in,          // 实际分支结果，来自 EX 阶段 branch_unit
    output wire        branch_taken_out,   // 预测结果：1=预测跳转, 0=预测不跳
    output wire        wrong_predict_out,  // 预测错误标志：1=预测≠实际, 0=正确或非分支
    output wire        is_branch           // 分支/跳转指令标志：1=当前指令可能改变控制流
);

    // ========================================================================
    //  4.1  指令类型识别 — 使用 Def.v 中定义的全局操作码宏
    // ========================================================================
    //
    //  通过 opcode_in 的低 7 位区分三类控制流指令。
    //  宏定义 `OPCODE_BRANCH、`OPCODE_JAL、`OPCODE_JALR 在 Def.v 中
    //  已按 RISC-V 规范赋值为对应的 7-bit 操作码常量。
    //
    //  RISC-V 操作码参考（inst[6:0]）：
    //    `OPCODE_BRANCH  = 7'b1100011   // B-type: BEQ, BNE, BLT, BGE, BLTU, BGEU
    //    `OPCODE_JAL     = 7'b1101111   // J-type: JAL
    //    `OPCODE_JALR    = 7'b1100111   // I-type: JALR
    //
    //  注意：这些比较器是纯组合逻辑，每个时钟周期都会对 opcode_in 求值。
    //  对于非分支指令（如 R-type 7'b0110011），三条 wire 全为 0。

    wire is_b_type = (opcode_in == `OPCODE_BRANCH);   // 条件分支指令 (B-type)
    wire is_jal    = (opcode_in == `OPCODE_JAL);      // 无条件跳转并链接 (JAL)
    wire is_jalr   = (opcode_in == `OPCODE_JALR);     // 无条件跳转并链接-寄存器版 (JALR)

    // is_branch 汇总：只要 opcode 匹配上述三类之一，就认为当前指令可能改变控制流。
    // 使用逻辑或（||）组合，任意一个条件成立 is_branch 即为 1。
    assign is_branch = is_b_type || is_jal || is_jalr;

    // ========================================================================
    //  4.2  预测逻辑 — 静态预测核心
    // ========================================================================
    //
    //  编码策略：
    //    - JAL 或 JALR（无条件跳转）→ 预测跳转 (1'b1)
    //      无条件跳转指令 100% 会跳转，预测"不跳"必然导致误预测。
    //    - 其他所有情况（包括 B-type 条件分支、非分支指令）
    //      → 预测不跳 (1'b0)
    //      B-type：多数条件分支不跳（循环回跳在末次才跳），
    //              静态"不跳"预测准确率可达 60%~70%。
    //      非分支：预测值无关紧要，统一给 0 减少逻辑。
    //
    //  设计理由（为什么使用三目运算符而非 case 语句）：
    //    - 只有两类结果（JAL/JALR → 1，其余 → 0），逻辑简单。
    //    - 三目运算符映射为单个 2:1 MUX，面积和延迟最小。
    //    - 综合工具对其优化成熟，不易产生意外锁存器。
    //
    //  为什么 B-type 不区分具体条件（BEQ/BNE/BLT 等）？
    //    - IF 阶段只能看到 opcode，看不到 funct3（条件码），
    //      无法区分"相等""不等""小于"等具体条件。
    //    - 即使能区分，静态预测也无法根据条件码做出更优判断。
    //    - 动态预测器通过记录历史跳转行为来解决这一问题。

    assign branch_taken_out = (is_jal || is_jalr) ? 1'b1 : 1'b0;

    // ========================================================================
    //  4.3  预测错误检测 — 通用 XOR 公式
    // ========================================================================
    //
    //  公式：wrong_predict_out = is_branch && (branch_taken_out ^ branch_in)
    //
    //  分解说明：
    //    branch_taken_out ^ branch_in  — 预测值与实际值按位异或。
    //      预测=0, 实际=0 → 0^0 = 0（正确：预测不跳，实际不跳 ✓）
    //      预测=1, 实际=1 → 1^1 = 0（正确：预测跳，实际跳   ✓）
    //      预测=0, 实际=1 → 0^1 = 1（错误：预测不跳，实际跳  ✗）
    //      预测=1, 实际=0 → 1^0 = 1（错误：预测跳，实际不跳  ✗）
    //
    //    is_branch && (...)  — 仅当指令是分支/跳转时才判断预测对错。
    //      对于非分支指令（如 ADD、LW），branch_taken_out ^ branch_in
    //      的结果无意义，必须用 is_branch 屏蔽。
    //
    //  公式通用性：
    //    该 XOR 公式适用于任何预测策略：
    //      - 静态"永远不跳"预测（本设计）
    //      - 静态"永远跳"预测
    //      - 动态 2-bit 饱和计数器（BTB 常用）
    //      - 全局/局部历史预测器（Gshare, Tournament 等）
    //    无论用哪种策略得到 branch_taken_out，wrong_predict_out 的
    //    计算方式不变。

    assign wrong_predict_out = is_branch && (branch_taken_out ^ branch_in);

endmodule
