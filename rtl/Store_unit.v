// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║                        Store_unit.v — 存储指令执行单元                         ║
// ╟──────────────────────────────────────────────────────────────────────────────╢
// ║  ★ 模块在 CPU 流水线中的角色：                                                  ║
// ║     位于 执行阶段（EX），承接 译码阶段（ID）送来的 rs2 数据、funct3、               ║
// ║     地址低 2 位，以及来自控制逻辑的 mem_write 使能。                               ║
// ║     输出对齐后的写数据 rs2_out、字节使能掩码 wb_mask_out、写请求 wr_req_out，        ║
// ║     直接交付给 访存阶段（MEM）/ LSU（Load-Store Unit）使用。                       ║
// ║                                                                              ║
// ║  ★ 核心功能：                                                                  ║
// ║     1. 根据存储宽度（SB/SH/SW）生成 4 位字节写掩码（byte write enable mask）。       ║
// ║     2. 将 rs2 低位的待写数据 移位对齐 到目标字节/半字在 32-bit 字中的自然位置。        ║
// ║     3. 向 LSU 发出写请求信号。                                                  ║
// ║                                                                              ║
// ║  ★ 设计原理 — RISC-V 非对齐存储的寄存器-内存映射：                                 ║
// ║     RISC-V 的 SB/SH/SW 指令中，要写入的数据总是存放在 rs2 寄存器 的 低 bit 上        ║
// ║     （SB: rs2[7:0], SH: rs2[15:0], SW: rs2[31:0] 全字）。                       ║
// ║     但内存总线是 32-bit 宽的，写入时必须把数据放在对应字节通道（byte lane）上。        ║
// ║     举例：SB 写入地址 0x1001（addr[1:0]=01）时，rs2[7:0] 里那个字节必须搬到          ║
// ║           位 [15:8] 的位置，这样内存控制器写入地址 0x1000 处的 32-bit 字时，           ║
// ║           实际的 byte 才会落在物理地址 0x1001 上。                                ║
// ║     同时 wb_mask_out 告诉内存控制器：只使能目标字节的写，其余字节保持不变。            ║
// ║                                                                              ║
// ║  ★ 与其他流水级的交互：                                                          ║
// ║     ← 输入端 ←                                                                ║
// ║     · ID 阶段：rs2_in（寄存器堆读出的待写数据）                                    ║
// ║     · ID 阶段：funct3_in[1:0]（指令编码中的宽度字段，00=SB, 01=SH, 10/11=SW）      ║
// ║     · EX 阶段：addr_low[1:0]（AGU 算出的存储地址的低 2 位，决定字节偏移）            ║
// ║     · 控制逻辑：mem_write（来自译码或控制器，1=当前指令为 store）                    ║
// ║     → 输出端 →                                                                ║
// ║     · MEM/LSU：rs2_out（对齐后的写数据，连到数据总线的写数据端口）                    ║
// ║     · MEM/LSU：wb_mask_out（4-bit 字节写使能，连到 SRAM/Dcache 的 byteen 端口）    ║
// ║     · MEM/LSU：wr_req_out（写请求，触发 LSU 执行内存写入）                          ║
// ║                                                                              ║
// ║  ★ 写掩码生成规则（带地址偏移）：                                                   ║
// ║     SB (funct3[1:0]=00): 掩码 = 4'b0001 << addr[1:0]                           ║
// ║         addr=00→0001  addr=01→0010  addr=10→0100  addr=11→1000                ║
// ║     SH (funct3[1:0]=01): 掩码 = addr[1] ? 4'b1100 : 4'b0011                    ║
// ║         addr[1]=0 (低半字 0/1)→0011    addr[1]=1 (高半字 2/3)→1100               ║
// ║     SW (default):        掩码 = 4'b1111（全字写入，4 字节全使能）                    ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

module store_unit (
    // ---- 输入端口 ----------------------------------------------------------------
    // rs2_in: 来自寄存器堆 rs2 的待写入数据（32-bit）。
    //         · SB 时有效数据在 [7:0]  低字节
    //         · SH 时有效数据在 [15:0] 低半字
    //         · SW 时有效数据在 [31:0] 全字
    //         本模块负责将其移位对齐到输出字中正确的 byte lane。
    input  wire [31:0] rs2_in,

    // funct3_in: 来自指令编码 funct3 的低 2 位，决定存储宽度。
    //         · 2'b00 = SB (Store Byte)   — 写 1 字节
    //         · 2'b01 = SH (Store Halfword) — 写 2 字节
    //         · 2'b10 / 2'b11 = SW (Store Word) — 写 4 字节
    input  wire [1:0]  funct3_in,

    // addr_low: 存储地址的低 2 位 [1:0]，即地址 mod 4 的偏移，决定数据放在哪个 byte lane。
    //         · 由 AGU（地址生成单元）在 EX 阶段计算产生
    //         · 0 = 0 字节偏移，1 = 1 字节偏移，2 = 2 字节偏移，3 = 3 字节偏移
    //         · 用于 wb_mask 生成和数据移位对齐两个关键功能
    input  wire [1:0]  addr_low,

    // mem_write: 来自控制逻辑的存储使能信号。
    //         · 1 = 当前指令为 store（SB / SH / SW），需要进行内存写入
    //         · 0 = 当前指令不是 store（可能是 load、ALU 等），本模块输出无效
    //         · 替代旧的 op_for 信号 —— 语义更明确，直接表达"是否写内存"
    input  wire        mem_write,

    // ---- 输出端口 ----------------------------------------------------------------
    // rs2_out: 对齐后的写数据，连接到 LSU 的写数据总线。
    //         · store 指令时输出 data_aligned（数据已按 byte lane 对齐）
    //         · 非 store 指令时输出 32'b0（节省功耗、防止误写）
    //         · LSU 将此数据与 wb_mask_out 配合，写入 Dcache / 数据 SRAM
    output wire [31:0] rs2_out,

    // wb_mask_out: 字节写使能掩码（write byte mask），4-bit，每位控制一个字节通道。
    //         · bit[0] → data[7:0]   字节 0（地址偏移 0）
    //         · bit[1] → data[15:8]  字节 1（地址偏移 1）
    //         · bit[2] → data[23:16] 字节 2（地址偏移 2）
    //         · bit[3] → data[31:24] 字节 3（地址偏移 3）
    //         · 某位为 1：对应字节被写入；某位为 0：对应字节保持不变
    //         · 连接到 SRAM 的 byteen / ben 端口或 Dcache 的字节使能接口
    output reg  [3:0]  wb_mask_out,

    // wr_req_out: 写请求信号，输出给 LSU（Load-Store Unit）或内存控制器。
    //         · 1 = 发起一次内存写操作
    //         · 0 = 无写请求
    //         · 直接透传 mem_write，无组合逻辑延迟
    output wire        wr_req_out
);

    // =========================================================================
    //  写请求生成
    // =========================================================================
    // 写请求直接等于 mem_write 信号。
    // 这样 LSU 在同一周期就能收到写请求，不必再等一拍。
    // 时序路径：mem_write（控制逻辑） → wr_req_out → LSU → Dcache
    assign wr_req_out = mem_write;

    // =========================================================================
    //  写掩码生成（★ 根据 addr_low 移位 ★）
    // =========================================================================
    // 设计原理：
    //   32-bit 内存总线一次访问 4 字节，但我们可能只想写其中 1~4 个字节。
    //   4-bit 掩码 wb_mask_out 每一位控制一个字节通道的写入使能。
    //
    //   RISC-V 规范定义的字节序为 little-endian：
    //     物理地址 A[1:0] 为 0 的字节 → 总线上 data[7:0] （掩码 bit[0]）
    //     物理地址 A[1:0] 为 1 的字节 → 总线上 data[15:8]（掩码 bit[1]）
    //     物理地址 A[1:0] 为 2 的字节 → 总线上 data[23:16]（掩码 bit[2]）
    //     物理地址 A[1:0] 为 3 的字节 → 总线上 data[31:24]（掩码 bit[3]）
    //
    //   各分支解释：
    //     SB: 只写 1 个字节。将 4'b0001 左移 addr_low 位，
    //         使能位就精确落在目标字节对应的 bit 上。
    //         例：addr_low=2 → 掩码 4'b0100 → 只写 byte[2]（地址 0x???????2）
    //
    //     SH: 写 2 个字节（半字）。RISC-V 要求半字对齐（地址为 2 的倍数），
    //         所以 addr_low[0] 必然是 0（若不对齐会触发异常，但 LSU 另做处理）。
    //         因此只需看 addr_low[1]:
    //           addr_low[1]=0 → 低半字 [1:0]=[0,1] → 掩码 4'b0011（写 byte 0+1）
    //           addr_low[1]=1 → 高半字 [1:0]=[2,3] → 掩码 4'b1100（写 byte 2+3）
    //
    //     SW: 写 4 个字节（全字），掩码全 1，所有 byte lane 都使能。
    //
    //   ★ 为什么用 always @(*) 而不写 default？
    //     funct3_in 只有 2 位，00/01 显式处理，10/11 走 default 分支。
    //     若未来扩展或出现意外状态，default=4'b1111 最安全（全写，不漏数据）。
    always @(*) begin
        case (funct3_in)
            2'b00:   wb_mask_out = 4'b0001 << addr_low;                // SB：单字节掩码，移位到目标 byte lane
            2'b01:   wb_mask_out = addr_low[1] ? 4'b1100 : 4'b0011;   // SH：双字节掩码，高半字或低半字
            default: wb_mask_out = 4'b1111;                            // SW：全字掩码，4 字节全部使能
        endcase
    end

    // =========================================================================
    //  写数据对齐（★ 根据 addr_low 将 rs2 低位数移到正确的 byte lane ★）
    // =========================================================================
    // 设计原理：
    //   RISC-V 规定 rs2 中待写数据始终在寄存器的 低位：
    //     SB: 待写 byte 在 rs2[7:0]
    //     SH: 待写 halfword 在 rs2[15:0]
    //     SW: 待写 word 在 rs2[31:0]（无需移位）
    //
    //   但内存总线是 32-bit 宽的，数据必须放在对应地址偏移的 byte lane 上。
    //   例如：SB 写入地址 0x1001 → 该 byte 在 32-bit 数据字中位于 [15:8]
    //         需要将 rs2[7:0] 拷贝到 data_aligned[15:8]，其他地方填 0。
    //
    //   因为 wb_mask_out 会屏蔽不需要的字节，所以其他位置填 0 即可（也可填任意值）。
    //
    //   各分支解释：
    //     SB (2'b00)：只有 rs2[7:0] 有效。
    //       用内层 case(addr_low) 精确将 byte 放到目标字节位置：
    //         addr_low=00 → [7:0]   （地址 0 字节偏移）
    //         addr_low=01 → [15:8]  （地址 1 字节偏移）
    //         addr_low=10 → [23:16] （地址 2 字节偏移）
    //         addr_low=11 → [31:24] （地址 3 字节偏移）
    //       其余字节填 0（会被 wb_mask_out 屏蔽，不影响内存内容）。
    //
    //     SH (2'b01)：只有 rs2[15:0] 有效。
    //       RISC-V 半字对齐要求 addr_low[0]=0，所以只有两种可能：
    //         addr_low[1]=0 → 低半字 [15:0]   ← 数据在 rs2[15:0] 位置不变
    //         addr_low[1]=1 → 高半字 [31:16]  ← 需将 rs2[15:0] 搬到 [31:16]
    //       其余半字填 0（会被 wb_mask_out 屏蔽）。
    //
    //     SW (default)：全字写入，数据本身就在 rs2[31:0]，直接透传即可。
    //
    //   ★ 为什么用组合逻辑 always @(*) 和阻塞赋值？
    //     所有 case 分支相互排斥，不会产生锁存器。
    //     使用 = 阻塞赋值保证 case 按顺序求值（虽然这里无优先级依赖，但风格统一）。
    reg [31:0] data_aligned;
    always @(*) begin
        case (funct3_in)
            2'b00: begin  // SB: 将 rs2 的低字节复制到目标 byte lane
                data_aligned = 32'b0;   // 先全部清 0（非目标字节由写掩码屏蔽）
                case (addr_low)
                    2'b00: data_aligned[7:0]   = rs2_in[7:0];   // 字节偏移 0，数据放 bit[7:0]
                    2'b01: data_aligned[15:8]  = rs2_in[7:0];   // 字节偏移 1，数据放 bit[15:8]
                    2'b10: data_aligned[23:16] = rs2_in[7:0];   // 字节偏移 2，数据放 bit[23:16]
                    2'b11: data_aligned[31:24] = rs2_in[7:0];   // 字节偏移 3，数据放 bit[31:24]
                endcase
            end
            2'b01: begin  // SH: 将 rs2 的低半字复制到目标半字位置
                data_aligned = 32'b0;   // 先全部清 0
                if (addr_low[1])
                    data_aligned[31:16] = rs2_in[15:0];  // 高半字地址（偏移 2/3），数据搬到 [31:16]
                else
                    data_aligned[15:0]  = rs2_in[15:0];  // 低半字地址（偏移 0/1），数据留在 [15:0]
            end
            default: data_aligned = rs2_in;  // SW: 全字写入，rs2 全字直接透传，无需移位
        endcase
    end

    // =========================================================================
    //  rs2_out 输出选择
    // =========================================================================
    // 仅在当前指令是 store 时才输出对齐后的数据。
    // mem_write=0（非 store 指令）时输出全 0：
    //   · 防止无效数据翻转消耗动态功耗
    //   · 配合 wr_req_out=0 使 LSU 忽略该数据通路
    //   · 避免组合逻辑毛刺误触发 LSU 的部分写逻辑
    assign rs2_out = mem_write ? data_aligned : 32'b0;

endmodule
