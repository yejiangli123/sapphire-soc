// ╔══════════════════════════════════════════════════════════════════════════╗
// ║  ALU_Control.v — ALU 操作码译码器（组合逻辑）                          ║
// ╚══════════════════════════════════════════════════════════════════════════╝
//
// ──────────────────────────────────────────────────────────────────────────
// 一、模块在 CPU 流水线中的角色与位置
// ──────────────────────────────────────────────────────────────────────────
//
//   ALU_Control 位于 ID（译码）阶段，紧接在 Control_unit 主译码器之后。
//   它负责将 RISC-V 指令中的 opcode、funct3、funct7 字段“翻译”为一个
//   4-bit 的 ALU 操作码（aluop），下发给 EX（执行）阶段的 ALU 模块。
//
//   流水线数据流向：
//     IF → ID(Control_unit → ALU_Control) → EX(ALU) → MEM → WB
//
//   设计定位：这是一个纯组合逻辑的“二级译码器”。
//   - 第一级（Control_unit）：opcode → 宏观控制信号（RegWrite, MemRead 等）
//   - 第二级（ALU_Control）：opcode + funct3 + funct7 → 具体 ALU 算/逻操作码
//
// ──────────────────────────────────────────────────────────────────────────
// 二、输入/输出端口说明
// ──────────────────────────────────────────────────────────────────────────
//
//   ┌─────────────────────────────────────────────────────────────────────┐
//   │ 端口名    │ 位宽  │ 方向 │ 来源/去向          │ 用途               │
//   ├─────────────────────────────────────────────────────────────────────┤
//   │ opcode    │ [6:0] │ in   │ 指令 [6:0]         │ 区分指令大类(R/I/B/访存等) │
//   │ funct3    │ [2:0] │ in   │ 指令 [14:12]       │ 区分同类指令中的子操作     │
//   │ funct7    │ [6:0] │ in   │ 指令 [31:25]       │ 仅用 bit[5] 区分 ADD/SUB、SRL/SRA │
//   │ aluop     │ [3:0] │ out  │ → ALU 模块         │ ALU 执行的具体运算编码      │
//   └─────────────────────────────────────────────────────────────────────┘
//
//   ★ 关键设计点：
//   funct7 的 7 位中只用到了 bit[5]（即指令的第 30 位）。原因：
//   - RISC-V ISA 中：
//       func7 = 0x00 (bit5=0) → ADD,  SRL
//       func7 = 0x20 (bit5=1) → SUB,  SRA
//   - 其余 bit 在标准 RV32I 中恒为 0 或不需要区分，故仅检查 bit5 即可。
//
// ──────────────────────────────────────────────────────────────────────────
// 三、aluop 编码表（与 Def.v 中的 ALU_* 宏一一对应）
// ──────────────────────────────────────────────────────────────────────────
//
//   ┌──────────┬──────────┬────────────────────────────────────┐
//   │ aluop    │ 宏名     │ 含义                               │
//   ├──────────┼──────────┼────────────────────────────────────┤
//   │ 4'b0000  │ ALU_ADD  │ 加法（有符号/无符号加法位宽相同）  │
//   │ 4'b0001  │ ALU_SUB  │ 减法（用于分支比较和 SUB 指令）    │
//   │ 4'b0010  │ ALU_XOR  │ 按位异或                           │
//   │ 4'b0011  │ ALU_OR   │ 按位或                             │
//   │ 4'b0100  │ ALU_AND  │ 按位与                             │
//   │ 4'b0101  │ ALU_SLL  │ 逻辑左移（低位补 0）               │
//   │ 4'b0110  │ ALU_SRL  │ 逻辑右移（高位补 0）               │
//   │ 4'b0111  │ ALU_SRA  │ 算术右移（高位补符号位）           │
//   │ 4'b1000  │ ALU_SLT  │ 有符号比较 置位（a < b ? 1 : 0）  │
//   │ 4'b1001  │ ALU_SLTU │ 无符号比较 置位（a < b ? 1 : 0）  │
//   └──────────┴──────────┴────────────────────────────────────┘
//
//   编码设计理由：
//   - ADD=0 作为默认安全值（上电/非法指令时加法不会破坏状态）
//   - 最低位区分 ADD/SUB（bit0=0→加, bit0=1→减），以便 ALU 内部复用加法器
//   - SLT/SLTU 使用 4'b100x 区间，与 I-type 立即数 SLT 系列编码空间对齐
//
// ──────────────────────────────────────────────────────────────────────────
// 四、各类指令的译码逻辑详解
// ──────────────────────────────────────────────────────────────────────────
//
// ─── 4.1 R-type（OPCODE_OP = 7'b0110011）：寄存器-寄存器运算
//
//   译码策略：funct3 为主、funct7[5] 为辅。
//
//   ┌────────┬───────────┬────────────┬──────────────────────────────────┐
//   │ funct3 │ 指令      │ aluop      │ 设计理由                         │
//   ├────────┼───────────┼────────────┼──────────────────────────────────┤
//   │ 000    │ ADD / SUB │ ADD / SUB  │ 仅 funct3 无法区分，需 funct7[5] │
//   │ 001    │ SLL       │ SLL        │ 移位量由 rs2[4:0] 提供           │
//   │ 010    │ SLT       │ SLT        │ 有符号比较                       │
//   │ 011    │ SLTU      │ SLTU       │ 无符号比较                       │
//   │ 100    │ XOR       │ XOR        │ 按位异或                         │
//   │ 101    │ SRL / SRA │ SRL / SRA  │ funct7[5]=0→逻辑右移, =1→算术右移│
//   │ 110    │ OR        │ OR         │ 按位或                           │
//   │ 111    │ AND       │ AND        │ 按位与                           │
//   └────────┴───────────┴────────────┴──────────────────────────────────┘
//
//   ★ 为什么 ADD/SUB 共用一个 funct3=000？
//     RISC-V 精简设计：ADD 和 SUB 只在 funct7[5] 不同，前者为 0、后者为 1，
//     这样可以节省 funct3 编码空间，以容纳更多指令类型。
//
// ─── 4.2 I-type ALU（OPCODE_OP_IMM = 7'b0010011）：寄存器-立即数运算
//
//   译码策略与 R-type 完全一致，因为 RISC-V 故意让 I-type ALU 的 funct3
//   与 R-type ALU 的 funct3 一一对应：
//     ADDI↔ADD, SLLI↔SLL, SLTI↔SLT, XORI↔XOR, SRLI/SRAI↔SRL/SRA,
//     ORI↔OR, ANDI↔AND
//
//   ★ 为什么 I-type 没有 SUBI？
//     减法可以通过 ADDI 加负数立即数实现：rs1 - imm = rs1 + (-imm)，
//     因此不需要专门的 SUBI 指令，体现了 RISC-V 架构的完整性原则。
//
// ─── 4.3 B-type（OPCODE_BRANCH = 7'b1100011）：条件分支
//
//   这是本模块最“不直观”但最精巧的部分。分支指令并不直接将 ALU 结果
//   写回寄存器堆，而是利用 ALU 计算结果送给 Branch_unit 做条件判断。
//
//   ┌────────┬──────────┬────────┬──────────────────────────────────────┐
//   │ funct3 │ 指令     │ aluop  │ Branch_unit 如何使用 ALU 结果         │
//   ├────────┼──────────┼────────┼──────────────────────────────────────┤
//   │ 000    │ BEQ      │ SUB    │ rs1 - rs2 == 0 → 相等则跳转           │
//   │ 001    │ BNE      │ SUB    │ rs1 - rs2 != 0 → 不等则跳转           │
//   │ 100    │ BLT      │ SLT    │ rs1 < rs2 (有符号) → 小于则跳转       │
//   │ 101    │ BGE      │ SLT    │ rs1 ≥ rs2 (有符号) → Branch_unit 取反  │
//   │ 110    │ BLTU     │ SLTU   │ rs1 < rs2 (无符号) → 小于则跳转       │
//   │ 111    │ BGEU     │ SLTU   │ rs1 ≥ rs2 (无符号) → Branch_unit 取反  │
//   └────────┴──────────┴────────┴──────────────────────────────────────┘
//
//   ★ 为什么 BEQ/BNE 都用 SUB、BGE/BGEU 复用 SLT/SLTU？
//     - BEQ/BNE：做减法，结果是否为 0 即判相等。Brach_unit 根据指令
//       是 BEQ 还是 BNE 决定跳转极性（Zero==1 跳 vs Zero==0 跳）。
//     - BGE：BGE(rs1,rs2) ≡ !BLT(rs1,rs2)。ALU 统一算 SLT，
//       Branch_unit 根据funct3 的最高位 (bit2) 决定是否翻转条件。
//       这样可以复用同一套 ALU 比较器，节省面积。
//
//   ★ 分支类 ALU 结果为何不写回寄存器堆？
//     写控制由 Control_unit 的 RegWrite 信号控制。对于 B-type 指令，
//     RegWrite=0，因此 ALU 的减法/比较结果只被 Branch_unit 消费，
//     不存在写入 RF 的数据通路，这是经典的旁路设计。
//
// ─── 4.4 地址计算类指令：统一映射为 ADD
//
//   以下指令都需要“两个操作数的加法”来计算有效地址或得到最终结果：
//
//   ┌─────────────────────────────┬────────────────────────────────────────┐
//   │ 指令                       │ 加法操作                               │
//   ├─────────────────────────────┼────────────────────────────────────────┤
//   │ LOAD  (LW/LH/LB/LBU/LHU)   │ rs1（基址寄存器）+ 符号扩展立即数偏移  │
//   │ STORE (SW/SH/SB)           │ rs1（基址寄存器）+ 符号扩展立即数偏移  │
//   │ JALR                       │ rs1 + 符号扩展立即数，结果写 PC 和 rd  │
//   │ LUI                        │ 0 + 立即数左移12位（直通加法器）       │
//   │ AUIPC                      │ PC + 立即数左移12位，结果写 rd          │
//   └─────────────────────────────┴────────────────────────────────────────┘
//
//   ★ 为什么 LUI 用 ADD 而不是直通？
//     LUI 的语义是 rd = imm << 12。在硬件实现上，两个操作数分别是
//     0 和 (imm<<12)，通过 ADD 加法器计算 0 + (imm<<12)，结果等价。
//     这样做的好处是复用 ALU 的加法器，而不需要额外的直通数据通路。
//
//   ★ default 分支为何也设为 ALU_ADD？
//     - 当 opcode 不属于任何已定义指令时（如非法指令），ALU 执行 ADD
//       是一种安全退化：加法不会产生异常值，电路也不会进入不定态。
//     - ALU 结果仍可能被下游模块消费，但最终是否写回由 Control_unit
//       的写使能信号决定，非法指令不会破坏架构状态。
//
// ──────────────────────────────────────────────────────────────────────────
// 五、与其他流水线阶段的交互关系
// ──────────────────────────────────────────────────────────────────────────
//
//   ┌───────────┐    opcode[6:0]     ┌───────────────┐
//   │  IF 阶段   │ ────指令──→       │  ID 阶段       │
//   │ (取指)     │                   │                │
//   │           │                   │ Control_unit ──→ RegWrite, MemRead,
//   │ 指令存储器 │                   │ (主控制)        MemWrite, ALUSrc, ...
//   └───────────┘                   │                │
//                                   │ ALU_Control  ──→ aluop[3:0]
//                                   │ (本模块)        │
//                                   └───────┬────────┘
//                                           │ aluop
//                                           ▼
//   ┌───────────┐                  ┌───────────────┐
//   │  WB 阶段   │ ←────────────── │  EX 阶段       │
//   │ (写回)     │    ALU 结果     │                │
//   │           │                  │ ALU 模块        │
//   │ 寄存器堆  │                  │ (执行单元)      │
//   └───────────┘                  └───────────────┘
//
//   具体交互：
//   ① Control_unit → ALU_Control：无直接连线。两者并列接收 opcode，各自译码
//   ② ALU_Control → ALU：aluop 直接控制 ALU 内部的运算选择 MUX
//   ③ ALU_Control → Branch_unit（间接）：通过 ALU 的 SUB/SLT/SLTU 结果，
//      Branch_unit 结合 funct3 作最终跳转判决
//   ④ 时钟/复位：本模块为纯组合逻辑，无 clk、rst，无需进行时序约束
//
// ──────────────────────────────────────────────────────────────────────────
// 六、设计权衡与备选方案
// ──────────────────────────────────────────────────────────────────────────
//
//   方案A（当前实现）：组合逻辑 case 嵌套
//     ✓ 面积小，延迟可控（两级 MUX 深度）
//     ✓ 代码直观，易于对照 ISA 手册验证
//     ✗ 指令集扩展时需要添加新的 case 分支
//
//   方案B：ROM / 查找表 译码
//     ✓ 规则性强，扩展时直接填表
//     ✗ 面积更大，需要时钟（增加延迟）
//     ✗ 对于 RV32I 的 ~40 条指令而言 ROM 过于奢侈
//
//   方案C：结构化译码（逻辑门直接推导）
//     ✓ 延迟最低
//     ✗ 可维护性差，修改一条指令可能牵动全局逻辑
//
//   结论：对于 RV32I 基础整数指令集，组合逻辑 case 嵌套是最佳平衡点。
//   本模块的编码覆盖：
//     - R-type：10 条（ADD/SUB/SLL/SLT/SLTU/XOR/SRL/SRA/OR/AND）
//     - I-type ALU：9 条（ADDI/SLLI/SLTI/SLTIU/XORI/SRLI/SRAI/ORI/ANDI）
//     - Branch：6 条（BEQ/BNE/BLT/BGE/BLTU/BGEU）
//     - 访存/跳转：5 条（LW/LH/LB/SW/SH/SB + JALR/LUI/AUIPC，均映射为 ADD）
//     共约 30 条指令，case 嵌套完全胜任。

`include "Def.v"

module alu_control_unit (
    // ── 输入 ──
    input  [6:0] opcode,           // 指令 opcode（指令字 [6:0]）
                                   // 来源：ID 阶段从指令存储器读出的 32-bit 指令
                                   // 作用：区分指令大类
                                   //   OPCODE_OP       (0110011): R-type 寄存器运算
                                   //   OPCODE_OP_IMM   (0010011): I-type 立即数运算
                                   //   OPCODE_BRANCH   (1100011): B-type 条件分支
                                   //   OPCODE_LOAD     (0000011): 访存 Load
                                   //   OPCODE_STORE    (0100011): 访存 Store
                                   //   OPCODE_JALR     (1100111): 跳转并链接寄存器
                                   //   OPCODE_LUI      (0110111): 加载高位立即数
                                   //   OPCODE_AUIPC    (0010111): PC 加立即数

    input  [2:0] funct3,           // funct3 字段（指令字 [14:12]）
                                   // 来源：ID 阶段从指令中提取
                                   // 作用：在 opcode 确定的大类内，选择具体子操作
                                   // 例如 opcode=R-type 时，funct3=000 代表 ADD/SUB
                                   //                          funct3=111 代表 AND

    input  [6:0] funct7,           // funct7 字段（指令字 [31:25]）
                                   // 来源：ID 阶段从指令中提取
                                   // 作用：本模块仅使用 bit[5]（即指令第 30 位）
                                   //   bit5 = 0：ADD (funct3=000 时)、SRL (funct3=101 时)
                                   //   bit5 = 1：SUB (funct3=000 时)、SRA (funct3=101 时)
                                   // 对于非 000/101 的 funct3，funct7 的值不影响译码结果
                                   // （RV32I 标准要求这些情况下 funct7 为 0x00）

    // ── 输出 ──
    output reg [3:0] aluop         // ALU 操作码
                                   // 去向：EX 阶段的 ALU 模块（算术逻辑单元）
                                   // 编码：参见 Def.v 中的 ALU_* 宏定义
                                   //   0000 = ADD    0001 = SUB
                                   //   0010 = XOR    0011 = OR
                                   //   0100 = AND    0101 = SLL
                                   //   0110 = SRL    0111 = SRA
                                   //   1000 = SLT    1001 = SLTU
);

    // ══════════════════════════════════════════════════════════════════════
    // 组合逻辑译码：opcode + funct3 + funct7[5] → aluop
    // ══════════════════════════════════════════════════════════════════════
    //
    // 译码过程分两级：
    //   第一级（外层 case）：按 opcode 将指令分为 R/I/B/访存/跳转 五大类
    //   第二级（内层 case）：按 funct3 细分为具体的 ALU 操作
    //   funct7[5] 仅在 funct3=000(ADD/SUB) 和 101(SRL/SRA) 时参与判断
    //
    // 默认情况下 aluop = ALU_ADD，作为安全回退。
    always @(*) begin
        case (opcode)

            // ==============================================================
            // R-type (OPCODE_OP = 7'b0110011)：寄存器-寄存器 ALU 运算
            // ==============================================================
            // 格式：| funct7[31:25] | rs2[24:20] | rs1[19:15] | funct3[14:12] | rd[11:7] | opcode[6:0] |
            // 操作：rd = rs1 OP rs2
            // 两个源操作数均来自寄存器堆，ALU 结果写回 rd。
            `OPCODE_OP: begin
                case (funct3)
                    // ── funct3 = 000：ADD 或 SUB ──
                    //    区分依据：funct7[5]（指令第 30 位）
                    //      funct7 = 0x00 (bit5=0) → ADD  (rd = rs1 + rs2)
                    //      funct7 = 0x20 (bit5=1) → SUB  (rd = rs1 - rs2)
                    //    为什么共用 funct3？RISC-V 将加减视为"同类二元运算"，
                    //    用 funct7 的一个 bit 区分即可，节省 funct3 编码空间。
                    3'b000: aluop = funct7[5] ? `ALU_SUB  : `ALU_ADD;

                    // ── funct3 = 001：SLL 逻辑左移 ──
                    //    rd = rs1 << rs2[4:0]（仅用 rs2 低 5 位做移位量）
                    //    低位补 0，溢出位丢弃（无符号扩展）。
                    3'b001: aluop = `ALU_SLL;

                    // ── funct3 = 010：SLT 有符号比较置位 ──
                    //    rd = (rs1 < rs2) ? 1 : 0（将 rs1、rs2 视为有符号数）
                    //    结果只有 0 或 1，所以只需 1 bit 信息量，但约定扩展为字长。
                    3'b010: aluop = `ALU_SLT;

                    // ── funct3 = 011：SLTU 无符号比较置位 ──
                    //    rd = (rs1 < rs2) ? 1 : 0（将 rs1、rs2 视为无符号数）
                    3'b011: aluop = `ALU_SLTU;

                    // ── funct3 = 100：XOR 按位异或 ──
                    3'b100: aluop = `ALU_XOR;

                    // ── funct3 = 101：SRL 或 SRA ──
                    //    区分依据：funct7[5]
                    //      funct7 = 0x00 (bit5=0) → SRL：高位补 0（逻辑右移）
                    //      funct7 = 0x20 (bit5=1) → SRA：高位补符号位（算术右移）
                    //    注意：SLL 不需要 funct7 区分，因为左移只有一种语义；
                    //    右移才分逻辑/算术，因为涉及符号扩展。
                    3'b101: aluop = funct7[5] ? `ALU_SRA  : `ALU_SRL;

                    // ── funct3 = 110：OR 按位或 ──
                    3'b110: aluop = `ALU_OR;

                    // ── funct3 = 111：AND 按位与 ──
                    3'b111: aluop = `ALU_AND;

                    // ── 未定义的 funct3（RV32I 不会出现）→ 安全默认 ADD ──
                    default: aluop = `ALU_ADD;
                endcase
            end

            // ==============================================================
            // I-type ALU (OPCODE_OP_IMM = 7'b0010011)：寄存器-立即数运算
            // ==============================================================
            // 格式：| imm[11:0] | rs1[19:15] | funct3[14:12] | rd[11:7] | opcode[6:0] |
            // 操作：rd = rs1 OP imm（imm 经符号扩展至 32 位）
            //
            // 译码结构与 R-type 完全对称——这是 RISC-V ISA 刻意设计的：
            // I-type ALU 的 funct3 编码与 R-type ALU 一一对应，
            // 唯一的区别在于第二个操作数来源（立即数 vs 寄存器），
            // 该选择由 Control_unit 的 ALUSrc 信号控制，不在本模块处理。
            `OPCODE_OP_IMM: begin
                case (funct3)
                    // ADDI：rd = rs1 + sext(imm)
                    3'b000: aluop = `ALU_ADD;

                    // SLLI：rd = rs1 << shamt（shamt = imm[4:0]）
                    // 注意：RV32I 中 SLLI 的 funct7 必须为 0x00，
                    // imm[11:5] 位全部为 0，但本模块不检查——由上层保证。
                    3'b001: aluop = `ALU_SLL;

                    // SLTI：rd = (rs1 < sext(imm)) ? 1 : 0（有符号）
                    3'b010: aluop = `ALU_SLT;

                    // SLTIU：rd = (rs1 < sext(imm)) ? 1 : 0（无符号）
                    // 注意：即使立即数经过符号扩展，无符号比较仍然正确，
                    // 因为 SLTIU 的比较操作数位宽相同（均为 32 位）。
                    3'b011: aluop = `ALU_SLTU;

                    // XORI：rd = rs1 ^ sext(imm)
                    3'b100: aluop = `ALU_XOR;

                    // SRLI / SRAI：funct7[5] 区分
                    //   funct7=0x00 → SRLI（逻辑右移立即数）
                    //   funct7=0x20 → SRAI（算术右移立即数）
                    //   imm[11:5] 在 SRLI 时为 0x00，在 SRAI 时为 0x20
                    3'b101: aluop = funct7[5] ? `ALU_SRA  : `ALU_SRL;

                    // ORI：rd = rs1 | sext(imm)
                    3'b110: aluop = `ALU_OR;

                    // ANDI：rd = rs1 & sext(imm)
                    3'b111: aluop = `ALU_AND;

                    default: aluop = `ALU_ADD;
                endcase
            end

            // ==============================================================
            // B-type (OPCODE_BRANCH = 7'b1100011)：条件分支
            // ==============================================================
            // 格式：| imm[12|10:5] | rs2[24:20] | rs1[19:15] | funct3[14:12] | imm[4:1|11] | opcode[6:0] |
            //
            // ★ 核心理念：ALU 只负责"计算比较结果"，跳转决策由 Branch_unit 完成。
            //   这实现了关注点分离：
            //     - ALU 提供 rs1 - rs2（SUB）或 rs1 < rs2（SLT/SLTU）的结果
            //     - Branch_unit 结合 funct3 和 ALU 结果决定是否跳转
            //
            // ★ 为什么 BEQ/BNE 都用 SUB？
            //   BEQ：rs1 == rs2  ⇔  rs1 - rs2 == 0  → Branch_unit 检查 Zero 标志
            //   BNE：rs1 != rs2  ⇔  rs1 - rs2 != 0  → Branch_unit 检查 !Zero 标志
            //   两者都依赖同一个减法结果，区别只在于 Branch_unit 的判断极性。
            //
            // ★ 为什么 BGE/BGEU 分别复用 SLT/SLTU？
            //   BGE  (rs1, rs2)  ⇔  rs1 ≥ rs2  ⇔  !(rs1 < rs2)  ⇔  !SLT(rs1, rs2)
            //   BGEU (rs1, rs2)  ⇔  rs1 ≥ rs2  ⇔  !(rs1 < rs2)  ⇔  !SLTU(rs1, rs2)
            //   ALU 统一计算 SLT/SLTU，Branch_unit 根据 funct3 决定是否取反。
            //   这样节省了一组减法/比较器，面积和功耗都更优。
            `OPCODE_BRANCH: begin
                case (funct3)
                    3'b000: aluop = `ALU_SUB;    // BEQ  — 相等则跳（Zero=1 时跳转）
                    3'b001: aluop = `ALU_SUB;    // BNE  — 不等则跳（Zero=0 时跳转）
                    3'b100: aluop = `ALU_SLT;    // BLT  — 有符号小于（SLT=1 时跳转）
                    3'b101: aluop = `ALU_SLT;    // BGE  — 有符号≥（SLT=0 时跳转，Branch_unit 取反）
                    3'b110: aluop = `ALU_SLTU;   // BLTU — 无符号小于（SLTU=1 时跳转）
                    3'b111: aluop = `ALU_SLTU;   // BGEU — 无符号≥（SLTU=0 时跳转，Branch_unit 取反）
                    default: aluop = `ALU_SUB;   // 未定义 funct3 → 默认减法（安全）
                endcase
            end

            // ==============================================================
            // 地址计算类指令：统一映射为 ALU_ADD
            // ==============================================================
            //
            // 以下指令的 ALU 计算本质都是两个 32-bit 数的加法：
            //
            //   STORE (OPCODE_STORE):
            //     计算目标地址：addr = rs1 + sext(offset)
            //     rs1 来自寄存器堆，offset 来自指令立即数字段
            //     结果送给 MEM 阶段的 DMEM 写地址端口
            //
            //   LOAD (OPCODE_LOAD):
            //     计算源地址：addr = rs1 + sext(offset)
            //     结果送给 MEM 阶段的 DMEM 读地址端口
            //     注意：Load 有符号/无符号扩展在 MEM 阶段的 Load 对齐模块处理，
            //     不在 ALU 中完成
            //
            //   JALR (OPCODE_JALR):
            //     计算跳转目标：target = rs1 + sext(imm)
            //     结果同时用于写 PC（跳转）和写 rd（返回地址=PC+4，由上层处理）
            //
            //   LUI (OPCODE_LUI):
            //     语义：rd = imm << 12
            //     硬件实现：A=0, B=imm<<12, ALU=A+B → 结果即 imm<<12
            //     为什么用 ADD 直通？复用现有加法器，避免额外的直通数据通路
            //
            //   AUIPC (OPCODE_AUIPC):
            //     语义：rd = PC + (imm << 12)
            //     PC 值由 IF/ID 流水线寄存器传入，imm<<12 由立即数生成单元提供

            `OPCODE_STORE:  aluop = `ALU_ADD;
            `OPCODE_LOAD:   aluop = `ALU_ADD;
            `OPCODE_JALR:   aluop = `ALU_ADD;
            `OPCODE_LUI:    aluop = `ALU_ADD;
            `OPCODE_AUIPC:  aluop = `ALU_ADD;

            // ==============================================================
            // 默认分支：非法/未实现指令 → ALU_ADD
            // ==============================================================
            // 选择 ADD 作为默认操作的理由：
            //   1. ADD 不会产生溢出异常之外的副作用，对架构状态影响最小
            //   2. 即使指令非法，ADD 的输出是确定性的（不会出现 X 态）
            //   3. Control_unit 会同时关闭 RegWrite，确保非法指令不破坏寄存器堆
            default: aluop = `ALU_ADD;

        endcase
    end

endmodule
