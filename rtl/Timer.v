// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  Timer.v — 可编程定时器 / 比较器外设                                       ║
// ╠══════════════════════════════════════════════════════════════════════════════╣
// ║                                                                            ║
// ║  【外设用途】                                                              ║
// ║  本模块实现一个简单的 32 位定时计数器，具备以下功能：                       ║
// ║    (1) 在使能后每个时钟周期递增 1。                                        ║
// ║    (2) 当计数值等于比较值 (COMPARE) 时产生中断。                            ║
// ║    (3) 中断为电平型锁存输出，软件写 COMPARE 寄存器时清除。                  ║
// ║                                                                            ║
// ║  【寄存器映射 (Register Map)】                                              ║
// ║  偏移地址由 addr[3:0] 的低 4 位译码（支持字对齐访问，地址步长 4 字节）：   ║
// ║  ┌─────────┬──────┬──────────┬──────────────────────────────────────────┐   ║
// ║  │ 偏移    │ 名称 │ 访问类型 │ 说明                                     │   ║
// ║  ├─────────┼──────┼──────────┼──────────────────────────────────────────┤   ║
// ║  │ 0x0     │ COUNT│ RO (只读)│ 当前计数值。复位后为 0。                 │   ║
// ║  │         │      │          │ 使能时每个时钟周期递增 1，溢出后回绕。    │   ║
// ║  ├─────────┼──────┼──────────┼──────────────────────────────────────────┤   ║
// ║  │ 0x4     │CMPARE│ RW (读写)│ 比较阈值。复位后为 0xFFFF_FFFF。          │   ║
// ║  │         │      │          │ 当 COUNT == COMPARE 且使能时产生中断。    │   ║
// ║  │         │      │          │ ★ 写该寄存器会清除已锁存的中断。          │   ║
// ║  ├─────────┼──────┼──────────┼──────────────────────────────────────────┤   ║
// ║  │ 0x8     │ CTRL │ RW (读写)│ 控制寄存器。目前仅 bit[0] 有效：          │   ║
// ║  │         │      │          │   bit[0]=1: 使能计数 (enable)             │   ║
// ║  │         │      │          │   bit[0]=0: 停止计数                      │   ║
// ║  │         │      │          │   高 31 位保留，读回 0。                   │   ║
// ║  └─────────┴──────┴──────────┴──────────────────────────────────────────┘   ║
// ║                                                                            ║
// ║  【接口协议 — 总线侧 (Bus Interface)】                                     ║
// ║  本模块挂载在处理器内存映射总线上，遵循简单的同步读写协议：                ║
// ║   - clk:     总线时钟。所有寄存器在 posedge 采样/更新。                   ║
// ║   - reset:   同步复位（高有效）。复位后各寄存器回到默认值。                ║
// ║   - addr:    32 位地址输入。仅低 4 位 addr[3:0] 参与寄存器选择译码。      ║
// ║   - wdata:   32 位写数据。写操作时携带要写入的数据。                       ║
// ║   - we:      写使能（高有效）。we=1 时执行寄存器写操作。                    ║
// ║   - rdata:   32 位读数据。组合逻辑输出，读操作零延迟返回。                  ║
// ║   ★ 读路径为纯组合逻辑 (assign)，因此读操作在同一个时钟周期内返回数据，   ║
// ║     适合典型的单周期总线读时序。                                           ║
// ║   ★ 写路径为时序逻辑 (always @posedge)，写入的数据在下一个时钟周期生效。  ║
// ║     若同一周期内既有读又有写，rdata 反映的是写之前的旧值。                 ║
// ║                                                                            ║
// ║  【接口协议 — 外部侧 (External / Interrupt Interface)】                    ║
// ║   - timer_irq: 中断输出。电平信号，连接到处理器中断控制器。                ║
// ║     * 当 counter == compare 且 enable=1 时，irq_latch 被置为 1。          ║
// ║     * irq_latch 一旦置位则保持为 1（锁存），即使后续 counter 不再等于      ║
// ║       compare 也不会自动清零。                                             ║
// ║     * 仅在写 COMPARE 寄存器 (addr=0x4, we=1) 时，irq_latch 被清零。        ║
// ║     * 软件中断服务例程 (ISR) 中应写入 COMPARE（可写相同值）以清除中断。    ║
// ║                                                                            ║
// ║  【时序与同步设计 (Timing & Synchronization)】                             ║
// ║                                                                              ║
// ║  1. 计数器时序：                                                            ║
// ║     - counter 在每个 clk 上升沿更新。                                        ║
// ║     - enable=1 时 counter <= counter + 1。                                   ║
// ║     - enable=0 时 counter 保持不变。                                         ║
// ║     - counter 溢出后自动回绕到 0（32 位加法器的自然行为）。                  ║
// ║     - 复位时 counter 清零。                                                  ║
// ║                                                                              ║
// ║  2. 中断产生与清除时序：                                                     ║
// ║     - cycle N:  counter 更新 (假设此时 enable=1)。                           ║
// ║     - cycle N+1: match 信号在时钟上升沿后采样 counter 和 compare 的值，     ║
// ║                  若 match=1，则 irq_latch <= 1。                             ║
// ║     - 即：计数器匹配后的下一个周期，timer_irq 输出变为高电平。              ║
// ║     - 中断清除：写 COMPARE 的同一周期内 irq_latch <= 0，                    ║
// ║       timer_irq 在该周期末变为低电平。                                       ║
// ║                                                                              ║
// ║  3. 同周期读写竞争：                                                         ║
// ║     - 因为 rdata 是组合逻辑直接从寄存器读取，而寄存器在同一个 posedge       ║
// ║       更新（写操作），读操作看到的是写之前的旧值。                            ║
// ║     - 处理器总线通常不会在同一周期对同一地址既读又写，因此不存在竞争。      ║
// ║                                                                              ║
// ║  4. 中断锁存设计动机：                                                       ║
// ║     - 若不锁存，count 递增越过 compare 后中断会立即消失，软件可能来不及    ║
// ║       响应（"中断脉冲"问题）。                                               ║
// ║     - 锁存后，中断保持高电平直到软件显式写入 COMPARE 清除，确保中断          ║
// ║       控制器和 ISR 能可靠捕获并处理事件。                                    ║
// ║     - 锁存清除操作和写 COMPARE 绑定在同一地址，简化了寄存器地址空间。       ║
// ║                                                                              ║
// ║  【典型使用流程】                                                            ║
// ║   (a) 写 CTRL=0（关闭使能），防止配置过程中意外触发中断。                    ║
// ║   (b) 写 COMPARE（例如 1000）设定超时/定时间隔。                            ║
// ║        此操作同时清除可能残留的 irq_latch。                                  ║
// ║   (c) 写 CTRL=1（使能计数）。                                                ║
// ║   (d) 等待中断；ISR 中写 COMPARE（可写当前 compare 值）清除中断，          ║
// ║        然后执行定时到期后的业务逻辑。                                        ║
// ║   (e) 若要重新启动定时，重复步骤 (b)-(d)。                                   ║
// ║                                                                            ║
// ║  【寄存器复位值汇总】                                                      ║
// ║    COUNT   = 0x0000_0000                                                  ║
// ║    COMPARE = 0xFFFF_FFFF                                                   ║
// ║    CTRL    = 0x0000_0000 (enable=0)                                       ║
// ║    irq_latch = 0                                                           ║
// ║                                                                            ║
// ╚══════════════════════════════════════════════════════════════════════════════╝

module timer (
    // ═══════════════════════════════════════════════════════════════════════════
    //  总线侧接口 (Bus / Memory-Mapped Interface)
    //  - 处理器通过这组信号读写定时器的寄存器
    // ═══════════════════════════════════════════════════════════════════════════
    input  wire        clk,          // 总线时钟：所有同步逻辑的基准时钟
    input  wire        reset,        // 同步复位（高有效）：将寄存器恢复为默认值

    input  wire [31:0] addr,         // 地址总线：指向要读写的寄存器
                                     //   有效译码位为 addr[3:0]（低 4 位）：
                                     //     4'h0 → COUNT       4'h4 → COMPARE
                                     //     4'h8 → CTRL        其他 → 零值

    input  wire [31:0] wdata,        // 写数据总线：写操作时携带要写入的值
    input  wire        we,           // 写使能（高有效）：we=1 时触发写操作

    output wire [31:0] rdata,        // 读数据总线：组合逻辑输出，读操作零延迟返回

    // ═══════════════════════════════════════════════════════════════════════════
    //  外部侧接口 (External / Interrupt Interface)
    //  - 连接到中断控制器的中断请求信号
    // ═══════════════════════════════════════════════════════════════════════════
    output wire        timer_irq     // 定时器中断请求：当 counter==compare 且 enable=1 时
                                     //   锁存为高电平，写 COMPARE 寄存器时清除
);

    // ═══════════════════════════════════════════════════════════════════════════
    //  内部寄存器 (Internal Registers)
    // ═══════════════════════════════════════════════════════════════════════════
    reg [31:0] counter;              // 计数器：使能时每个时钟周期递增 1，读地址 0x0
    reg [31:0] compare;              // 比较值：当 counter==compare 时触发中断，读/写地址 0x4
    reg        enable;               // 使能位：1=计数运行，0=暂停，映射到 CTRL 寄存器的 bit[0]
    reg        irq_latch;            // ★ 中断锁存器：match 时置 1，写 COMPARE 时清 0
                                     //   确保中断信号跨多个时钟周期稳定保持，避免"脉冲式"中断被漏掉

    // ═══════════════════════════════════════════════════════════════════════════
    //  组合逻辑 — 匹配检测
    //  - match 为 1 表示当前计数已达比较阈值
    //  - 纯组合逻辑，无延迟：counter 更新后同一周期 match 即反映新值
    // ═══════════════════════════════════════════════════════════════════════════
    wire match = (counter == compare);

    // ═══════════════════════════════════════════════════════════════════════════
    //  读数据通路 (Read Data Path)
    //  - 组合逻辑多路选择器，根据 addr[3:0] 选择对应的寄存器输出
    //  - 零时钟延迟：处理器可在发起读请求的同一周期获取数据
    //  - 未定义的地址返回 32'h0
    // ═══════════════════════════════════════════════════════════════════════════
    assign rdata = (addr[3:0] == 4'h0) ? counter               // 0x0: COUNT — 读当前计数值
                 : (addr[3:0] == 4'h4) ? compare               // 0x4: COMPARE — 读比较阈值
                 : (addr[3:0] == 4'h8) ? {31'h0, enable}       // 0x8: CTRL — bit[0] 为使能位，高位补 0
                 : 32'h0;                                      // 其他地址：返回零

    // ═══════════════════════════════════════════════════════════════════════════
    //  时序逻辑 — 寄存器写 + 计数器 + 中断锁存
    //  所有状态在 clk 上升沿统一更新，确保同步设计的时序收敛性。
    //  写操作、计数递增、中断锁存 三者在同一个 always 块中处理，
    //  优先级顺序为：reset > 写操作 > 计数 > 中断锁存。
    // ═══════════════════════════════════════════════════════════════════════════
    always @(posedge clk) begin
        // ── 复位处理 ──────────────────────────────────────────────────────────
        // reset=1 时将所有寄存器恢复到默认值：
        //   counter=0, compare=最大值(避免误触发), enable=0, irq_latch=0
        if (reset) begin
            counter   <= 32'h0;
            compare   <= 32'hFFFF_FFFF;   // 默认比较值为最大值，确保复位后不会
            enable    <= 1'b0;            // 立即匹配触发中断
            irq_latch <= 1'b0;

        end else begin
            // ── 写操作处理 ────────────────────────────────────────────────────
            // 仅在 we=1 时对译码选中的寄存器执行写操作。
            // 写 COMPARE (0x4) 时同时清除中断锁存器 irq_latch。
            if (we) begin
                case (addr[3:0])
                    4'h4: begin
                        compare   <= wdata;        // 更新比较值
                        irq_latch <= 1'b0;         // ★ 写 COMPARE 清除中断
                                                    //   将中断锁存清零，timer_irq
                                                    //   将在下一时钟周期变为低电平
                    end
                    4'h8: enable <= wdata[0];       // CTRL 寄存器：仅 bit[0] 有效
                                                    //   1=启动计数，0=暂停计数
                    // ★ 注意：COUNT (0x0) 为只读寄存器，写操作被忽略
                    // ★ 未定义的地址写操作也被忽略
                endcase
            end

            // ── 计数逻辑 ──────────────────────────────────────────────────────
            // enable=1 时每个时钟周期 counter 递增 1。
            // 32 位加法器溢出后自动回绕到 0，无需额外逻辑。
            // enable=0 时 counter 保持当前值不变。
            if (enable) begin
                counter <= counter + 32'd1;
            end

            // ── 中断锁存逻辑 ───────────────────────────────────────────────────
            // 仅在 match=1 且 enable=1 时将 irq_latch 置 1。
            //   - match=1 意味着 counter == compare（当前计数已达阈值）。
            //   - enable=1 意味着计数器正在运行（避免 disable 状态下
            //     因 counter 恰好等于 compare 而误触发中断）。
            // ★ 注意：此处使用的是时序逻辑中的阻塞判断——本周期新的
            //   counter 值（若 enable=1 时已递增）与 compare 比较，
            //   若相等则 irq_latch 被置位。
            // ★ irq_latch 没有在同 always 块中被自动清零的逻辑，
            //   因此一旦置位，只会在写 COMPARE 或复位时清除——
            //   实现了中断的锁存（latch-on-match）行为。
            if (match && enable)
                irq_latch <= 1'b1;
        end
    end

    // ═══════════════════════════════════════════════════════════════════════════
    //  中断输出
    //  - timer_irq 直接由 irq_latch 驱动（电平型输出）
    //  - 连接到处理器的中断控制器，高电平表示定时器中断待处理
    // ═══════════════════════════════════════════════════════════════════════════
    assign timer_irq = irq_latch;

endmodule
