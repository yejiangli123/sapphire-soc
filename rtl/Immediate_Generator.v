// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║            Immediate_Generator.v — RISC-V 立即数生成器                        ║
// ║  功能: 从 32-bit 指令中提取并符号扩展 6 种格式的立即数                          ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
//
// ============================================================================
// 一、模块在 CPU 流水线中的角色与位置
// ============================================================================
//
//   本模块位于 CPU 流水线的 **译码阶段 (Decode Stage, ID)**，紧接在取指阶段
//   (Instruction Fetch, IF) 之后、执行阶段 (Execute, EX) 之前。
//
//   数据流路径:
//
//     IF (取指)  ──►  ID (译码) ────────────────────────────────►  EX (执行)
//                     │                                              │
//                     ├─ 主译码器 (decoder) ──► 产生 imm_mux 控制信号 ─┤
//                     │                                              │
//                     └─ 立即数生成器 (本模块) ──► 输出 32-bit imm ────┘
//
//   职责:
//     - 接收来自取指级锁存下来的指令高 25 位 instr[31:7]。
//     - 接收来自主译码器 (decoder) 的 3-bit 选通信号 imm_mux。
//     - 根据 RISC-V 规范中定义的 6 类立即数编码格式 (I/S/B/J/U + I-load),
//       将指令中分散的立即数位段重新拼接, 并进行符号扩展 (或零扩展), 生成一个
//       完整的 32-bit 立即数。
//     - 该立即数随后送入 ALU 的第二个操作数端口 (或 PC 加法器), 用于计算
//       目标地址 / 算术结果 / 访存偏移量等。
//
//   为何需要本模块:
//     RISC-V 指令集中, 立即数位段在编码时被刻意打乱顺序、散布在指令字的
//     不同 bit 位置。这种"打乱"的设计是为了简化译码硬件: 使得寄存器索引
//     (rs1/rs2/rd) 在不同指令格式中尽可能对齐到相同的 bit 位置, 从而可以
//     在译码阶段尽早读出寄存器操作数 (无需等待立即数格式识别完成)。代价是
//     立即数需要额外的重排逻辑 —— 这就是本模块存在的意义。
//
//
// ============================================================================
// 二、RISC-V 立即数位段映射总览 (指令 bit 编号从 31 到 0)
// ============================================================================
//
//   6 种格式的立即数位段在 32-bit 指令中的物理位置如下:
//
//   ┌─────────┬──────────────────────────────────────────────────────┐
//   │  格式    │  指令 bit 位置 (31..0)                                │
//   ├─────────┼──────────────────────────────────────────────────────┤
//   │ I-type   │ imm = [31]×20 ++ instr[31:20]                        │
//   │          │ 例: ADDI, SLTI, ANDI, ORI, XORI, SLLI, SRLI, SRAI   │
//   │          │     JALR, LW/LH/LB (load 类)                         │
//   ├─────────┼──────────────────────────────────────────────────────┤
//   │ S-type   │ imm = [31]×20 ++ instr[31:25] ++ instr[11:7]         │
//   │          │ 例: SW, SH, SB                                       │
//   ├─────────┼──────────────────────────────────────────────────────┤
//   │ B-type   │ imm = [31]×20 ++ instr[7] ++ instr[30:25]            │
//   │          │              ++ instr[11:8] ++ 1'b0                   │
//   │          │ 例: BEQ, BNE, BLT, BGE, BLTU, BGEU                   │
//   ├─────────┼──────────────────────────────────────────────────────┤
//   │ J-type   │ imm = [31]×12 ++ instr[19:12] ++ instr[20]           │
//   │          │              ++ instr[30:21] ++ 1'b0                   │
//   │          │ 例: JAL (跳转并链接)                                   │
//   ├─────────┼──────────────────────────────────────────────────────┤
//   │ U-type   │ imm = instr[31:12] ++ 12'h000                         │
//   │          │ 例: LUI (加载高位立即数), AUIPC (PC 加高位立即数)       │
//   └─────────┴──────────────────────────────────────────────────────┘
//
//   符号扩展规则:
//     - I/S/B/J 型: 最高位 (instr[31]) 复制填充高位, 实现有符号扩展。
//     - U 型:     低 12 位直接补 0 (相当于 imm × 4096), 不需要符号扩展,
//                 因为 U 型立即数本身就是 20-bit 放在高 20 位。
//     - B/J 型:   最低位 (LSB) 强制补 1'b0, 因为分支 / 跳转目标地址
//                 在 RISC-V 中总是 2 字节对齐的, bit[0] 恒为 0。
//
//
// ============================================================================
// 三、I/O 端口说明
// ============================================================================
//
//   ★ input  [31:7] instr:
//       - 来自取指级 (IF) 锁存的 32-bit 指令的高 25 位, 即 instr[31:7]。
//       - 为什么不传完整的 instr[31:0] ?
//         因为低 7 位 instr[6:0] 是 opcode, 它在主译码器中已经用于判断
//         指令类型并生成本模块所需的 imm_mux 信号; 立即数的位段提取只需要
//         instr[31:7], 不涉及 opcode 位域。
//       - 传入 instr[31:7] 而非完整 32-bit 也节省了一根 7-bit 的总线连线。
//
//   ★ input  [2:0] imm_mux:
//       - 来自主译码器 (decoder) 的 3-bit 控制信号, 用于选通 6 种立即数格式
//         中的一种。编码定义在 Def.v 中:
//
//           `I_type      = 3'b000   // I-type 算术立即数 (ADDI/SLTI/ANDI/...)
//           `I_type_load = 3'b001   // I-type 加载指令 (LW/LH/LB/LBU/LHU)
//           `S_type      = 3'b010   // S-type 存储指令 (SW/SH/SB)
//           `B_type      = 3'b011   // B-type 分支指令 (BEQ/BNE/BLT/...)
//           `J_type      = 3'b100   // J-type 跳转指令 (JAL)
//           `U_type_LUI  = 3'b101   // U-type LUI
//           `U_type_AUIPC= 3'b110   // U-type AUIPC
//
//       - 为何 I_type 和 I_type_load 是两个不同编码 ?
//         它们的立即数位段物理位置完全相同 (都是 I-type 格式), 但区分它们
//         是因为:
//         (a) 加载指令的立即数会用作访存地址偏移量, 下游的流水线控制逻辑
//             需要据此产生 MEM_READ 信号;
//         (b) 算术立即数指令的立即数直接参与 ALU 运算, 不产生访存请求。
//         两者在流水线控制状态机中的处理路径不同, 因此 imm_mux 将其区分。
//
//       - 为何 U_type_LUI 和 U_type_AUIPC 是两个不同编码 ?
//         同样, 两者的立即数位段格式完全相同 (都是 U-type), 但 LUI 直接将
//         立即数加载到目标寄存器 (rd = imm << 12), 而 AUIPC 将立即数与 PC
//         相加后写入目标寄存器 (rd = PC + (imm << 12))。下游的控制逻辑
//         需要据此选择不同的数据通路 (直通 vs. PC+imm), 因此区分编码。
//
//   ★ output reg [31:0] imm:
//       - 组合逻辑输出, 生成 32-bit 完整立即数。
//       - 该输出直接连到 ALU 的第二个操作数 (operand B) 的多路选择器输入端,
//         或连接到 PC 加法器用于计算分支 / 跳转目标地址。
//       - 定义为 reg 类型是因为在 always @(*) 组合逻辑块中被赋值;
//         实际综合为纯组合逻辑 (多路选择器), 不产生寄存器 / 锁存器。
//
//
// ============================================================================
// 四、内部信号逻辑与逐 case 分析
// ============================================================================
//

`include "Def.v"

module imm_gen (
    input  [31:7] instr,
    input  [2:0]  imm_mux,
    output reg [31:0] imm
);

    // --- 中间线网: 6 种格式的立即数拼接结果 ---
    // 所有 assign 语句为纯组合逻辑, 并行计算所有格式的立即数,
    // 然后由下方的 case 多路选择器根据 imm_mux 选出最终输出。
    // 这种"先全部计算, 再选通输出"的结构比在 case 内部逐个拼接
    // 更清晰、更易于综合器优化 (综合器可做资源共享, 如共享符号扩展逻辑)。

    wire [31:0] i_type, s_type, b_type, j_type, u_type;

    // =====================================================================
    // I-type 立即数拼接 (I_type 和 I_type_load 共用)
    // =====================================================================
    // 格式: imm = { {20{instr[31]}}, instr[31:20] }
    //
    // 含义:
    //   - instr[31:20] 是 12-bit 立即数的物理位置 (指令 bit 31 到 bit 20)。
    //   - {20{instr[31]}} 是将 instr[31] (符号位) 复制 20 份, 实现从 12-bit
    //     到 32-bit 的符号扩展。
    //
    // 覆盖指令:
    //   - ADDI  (加立即数)            — ALU 将 rs1 + imm 写入 rd
    //   - SLTI  (有符号小于立即数)     — 比较 rs1 < imm (有符号)
    //   - SLTIU (无符号小于立即数)     — 比较 rs1 < imm (无符号)
    //   - ANDI  (按位与立即数)         — rs1 & imm → rd
    //   - ORI   (按位或立即数)         — rs1 | imm → rd
    //   - XORI  (按位异或立即数)       — rs1 ^ imm → rd
    //   - SLLI  (逻辑左移立即数)       — rs1 << shamt (shamt 在 imm 低 5 位)
    //   - SRLI  (逻辑右移立即数)       — rs1 >> shamt
    //   - SRAI  (算术右移立即数)       — rs1 >>> shamt
    //   - JALR  (寄存器跳转并链接)     — PC = (rs1 + imm) & ~1, rd = PC+4
    //   - LW/LH/LB/LBU/LHU (加载)      — 访存地址 = rs1 + imm
    //   - CSR 指令                     — CSR 地址 / 操作数在 imm 中
    //
    // 设计原理:
    //   I-type 是最"规整"的立即数格式 — 12-bit 立即数连续放在 instr[31:20],
    //   不需要位段重排, 只需要符号扩展。这种规整性反映了 I-type 指令在设计时
    //   优先级最高: 它是 RV32I 中使用最频繁的立即数格式, 包含所有算术立即数
    //   运算和所有加载指令。
    assign i_type = {{20{instr[31]}}, instr[31:20]};


    // =====================================================================
    // S-type 立即数拼接
    // =====================================================================
    // 格式: imm = { {20{instr[31]}}, instr[31:25], instr[11:7] }
    //
    // 含义:
    //   - 12-bit 立即数被拆成两段:
    //       imm[11:5] = instr[31:25] (高 7 位, 物理位置在指令 bit 31..25)
    //       imm[4:0]  = instr[11:7]  (低 5 位, 物理位置在指令 bit 11..7)
    //   - {20{instr[31]}} 是符号扩展到 32-bit。
    //
    // 覆盖指令: SW (存字), SH (存半字), SB (存字节)
    //
    // 设计原理: 为什么 S-type 的立即数被拆成两段 ?
    //   RISC-V 编码规范的核心原则是: 在所有指令格式中, 寄存器索引域
    //   (rs1, rs2, rd) 尽可能对齐到相同的 bit 位置, 以简化译码级寄存器
    //   文件的读出逻辑。具体来说:
    //     - rs1 总是在 instr[19:15] (R/I/S/B 型均一致)
    //     - rs2 总是在 instr[24:20] (R/S/B 型均一致)
    //     - rd  总是在 instr[11:7]  (R/I/U/J 型均一致)
    //   S-type 有 rs1 和 rs2 但没有 rd, 它的 instr[11:7] 位置空出来,
    //   被用来放立即数的低 5 位 imm[4:0]; 而立即数的高 7 位 imm[11:5]
    //   被放在 instr[31:25], 与 R-type 的 funct7 域 (也位于 instr[31:25])
    //   共享同一物理位置。这种设计使得译码器在处理 R-type 和 S-type 时,
    //   rs2 的读出逻辑完全一致 (都从 instr[24:20] 读取)。
    assign s_type = {{20{instr[31]}}, instr[31:25], instr[11:7]};


    // =====================================================================
    // B-type 立即数拼接
    // =====================================================================
    // 格式: imm = { {20{instr[31]}}, instr[7], instr[30:25], instr[11:8], 1'b0 }
    //
    // 含义 (从 MSB 到 LSB):
    //   - {20{instr[31]}}: 符号扩展 (bit[31] 复制 20 份)
    //   - instr[7]:        imm[11] (立即数的最高位, 符号扩展实际上用 instr[31],
    //                      但 imm[11] 层级的 MSB 在这里)
    //                      ★ 位置在 bit 7, 与 I/S 型的 instr[31] 不同
    //   - instr[30:25]:    imm[10:5] (高 6 位)
    //   - instr[11:8]:     imm[4:1]  (低 4 位)
    //   - 1'b0:            imm[0]    (最低位恒为 0, 见下方说明)
    //
    // 覆盖指令: BEQ, BNE, BLT, BGE, BLTU, BGEU
    //
    // 设计原理:
    //   (1) 位段为何如此分散 ?
    //       B-type 与 S-type 一样, 遵循"寄存器域优先对齐"原则:
    //       - rs1 在 instr[19:15], rs2 在 instr[24:20] — 与 R/I/S 型一致。
    //       - 立即数位段填充了剩余的"空位", 导致 imm[11] 被推到 instr[7]。
    //
    //   (2) 为何 LSB 补 0 (1'b0) ?
    //       RISC-V 规范强制要求: 所有分支目标地址必须是 2 字节对齐的
    //       (即地址的最低 1 bit 恒为 0)。这是因为 RISC-V 支持压缩指令扩展
    //       (C 扩展, 16-bit 指令), 但即便在非压缩模式下, 分支偏移量的粒度
    //       也是 2 字节。实际的 PC 偏移 = imm (符号扩展) << 1, 也就是说
    //       imm 中编码的是"半字数" (halfword count), 而硬件将其左移 1 位
    //       得到实际字节地址偏移。因此在立即数重排时, LSB 硬连线为 0,
    //       硬件不再需要额外做 << 1 移位。
    //
    //   (3) 为什么 instr[31] 仍用于符号扩展, 而 instr[7] 是 imm[11] ?
    //       在 B-type 中, 12-bit 有符号立即数的最高位 imm[12] 被隐式包含
    //       在符号扩展中 (用 instr[31] 代替), 而 instr[7] 是 imm[11]。
    //       理解这个映射的关键是: 立即数实际是 13-bit 的偏移量 (bit[12:1]),
    //       bit[0] 恒为 0。在硬件实现中, 我们将 13-bit 编码拆成:
    //         bit[12]       = instr[31]  (符号位)
    //         bit[11]       = instr[7]
    //         bit[10:5]     = instr[30:25]
    //         bit[4:1]      = instr[11:8]
    //         bit[0]        = 1'b0
    //       然后对 bit[12] 符号扩展到 32-bit。
    assign b_type = {{20{instr[31]}}, instr[7], instr[30:25], instr[11:8], 1'b0};


    // =====================================================================
    // J-type 立即数拼接
    // =====================================================================
    // 格式: imm = { {12{instr[31]}}, instr[19:12], instr[20], instr[30:21], 1'b0 }
    //
    // 含义 (从 MSB 到 LSB):
    //   - {12{instr[31]}}: 符号扩展 (bit[31] 复制 12 份, 而非 I/S/B 的 20 份,
    //                       因为 J-type 立即数有 20-bit 有效数据位, 仅需扩展到
    //                       高 12 位即可填满 32-bit)
    //   - instr[19:12]:    imm[19:12] (低段, 8-bit)
    //   - instr[20]:       imm[11]    (中间 1-bit)
    //   - instr[30:21]:    imm[10:1]  (高段, 10-bit)
    //   - 1'b0:            imm[0]     (最低位恒为 0)
    //
    // 覆盖指令: JAL (跳转并链接, rd = PC+4; PC = PC + imm)
    //
    // 设计原理:
    //   (1) 位段为何如此分散 ?
    //       J-type 只有一个目标寄存器 rd (位于 instr[11:7], 与 R/I/U 型对齐),
    //       没有 rs1 和 rs2。20-bit 立即数位段被拆成多段填充到指令字的不同位置,
    //       基本原则仍然是: 尽量保存 rd 的固定位置, 其余位段散布到可用的空闲位。
    //
    //   (2) 为何 LSB 补 0 ?
    //       与 B-type 同理: 跳转目标地址必须是 2 字节对齐的。实际 PC 偏移
    //       = imm << 1 (即 imm 中编码的是半字数), bit[0] 硬连线为 0。
    //
    //   (3) 为何符号扩展只复制 12 份 (而非 20 份) ?
    //       因为 J-type 的立即数是 20-bit 有符号数 (有效位 [20:1]), 将其放在
    //       32-bit 结果中时, [20:1] 占用了 20 bit, 最高位 (bit[20]) 需要符号
    //       扩展到 bit[31:21], 共 11 bit。这里的代码用 instr[31] (它恰好等于
    //       立即数的 bit[20]) 复制 12 份来覆盖 bit[31:20], 然后与 instr[19:12]
    //       等低位段拼接, 结果恰好是 32-bit。多出的一份 (bit[20]) 会与后续
    //       的 instr[19:12] 的 bit[19] 自然拼接, 不产生冲突 (因为拼接是
    //       从上到下逐段覆盖的)。
    //
    //       等效的另一种理解:
    //         imm[20]    = instr[31]    — 符号位
    //         imm[19:12] = instr[19:12]
    //         imm[11]    = instr[20]
    //         imm[10:1]  = instr[30:21]
    //         imm[0]     = 1'b0
    //       然后 { {11{imm[20]}}, imm[20:0] } 得到 32-bit, 这与代码中的
    //       { {12{instr[31]}}, ... } 效果等价 (多复制了一位但被立即数高位覆盖)。
    assign j_type = {{12{instr[31]}}, instr[19:12], instr[20], instr[30:21], 1'b0};


    // =====================================================================
    // U-type 立即数拼接 (LUI 和 AUIPC 共用)
    // =====================================================================
    // 格式: imm = { instr[31:12], 12'h000 }
    //
    // 含义:
    //   - instr[31:12] 是 20-bit 立即数, 直接放在结果的高 20 位。
    //   - 12'h000 是低 12 位补 0, 等价于将 20-bit 立即数左移 12 位,
    //     即 imm = (20-bit 立即数) × 4096。
    //
    // 覆盖指令:
    //   - LUI:   rd = imm  (将立即数加载到 rd 的高 20 位, 低 12 位为 0)
    //   - AUIPC: rd = PC + imm  (PC 加高位立即数, 用于 PC 相对寻址)
    //
    // 设计原理:
    //   (1) 为何 U-type 不做符号扩展 ?
    //       U-type 的 20-bit 立即数本身就是无符号数, 它占据结果的高 20 位,
    //       低 12 位为 0。不需要符号扩展 — 它天然就是 32-bit 值。
    //       例如 LUI rd, 0xABCDE 的结果是 rd = 0xABCDE000。
    //
    //   (2) 为何 LUI 和 AUIPC 的立即数格式相同但使用不同的 imm_mux 编码 ?
    //       两者的立即数位段完全一致, 但下游执行阶段需要区分处理:
    //       - LUI:  ALU 操作数 A = 0, 操作数 B = imm, 结果 = imm (直通)
    //       - AUIPC: ALU 操作数 A = PC, 操作数 B = imm, 结果 = PC + imm
    //       控制逻辑根据 U_type_LUI / U_type_AUIPC 的不同编码来选通
    //       不同的 ALU 输入路径。
    //
    //   (3) U-type 的指令格式特点:
    //       U-type 指令只有 rd (instr[11:7]) 和 20-bit 立即数 (instr[31:12]),
    //       没有 rs1/rs2/funct3/funct7 等域。这使得 U-type 能加载 32-bit 中
    //       任意的高 20 位值, 配合 ADDI 的 12-bit 立即数, 可以构造任意 32-bit
    //       常数 (LUI 加载高 20 位 + ADDI 加低 12 位, 注意 ADDI 的符号扩展
    //       可能需要修正, 这是编译器 / 链接器的职责)。
    assign u_type = {instr[31:12], 12'h000};


    // =====================================================================
    // 多路选择器: 根据 imm_mux 控制信号选择正确的立即数格式输出
    // =====================================================================
    //
    // 这是一个纯组合逻辑的 always @(*) 块, 综合为 8-to-1 多路选择器。
    // 所有 6 种格式的立即数已经由上方的 assign 语句并行计算完成,
    // 这里只负责根据控制信号选通最终值。
    //
    // case 分支说明:
    //
    //   `I_type (3'b000):
    //       算术 I-type 立即数, 用于 ADDI/SLTI/ANDI/ORI/XORI/SLLI/SRLI/SRAI/JALR
    //       等指令。这些指令将立即数直接送入 ALU 作为操作数 B。
    //
    //   `I_type_load (3'b001):
    //       Load 类 I-type 立即数, 用于 LW/LH/LB/LBU/LHU 等访存指令。
    //       立即数作为访存地址偏移量 (base + offset), 送入 ALU 计算有效地址。
    //       虽然位段格式与 `I_type 完全相同, 但下游控制逻辑根据此编码
    //       驱动 MEM_READ 信号, 使能 AXI 读事务。
    //
    //   `S_type (3'b010):
    //       Store 类立即数, 用于 SW/SH/SB 指令。
    //       立即数作为访存地址偏移量 (base + offset), 送入 ALU 计算有效地址。
    //       下游控制逻辑根据此编码驱动 MEM_WRITE 信号, 使能 AXI 写事务。
    //
    //   `B_type (3'b011):
    //       分支类立即数, 用于 BEQ/BNE/BLT/BGE/BLTU/BGEU 指令。
    //       立即数送入 PC 加法器计算分支目标地址 (target = PC + imm)。
    //       同时, ALU 用减法比较 rs1 和 rs2, 结果控制 PC 是否采用分支目标。
    //
    //   `J_type (3'b100):
    //       跳转类立即数, 用于 JAL 指令。
    //       立即数送入 PC 加法器计算跳转目标地址 (target = PC + imm)。
    //       JAL 是无条件跳转, 不需要 ALU 判定条件。
    //
    //   `U_type_LUI (3'b101):
    //       LUI 的 U-type 立即数。立即数高 20 位有值, 低 12 位为 0。
    //       送入 ALU, ALU 将其直通写入目标寄存器。
    //
    //   `U_type_AUIPC (3'b110):
    //       AUIPC 的 U-type 立即数。位段格式与 LUI 完全相同,
    //       但下游 ALU 将其与当前 PC 相加后写入目标寄存器。
    //
    //   default:
    //       兜底分支, 默认使用 I-type 立即数, 防止因未知编码
    //       产生锁存器 (latch) 或不定态 (X) 传播。在实际设计中,
    //       只要译码器正确工作, 不会命中此分支。
    //
    always @(*) begin
        case (imm_mux)
            `I_type:       imm = i_type;
            `I_type_load:  imm = i_type;
            `S_type:       imm = s_type;
            `B_type:       imm = b_type;
            `J_type:       imm = j_type;
            `U_type_LUI:   imm = u_type;
            `U_type_AUIPC: imm = u_type;
            default:       imm = i_type;
        endcase
    end


    // ============================================================================
    // 五、与其他流水线阶段的交互关系
    // ============================================================================
    //
    //   本模块的输出 `imm` 连接到后续流水线阶段, 主要有以下三条使用路径:
    //
    //   路径 1: ALU 操作数 B (最常见)
    //     imm ──► ALU 输入多路选择器 (选择 imm 或 rs2_data 或 CSR 读数据)
    //              ──► ALU 运算 ──► 结果写回寄存器文件 (EX/MEM/WB)
    //     适用指令: ADDI, SLTI, ANDI, ORI, XORI, SLLI, SRLI, SRAI,
    //               LW/LH/LB/LBU/LHU (地址计算), SW/SH/SB (地址计算),
    //               LUI (直通), AUIPC (与 PC 相加)
    //
    //   路径 2: 分支/跳转目标地址计算
    //     imm ──► PC 加法器 (PC + imm) ──► 分支目标 PC
    //              ──► PC 多路选择器 (选择 PC+4 或 target)
    //     适用指令: BEQ/BNE/BLT/BGE/BLTU/BGEU, JAL, JALR (I-type 经 ALU)
    //
    //   路径 3: CSR 操作数
    //     imm[4:0] ──► CSR 地址译码 (用于 CSRRW/CSRRS/CSRRC 等)
    //     imm[4:0] ──► 零扩展为 5-bit 无符号操作数 (用于 CSRRWI/CSRRSI/CSRRCI)
    //     适用指令: CSRRW, CSRRS, CSRRC, CSRRWI, CSRRSI, CSRRCI
    //     (CSR 指令的 uimm[4:0] 也在 I-type 的 instr[19:15] 位置,
    //      通过本模块提取后送入 CSR 模块)
    //
    //   时间序: 本模块为纯组合逻辑, 无时钟, 无使能, 无复位。
    //     其输入 (instr, imm_mux) 在 ID 阶段稳定后, 输出 imm 在同一时钟周期
    //     内稳定 (经过若干门延迟), 并在时钟上升沿被 EX 阶段的流水线寄存器
    //     锁存, 供下一个时钟周期使用。
    //
    //   关键时序路径:
    //     IF 寄存器输出 → 译码器 (组合逻辑) → imm_mux → 本模块 (组合逻辑)
    //       → imm → EX/MEM 流水线寄存器输入
    //     这条路径是整个 ID 阶段最长的组合逻辑链之一, 如果频率不达标,
    //     可以在本模块输出端加一级流水线寄存器 (割裂 ID 为 ID1/ID2)。
    //     当前设计假设该路径在一个时钟周期内可以闭合。
    //

endmodule
